      SUBROUTINE NMNOLI(COMPOR,SDDISC,CRITNL,FONACT,DERNIE,
     &                  MODELE,MAILLA,MATE  ,SDSENS,CARELE,
     &                  LISCHA,RESOCO,SDDYNA)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 19/12/2007   AUTEUR ABBAS M.ABBAS 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.

C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.

C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS
C
      IMPLICIT NONE
      INTEGER      DERNIE
      CHARACTER*19 SDDISC,CRITNL, LISCHA,SDDYNA
      CHARACTER*24 COMPOR
      CHARACTER*24 MODELE,MATE,CARELE
      CHARACTER*24 RESOCO,SDSENS
      CHARACTER*8  MAILLA
      LOGICAL      FONACT(*)
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (SD EVOL_NOLI)
C
C PREPARATION DE LA SD EVOL_NOLI
C      
C ----------------------------------------------------------------------
C
C
C IN  COMPOR : COMPORTEMENT
C IN  SDDISC : SD DISC_INST
C IN  CRITNL : INFORMATIONS RELATIVES A LA CONVERGENCE
C IN  DERNIE : NUMERO DU DERNIER NUMERO ARCHIVE (OU 0 SI NOUVEAU)
C IN  DEPMOI : DEPLACEMENT DE L'ETAT INITIAL
C IN  SIGMOI : CONTRAINTES DE L'ETAT INITIAL
C IN  VARMOI : VARIABLES INTERNES DE L'ETAT INITIAL
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  SDDYNA : SD DEDIEE A LA DYNAMIQUE
C IN  SDSENS : SD DEDIEE A LA SENSIBILITE
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER      NUMARC,DINUAR
      INTEGER      NUMINS
      LOGICAL      FORCE,INCR,NDYNLO,LDYNA
      CHARACTER*8  RESULT,NOPASE
      CHARACTER*24 VALINI(8),K24BLA
      CHARACTER*24 DEPMOI,SIGMOI,VARMOI 
      CHARACTER*24 VITPLU,ACCPLU      
      LOGICAL      ISFONC,LCONT  
      INTEGER      NRPASE,NBPASE
      CHARACTER*24 SENSNB
      INTEGER      JSENSN          
      INTEGER      IFM,NIV         
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> PREPARATION DE LA SD EVOL_NOLI' 
      ENDIF  
C
C --- INITIALISATIONS
C
      K24BLA = ' '
C
C --- FONCTIONNALITES ACTIVEES
C      
      LCONT  = ISFONC(FONACT,'CONTACT')
      LDYNA  = NDYNLO(SDDYNA,'DYNAMIQUE')
C
C --- ACCES SD SENSIBILITE
C   
      SENSNB = SDSENS(1:16)//'.NBPASE '
      CALL JEVEUO(SENSNB,'L',JSENSN)      
      NBPASE = ZI(JSENSN+1-1)       
C
C --- NOUVEAU CONCEPT
C
      IF (DERNIE.EQ.0) THEN
C          
C --- NOM DES CHAMPS
C      
        NUMARC = DINUAR(SDDISC)
        DO 10 NRPASE = NBPASE,0,-1
          CALL NMNSLE(SDSENS,NRPASE,'RESULT',RESULT)
          CALL NMNSLE(SDSENS,NRPASE,'DEPMOI',DEPMOI)          
          CALL NMNSLE(SDSENS,NRPASE,'SIGMOI',SIGMOI)          
          CALL NMNSLE(SDSENS,NRPASE,'VARMOI',VARMOI)          
          IF (LDYNA) THEN
            CALL NMNSLE(SDSENS,NRPASE,'VITPLU',VITPLU)
            CALL NMNSLE(SDSENS,NRPASE,'ACCPLU',ACCPLU)
          END IF
C          
C --- CREATION DE LA SD EVOL_NOLI
C
          CALL RSCRSD(RESULT,'EVOL_NOLI',100)
C
C --- ARCHIVAGE DE L'ETAT INITIAL
C
          IF (NUMARC.EQ.0) THEN
C
            CALL AGGLOM(DEPMOI,SIGMOI,VARMOI,K24BLA,
     &                  VITPLU,ACCPLU,K24BLA,K24BLA,6,VALINI)
C
C --- CREATION D'UN VALE_CONT INITIAL VIERGE
C
            IF (LCONT) THEN
              CALL CFMXR0(RESOCO,MAILLA,FONACT)
            ENDIF  

            CALL NMIMPR('IMPR','ARCH_INIT',' ',0.D0,0)
            IF (NRPASE.GT.0) THEN
              CALL NMNSLE(SDSENS,NRPASE,'NOPASE',NOPASE)
              WRITE (IFM,*)
     &          'ARCHIVAGE DES CHAMPS DERIVES PAR RAPPORT A '//NOPASE
            END IF
C            
            NUMINS = 0
            FORCE  = .TRUE.
            INCR   = NRPASE.EQ.0         
            CALL NMARCH(RESULT,NUMINS,SDDISC,FORCE ,COMPOR,
     &                  CRITNL,RESOCO,VALINI,INCR  ,MODELE,
     &                  MATE  ,CARELE,FONACT,LISCHA,SDDYNA)
          END IF

   10   CONTINUE


C  --  CONCEPT REENTRANT

      ELSE

C      SUPPRESSION DES NUMEROS D'ARCHIVAGE A ECRASER
        NUMARC = DINUAR(SDDISC)
        DO 20 NRPASE = 0,NBPASE
          CALL NMNSLE(SDSENS,NRPASE,'RESULT',RESULT)
          CALL RSRUSD(RESULT,NUMARC)
   20   CONTINUE


      END IF
C
      CALL JEDEMA()

      END
