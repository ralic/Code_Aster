      SUBROUTINE NMDESC(MODELE,NUMEDD,MATE  ,CARELE,COMREF,
     &                  COMPOR,LISCHA,RESOCO,METHOD,SOLVEU,
     &                  PARMET,CARCRI,SDPILO,SDDISC,FONACT,
     &                  NUMINS,ITERAT,VALMOI,POUGD ,DEPALG,
     &                  VALPLU,SECMBR,CNFINT,CNDIRI,ETA   ,
     &                  LICCVG,DEFICO,PREMIE,MATASS,MAPREC,
     &                  MEELEM,MEASSE,VEELEM,SDDYNA)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 19/12/2007   AUTEUR ABBAS M.ABBAS 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS
C TOLE CRP_21
C
      IMPLICIT NONE
      INTEGER      NUMINS, ITERAT, LICCVG(*)
      REAL*8       PARMET(*), ETA
      CHARACTER*14 SDPILO
      CHARACTER*16 METHOD(*)
      CHARACTER*19 CNFINT,CNDIRI 
      CHARACTER*19 MATASS,MAPREC      
      CHARACTER*19 LISCHA, SOLVEU, SDDISC,SDDYNA
      CHARACTER*24 MODELE, NUMEDD, MATE, CARELE, COMREF, COMPOR,CARCRI
      CHARACTER*24 VALMOI(8),POUGD(8),VALPLU(8),DEPALG(8),SECMBR(8)
      CHARACTER*24 DEFICO,RESOCO
      LOGICAL      PREMIE,FONACT(*)
      CHARACTER*8  MEELEM(8),VEELEM(30)      
      CHARACTER*19 MEASSE(8)      
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C CALCUL DE LA DIRECTION DE DESCENTE
C      
C ----------------------------------------------------------------------
C
C
C IN       MODELE K24  MODELE
C IN       NUMEDD K24  NUME_DDL
C IN       MATE   K24  CHAMP MATERIAU
C IN       CARELE K24  CARACTERISTIQUES DES ELEMENTS DE STRUCTURE
C IN       COMREF K24  VARI_COM DE REFERENCE
C IN       COMPOR K24  COMPORTEMENT
C IN       LISCHA K19  L_CHARGES
C IN       MEDIRI K24  MATRICES ELEMENTAIRES DE DIRICHLET (B)
C IN       RIGID  K24  MATRICE DE RIGIDITE ASSEMBLEE (POUR LA DYNAMIQUE)
C IN       MATASS K19  MATRICE DU PREMIER MEMBRE ASSEMBLEE
C IN       MAPREC K19  MATRICE DE PRECONDITIONNEMENT
C                       DU PREMIER MEMBRE ASSEMBLEE

C IN       RESOCO K24  SD CONTACT
C IN       METHOD K16  INFORMATIONS SUR LES METHODES DE RESOLUTION
C IN       SOLVEU K19  SOLVEUR
C IN       PARMET  R8  PARAMETRES DES METHODES DE RESOLUTION
C IN       CARCRI K24  PARAMETRES DES METHODES D'INTEGRATION LOCALES
C IN       SDPILO K14  SD PILOTAGE
C IN       SDDISC K19  SD DISC_INST
C IN       NUMINS  I   NUMERO D'INSTANT
C IN       ITERAT  I   NUMERO D'ITERATION
C IN       VALMOI K24  ETAT EN T-
C IN       POUGD  K24  DONNES POUR POUTRES GRANDES DEFORMATIONS
C IN       DEPDEL K24  INCREMENT DE DEPLACEMENT
C IN       VALPLU K24  ETAT EN T+
C IN       SECMBR K24  VECTEURS ASSEMBLES DES CHARGEMENTS
C IN       CNFINT K24  FINT + BT.LAMBDA + AT.MU
C IN/JXOUT DDEPLA K24  CORRECTION DE DEPLACEMENT
C IN      ETA     R8  REACTUALISATION DE ETA_PILOTAGE
C OUT      LICCVG  I   CODES RETOURS
C                      (4) - MATRICE SINGULIERE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      NBDONN,NBPILO
      PARAMETER   (NBDONN = 5,NBPILO = 3)
      REAL*8       CODONN(NBDONN),COPILO(NBPILO)
      CHARACTER*19 CNDONN(NBDONN),CNPILO(NBPILO)
C
      CHARACTER*19 MATRIX(2)
      CHARACTER*16 K16BID
      CHARACTER*24 CNFEDO,CNFEPI,CNFSDO,CNFSPI,CNDIDO,CNDIPI
      CHARACTER*24 K24BID,K24CIN,CNCINE
      CHARACTER*24 DDEPLA,DEPDEL,DEPOLD,DEPPRE(2)
      INTEGER      FACCVG
      INTEGER      IFM,NIV
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> CALCUL DIRECTION DE DESCENTE...' 
      ENDIF 
C
C --- INITIALISATIONS
C
      FACCVG    = 0  
      MATRIX(1) = MATASS   
      MATRIX(2) = MAPREC                  
C
C --- DECOMPACTION DES VARIABLES CHAPEAU
C
      CALL DESAGG(SECMBR,CNFEDO,CNFEPI,CNDIDO,CNDIPI,
     &            CNFSDO,CNFSPI,K24BID,CNCINE)
      CALL DESAGG(DEPALG,DDEPLA,DEPDEL,DEPOLD,DEPPRE(1),
     &            DEPPRE(2),K24BID,K24BID,K24BID)
C
C --- LECTURE DE LA MATRICE ASSEMBLEE (ET ASSEMBLAGE SI BESOIN)
C
      CALL NMMATR('CORRECTION' ,MODELE,NUMEDD,MATE  ,CARELE,
     &            COMREF,COMPOR,LISCHA,RESOCO,METHOD,
     &            SOLVEU,PARMET,CARCRI,SDDISC,NUMINS,
     &            ITERAT,VALMOI,POUGD ,VALPLU,MATASS,
     &            MAPREC,K16BID,DEFICO,DEPALG,PREMIE,
     &            FONACT,MEELEM,MEASSE,VEELEM,FACCVG,
     &            SDDYNA)       
C     
      IF ((FACCVG.EQ.1).OR.(FACCVG.EQ.2)) GOTO 9999
C
C --- PREPARATION DU SECOND MEMBRE
C
      CNDONN(1) = CNFEDO(1:19)
      CNDONN(2) = CNFSDO(1:19)
      CNDONN(3) = CNFINT(1:19)
      CNDONN(4) = CNDIPI(1:19)
      CNDONN(5) = CNDIRI(1:19)     
      CODONN(1) =  1
      CODONN(2) =  1
      CODONN(3) = -1
      CODONN(4) = -ETA
      CODONN(5) = -1      
      CNPILO(1) = CNFEPI(1:19)
      CNPILO(2) = CNFSPI(1:19)
      CNPILO(3) = CNDIPI(1:19)
      COPILO(1) =  1
      COPILO(2) =  1
      COPILO(3) =  1
C
C --- POUR LE CALCUL DE DDEPLA, IL FAUT METTRE CNCINE A ZERO :
C --- ON LE FAIT PROPREMENT VIA DES ROUTINES DEDIEES CHAM_NO
C
      K24CIN = '&&NMDESC.CNCINE'
      CALL COPISD('CHAMP_GD','V',CNCINE,K24CIN)
      CALL VTZERO(CNCINE)
C
C --- CALCUL DE LA DIRECTION DE DESCENTE
C
      CALL NMRESO(SDPILO,NBDONN,CODONN,CNDONN,NBPILO,
     &            COPILO,CNPILO,CNCINE,SOLVEU,MATRIX,
     &            DEPPRE)  
C
C --- ON RETABLIT LES VALEURS DE CNCINE 
C
      CALL DETRSD('CHAMP_GD',CNCINE)
      CALL COPISD('CHAMP_GD','V',K24CIN,CNCINE)
      CALL DETRSD('CHAMP_GD',K24CIN)
C
9999  CONTINUE
C
C --- RECOPIE CODE RETOUR ERREURS      
C
      LICCVG(5) = FACCVG
      CALL JEDEMA()
      END
