C ROUTINE MECA_NON_LINE (INITIALISATION)
C
C INITIALISATION DU CHARGEMENT GRAPPE_FLUIDE
C      
C ----------------------------------------------------------------------
C
C
C IN  SDDYNA : SD DYNAMIQUE
C IN  NUMEDD : NUME_DDL
C IN  VALMOI : ETAT EN T-
C IN  VALPLU : ETAT EN T+
C IN  NUMINI : NUME_ORDRE INITIAL
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      JINST,N1
      CHARACTER*24 K24BID,NDYNKK
      CHARACTER*24 DEPMOI,VITPLU,ACCPLU,CHGRFL
      LOGICAL      LGRFL,NDYNLO
      CHARACTER*19 LISINS
      REAL*8       DT
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C   
      DT     = 0.D0    
      LGRFL  = NDYNLO(SDDYNA,'FORCE_FLUIDE')
C
C --- EXTRACTION VARIABLES CHAPEAUX
C       
      CALL DESAGG(VALMOI,DEPMOI,K24BID,K24BID,K24BID,
     &            K24BID,K24BID,K24BID,K24BID)
      CALL DESAGG(VALPLU,K24BID,K24BID,K24BID,K24BID,
     &            VITPLU,ACCPLU,K24BID,K24BID)         
C
C --- INITIALISATION DES COMMONS "FORCE_FLUIDE"
C
      IF ( LGRFL )  THEN
        CHGRFL = NDYNKK(SDDYNA,'CHGRFL')
        CALL GETVID('INCREMENT','LIST_INST',1,1,1,LISINS,N1)
        CALL JEVEUO(LISINS(1:19)// '.VALE', 'L', JINST)
        DT = ZR(JINST+1) - ZR(JINST)
        CALL GFINIT(NUMEDD,NUMINI,DEPMOI,VITPLU,ACCPLU,
     &              DT    ,CHGRFL)
      ENDIF  
C
      CALL JEDEMA()      
C
      END
