C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT     NONE
      INTEGER      IFM
      CHARACTER*24 RESOCU
      CHARACTER*1  TYPOPE   
      CHARACTER*3  TYPEOU   
      INTEGER      ILIAI
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : ALGOCU
C ----------------------------------------------------------------------
C
C IMPRESSION DE L'ACTIVATION/DESACTIVATION DE LA LIAISON 
C
C IN  IFM    : UNITE D'IMPRESSION DU MESSAGE
C IN  ILIAI  : NUMERO DE LA LIAISON 
C IN  DEFICU : SD DE DEFINITION (ISSUE D'AFFE_CHAR_MECA)
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*8  NOE,CMP
      CHARACTER*20 CHAIAC
      CHARACTER*24 NOMNOE,NOMCMP
      INTEGER      JNOMNO,JNOMCM
C
C ----------------------------------------------------------------------
C
  
      NOMNOE = RESOCU(1:14)//'.NOMNOE'
      NOMCMP = RESOCU(1:14)//'.NOMCMP'
      CALL JEVEUO(NOMNOE,'L',JNOMNO)
      CALL JEVEUO(NOMCMP,'L',JNOMCM)      
            
      CMP = ZK8(JNOMCM-1+ILIAI)
      NOE = ZK8(JNOMNO-1+ILIAI)
      
      IF (TYPOPE.EQ.'A') THEN
        CHAIAC = ' ACTIVEE     (ECART:'
      ELSE IF (TYPOPE.EQ.'S') THEN
        CHAIAC = ' DESACTIVEE  (ECART:'
      ENDIF

      IF (TYPEOU.EQ.'ALG') THEN
        WRITE (IFM,1000) ILIAI,'(',NOE,' - ',CMP,'): ',
     &                   CHAIAC,')'
      ELSE IF (TYPEOU.EQ.'PIV') THEN
        CHAIAC = ' PIVOT NUL         ('
        WRITE (IFM,1001) ILIAI,'(',NOE,' - ',CMP,'): PIVOT NUL '
      ELSE
        CALL UTMESS('F','CUIMP2','ERREUR CODE DANS AFFICHAGE (DVLP)')
      ENDIF


    
 1000 FORMAT (' <LIA_UNIL> <> LIAISON ',I5,A1,A8,A3,A8,A3,A20,A1)
 1001 FORMAT (' <LIA_UNIL> <> LIAISON ',I5,A1,A8,A4,A8,A13)



      END
