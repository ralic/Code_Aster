      SUBROUTINE MMEXCL(RESOCO,TYPINT,LGLISS,IPTC  ,IPTM  ,
     &                  NDEXCL,NDEXFR,TYPBAR,TYPRAC,LSSCON,
     &                  TYPAPP)
