      SUBROUTINE PROJEC(IZONE ,NDIM  ,NEWGEO,RESOCO,DEFICO,
     &                  POSESC,POSMA ,NORM  ,TANG  ,COEF  ,
     &                  COFX  ,COFY  ,JEU   ,NBNOM ,POSNO ,
     &                  NBDDLT,DDL   ,
     &                  NOMA  ,PROJ  ,ZONESY,JEUPM,PROYES)
C     
