C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT)
C
C ROUTINE PRINCIPALE POUR L'APPARIEMENT
C
C ----------------------------------------------------------------------
C
C
C IN  PREMIE : VAUT .TRUE. SI PREMIER INSTANT DE STAT/DYNA_NON_LINE
C IN  NOMA   : NOM DU MAILLAGE
C IN  NUMEDD : NOM DU NUME_DDL
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C
C
C
C
      CHARACTER*19 SDAPPA
      CHARACTER*19 NEWGEO
      INTEGER      IFM,NIV
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> ... APPARIEMENT'
      ENDIF
C
C --- ACCES SD CONTACT
C
      SDAPPA = RESOCO(1:14)//'.APPA'
      NEWGEO = RESOCO(1:14)//'.NEWG'
C
C --- REMPLISSAGE DE LA SD APPARIEMENT - POINTS (COORD. ET NOMS)
C
      CALL MMPOIN(NOMA  ,DEFICO,NEWGEO,SDAPPA)
C
C --- REALISATION DE L'APPARIEMENT
C
      CALL APCALC(SDAPPA)
C
C --- RECOPIE DE L'APPARIEMENT
C
      CALL MMAPRE(PREMIE,NOMA  ,NUMEDD,DEFICO,RESOCO,
     &            SDAPPA)
C
      CALL JEDEMA()
      END
