C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 MODELE,RESOCO
      CHARACTER*24 DEPMOI,DEPDEL,ACCMOI,VITMOI,VITPLU
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - CALCUL)
C
C CALCUL DES MATRICES ELEMENTAIRES
C
C ----------------------------------------------------------------------
C
C
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  MODELE : NOM DU MODELE
C IN  DEPMOI : CHAM_NO DES DEPLACEMENTS A L'INSTANT PRECEDENT
C IN  DEPDEL : INCREMENT DE DEPLACEMENT CUMULE
C IN  ACCMOI : CHAM_NO DES ACCELERATIONS A L'INSTANT PRECEDENT
C IN  VITMOI : CHAM_NO DES VITESSES A L'INSTANT PRECEDENT
C IN  VITPLU : CHAM_NO DES VITESSES A L'INSTANT SUIVANT
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*8  LPAIN(8),LPAOUT,K8BID
      CHARACTER*19 CHGEOM,LCHIN(8),LCHOUT,USUPLU
      INTEGER      JLIRES
      LOGICAL      EXIGEO
      INTEGER      IFM,NIV
      CHARACTER*19 LIGRCF,CARTCF,MCON19
      CHARACTER*24 MELEME
      INTEGER      JMELEM
      CHARACTER*8  MCONEL
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> CALCUL DES MATRICES ELEMENTAIRES'
      ENDIF
C
C --- LIGREL DES ELEMENTS TARDIFS DE CONTACT/FROTTEMENT
C
      LIGRCF = RESOCO(1:14)//'.LIGR'
C
C --- CARTE POUR ELEMENTS TARDIFS DE CONTACT/FROTTEMENT
C
      CARTCF = RESOCO(1:14)//'.CART'
C
C --- NOMS DES MATR_ELEM CONTACT
C
      MELEME = RESOCO(1:14)//'.MELEM'
      CALL JEVEUO(MELEME,'L',JMELEM)
      MCONEL = ZK8(JMELEM+1-1)
      MCON19 = MCONEL
      USUPLU = RESOCO(1:14)//'.USUP'
C
C --- ON DETRUIT MCONEL ET SCONEL S'ILS EXISTENT
C
      CALL DETRSD('MATR_ELEM',MCON19)
C
C --- RECUPERATION DE LA GEOMETRIE
C
      CALL MEGEOM(MODELE,' ',EXIGEO,CHGEOM)
      IF (.NOT.EXIGEO) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- CREATION DES LISTES DES CHAMPS IN ET OUT
C --- GEOMETRIE ET DEPLACEMENTS
C
      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      LPAIN(2) = 'PDEPL_M'
      LCHIN(2) = DEPMOI
      LPAIN(3) = 'PDEPL_P'
      LCHIN(3) = DEPDEL
      LPAIN(4) = 'PCONFR'
      LCHIN(4) = CARTCF
      LPAIN(5) = 'PDEPLAR'
      LCHIN(5) = VITMOI
      LPAIN(6) = 'PCHDYNR'
      LCHIN(6) = ACCMOI
      LPAIN(7) = 'PUSULAR'
      LCHIN(7) = USUPLU
      LPAIN(8) = 'PVITE_P'
      LCHIN(8) = VITPLU
C
C --- CALCUL DE MCONEL: MATRICES ELEMENTAIRES
C
      CALL MEMARE('V',MCON19,MODELE,' ',' ','RIGI_MECA')
C
C --- PARTIE CONTACT
C
      LPAOUT = 'PMATUUR'
      LCHOUT = MCON19(1:8) // '.ME001'
      CALL CALCUL('S','RIGI_CONT',LIGRCF,8,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &           'V')
      CALL REAJRE(MCON19,LCHOUT,'V')

C
C --- PARTIE FROTTEMENT
C
      LPAOUT = 'PMATUUR'
      LCHOUT = MCON19(1:8) // '.ME002'
      CALL CALCUL('S','RIGI_FROT',LIGRCF,8,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &           'V')
      CALL REAJRE(MCON19,LCHOUT,'V')
C
      CALL JEDEMA()
      END
