C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT      NONE
      CHARACTER*8   NOMO
      CHARACTER*24  MATE,CARELE
      CHARACTER*4   TYPCAL
      REAL*8        PARTPS(3)
      CHARACTER*19  LISCHA,VARPLU
      CHARACTER*(*) NOPASE
      CHARACTER*19  VECELE
      INTEGER       TYPESE
C
C ----------------------------------------------------------------------
C
C CALCUL DES VECTEURS ELEMENTAIRES DES CHARGEMENTS MECANIQUES
C DE NEUMANN STANDARD (VOIR DEFINITION DANS LISDEF)
C
C CALCUL EFFECTIF - BOUCLE SUR LES CHARGES
C
C ----------------------------------------------------------------------
C
C
C IN  NOMO   : NOM DU MODELE
C IN  TYPCAL : TYPE DU CALCUL :
C               'MECA', POUR LA RESOLUTION DE LA MECANIQUE,
C               'DLAG', POUR LE CALCUL DE LA DERIVEE LAGRANGIENNE
C IN  LISCHA : SD LISTE DES CHARGES
C IN  PARTPS : TABLEAU DONNANT T+, DELTAT ET THETA (POUR LE THM)
C IN  CARELE : CARACTERISTIQUES DES POUTRES ET COQUES
C IN  MATE   : MATERIAU CODE
C IN  VARPLU : VARIABLES DE COMMANDE A L'INSTANT T+
C IN  NOPASE : NOM DU PARAMETRE SENSIBLE
C              . POUR LA DERIVEE LAGRANGIENNE, C'EST UN CHAMP THETA
C IN  TYPESE : TYPE DE SENSIBILITE
C               0 : CALCUL STANDARD, NON DERIVE
C               SINON : DERIVE (VOIR METYSE)
C IN  NBIN   : NOMBRE MAXI DE CHAMPS D'ENTREE
C IN  LPAIN  : LISTE DES PARAMETRES IN
C IN  LCHIN  : LISTE DES CHAMPS IN
C IN  LASTIN : NOMBRE EFFECTIF DE CHAMPS IN
C OUT VECELE : VECT_ELEM RESULTAT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      NBIN
      PARAMETER    (NBIN=42)
      CHARACTER*8  LPAIN(NBIN)
      CHARACTER*19 LCHIN(NBIN)
C
      INTEGER      EXICHA,ICHAR,NBCHAR,LASTIN
      CHARACTER*8  NOMCH0,NOMCHS
      CHARACTER*24 NOMLIS
      INTEGER      CODCHA,IER
      LOGICAL      LISICO,LNEUM,LXFEM
      INTEGER      NBCH,NBNEUM,LISNBG
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NOMLIS = '&&NOMLIS'
      CALL EXIXFE(NOMO,IER)
      LXFEM  = IER.NE.0
      CALL ASSERT(TYPCAL.EQ.'MECA' .OR. TYPCAL.EQ.'DLAG')
      CALL DETRSD('VECT_ELEM',VECELE)
C
C --- NOMBRE DE CHARGES
C
      CALL LISNNB(LISCHA,NBCHAR)
C
C --- NOMBRE DE CHARGES DE TYPE NEUMANN MECANIQUE
C
      NBNEUM = LISNBG(LISCHA,'NEUM_MECA')
      IF (NBNEUM.EQ.0) GOTO 99
C
C --- CHAMPS D'ENTREES STANDARDS
C
      CALL VECHMP(NOMO  ,MATE  ,CARELE,VARPLU,LXFEM ,
     &            PARTPS,TYPCAL,NOPASE,NBIN  ,LPAIN ,
     &            LCHIN ,LASTIN)
C
C --- LISTE DES INDEX DES CHARGES
C
      CALL LISNOL(LISCHA,'NEUM_MECA',NOMLIS,NBCH  )
      CALL ASSERT(NBCH.GT.0)
C
C --- CALCUL
C
      DO 80 ICHAR = 1,NBCHAR
        CALL LISLCO(LISCHA,ICHAR ,CODCHA)
        LNEUM  = LISICO('NEUM_MECA',CODCHA)
        IF (LNEUM) THEN
C
C ------- CALCUL DE LA CHARGE ?
C
          CALL LISLCH(LISCHA,ICHAR ,NOMCH0)
          IF (TYPESE.NE.0) THEN
            CALL PSGENC(NOMCH0,NOPASE,NOMCHS,EXICHA)
          ELSE
            EXICHA = 0
          ENDIF
C
C ------- BOUCLE SUR LES TOUS LES TYPES DE CHARGE PREVUS
C
          IF (EXICHA.EQ.0 .OR. TYPCAL.EQ.'DLAG') THEN
            CALL VECHMX(NOMO  ,TYPCAL,LISCHA,ICHAR ,TYPESE,
     &                  NOMCHS,NBCH  ,NOMLIS,NBIN  ,LPAIN ,
     &                  LCHIN ,LASTIN,VECELE)
         ENDIF
        ENDIF
   80 CONTINUE
C
   99 CONTINUE
C
      CALL JEDETR(NOMLIS)
C
      CALL JEDEMA()
      END
