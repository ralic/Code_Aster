C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE GENIAUT S.GENIAUT
C
C     BUT:  CREER LA LISTE DES RELATIONS QUI SUPPRIMENT LES DDLS
C          HEAVISIDE PROVOQUANT UN PIVOT TRÈS PETIT DANS LA CADRE
C               DE LA MÉTHODE X-FEM (VOIR BOOK V 15/04/05)
C
C
C IN   MOD     : NOM DE L'OBJET MODELE
C IN   MA      : NOM DE L'OBJET MAILLAGE
C IN   GRNO    : LISTE DES NUMÉROS DES NOEUDS OÙ ILS FAUT ANNULER LES
C                DDLS HEAVISIDE
C
C OUT  LISREL  : LISTE DES RELATIONS IMPLIQUÉES
C OUT  NREL    : NOMBRE DE RELATIONS

C     ----------- COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNOM,JEXNUM,JEXATR
C---------------- FIN COMMUNS NORMALISES  JEVEUX  ----------------------
C-----------------------------------------------------------------------
C---------------- DECLARATION DES VARIABLES LOCALES  -------------------

      REAL*8       RBID,BETAR
      INTEGER      IER,JLISNO,LONG,INO,NUNO,K
      INTEGER      NDIM,IBID,IRET
      CHARACTER*8  K8BID,NOMNO,DDLH(3),KBID
      COMPLEX*16   CBID
      DATA         DDLH/'H1X','H1Y','H1Z'/
      DATA         BETAR/0.D0/
C-----------------------------------------------------------------------
      CALL JEMARQ()

      CALL JEEXIN(GRNO,IER)
      IF (IER.EQ.0) GOTO 999

      CALL JEVEUO(GRNO,'L',JLISNO)
      CALL JELIRA(GRNO,'LONMAX',LONG,K8BID)
C     RECUPERATION DE LA DIMENSION
      CALL DISMOI('F','DIM_GEOM',MA,'MAILLAGE',NDIM,KBID,IRET)

C     BOUCLE SUR LES NOEUDS DE GRNO
      DO 10 INO = 1,LONG

        NUNO=ZI(JLISNO-1+INO)
        CALL JENUNO(JEXNUM(MA//'.NOMNOE',NUNO),NOMNO)

C       RELATIONS POUR LE SUPPRESSION LES DDLS H
        DO 20 K = 1,NDIM
          CALL AFRELA(1.D0,CBID,DDLH(K),NOMNO,0,RBID,1,BETAR,CBID,
     &                          K8BID,'REEL','REEL','12',0.D0,LISREL)
          NREL = NREL + 1
 20     CONTINUE

 10   CONTINUE

 999  CONTINUE
      CALL JEDEMA()
      END
