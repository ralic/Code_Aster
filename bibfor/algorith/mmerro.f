      IMPLICIT NONE
      CHARACTER*24  DEFICO
      CHARACTER*24  RESOCO
      CHARACTER*8   NOMA
      CHARACTER*6   CONTEX
      CHARACTER*1   TYPERR
      CHARACTER*(*) ERROR
      INTEGER       NMA
      INTEGER       NND
      INTEGER       NPT
      INTEGER       IINF(*)
      REAL*8        RINF(*)
      CHARACTER*24  KINF(*)
C
C ----------------------------------------------------------------------
C ROUTINE UTILITAIRE (CONTACT METHODE CONTINUE)
C ----------------------------------------------------------------------
C
C GESTION DES MESSAGES D'ERREURS
C
C IN  DEFICO : SD POUR LA DEFINITION DU CONTACT
C IN  RESOCO : SD POUR LA RESOLUTION DU CONTACT
C IN  NOMA   : NOM DE LA SD MAILLAGE
C IN  CONTEX : CONTEXTE DE DECLENCHEMENT DE L'ERREUR
C IN  TYPERR : TYPE DE L'ERREUR (ALARME, FATAL,...)
C IN  ERROR  : NOM DE L'ERREUR
C IN  NMA    : NUMERO DE LA MAILLE FAUTIVE
C IN  NND    : NUMERO DU NOEUD FAUTIF
C IN  NPT    : NUMERO DU PG FAUTIF
C IN  IINF   : INFOS SUPPLEMENTAIRES DE TYPE ENTIER
C IN  RINF   : INFOS SUPPLEMENTAIRES DE TYPE REEL
C IN  KINF   : INFOS SUPPLEMENTAIRES DE TYPE CHARACTER
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER     IRET
      CHARACTER*8 NOMMAI,NOMNOE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      CALL JEEXIN(DEFICO(1:16) // '.CARACF',IRET)
      IF (IRET.EQ.0) THEN
        CALL U2MESS('F','ALGORITH5_94')
      ENDIF

      IF (ERROR(1:8).EQ.'MAT_SING') THEN
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NMA),NOMMAI)
        IF (NND.NE.-1) THEN
          CALL JENUNO(JEXNUM(NOMA//'.NOMNOE',NND),NOMNOE)
          CALL UTMESS(TYPERR,CONTEX,'MATRICE SINGULIERE LORS DU '//
     &              ' CALCUL DU REPERE LOCAL TANGENT AU '//
     &              ' NOEUD MAITRE '//NOMNOE//
     &              ' SUR LA MAILLE MAITRE '//NOMMAI//
     &              ' VECTEURS TANGENTS COLINEAIRES ?')
C        CALL U2MESK(TYPERR,'ALGORITH5_95', 2 ,VALK)
        ELSE
          CALL U2MESK(TYPERR,'ALGORITH5_96',1,NOMMAI)
        ENDIF
      ELSEIF (ERROR(1:8).EQ.'NON_CONV') THEN
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NMA),NOMMAI)
        IF (NND.NE.-1) THEN
          CALL JENUNO(JEXNUM(NOMA//'.NOMNOE',NND),NOMNOE)
          CALL UTMESS(TYPERR,CONTEX,'NEWTON ECHOUE LORS DU'//
     &              ' CALCUL DU REPERE LOCAL TANGENT AU '//
     &              ' NOEUD MAITRE '//NOMNOE//
     &              ' SUR LA MAILLE MAITRE '//NOMMAI)
C        CALL U2MESK(TYPERR,'ALGORITH5_97', 2 ,VALK)
        ELSE
          CALL U2MESK(TYPERR,'ALGORITH5_98',1,NOMMAI)
        ENDIF
      ELSEIF (ERROR(1:13).EQ.'VECT_TANG_NUL') THEN
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NMA),NOMMAI)
        IF (NND.NE.-1) THEN
          CALL JENUNO(JEXNUM(NOMA//'.NOMNOE',NND),NOMNOE)
          CALL UTMESS(TYPERR,CONTEX,'VECTEURS TANGENTS NULS AU'//
     &              ' NOEUD MAITRE '//NOMNOE//
     &              ' SUR LA MAILLE MAITRE '//NOMMAI)
C        CALL U2MESK(TYPERR,'ALGORITH5_99', 2 ,VALK)
        ELSE
          CALL U2MESK(TYPERR,'ALGORITH6_1',1,NOMMAI)
        ENDIF
      ELSEIF (ERROR(1:8).EQ.'ELEM_INC') THEN
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NMA),NOMMAI)
        CALL U2MESK(TYPERR,'ALGORITH6_2',1,NOMMAI)
      ELSEIF (ERROR(1:8).EQ.'NORM_NUL') THEN
        CALL JENUNO(JEXNUM(NOMA//'.NOMNOE',NND),NOMNOE)
        CALL U2MESK(TYPERR,'ALGORITH6_3',1,NOMNOE)
      ELSEIF (ERROR(1:8).EQ.'TROP_NDS') THEN
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NMA),NOMMAI)
        CALL U2MESK(TYPERR,'ALGORITH6_4',1,NOMMAI)
      ELSEIF (ERROR(1:11).EQ.'BARSOUM_NEX') THEN
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NMA),NOMMAI)
        CALL UTMESS(TYPERR,CONTEX,'L''ELEMENT PORTE PAR LA  '//
     &              ' LA MAILLE ESCLAVE '//NOMMAI//
     &              ' N''EST PAS DU BON TYPE POUR UN FOND DE FISSURE'//
     &              ' , ELLE EST DE TYPE '//KINF(1))
C        CALL U2MESK(TYPERR,'ALGORITH6_5', 2 ,VALK)
      ELSEIF (ERROR(1:10).EQ.'ERR_INTEGR') THEN
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NMA),NOMMAI)
        CALL U2MESK(TYPERR,'ALGORITH6_6',1,NOMMAI)
      ELSEIF (ERROR(1:14).EQ.'FLIP_FLOP_MAXI') THEN
        CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NMA),NOMMAI)
        CALL UTDEBM(TYPERR,CONTEX,'LE STATUT DU CONTACT ')
        CALL UTIMPI('S','A CHANGE ',1,IINF(1))
        CALL UTIMPI('S',' FOIS AU POINT DE CONTACT ',1,NND)
        CALL UTIMPI('S',' SUR LA MAILLE ESCLAVE '//NOMMAI,0,IINF(1))
        CALL UTIMPI('L',' PRESENCE DE FLIP-FLOP !!',0,IINF(1))
        CALL UTFINM()
      ELSE
        CALL U2MESS('F','ALGORITH6_7')
      ENDIF


C
      CALL JEDEMA()
      END
