C
C CREATION D'UNE SD
C
C ----------------------------------------------------------------------
C
C
C IN  TYPESD :  TYPE DE LA SD
C               'POST_TRAITEMENT' - MODES VIBRATOIRES OU FLAMBAGE
C               'ENERGIE        ' - ENERGIES
C IN  NOMSD  : NOM DE LA SD
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ---------------------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX -----------------------------
C
      INTEGER      ZPOSTI,ZPOSTR,ZPOSTK,ZENER
      PARAMETER   (ZPOSTI=11,ZPOSTR=8,ZPOSTK=14,ZENER=6)
C      
      INTEGER      IFM,NIV
      CHARACTER*24 SDINFI,SDINFR,SDINFK
      INTEGER      JPINFI,JPINFR,JPINFK,JENER
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()

      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> ... CREATION SD'
      ENDIF
C
C --- CREATION
C 
      IF (TYPESD.EQ.'POST_TRAITEMENT') THEN
        SDINFI = NOMSD(1:19)//'.INFI'
        SDINFR = NOMSD(1:19)//'.INFR'
        SDINFK = NOMSD(1:19)//'.INFK'         
        CALL WKVECT(SDINFI,'V V I  ',ZPOSTI,JPINFI)
        CALL WKVECT(SDINFR,'V V R  ',ZPOSTR,JPINFR)      
        CALL WKVECT(SDINFK,'V V K24',ZPOSTK,JPINFK)              
      ELSE IF (TYPESD.EQ.'ENERGIE') THEN
C COMPOSANTES DE SDENER//'.VALE'
C WEXT : TRAVAIL DES EFFORTS EXTERIEURS
C ECIN : ENERGIE CINETIQUE
C WINT : ENERGIE DE DEFORMATION IRREVERSIBLE
C AMOR : ENERGIE DISSIPEE PAR AMORTISSEMENT
C LIAI : ENERGIE DISSIPEE PAR LES LIAISONS
C WSCH : ENERGIE DISSIPEE PAR LE SCHEMA
        CALL WKVECT(NOMSD(1:19)//'.VALE','V V R  ',ZENER,JENER)
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF 
C          
      CALL JEDEMA()
      END
