      SUBROUTINE XMMRES(DEFICO,DEPDEL,NUMEDD,MODELE,CNSINR)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 02/05/2006   AUTEUR GENIAUT S.GENIAUT 
