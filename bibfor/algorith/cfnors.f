      SUBROUTINE CFNORS(NOMA  ,DEFICO,RESOCO,POSMAI,TYPENT,
     &                  NUMENT,LPOUTR,LPOINT,KSI1  ,KSI2  ,
     &                  LLISS ,ITYPE ,VECTOR,TAU1  ,TAU2  ,
     &                  LNFIXE)
