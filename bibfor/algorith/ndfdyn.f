C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*19 SDDYNA
      CHARACTER*19 MEASSE(*)
      CHARACTER*24 VITPLU,ACCPLU,CNDYNA
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (DYNAMIQUE)
C
C CALCUL DES FORCES D'EQUILIBRE DYNAMIQUE
C
C ----------------------------------------------------------------------
C
C
  
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*19 NMCHEX,AMORT,MASSE,RIGID
      CHARACTER*24 VITES,ACCEL
      CHARACTER*24 CNINER,CNHYST
      REAL*8       NDYNRE,COERMA,COERAM,COERRI      
      LOGICAL      NDYNLO,LAMOR,LIMPL
      LOGICAL      LNEWMA,LTHETA,LTHETD,LTHETV
      INTEGER      NDYNIN
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ() 
C
C --- COEFFICIENTS DEVANTS MATRICES
C
      COERMA = NDYNRE(SDDYNA,'COEF_FDYN_MASSE')
      COERAM = NDYNRE(SDDYNA,'COEF_FDYN_AMORT')      
      COERRI = NDYNRE(SDDYNA,'COEF_FDYN_RIGID')      
C
C --- FONCTIONNALITES ACTIVEES
C
      LAMOR  = NDYNLO(SDDYNA,'MAT_AMORT')
      LIMPL  = NDYNLO(SDDYNA,'IMPLICITE')
C
C --- TYPE DE SCHEMA: NEWMARK (ET SES DERIVEES) OU THETA    
C
      IF ((NDYNLO(SDDYNA,'NEWMARK')).OR.
     &    (NDYNLO(SDDYNA,'DIFF_CENT')).OR.
     &    (NDYNLO(SDDYNA,'HHT')).OR.
     &    (NDYNLO(SDDYNA,'HHT_COMPLET')).OR.
     &    (NDYNLO(SDDYNA,'TCHAMWA'))) THEN
        LNEWMA = .TRUE.  
        LTHETA = .FALSE.
      ELSEIF (NDYNLO(SDDYNA,'THETA_METHODE')) THEN 
        LNEWMA = .FALSE.  
        LTHETA = .TRUE.
      ELSE
        CALL ASSERT(.FALSE.)           
      ENDIF   
      IF (LTHETA) THEN
        IF (NDYNIN(SDDYNA,'FORMUL_DYNAMIQUE').EQ.1) THEN
          LTHETD = .TRUE.
          LTHETV = .FALSE.
        ELSEIF (NDYNIN(SDDYNA,'FORMUL_DYNAMIQUE').EQ.2) THEN
          LTHETD = .FALSE.
          LTHETV = .TRUE.        
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
      ENDIF     
C
C --- MATRICES ASSEMBLEES      
C           
      AMORT  = NMCHEX(MEASSE,'MEASSE','MEAMOR')
      MASSE  = NMCHEX(MEASSE,'MEASSE','MEMASS')
      RIGID  = NMCHEX(MEASSE,'MEASSE','MERIGI')
C
C --- VECTEURS RESULTATS
C      
      CNINER = '&&CNPART.CHP1'
      CNHYST = '&&CNPART.CHP2'
      CALL VTZERO(CNINER)
      CALL VTZERO(CNHYST)
      CALL VTZERO(CNDYNA)
C
C --- VECTEURS SOLUTIONS
C
      VITES = VITPLU
      ACCEL = ACCPLU
      IF (LIMPL) THEN
        IF (LNEWMA.OR.LTHETD) THEN
          CALL NMINER(MASSE ,ACCEL,CNINER)
          CALL VTAXPY(COERMA,CNINER,CNDYNA)
        ELSEIF (LTHETV) THEN   
          CALL NMINER(RIGID ,VITES,CNINER)
          CALL VTAXPY(COERRI,CNINER,CNDYNA)              
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF  
      ENDIF
      
      
      IF (LAMOR) THEN
        CALL NMHYST(AMORT ,VITES,CNHYST)
        CALL VTAXPY(COERAM,CNHYST,CNDYNA)
      ENDIF
C
      CALL JEDEMA()
      END
