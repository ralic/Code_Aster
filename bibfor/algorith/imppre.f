      IMPLICIT NONE
      CHARACTER*16 MOTFAC
      INTEGER      IOCC
      INTEGER      LONGR
      INTEGER      PRECR
      INTEGER      LONGI
      INTEGER      LONGK
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (AFFICHAGE - LECTURE)
C
C RECUPERATION DES PRECISIONS POUR AFFICHAGE DES COLONNES DANS LE
C TABLEAU DE CONVERGENCE
C
C ----------------------------------------------------------------------
C
C
C IN MOTFAC : MOT-CLEF FACTEUR AFFICHAGE
C IN IOCC   : OCCURRENCE DU MOT-CLEF FACTEUR AFFICHAGE
C IN LONGR  : LONGUEUR D'AFFICHAGE DU REEL
C IN PRECR  : LONGUEUR D'AFFICHAGE DE LA PRECISION DU REEL
C IN LONGI  : LONGUEUR D'AFFICHAGE DE L'ENTIER
C IN LONGK  : LONGUEUR D'AFFICHAGE DE LA CHAINE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IRESU,NOC
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- FORMATS D'AFFICHAGE PAR DEFAUT
C
      LONGR = 12
      PRECR = 5
      LONGI = 6
      LONGK = 16

      IF (IOCC.EQ.0) THEN
        GOTO 999
      ENDIF

      CALL GETVIS(MOTFAC,'LONG_R',IOCC,1,1,IRESU,NOC)
      IF (NOC.NE.0) THEN
        LONGR = IRESU
      ENDIF
      CALL GETVIS(MOTFAC,'PREC_R',IOCC,1,1,IRESU,NOC)
      IF (NOC.NE.0) THEN
        PRECR = IRESU
      ENDIF
      CALL GETVIS(MOTFAC,'LONG_I',IOCC,1,1,IRESU,NOC)
      IF (NOC.NE.0) THEN
        LONGI = IRESU
      ENDIF

  999 CONTINUE
C
      CALL JEDEMA()

      END
