C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C

C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
C
C --- INITIALISATIONS
C 
      NMIGNO = .FALSE.
   
      IF (LVCINE) THEN
        IF (ZI(JCCID+IEQ-1).EQ.1) THEN
          NMIGNO = .TRUE.
        ENDIF
        GOTO 99
      ENDIF
      IF (LNDEPL) THEN
        IF (ZR(JDIRI+IEQ-1).NE.0.D0) THEN
          NMIGNO = .TRUE.
        ENDIF
      ENDIF

  99  CONTINUE
C
      CALL JEDEMA()
      END
