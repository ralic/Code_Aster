C ----------------------------------------------------------------------
C  RESOLUTION D'EQUATIONS SCALAIRES PAR UNE METHODE DE DICHOTOMIE
C    ON PERMUTE AUSSI LES VALEURS DES DERIVEES PLACEES EN Z
C ----------------------------------------------------------------------
C VAR X(1) BORNE DE L'INTERVALLE DE RECHERCHE  TQ Y(1) < 0
C VAR X(2) BORNE DE L'INTERVALLE DE RECHERCHE  TQ Y(2) > 0
C VAR X(3) SOLUTION X(N-1) PUIS SOLUTION EN X(N)  (NE SERT PAS)
C VAR X(4) SOLUTION X(N)   PUIS SOLUTION EN X(N+1)
C VAR Y(I) VALEUR DE LA FONCTION EN X(I)
C ----------------------------------------------------------------------

      REAL*8   XP,P,S

C    REACTUALISATION DE L'INTERVALLE DE RECHERCHE
      IF (Y(3) .LT. 0.D0) THEN
        X(1) = X(3)
        Y(1) = Y(3)
        Z(1) = Z(3)
      END IF

      IF (Y(3) .GT. 0.D0) THEN
        X(2) = X(3)
        Y(2) = Y(3)
        Z(2) = Z(3)
      END IF

C    CONSTRUCTION D'UN NOUVEL ESTIME
      IF (X(1).EQ.X(2))
     &  CALL U2MESS('F','ALGORITH9_84')
      XP = (X(1) + X(2)) / 2.D0

C    DECALAGE DES ITERES
      X(3) = XP

      END
