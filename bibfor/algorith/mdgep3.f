C-----------------------------------------------------------------------
C    MULTI-APPUIS :
C    CONVERSION LES DDL GENERALISES EN BASE PHYSIQUE : CONTRIBUTION 
C    DES DEPLACEMENTS DIFFERENTIELS DES ANCRAGES
C-----------------------------------------------------------------------
C IN  : NEQ    : NB D'EQUATIONS DU SYSTEME ASSEMBLE
C IN  : NBEXCI : NOMBRE D'ACCELERO DIFFERENTS
C IN  : PSIDEL : VALEUR DU VECTEUR PSI*DELTA
C IN  : TEMPS  : INSTANT DE CALCUL DES DEPL_IMPO
C IN  : NOMFON : NOM DE LA FONCTION DEPL_IMPO
C OUT : TAB    : VALEUR DE PSIDEL*VALE_NOMFOM(TEMPS)
C .________________.____.______________________________________________.
      IMPLICIT REAL*8 (A-H,O-Z)
      REAL*8 TAB(NEQ)
      REAL*8 PSIDEL(NEQ,NBEXCI),TEMPS
      CHARACTER*8 NOMFON(2*NBEXCI)
C ----------------------------------------------------------------------
      CHARACTER*8 NOMPAR, K8BID
      REAL*8 COEF
C
      K8BID = '        '
      CALL R8INIR(NEQ,0.D0,TAB,1)
      NOMPAR = 'INST'
      IPAR = 1
      DO 10 IEX=1,NBEXCI
         IF (NOMFON(IEX).EQ.K8BID) THEN
            CALL UTDEBM('F','MDGEP3','!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!')
            CALL UTIMPK('L','INCOMPATIBILITE AVEC DYNA_TRAN_MODAL :',
     +                   1,'CALCUL EN MONO APPUI')
            CALL UTFINM()
         ENDIF
         IF (NOMFON(IEX+NBEXCI).EQ.'INTERPRE') THEN
           CALL FIINTE('F',NOMFON(IEX),1,IPAR,TEMPS,COEF,IER)
         ELSE
           CALL FOINTE('F ',NOMFON(IEX),1,NOMPAR,TEMPS,COEF,IER)
         ENDIF
         DO 11 IEQ = 1,NEQ
            TAB(IEQ) = TAB(IEQ) + PSIDEL(IEQ,IEX)*COEF
 11      CONTINUE
 10   CONTINUE
      END
