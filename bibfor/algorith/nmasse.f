C
C IN  ICODMA  : ADRESSE DU MATERIAU CODE
C IN  INST    : INSTANT CONSIDERE
C OUT E       : MODULE DE YOUNG
C OUT NU      : COEFFICIENT DE POISSON
C OUT ALPHA   : COEFFICIENT DE DILATATION
C OUT DEUXMU  : COEFFICIENT DE LAME 1
C OUT TROISK  : COEFFICIENT DE LAME 2

      CHARACTER*8       NOMRES(2),NOMPAR
      REAL*8            VALRES(2),VALPAR
      INTEGER ICODRE(2)

      NOMPAR='INST'
      VALPAR=INST
C CARACTERISTIQUES: MODULE D'YOUNG, COEFFICIENT DE POISSON
      NOMRES(1)='E'
      NOMRES(2)='NU'

      CALL RCVALB(FAMI,KPG,KSP,POUM,ICODMA,MATERI,'ELAS',1,NOMPAR,
     &              VALPAR,2,NOMRES,VALRES,ICODRE, 2)

      E     = VALRES(1)
      NU    = VALRES(2)

      DEUXMU = E/(1.D0+NU)
      TROISK = E/(1.D0-2.D0*NU)


      END
