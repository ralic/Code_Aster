      CHARACTER*(*) MODELZ, MATZ, CARELZ, COMREZ
C ----------------------------------------------------------------------
C  CREATION DES VALEURS DE REFERENCE DES VARIABLES DE COMMANDE
C ----------------------------------------------------------------------
C IN/JXIN   MODELZ  K8  SD MODELE
C IN/JXIN   MATZ    K8  SD MATERIAU
C IN        CARELE  K8  SD CARAELEM
C IN/JXOUT  COMREZ  K14 SD VARI_COM
C ----------------------------------------------------------------------

C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------

      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)

C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------

      CHARACTER*8   MODELE, MATE, K8BID,CARELE
      CHARACTER*14  COMREF
      CHARACTER*24  CHTREF, CHVREF
      CHARACTER*8   MAILLA
      INTEGER        IBID, IRET, IEX, NCHAR, JINF,NUMCH
      LOGICAL        EXITRF, EXIREF,EXI


      CALL JEMARQ()
      MODELE = MODELZ
      MATE   = MATZ
      CARELE = CARELZ
      COMREF = COMREZ
      CHVREF = '&&NMVCRE.VREF'

C - EXISTENCE (NON BIDON) DES CHAMPS
      CALL DETRSD('VARI_COM', COMREF)
       
      CALL DISMOI('F','NOM_MAILLA',MODELE,'MODELE',IBID,MAILLA,IRET)

C - STOCKAGE DES VALEURS DE REFERENCE DES VARIABLES DE COMMANDE
C -------------------------------------------------------------
C - TEMPERATURE DE REFERENCE
   
      CALL METREF(MATE, MAILLA, EXITRF, CHTREF)
      CALL COPISD('CHAMP_GD','V',CHTREF, COMREF // '.TEMP')
      
C - SECHAGE DE REFERENCE

      CALL VRCREF(MODELE,MATE,CARELE,CHVREF(1:19))
      CALL COPISD('CHAMP_GD','V',CHVREF, COMREF // '.TOUT')

C -  VERIFICATION PRESENCE VARIABLE DE COMMANDE ET 
C -  VARIABLE DE COMMANDE DE REFERENCE       
      CALL NMVCD2('SECH',MATE,EXI,EXIREF)
      IF ((.NOT.EXI .AND. EXIREF).OR.(EXI .AND. .NOT.EXIREF)) THEN
          CALL UTMESS('F','NMVCRE','PRESENCE D UN CHAMP DE SECHAGE'//
     &    ' OU D UN CHAMP DE SECHAGE DE REFERENCE MAIS PAS DES DEUX!')
      ENDIF
      CALL JEDEMA()
      
      END
