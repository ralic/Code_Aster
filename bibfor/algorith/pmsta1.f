     &                  NBPAR,NOMPAR,VR,IGRAD,TYPPAR,NOMVI,
     &                  SDDISC, LICCVG,ITEMAX,CONVER,ACTITE)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 18/09/2012   AUTEUR LADIER A.LADIER 
