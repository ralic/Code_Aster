C
C IN  ACCPLU : ACCELERATION COURANTE
C IN  MASSE  : MATR_ASSE MASSE
C OUT CNINER : VECT_ASSE FORCES INERTIE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      JACCP,JMASS,JINER
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES OBJETS JEVEUX
C
      CALL JEVEUO(ACCPLU(1:19)//'.VALE','L',JACCP)
      CALL JEVEUO(MASSE(1:19) //'.&INT','L',JMASS)
      CALL JEVEUO(CNINER(1:19)//'.VALE','E',JINER)
C
C --- CALCUL FORCES INERTIE
C
      CALL MRMULT('ZERO',JMASS,ZR(JACCP),ZR(JINER),1,.TRUE.)
C
      CALL JEDEMA()
      END
