C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS
C
      IMPLICIT NONE
      INTEGER      NBPASE
C
      REAL*8       TBGRCA(3)
C
      CHARACTER*8  RESULT
      CHARACTER*8  BASENO
      CHARACTER*16 INERTE
      CHARACTER*19 LISCHA, SOLVEU
      CHARACTER*24 MODELE, MATE, CARELE, COMPOR
      CHARACTER*(*) INPSCO
C
C ----------------------------------------------------------------------
C
C COMMANDES MECA_STATIQUE & STAT_NON_LINE
C
C LECTURE DES OPERANDES
C
C ----------------------------------------------------------------------
C
C OUT RESULT : NOM UTILISATEUR DU RESULTAT DE STAT_NON_LINE
C OUT MODELE : NOM DU MODELE
C OUT MATE   : NOM DU CHAMP DE MATERIAU
C OUT CARELE : CARACTERISTIQUES DES ELEMENTS DE STRUCTURE
C OUT COMPOR : CARTE DECRIVANT LE TYPE DE COMPORTEMENT
C OUT LISCHA : SD L_CHARGES
C OUT SOLVEU : NOM DU SOLVEUR
C OUT NBPASE : NOMBRE DE PARAMETRES SENSIBLES
C IN  BASENO : BASE DU NOM DES STRUCTURES DERIVEES
C OUT INPSCO : SD CONTENANT LA LISTE DES NOMS POUR LA SENSIBILITE
C OUT INERTE : FLAG CALCUL INDICATEUR D'ERREUR TEMPORELLE (HM)
C OUT TBGRCA : TABLEAU DES GRANDEURS CARACTERISTIQUES AVEC LA
C              CONVENTION SUIVANTE : (HM)
C                1 : LONGUEUR
C                2 : PRESSION
C                3 : TEMPERATURE
C ----------------------------------------------------------------------
      INTEGER      IBID, IAUX, IRET
      CHARACTER*16 K16BID, NOMCMD
      CHARACTER*8  K8BLA
C
C ----------------------------------------------------------------------
C
      K8BLA  = '        '
C
C -- NOM UTILISATEUR DU CONCEPT RESULTAT CREE PAR LA COMMANDE
C
      CALL GETRES(RESULT,K16BID,NOMCMD)
C
C -- SENSIBILITE : LECTURE DES NOMS DES PARAMETRES
C
      IAUX = 1
      CALL PSLECT(' '   ,0     ,BASENO,RESULT,IAUX  ,
     &            NBPASE,INPSCO,IRET  )
C
C -- DONNEES MECANIQUES
C
      MODELE = ' '
      CALL NMDOME(MODELE,MATE  ,CARELE,LISCHA,
     &            NBPASE,INPSCO,K8BLA ,IBID  )
C
C -- PARAMETRES DONNES APRES LE MOT-CLE FACTEUR SOLVEUR
C
      CALL CRESOL(SOLVEU,' ')
C
C -- CALCUL EVENTUEL DE L'INDICATEUR D'ERREUR EN TEMPS
C
      CALL NMETDO (INERTE, NOMCMD)
C
C -- DANS LE CAS MECA_STATIQUE ON UTILISE LA CARTE COMPOR
C    STOCKEE DANS LE MATERIAU POUR LES POU_D_EM
C    (PAS UTILISEE PAR LES AUTRES ELEMENTS)
C
      IF ( NOMCMD(1:13).EQ.'MECA_STATIQUE' ) THEN
        COMPOR = MATE(1:8)//'.COMPOR'
      ENDIF
C
C -- GRANDEURS CARACTERISTIQUES DE L'ETUDE
C
      CALL CETULE ( MODELE, TBGRCA, IRET )
C
      END
