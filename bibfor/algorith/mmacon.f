C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
      IMPLICIT NONE
C
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : CONTCO
C ----------------------------------------------------------------------
C
C  CREATION DE MAESCL ET TABFIN CONTENANT RESPECTIVEMENT LES MAILLES
C  ESCLAVES ET LES POINTS DE CONTACT. 
C
C  IN  NOMA   : NOM DU MAILLAGE
C  IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER CFMMVD,ZMAES,ZCMCF,ZNOES,ZTABF
      INTEGER JMACO,JDIM,JMAESC,JTABF,JSUNO
      INTEGER NZOCO,NTMA,NUMA,JNOESC
      INTEGER JSUMA,JZONE,IZONE,ISUMA,NBMA,JDECMA,TYPINT
      INTEGER JCMCF,NTPC,IMA,NBN,JJEU,JUSU
      CHARACTER*24 CONTMA,MAESCL,CARACF,NDIMCO,JEUSUR
      CHARACTER*24 TABFIN,PSURMA,PZONE,NOESCL,PSURNO,JEUCON
      INTEGER NNINT,IN,IRET,IBID
      INTEGER PPOSNO,JDECNO,NBNO,ISUNO
      REAL*8  R8BID
      CHARACTER*24 K24BLA,K24BID   
      DATA K24BLA /' '/                   
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      CONTMA = DEFICO(1:16) // '.MAILCO'
      MAESCL = DEFICO(1:16) // '.MAESCL'
      TABFIN = DEFICO(1:16) // '.TABFIN'
      PSURMA = DEFICO(1:16) // '.PSUMACO'
      PSURNO = DEFICO(1:16) // '.PSUNOCO'
      PZONE  = DEFICO(1:16) // '.PZONECO'
      CARACF = DEFICO(1:16) // '.CARACF'
      NDIMCO = DEFICO(1:16) // '.NDIMCO'
      NOESCL = DEFICO(1:16) // '.NOESCL'
      JEUCON = DEFICO(1:16) // '.JEUCON'
      JEUSUR = DEFICO(1:16) // '.JEUSUR'
      CALL JEVEUO(CONTMA,'L',JMACO)
      CALL JEVEUO(NDIMCO,'L',JDIM)
      CALL JEVEUO(PSURMA,'L',JSUMA)
      CALL JEVEUO(PSURNO,'L',JSUNO)
      CALL JEVEUO(PZONE,'L',JZONE)
      CALL JEVEUO(CARACF,'L',JCMCF)
      CALL JEVEUO(NOESCL,'E',JNOESC)
C
      ZMAES = CFMMVD('ZMAES')
      ZCMCF = CFMMVD('ZCMCF')
      ZNOES = CFMMVD('ZNOES')
      ZTABF = CFMMVD('ZTABF')      
C
      NZOCO = ZI(JDIM+1)
      NTMA  = ZI(JDIM+3)
C      
C --- NOMBRE TOTAL DES MAILLES ESCLAVES DE CONTACT.
C
      NTMA = 0
      DO 10 IZONE = 1,NZOCO
        ISUMA = ZI(JZONE+IZONE-1) + 1
        NBMA  = ZI(JSUMA+ISUMA) - ZI(JSUMA+ISUMA-1)
        NTMA  = NTMA + NBMA
 10   CONTINUE       
C
C --- CREATION DU TABLEAU DES MAILLES ESCLAVES MAESC
C
      CALL WKVECT(MAESCL,'G V I',ZMAES*NTMA+1,JMAESC)
      ZI(JMAESC) = 0

      NTMA = 0
      DO 30 IZONE = 1,NZOCO  
        ISUMA  = ZI(JZONE+IZONE-1) + 1
        NBMA   = ZI(JSUMA+ISUMA) - ZI(JSUMA+ISUMA-1)
        JDECMA = ZI(JSUMA+ISUMA-1)
        TYPINT = NINT(ZR(JCMCF+ZCMCF*(IZONE-1)+1))  
        DO 20 IMA = 1,NBMA
          NUMA = ZI(JMACO-1+JDECMA+IMA)              
          CALL MMELIN(NOMA,NUMA,TYPINT,NNINT,IRET)
          IF (IRET.EQ.1) THEN
            CALL MMERRO(DEFICO,K24BLA,NOMA,
     &                 'MMACON','F','ERR_INTEGR',
     &                  NUMA,IBID,IBID,
     &                  IBID,R8BID,K24BID)          
          ENDIF              
          ZI(JMAESC+ZMAES*NTMA+ZMAES*IMA-2) = JDECMA + IMA
          ZI(JMAESC+ZMAES*NTMA+ZMAES*IMA-1) = IZONE
          ZI(JMAESC+ZMAES*NTMA+ZMAES*IMA)   = NNINT
 20     CONTINUE
        NTMA = NTMA + NBMA
 30   CONTINUE
      ZI(JMAESC) = NTMA 
C
C --- ON COMPTE LE NOMBRE DES POINTS ESCLAVES POUR CREER TABFIN
C
      NTPC = 0
      DO 40 IMA = 1,NTMA
        NBN  = ZI(JMAESC+3*(IMA-1)+3)
        NTPC = NTPC + NBN
 40   CONTINUE
C
C --- CALCUL DE LA NORMALE ?
C ---   POINTS MAITRES : 1 (ON FAIT LE CALCUL DE LA NORMALE)
C ---   POINTS ESCLAVES: -1 (ON FAIT PAS LE CALCUL DE LA NORMALE)
C
      DO 50 IZONE = 1,NZOCO
        ISUNO = ZI(JZONE+IZONE)
        NBNO = ZI(JSUNO+ISUNO) - ZI(JSUNO+ISUNO-1)
        JDECNO = ZI(JSUNO+ISUNO-1)
        DO 60 IN = 1,NBNO
          PPOSNO = JDECNO + IN
          ZR(JNOESC+ZNOES*(PPOSNO-1)+1) = 1.D0
 60     CONTINUE
 50   CONTINUE
      DO 70 IZONE = 1,NZOCO
        ISUNO = ZI(JZONE+IZONE-1) + 1
        NBNO = ZI(JSUNO+ISUNO) - ZI(JSUNO+ISUNO-1)
        JDECNO = ZI(JSUNO+ISUNO-1)
        DO 80 IN = 1,NBNO
          PPOSNO = JDECNO + IN
          ZR(JNOESC+ZNOES*(PPOSNO-1)+1) = -1.D0
 80     CONTINUE
 70   CONTINUE
C
C --- CREATION DES TABLEAUX TABFIN JEUCON ET JEUSUR
C
      CALL WKVECT(TABFIN,'G V R',ZTABF*NTPC+1,JTABF)
      CALL WKVECT(JEUCON,'G V R',NTPC,JJEU)
      CALL WKVECT(JEUSUR,'G V R',NTPC,JUSU)
      
      CALL JEDEMA
      END
