      SUBROUTINE NMFCOR(MODELE,NUMEDD,MATE  ,CARELE,COMREF,
     &                  COMPOR,LISCHA,FONACT,PARMET,CARCRI,
     &                  METHOD,NUMINS,ITERAT,SDDISC,SDDYNA,
     &                  SDSENS,SDTIME,DEFICO,RESOCO,RESOCU,
     &                  PARCON,VALMOI,VALPLU,POUGD ,SOLALG,
     &                  VEELEM,VEASSE,MEELEM,MEASSE,LICCVG)
C     
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 03/11/2008   AUTEUR ABBAS M.ABBAS 
C ======================================================================
C COPYRIGHT (C) 1991 - 2008  EDF R&D                  WWW.CODE-ASTER.ORG
