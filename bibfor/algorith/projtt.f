C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT    NONE
      INTEGER     PROJ
      INTEGER     MOYEN       
      REAL*8      COORD1(3),COORD2(3),COORD3(3)
      REAL*8      COORDP(3),ENORM(3)
      REAL*8      JEUPM,TOLEIN
      REAL*8      DEBORD,COORDM(3),LAMBDA(3)
      INTEGER     ARETE(3),NOEUD(3)      
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT - MAIT/ESCL - QUA)
C
C ENCAPSULATION PROJECTION SUR TRIANGLE A PARTIR DU QUADRANGLE DECOUPE
C
C ----------------------------------------------------------------------
C
C
C IN  PROJ   : PROJECTION LINEAIRE (1) OU QUADRATIQUE (2) SUR LA MAILLE
C              OU PAS DE NOUVELLE PROJECTION (0)
C IN  MOYEN  : NORMALES D'APPARIEMENT
C               0 MAIT
C               1 MAIT_ESCL
C IN  TOLEIN : TOLERANCE <IN> POUR LA PROJECTION GEOMETRIQUE
C              DETERMINE SI PROJECTION SUR ARETE OU NOEUD
C IN  COORD1 : COORDONNEES DU SOMMET 1 DU TRIANGLE
C IN  COORD2 : COORDONNEES DU SOMMET 2 DU TRIANGLE
C IN  COORD3 : COORDONNEES DU SOMMET 3 DU TRIANGLE
C IN  COORDP : COORDONNEES DU NOEUD ESCLAVE P
C IN  ENORM  : NORMALE AU NOEUD ESCLAVE
C OUT COORDM : COORDONNEES DE LA "PROJECTION" M
C OUT JEUPM  : JEU SUR LE VECTEUR PM
C OUT LAMBDA : COORDONNEES PARAMETRIQUES DE LA "PROJECTION" M APRES
C                RABATTEMENT DANS LA MAILLE SI ON DEPASSE
C OUT DEBORD : PROJECTION HORS DE LA MAILLE
C              >0 : PROJECTION HORS DE LA MAILLE
C              <0 : PROJECTION SUR LA MAILLE
C OUT NOEUD  : DETECTION DE PROJECTION SUR NOEUD 
C                 (1: SUR LE NOEUD, 0: NON)
C               TRIANGLE 123 -  NOEUD(1) : NOEUD 1
C                               NOEUD(2) : NOEUD 2
C                               NOEUD(3) : NOEUD 3
C OUT ARETE  : DETECTION DE PROJECTION SUR ARETE
C                 (1: SUR L'ARETE, 0: NON)
C               TRIANGLE 123 -  ARETE(1) : SEGMENT 1-2
C                               ARETE(2) : SEGMENT 2-3
C                               ARETE(3) : SEGMENT 1-3
C
C ----------------------------------------------------------------------
C   
      REAL*8       LAMOLD(3)
      CHARACTER*24 K24BID
      INTEGER      IBID
      REAL*8       UNORM(3),MNORM(3)  
C
C ----------------------------------------------------------------------
C
C
C --- CALCUL DE LA NORMALE
C     
      CALL CFNOMA(K24BID,IBID  ,IBID  ,MOYEN ,'TRI3',
     &            COORD1,COORD2,COORD3,ENORM ,MNORM ,
     &            UNORM)
C
C --- PROJECTION SUR LE TRIANGLE
C     
      CALL PROJTR('TRI3',PROJ  ,MOYEN ,UNORM ,MNORM,
     &            COORD1,COORD2,COORD3,COORDP,
     &            COORDM,LAMOLD,LAMBDA,DEBORD)
C
C --- CALCUL DU JEU SUR VECTEUR PM
C     
      CALL CFOLDJ(COORDP,COORDM,JEUPM)
C
C --- VERIFICATIONS DES PROJECTIONS SUR ARETES ET NOEUDS
C
      CALL PRDITR(LAMOLD,TOLEIN,ARETE,NOEUD)
      
      END
