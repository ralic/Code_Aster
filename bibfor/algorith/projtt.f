      SUBROUTINE PROJTT(PROJ  ,MOYEN ,ENORM ,TOLEIN,
     &                  COORD1,COORD2,COORD3,COORDP,COORDM,
     &                  JEUPM ,LAMBDA,ARETE ,NOEUD ,DEBORD)
