      IMPLICIT NONE
      INTEGER ICODMA
      INTEGER NCOEFG
      PARAMETER (NCOEFG = 3)
      REAL*8  COEFGR(NCOEFG)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 29/04/2004   AUTEUR JMBHH01 J.M.PROIX 
C ======================================================================
