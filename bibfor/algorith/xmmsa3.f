      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX --------------------
C
      REAL*8  RB,RBID
C
C ----------------------------------------------------------------------

      CALL ASSERT(NVEC.GT.0)
      IF(NVEC.EQ.1) THEN
         CALL XMMSA4(NDIM,NNO,NNOS,FFP,NDDL,NVEC,V1,RB,RBID,
     &               NFH,SINGU,RR,DDLS,DDLM,SAUT)
      ELSE IF(NVEC.EQ.2) THEN
         CALL XMMSA4(NDIM,NNO,NNOS,FFP,NDDL,NVEC,V1,V2,RBID,
     &               NFH,SINGU,RR,DDLS,DDLM,SAUT)
      ELSE IF(NVEC.EQ.3) THEN
         CALL XMMSA4(NDIM,NNO,NNOS,FFP,NDDL,NVEC,V1,V2,V3,
     &               NFH,SINGU,RR,DDLS,DDLM,SAUT)
      ELSE
         CALL ASSERT(.FALSE.)
      ENDIF

      END
