C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================

      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER NNO
      REAL*8 POIDS,SDFRDE(4,4),SDFRDK(4,4)
      REAL*8 GEOM(2,4),JAC

      INTEGER I,KPG,IADZI,IAZK24
      REAL*8 SDXDE,SDXDK,SDYDE,SDYDK
      REAL*8 SDEDX(4),SDKDX(4),SDKDY(4),SDEDY(4)
      REAL*8 SDFDY(4,4),SDFDX(4,4),R8GAEM
      CHARACTER*8 NOMAIL





      CALL JEMARQ()

      SDXDE = 0.D0
      SDXDK = 0.D0
      SDYDE = 0.D0
      SDYDK = 0.D0

      DO 10 I = 1,NNO
        SDXDE = SDXDE + GEOM(1,I)*SDFRDE(KPG,I)
        SDXDK = SDXDK + GEOM(1,I)*SDFRDK(KPG,I)
        SDYDE = SDYDE + GEOM(2,I)*SDFRDE(KPG,I)
        SDYDK = SDYDK + GEOM(2,I)*SDFRDK(KPG,I)
   10 CONTINUE

      JAC = SDXDE*SDYDK - SDXDK*SDYDE

      IF (ABS(JAC).LE.1.D0/R8GAEM()) THEN
        CALL TECAEL(IADZI,IAZK24)
        NOMAIL = ZK24(IAZK24-1+3) (1:8)
        CALL U2MESK('F','ALGORITH2_59',1,NOMAIL)
      END IF

      DO 20 I = 1,NNO
        SDFDX(KPG,I) = (SDYDK*SDFRDE(KPG,I)-SDYDE*SDFRDK(KPG,I))/JAC
        SDFDY(KPG,I) = (SDXDE*SDFRDK(KPG,I)-SDXDK*SDFRDE(KPG,I))/JAC
   20 CONTINUE

      SDEDX(KPG) = SDYDK/JAC
      SDKDY(KPG) = SDXDE/JAC
      SDEDY(KPG) = -SDXDK/JAC
      SDKDX(KPG) = -SDYDE/JAC
      JAC = ABS(JAC)*POIDS

      CALL JEDEMA()
      END
