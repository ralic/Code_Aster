C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT NONE
      LOGICAL         EXIIRR
      CHARACTER*19    IRRA
      CHARACTER*8     MODELE
      REAL*8          INSTAN
C ----------------------------------------------------------------------
C     DETERMINATION DU CHAMP D'IRRADIATION
C
C IN        MODELE  K8   NOM DU MODELE
C IN        INSTAN  R8   INSTANT DE LA DETERMINATION
C IN/JXOUT  IRRA  K19    IRRADIATION (CHAM_NO_IRRA_R)
C OUT       EXIIRR   L   TRUE SI IRRADIATION N'EST PAS PAR DEFAUT
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER            IBID ,ICORET,IERD,NBOCC,N1,NBCHAM
      CHARACTER*8        K8BID,NOMA,EVOVC
      COMPLEX*16         CBID
      LOGICAL            LIRRA, GETEXM
C
      CALL JEMARQ()
      CALL DETRSD('CHAMP_GD',IRRA)
      EXIIRR = .FALSE.
C
      CALL DISMOI('F','NOM_MAILLA',MODELE,'MODELE',IBID,NOMA,IERD)
C
      LIRRA = GETEXM('VARI_COMM','IRRA')
C
      IF( LIRRA ) THEN
C       
        CALL GETFAC( 'VARI_COMM', NBOCC )
C
        IF (NBOCC.EQ.1) THEN
          CALL GETVID('VARI_COMM','IRRA',1,1,1,EVOVC,N1)
          IF (N1.EQ.1) THEN
            CALL DISMOI('F','NB_CHAMP_UTI',EVOVC,'RESULTAT',NBCHAM,
     &                  K8BID,IERD)
            IF (NBCHAM.GT.0) THEN
              CALL RSINCH(EVOVC,'IRRA','INST',INSTAN,IRRA,
     &                    'CONSTANT','CONSTANT',1,'V',ICORET)     
              IF (ICORET.GE.10) THEN
                CALL UTDEBM('F','NMDEIR','INTERPOLATION IRRADIATION:')
                CALL UTIMPK('L','VARI_COMM:',1,EVOVC)
                CALL UTIMPR('S','INSTANT:',1,INSTAN)
                CALL UTIMPI('L','ICORET:',1,ICORET)
                CALL UTFINM()
              END IF
              EXIIRR = .TRUE.
            ELSE
              CALL UTMESS('F','NMDEIR',' LE CONCEPT VARI_COMM : '//
     &                EVOVC//' NE CONTIENT AUCUN CHAMP D IRRADIATION')
            END IF
          END IF
        END IF
C
C
        IF (.NOT.EXIIRR) THEN
C       CREATION D'UNE CARTE CONSTANTE AFFECTEE A 0.D0
C       CALL MECACT('V',IRRA,'MAILLAGE',NOMA,'IRRA_R',1,'TEMP',IBID,
C    &            0.0D0,CBID,K8BID)
        END IF
C
      ENDIF
C
C
      CALL JEDEMA()
      END
