     +                  MATE,SOLVEZ,MA,NU)
      IMPLICIT NONE
      INTEGER           NBCHAR
      CHARACTER*(*)     MATE,SOLVEZ

C---------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 21/09/2011   AUTEUR COURTOIS M.COURTOIS 
