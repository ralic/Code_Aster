     &                    IPGF,IVFF  ,FFC   ,JAC  ,
     &                    NOEUD ,TAU1,TAU2,
     &                    IFA,CFACE,LACT ,
     &                    MMAT )

      IMPLICIT NONE
      INTEGER     NDIM,NNOL,NNOF,IVFF,IPGF
      INTEGER     CFACE(5,3),IFA
      INTEGER     PLA(27),LACT(8)
      REAL*8      MMAT(204,204)
      REAL*8      FFC(8),JAC,TAU1(3),TAU2(3)
      LOGICAL     NOEUD

