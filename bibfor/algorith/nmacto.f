C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C RECHERCHE DES EVENEMENTS ACTIVES
C
C PASSAGE DE NEWTON -> BOUCLE CONTACT
C      
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION
C OUT IEVDAC : VAUT IECHEC SI EVENEMENT ACTIVE
C                   0 SINON      
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      REAL*8       R8BID
      INTEGER      IBID,NECHEC,IECHEC
      CHARACTER*8  K8BID
      CHARACTER*16 NOMEVD
      LOGICAL      LACTI
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- NOMBRE D'EVENT-DRIVEN : NECHEC
C
      CALL UTDIDT('L'   ,SDDISC,'LIST',IBID  ,'NECHEC',
     &            R8BID ,NECHEC,K8BID )
      LACTI = .FALSE.
C      
C --- BOUCLE SUR LES EVENT-DRIVEN
C --- DES QU'UN EVENT-DRIVEN EST SATISFAIT, ON SORT
C --- ON NE CHERCHE PAS A VERIFIER LES AUTRES EVENEMENTS
C --- ATTENTION, L'ORDRE D'EVALUATION A DONC UNE IMPORTANCE !
C
      IEVDAC = 0
      DO 100 IECHEC = 1,NECHEC
        CALL DIEVEN(SDDISC,IECHEC,LACTI )
        IF (LACTI) THEN
          IEVDAC = IECHEC
          GOTO 888
        ENDIF
 100  CONTINUE
C
 888  CONTINUE
C
C --- AFFICHAGE
C
      IF (LACTI) THEN
        CALL UTDIDT('L'   ,SDDISC,'ECHE',IEVDAC,'NOM_EVEN',
     &              R8BID ,IBID  ,NOMEVD)
        CALL U2MESK('I','DISCRETISATION_51',1,NOMEVD)
      ENDIF     
C
      CALL JEDEMA()
      END
