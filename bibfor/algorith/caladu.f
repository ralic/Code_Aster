C ROUTINE APPELEE PAR : ALGOCO
C ----------------------------------------------------------------------
C CALCUL DU TERME II DE (A.DEPL) OU A EST LA MATRICE DE CONTACT ET
C DEPL UN VECTEUR QUELCONQUE.
