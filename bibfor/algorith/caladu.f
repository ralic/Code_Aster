C IN  DEPL   : VECTEUR A MULTIPLIER PAR LA MATRICE A
C IN  VAL    : VALEUR DU TERME OBTENU
C
C ----------------------------------------------------------------------
C
      INTEGER J
C
C ----------------------------------------------------------------------
C
      VAL = 0.D0
      DO 10 J = 1,NBDDL
        VAL = VAL + COEF(J) * DEPL(DDL(J))
 10   CONTINUE
