      SUBROUTINE XMAFIS(NOMA,CNSLN,NXMAFI,MAFIS,NMAFIS,LISMA)
      IMPLICIT NONE

      INTEGER       NXMAFI,NMAFIS
      CHARACTER*8   NOMA
      CHARACTER*19  CNSLN
      CHARACTER*24  MAFIS,LISMA
C     ------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 26/04/2011   AUTEUR DELMAS J.DELMAS 
