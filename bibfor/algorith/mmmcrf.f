      IMPLICIT     NONE
      INCLUDE 'jeveux.h'
      CHARACTER*8  NOMA
      CHARACTER*19 DEPPLU
      CHARACTER*19 DDEPLA
      CHARACTER*16 NFROT
      REAL*8       VFROT
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - ALGORITHME - UTILITAIRE)
C
C CALCUL RESIDU DE FROTTEMENT
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEPGEO : CHAMP GEOMETRIQUE A L'ITERATION GEOM. N-1
C IN  DEPPLU : CHAMP GEOMETRIQUE A L'ITERATION GEOM. N
C OUT NFROT  : LIEU OU LE CRITERE EST MAX
C OUT VFROT  : VALEUR DU CRITERE MAX
C
C
C
C
      REAL*8       VMAX3,VMAX4,VMAXI
      REAL*8       CRILBD,R8VIDE
      CHARACTER*8  LICMP(3),NOMNOE
      INTEGER      NCMP,NUMNO3,NUMNO4,NUMNOE
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      VMAX3  = 0.D0
      VMAX4  = 0.D0
      CRILBD = 0.D0
      NFROT  = ' '
      VFROT  = R8VIDE()
C
C --- VARIATION RELATIVE SUR LES LAGS_C
C
      NCMP     = 1
      LICMP(1) = 'LAGS_C'
      LICMP(2) = ' '
      LICMP(3) = ' '
      CALL CNOMAX(DDEPLA,NCMP,LICMP(1),VMAX3,NUMNO3)
      CALL CNOMAX(DEPPLU,NCMP,LICMP(1),VMAX4,NUMNO4)
C
      IF (VMAX4.GT.0.D0) THEN
        CRILBD = VMAX3/VMAX4
      ELSE
        CRILBD = 0.D0
      END IF
C
C --- TEST DE CONVERGENCE
C
      NUMNOE = NUMNO3
      VMAXI  = CRILBD
      IF (NUMNOE.EQ.0) THEN
        NOMNOE = ' '
      ELSE
        CALL JENUNO(JEXNUM(NOMA//'.NOMNOE',NUMNOE),NOMNOE)
      ENDIF
      NFROT  = NOMNOE//'        '
      VFROT  = VMAXI
C
      CALL JEDEMA()
      END
