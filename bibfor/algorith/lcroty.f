C ======================================================================
C RESPONSABLE ADBHHVV V.CANO
      IMPLICIT NONE
      INTEGER ITEMAX
      REAL*8  T, PREC

C *****************************************************
C       INTEGRATION DE LA LOI DE ROUSSELIER LOCAL     *
C           RESOLUTION Y.EXP(Y) = T                   *
C *****************************************************

C IN  T       : SECOND MEMBRE CONSTANT
C IN  PREC    : PRECISION RELATIVE SOUHAITEE
C IN  ITEMAX  : NOMBRE MAX D'ITERATIONS

      INTEGER ITER
      REAL*8  Y, U, H, DH

C 1 - NEWTON SANS CHANGEMENT DE VARIABLE

      IF (T .LE. EXP(1.D0)) THEN

        Y = 0.D0
        DO 10 ITER = 1, ITEMAX
          H  = Y*EXP(Y) - T
          DH = (1+Y) * EXP(Y)        
          IF (ABS(H) .LE. PREC*T) GOTO 100
          Y = Y - H/DH
 10     CONTINUE


C 2 - NEWTON AVEC CHANGEMENT DE VARIABLE U=EXP(Y)

      ELSE
        U = T
        DO 20 ITER = 1, ITEMAX
          H  = U*LOG(U) - T
          DH = 1 + LOG(U)        
          IF (ABS(H) .LE. PREC*T) THEN
            Y = LOG(U)
            GOTO 100
          END IF
          U = U - H/DH
 20     CONTINUE
      END IF

      CALL UTMESS('F','ROUSSELIER LCROTY','ITER_INTE_MAXI INSUFFISANT')


 100  CONTINUE
      LCROTY = Y
      END 
