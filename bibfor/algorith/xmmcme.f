      IMPLICIT NONE
      CHARACTER*8  NOMO  ,NOMA
      CHARACTER*24 DEPMOI,DEPDEL,RESOCO
C     
C ----------------------------------------------------------------------
C
C ROUTINE XFEM (METHODE XFEM - CREATION OBJETS)
C
C CREATION DES OBJETS ELEMENTAIRES VECT_ELEM ET MATR_ELEM
C      
C ----------------------------------------------------------------------
C
C
C IN  DEPDEL : INCREMENT DE DEPLACEMENT CUMULE
C IN  DEPMOI : CHAM_NO DES DEPLACEMENTS A L'INSTANT PRECEDENT
C IN  NOMO   : NOM DE L'OBJET MODÈLE
C IN  NOMA   : NOM DE L'OBJET MAILLAGE 
C IN  RESOCO : SD CONTACT 
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      NBOUT,NBIN
      PARAMETER    (NBOUT=1, NBIN=14)
      CHARACTER*8  LPAOUT(NBOUT),LPAIN(NBIN)
      CHARACTER*19 LCHOUT(NBOUT),LCHIN(NBIN)
C
      CHARACTER*19 LIGRMO,CHGEOM,LNNO,LTNO,STANO
      CHARACTER*19 PINTER,AINTER,CFACE,FACLON,BASECO
      CHARACTER*19 XDONCO,XINDCO,XSEUCO,MCON19,SCON19,VECCON,VECFRO
      CHARACTER*24 MELEME
      INTEGER      JMELEM
      CHARACTER*8  MCONEL,SCONEL        
      CHARACTER*8  K8BID
      CHARACTER*16 OPTION
      LOGICAL      DEBUG
      INTEGER      IFM,NIV,IFMDBG,NIVDBG,IRET       
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('XFEM',IFM,NIV)
      CALL INFDBG('PRE_CALCUL',IFMDBG,NIVDBG)                 
C
C --- INITIALISATIONS
C 
      LIGRMO = NOMO(1:8)//'.MODELE'
      XINDCO = RESOCO(1:14)//'.XFIN'
      XDONCO = RESOCO(1:14)//'.XFDO'      
      XSEUCO = RESOCO(1:14)//'.XFSE'
      CHGEOM = NOMA(1:8)//'.COORDO'

      VECCON = '&&MMCCON           '
      VECFRO = '&&MMCFRO           '

      IF (NIVDBG.GE.2) THEN
        DEBUG  = .TRUE.
      ELSE
        DEBUG  = .FALSE.
      ENDIF 
C
C --- NOMS DES MATR_ELEM ET VECT_ELEM CONTACT
C      
      MELEME = RESOCO(1:14)//'.MELEM'
      CALL JEVEUO(MELEME,'L',JMELEM)
      MCONEL = ZK8(JMELEM+1-1)
      MCON19 = MCONEL
      SCONEL = ZK8(JMELEM+2-1) 
      SCON19 = SCONEL
C
C --- INITIALISATION DES CHAMPS POUR CALCUL
C
      CALL INICAL(NBIN  ,LPAIN ,LCHIN ,
     &            NBOUT ,LPAOUT,LCHOUT)     
C
C --- RECUPERATION DES DONNEES XFEM
C
      LNNO   = NOMO(1:8)//'.LNNO'     
      LTNO   = NOMO(1:8)//'.LTNO'
C      PINTER = NOMO(1:8)//'.TOPOFAC.PI'
      PINTER = NOMO(1:8)//'.TOPOFAC.OE'
      AINTER = NOMO(1:8)//'.TOPOFAC.AI'
      CFACE  = NOMO(1:8)//'.TOPOFAC.CF'
      FACLON = NOMO(1:8)//'.TOPOFAC.LO'
      BASECO = NOMO(1:8)//'.TOPOFAC.BA'
      STANO  = NOMO(1:8)//'.STNO'
C       
C --- CREATION DES LISTES DES CHAMPS IN
C
      LPAIN(1)  = 'PGEOMER'
      LCHIN(1)  = CHGEOM
      LPAIN(2)  = 'PDEPL_M'
      LCHIN(2)  = DEPMOI
      LPAIN(3)  = 'PDEPL_P'
      LCHIN(3)  = DEPDEL
      LPAIN(4)  = 'PINDCOI'
      LCHIN(4)  = XINDCO
      LPAIN(5)  = 'PDONCO'
      LCHIN(5)  = XDONCO
      LPAIN(6)  = 'PLSN'
      LCHIN(6)  = LNNO
      LPAIN(7)  = 'PLST'
      LCHIN(7)  = LTNO
      LPAIN(8)  = 'PPINTER'
      LCHIN(8)  = PINTER
      LPAIN(9)  = 'PAINTER'
      LCHIN(9)  = AINTER
      LPAIN(10) = 'PCFACE'
      LCHIN(10) = CFACE
      LPAIN(11) = 'PLONCHA'
      LCHIN(11) = FACLON
      LPAIN(12) = 'PBASECO'
      LCHIN(12) = BASECO
      LPAIN(13) = 'PSEUIL'
      LCHIN(13) = XSEUCO    
      LPAIN(14) = 'PSTANO'
      LCHIN(14) = STANO   
C      
C --- PREPARATION DES MATR_ELEM
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<XFEM  > CREATION DES MATR_ELEM' 
      ENDIF 
      CALL DETRSD('MATR_ELEM',MCON19) 
      CALL MEMARE('V',MCON19,NOMO,' ',' ','RIGI_MECA')  
C
C --- CALCUL DE MCON19 : PARTIE CONTACT
C 
      OPTION    = 'RIGI_CONT'
      LPAOUT(1) = 'PMATUUR'
      LCHOUT(1) = MCON19(1:8)//'.ME001'
C   
      CALL CALCUL('S',OPTION,LIGRMO,NBIN  ,LCHIN ,LPAIN,
     &                              NBOUT ,LCHOUT,LPAOUT,'V')
C
      IF (DEBUG) THEN
        CALL DBGCAL(OPTION,IFMDBG,
     &              NBIN  ,LPAIN ,LCHIN ,
     &              NBOUT ,LPAOUT,LCHOUT)
      ENDIF 
C
      CALL REAJRE(MCON19,LCHOUT(1),'V')
C
C --- CALCUL DE MCON19 : PARTIE FROTTEMENT
C
      OPTION    = 'RIGI_FROT'
      LPAOUT(1) = 'PMATUUR'
      LCHOUT(1) = MCON19(1:8)//'.ME002'
      CALL CALCUL('S',OPTION,LIGRMO,NBIN  ,LCHIN ,LPAIN,
     &                              NBOUT ,LCHOUT,LPAOUT,'V')  
C
      IF (DEBUG) THEN
        CALL DBGCAL(OPTION,IFMDBG,
     &              NBIN  ,LPAIN ,LCHIN ,
     &              NBOUT ,LPAOUT,LCHOUT)
      ENDIF 
C      
      CALL REAJRE(MCON19,LCHOUT(1),'V')
C      
C --- PREPARATION DES VECT_ELEM
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<XFEM  > CREATION DES VECT_ELEM' 
      ENDIF 
      CALL DETRSD('VECT_ELEM',SCON19)
      CALL MEMARE('V',SCON19,NOMO,' ',' ','CHAR_MECA')

      CALL DETRSD('VECT_ELEM',VECCON)
      CALL MEMARE('V',VECCON,NOMO,' ',' ','CHAR_MECA')

      CALL DETRSD('VECT_ELEM',VECFRO)
      CALL MEMARE('V',VECFRO,NOMO,' ',' ','CHAR_MECA')
C
C --- CALCUL DE SCON19 : PARTIE CONTACT
C 
      OPTION    = 'CHAR_MECA_CONT'
      LPAOUT(1) = 'PVECTUR'
      LCHOUT(1) = SCON19(1:8)//'.VE001'
C 
      CALL CALCUL('S',OPTION,LIGRMO,NBIN  ,LCHIN ,LPAIN,
     &                              NBOUT ,LCHOUT,LPAOUT,'V')
C
      IF (DEBUG) THEN
        CALL DBGCAL(OPTION,IFMDBG,
     &              NBIN  ,LPAIN ,LCHIN ,
     &              NBOUT ,LPAOUT,LCHOUT)
      ENDIF
C            
      CALL REAJRE(SCON19,LCHOUT(1),'V')
      CALL REAJRE(VECCON,LCHOUT(1),'V')
C
C --- CALCUL DE SCON19 : PARTIE FROTTEMENT
C 
      OPTION    = 'CHAR_MECA_FROT'
      LPAOUT(1) = 'PVECTUR'
      LCHOUT(1) = SCON19(1:8)//'.VE002'
C
      CALL CALCUL('S',OPTION,LIGRMO,NBIN  ,LCHIN ,LPAIN,
     &                              NBOUT ,LCHOUT,LPAOUT,'V')
C
      IF (DEBUG) THEN
        CALL DBGCAL(OPTION,IFMDBG,
     &              NBIN  ,LPAIN ,LCHIN ,
     &              NBOUT ,LPAOUT,LCHOUT)
      ENDIF
C        
      CALL REAJRE(SCON19,LCHOUT(1),'V')
      CALL REAJRE(VECFRO,LCHOUT(1),'V')
C
      CALL JEDEMA()
      END
