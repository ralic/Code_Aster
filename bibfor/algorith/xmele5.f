      INTEGER       IBID,IAD,IGRP,I,IMA,IFIS,ISPT,IZONE,XXCONI
      INTEGER       NMAENR,NBMA
      CHARACTER*8   NOMFIS,K8BID
      INTEGER       JCESL,JCESV,JCESD,JCESC,JCESK
      INTEGER       JMOFIS,JCOHE
      CHARACTER*24  GRP(3),XINDIC,EVOL,CHAMP,COHE
      INTEGER       JGRP,JINDIC,NOCC,NUME,IRET
      LOGICAL       LBID,EVONOL
      REAL*8        INSTIN
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('XFEM',IFM,NIV)

C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<XFEM  > CREATION DU CHAM_ELEM PCOHES'
      ENDIF
C
C --- INITIALISATIONS
C
      CALL DISMOI('F','NB_MA_MAILLA',NOMA,'MAILLAGE',NBMA,K8BID,IBID)
C
C --- CREATION DU CHAM_ELEM VIERGE
C
      CALL EXISD('CHAM_ELEM',CHELEM,IRET)
      IF (IRET.EQ.0) THEN
        CALL XMCHBA(NOMA  ,NBMA  ,LIGREL,'PCOHES','RIGI_CONT',
     &            CHELEM)
      ENDIF

      CALL JEDEMA()

      END
