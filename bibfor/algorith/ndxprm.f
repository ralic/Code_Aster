     &                  METHOD,LISCHA,NUMEDD,NUMFIX,SOLVEU,
     &                  COMREF,SDDISC,SDDYNA,SDTIME,NUMINS,
     &                  FONACT,VALINC,SOLALG,VEELEM,MEELEM,
     &                  MEASSE,MAPREC,MATASS,CODERE,FACCVG,
     &                  LDCCVG)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 24/05/2011   AUTEUR ABBAS M.ABBAS 
C ======================================================================
