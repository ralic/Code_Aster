     &                  JPCAI ,HPG   ,FFC   ,JACOBI,COEFCA,
     &                  DLAGRF,CFACE ,TYPMAI,TAU1  ,TAU2  ,
     &                  JDDLE,NCONTA,VTMP  )
C
