C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C TOLE CRP_21
C
      IMPLICIT     NONE
      CHARACTER*24 RESOCO,DEFICO
      INTEGER      TYPALF
      INTEGER      FROT3D
      INTEGER      POSNOE
      INTEGER      IESCL
      INTEGER      NESMAX
      REAL*8       NORM(3),TAU1(6),TAU2(6)
      REAL*8       COEF(30),COFX(30),COFY(30)
      REAL*8       JEU
      INTEGER      NBNOM
      INTEGER      POSNSM(9)
      INTEGER      NBDDLT
      INTEGER      DDL(30)
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT)
C
C ON AJOUTE UNE LIAISON MAITRE/ESCLAVE OU NODALE 
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT (ISSUE D'AFFE_CHAR_MECA)
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  TYPALF : TYPE ALGO UTILISE POUR LE FROTTEMENT
C                0 PAS DE FROTTEMENT 
C                1 FROTTEMENT 
C IN  FROT3D : DIMENSION POUR LE FROTTEMENT
C                0 FROTTEMENT 2D
C                1 FROTTEMENT 3D
C IN  POSNSM : INDICES DANS CONTNO DES NOEUDS MAITRES
C IN  POSNOE : INDICES DANS CONTNO DU NOEUD ESCLAVE
C IN  IESCL  : INDICE DU NOEUD ESCLAVE
C IN  NESMAX : NOMBRE MAX DE NOEUDS ESCLAVES
C IN  NORM   : VECTEUR NORMAL
C IN  TAU1   : PREMIER VECTEUR TANGENT
C IN  TAU2   : SECOND VECTEUR TANGENT
C IN  COEF   : VALEURS EN M DES FONCTIONS DE FORME ASSOCIEES AUX NOEUDS
C IN  COFX   : VALEURS EN M DES FONCTIONS DE FORME ASSOCIEES AUX NOEUDS
C                POUR LA PREMIERE DIRECTION TANGENTE
C IN  COFY   : VALEURS EN M DES FONCTIONS DE FORME ASSOCIEES AUX NOEUDS
C                POUR LA SECONDE DIRECTION TANGENTE
C IN  JEU    : JEU DANS LA DIRECTION DE LA NORMALE CHOISIE (PM.NORM)
C IN  NBNOM  : NOMBRE DE NOEUDS MAITRES CONCERNES (MAX: 9)
C IN  NBDDLT : NOMBRE DE DDL NOEUD ESCLAVE+NOEUDS MAITRES
C IN  DDL    : NUMEROS DES DDLS ESCLAVE ET MAITRES CONCERNES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*24 APJEU,APJEFX,APJEFY,NORMCO,TANGCO
      INTEGER      JAPJEU,JAPJFX,JAPJFY,JNORMO,JTANGO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C
      APJEFX = RESOCO(1:14)//'.APJEFX'
      APJEFY = RESOCO(1:14)//'.APJEFY'
      APJEU  = RESOCO(1:14)//'.APJEU'          
      NORMCO = RESOCO(1:14)//'.NORMCO'
      TANGCO = RESOCO(1:14)//'.TANGCO' 
      CALL JEVEUO(APJEU, 'E',JAPJEU) 
      CALL JEVEUO(NORMCO,'E',JNORMO)
      CALL JEVEUO(TANGCO,'E',JTANGO)
      IF (TYPALF.NE.0) THEN
        CALL JEVEUO(APJEFX,'E',JAPJFX)
        CALL JEVEUO(APJEFY,'E',JAPJFY)
      ENDIF              
C
C --- STOCKAGE NORMALES/TANGENTES DES NOEUDS ESCLAVES (POUR CFRESU)
C      
      CALL DCOPY(3,NORM,1,ZR(JNORMO+3*(IESCL-1)),1)
      ZR(JTANGO+6*(IESCL-1)+1-1) = TAU1(1)
      ZR(JTANGO+6*(IESCL-1)+2-1) = TAU1(2)
      ZR(JTANGO+6*(IESCL-1)+3-1) = TAU1(3)
      ZR(JTANGO+6*(IESCL-1)+4-1) = TAU2(1)
      ZR(JTANGO+6*(IESCL-1)+5-1) = TAU2(2)
      ZR(JTANGO+6*(IESCL-1)+6-1) = TAU2(3)        
C
C --- MISE A JOUR DU JEU POUR LE CONTACT
C  
      ZR(JAPJEU+IESCL-1)   = JEU      
C
C --- INITIALISATIONS JEUX FROTTEMENT
C         
      IF (FROT3D.EQ.1) THEN
        ZR(JAPJFX+IESCL-1) = 0.D0
        ZR(JAPJFY+IESCL-1) = 0.D0
      ENDIF                          
C
C --- COEFFICIENTS RELATIONS LINEAIRES APPARIEMENT 
C
      CALL CFCOEM(DEFICO,RESOCO,TYPALF,FROT3D,NESMAX,
     &            POSNOE,IESCL ,NBDDLT,NBNOM ,POSNSM,
     &            DDL   ,COEF  ,COFX  ,COFY)     
C
      CALL JEDEMA()
      END
