      SUBROUTINE CFADDM(DEFICO,RESOCO,TYPALF,FROT3D,POSNOE,
     &                  IESCL ,NESMAX,NORM  ,TAU1  ,TAU2  ,
     &                  COEF  ,COFX  ,COFY  ,JEU   ,NBNOM ,
     &                  POSNSM,NBDDLT,DDL)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 23/09/2008   AUTEUR ABBAS M.ABBAS 
C ======================================================================
