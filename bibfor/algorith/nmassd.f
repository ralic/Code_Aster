C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      INTEGER      FONACT(*)
      CHARACTER*24 DEFICO
      CHARACTER*19 LISCHA
      CHARACTER*24 MODELE,NUMEDD,MATE,CARELE
      CHARACTER*19 DEPEST
      CHARACTER*19 VEASSE(*)
      CHARACTER*19 CNPILO,CNDONN,MATASS
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C CALCUL DU SECOND MEMBRE POUR LA PREDICTION - DEPLACEMENT DONNE OU
C EXTRAPOLE
C
C ----------------------------------------------------------------------
C
C
C IN  MODELE : NOM DU MODELE
C IN  NUMEDD : NOM DE LA NUMEROTATION
C IN  MATE   : NOM DU CHAMP DE MATERIAU
C IN  CARELE : CARACTERISTIQUES DES ELEMENTS DE STRUCTURE
C IN  LISCHA : SD L_CHARGES
C IN  DEFICO : SD CONTACT DEFINITION
C IN  FONACT : FONCTIONNALITES ACTIVEES
C IN  DEPEST : DEPLACEMENT ESTIME (PAR DEPL_CALC OU EXTROPLATION)
C IN  VEASSE : VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C OUT CNPILO : VECTEUR ASSEMBLE DES FORCES PILOTEES
C OUT CNDONN : VECTEUR ASSEMBLE DES FORCES DONNEES
C IN  MATASS : SD MATRICE ASSEMBLEE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IFM,NIV
      INTEGER      I,NBVEC ,NEQ,NBCOEF
      PARAMETER    (NBCOEF=4)
      REAL*8       COEF(NBCOEF)
      CHARACTER*19 VECT(NBCOEF)
      CHARACTER*19 CNDIPI,CNDIDO,CNBEST(2),CNDIDI
      CHARACTER*19 VEDEST(2)
      LOGICAL      ISFONC,LDIDI
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> ...... CALCUL SECOND MEMBRE'
      ENDIF
C
C --- INITIALISATIONS
C
      VEDEST(1) = '&&NMASSD.VEEST1'
      VEDEST(2) = '&&NMASSD.VEEST2'
      CNBEST(1) = '&&NMASSD.CNEST1'
      CNBEST(2) = '&&NMASSD.CNEST2'
      CALL VTZERO(CNPILO)
      CALL VTZERO(CNDONN)
      CALL VTCREB(CNBEST(1),NUMEDD,'V','R',NEQ)
      CALL VTCREB(CNBEST(2),NUMEDD,'V','R',NEQ)
C
C --- FONCTIONNALITES ACTIVEES
C
      LDIDI  = ISFONC(FONACT,'DIDI')
C
C --- DEPLACEMENT IMPOSES
C
      CALL NMCHEX(VEASSE,'VEASSE','CNDIDO',CNDIDO)
      CALL NMCHEX(VEASSE,'VEASSE','CNDIDI',CNDIDI)
C
C --- DEPLACEMENT PILOTES
C
      CALL NMCHEX(VEASSE,'VEASSE','CNDIPI',CNDIPI)
C
C --- CONDITIONS DE DIRICHLET B.U
C
      CALL NMBUDI(MODELE,NUMEDD,LISCHA,DEPEST,VEDEST(2),
     &            CNBEST(2),MATASS)
C
C --- VALEURS POUR SOMME DES FORCES
C
      NBVEC   = 3
      COEF(1) = 1.D0
      COEF(2) = 0.D0
      COEF(3) = -1.D0
      VECT(1) = CNDIDO
      VECT(2) = CNBEST(1)
      VECT(3) = CNBEST(2)
      IF (LDIDI) THEN
        NBVEC = 4
        VECT(4) = CNDIDI
        COEF(4) = 1.D0
      ENDIF
C
C --- CHARGEMENT FIXE
C
      IF (NBVEC.GT.NBCOEF) THEN
        CALL ASSERT(.FALSE.)
      ENDIF
      DO 10 I = 1,NBVEC
        CALL VTAXPY(COEF(I),VECT(I),CNDONN)
 10   CONTINUE
C
C --- CHARGEMENT PILOTE
C
      CNPILO = CNDIPI
C
      CALL DETRSD('CHAMP',CNBEST(1))
      CALL DETRSD('CHAMP',CNBEST(2))
C
      CALL JEDEMA()
      END
