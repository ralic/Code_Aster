      X(1)=X1
      N = 2
      DO 10 K=1,NR8
       IF ((LR8(K) .GT. X1) .AND. (LR8(K) .LT. X2)) THEN
        X(N)=LR8(K)
        N=N+1
       ENDIF
10    CONTINUE
      X(N)=X2

