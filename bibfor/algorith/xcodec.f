      IMPLICIT NONE
      CHARACTER*8 NOMA,MODELX
      INTEGER     NFISS
      CHARACTER*8 FISS(NFISS)  
      INTEGER     NDIM    
      REAL*8      CRIMAX
C      
C ----------------------------------------------------------------------
C
C ROUTINE XFEM (MODIF. DU MODELE)
C
C CALCUL DU DÉCOUPAGE EN SOUS-TETRAS, DES FACETTES DE CONTACT
C ET VERIFICATION DES CRITERES DE CONDITIONNEMENT
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE 
C IN  MODELX : NOM DU MODELE XFEM MODIFIE
C IN  NFISS  : NOMBRE DE FISSURES
C I/O FISS   : IN  : NOM DES FISSURES (NOM SD DEFI_FISS_XFEM)
C              OUT : PINTTO = FISS(1:8)//'.TOPOSE.PIN'
C                    CNSETO = FISS(1:8)//'.TOPOSE.CNS'
C                    HEAVTO = FISS(1:8)//'.TOPOSE.HEA'
C                    LONCHA = FISS(1:8)//'.TOPOSE.LON'
C                    CRITER = FISS(1:8)//'.TOPOSE.CRI'
C                    XINTER = FISS(1:8)//'.TOPOFAC.PI'
C                    AINTER = FISS(1:8)//'.TOPOFAC.AI'
C                    CFACE  = FISS(1:8)//'.TOPOFAC.CF'
C                    FACLON = FISS(1:8)//'.TOPOFAC.LO'
C                    BASECO = FISS(1:8)//'.TOPOFAC.BA'   
C IN  NDIM   : DIMENSION DU PROBLEME
C IN  CRIMAX : CRITERE MAXIMUM
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFISS,IBID,NBMA
      CHARACTER*8  K8BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      DO 10 IFISS = 1,NFISS
C      
C --- CALCUL DES DONNEES UTILES POUR L'INTÉGRATION (SOUS-TETRAS...)
C
        CALL XTOPOI(MODELX,FISS(IFISS))  
C      
C --- ORIENTATION DES FACETTES DE PEAU X-FEM (COMME ORIE_PEAU)
C
        CALL XORIPE(MODELX,FISS(IFISS))
C      
C --- CALCUL DE LA TOPOLOGIE DES FACETTES DE CONTACT
C
        CALL XTOPOC(MODELX,FISS(IFISS))
C
C --- CREATION LISTE DE NOEUDS OU IL FAUDRA ANNULER LES DDLS HEAVISIDE
C 
        IF (NDIM .EQ.3) THEN
          CALL DISMOI('F','NB_MA_MAILLA',MODELX,'MODELE',NBMA,
     &                K8BID,IBID)
          CALL XSTAN2(CRIMAX,NOMA  ,NBMA  ,FISS(IFISS))
        ENDIF  
  10  CONTINUE
C
      CALL JEDEMA()
      END
