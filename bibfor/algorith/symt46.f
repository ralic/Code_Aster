      INTEGER I,J,K,L,IJK(3,3)
      REAL*8 FTOT(3,3,3,3),FTOS(6,6),EPS
C ---------------------------------------------------------------------
      IJK(1,1)=1
      IJK(2,2)=2
      IJK(3,3)=3
      IJK(1,2)=4
      IJK(2,1)=4
      IJK(1,3)=5
      IJK(3,1)=5
      IJK(2,3)=6
      IJK(3,2)=6
      DO 61 I=1,3
      DO 62 J=1,3
      DO 63 K=1,3
      DO 64 L=1,3
        FTOS(IJK(I,J),IJK(K,L))=FTOT(I,J,K,L)
 64   CONTINUE
 63   CONTINUE
 62   CONTINUE
 61   CONTINUE


      DO 67 I=1,6
      DO 67 J=4,6
        FTOS(I,J) = FTOS(I,J)*SQRT(2.D0)
 67   CONTINUE
      DO 68 I=4,6
      DO 68 J=1,6
        FTOS(I,J) = FTOS(I,J)*SQRT(2.D0)
 68   CONTINUE


