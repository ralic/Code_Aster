C     SYMETRISE UN TENSEUR SUR 2 PREMIERS ET 2 DERNIERS INDICES
C     NOTATION DE VOIGT AVEC RAC2 : XX,YY,ZZ,RAC2*XY,RAC2*XZ,RAC2*YZ
C IN  FTOT    : TENSEUR(3,3,3,3) F_IJKL AVEC SYMETRIES MINEURES
