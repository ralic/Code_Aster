C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE     
      LOGICAL       FONACT(*)
      CHARACTER*24  NUMEDD
      CHARACTER*19  VEASSE(*),VEELEM(*)
      CHARACTER*19  VEFINT,CNFINT
      CHARACTER*24  DEFICO
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C ASSEMBLAGE DU VECTEUR DES FORCES INTERNES
C      
C ----------------------------------------------------------------------
C
C
C IN  NUMEDD : NOM DE LA NUMEROTATION
C IN  DEFICO : SD DEFINITION CONTACT
C IN  FONACT : FONCTIONNALITES ACTIVEES
C IN  VEASSE : VARIABLE CHAPEAU POUR NOM DES VECT_ASSE
C IN  VEELEM : VARIABLE CHAPEAU POUR NOM DES VECT_ELEM
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IFM,NIV
      CHARACTER*1  BASE    
      LOGICAL      ISFONC,LCONT,LMACR
      CHARACTER*19 NMCHEX,CNCONT,CNSSTR
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> ... ASSEMBLAGE DES FORCES INTERNES'
      ENDIF               
C
C --- INITIALISATIONS
C  
      BASE   = 'V' 
      LCONT  = ISFONC(FONACT,'CONTACT')
      LMACR  = ISFONC(FONACT,'MACR_ELEM_STAT')       
      CNCONT = '&&CNCHAR.DUMM'
      CALL VTZERO(CNFINT)  
      CALL VTZERO(CNCONT)        
C
C --- CONTRIBUTIONS DU CONTACT
C
      IF (LCONT) THEN
        CALL NMASCO('CNFINT',FONACT,DEFICO,VEASSE,CNCONT)    
      ENDIF   
C
C --- ASSEMBLAGE DES FORCES INTERIEURES
C     
      CALL ASSVEC(BASE  ,CNFINT,1,VEFINT,1.D0,NUMEDD,' ','ZERO',1)
C
C --- CONTRIBUTIONS DU CONTACT
C          
      IF (LCONT) THEN
        CALL VTAXPY(1.D0,CNCONT,CNFINT)
      ENDIF
C
C --- FORCES ISSUES DES MACRO-ELEMENTS STATIQUES
C         
      IF (LMACR) THEN
        CNSSTR = NMCHEX(VEASSE,'VEASSE','CNSSTR') 
        CALL VTAXPY(1.D0,CNSSTR,CNFINT)        
      ENDIF        
C
C --- AFFICHAGE
C      
      IF (NIV.GE.2) THEN
        CALL NMDEBG('VECT',CNFINT,6)
      ENDIF        
C
      CALL JEDEMA()
      END
