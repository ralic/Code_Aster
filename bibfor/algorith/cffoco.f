C      
C IN  NUMEDD : NUME_DDL DE LA MATRICE
C IN  RESOCO : SD DE RESOLUTION DU CONTACT
C OUT CNFOCO : VECT_ASSE DES FORCES DE CONTACT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      NEQ,IRET,I
      INTEGER      JATMU,JCNCO
      CHARACTER*8  K8BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()  
C
C --- INITIALISATIONS
C
      CALL DISMOI('F','NB_EQUA',NUMEDD,'NUME_DDL',NEQ,K8BID,IRET)
      CALL JEVEUO(CNFOCO(1:19)//'.VALE','E',JCNCO)
C
C --- CALCUL DU VECT_ASSE
C
      CALL JEVEUO(RESOCO(1:14)//'.ATMU','L',JATMU)
      DO 10 I = 1, NEQ
        ZR(JCNCO+I-1) =  ZR(JATMU+I-1)
 10   CONTINUE                
C
      CALL JEDEMA()      
      END
