C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C MISE A JOUR DE L'INCONNUE EN DEPLACEMENT
C      
C ----------------------------------------------------------------------
C
C INCPLU = INCMOI + COEF*DDINNC
C
C IN  NUMEDD : NOM DU NUME_DDL
C IN  COEF   : COEFFICIENT MULTIPLICATEUR
C IN  INCMOI : CHAMP DE DEPL. INITIAL
C OUT INCPLU : CHAMP DE DEPL. CORRIGE
C IN  DDINNC : INCREMENT CHAMP DE DEPL.
C IN  LGROT  : .TRUE.  S'IL Y A DES DDL DE GRDE ROTATION
C IN  SDNUME : SD NUMEROTATION
C IN  ORDRE  : 0 -> MAJ DES INCREMENTS
C              1 -> MAJ DES DEPL 
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      JDEPP,JDEPM,JDDEPL    
      INTEGER      NEQ,IRET
      CHARACTER*8  K8BID
      CHARACTER*24 INCPLU,INCMOI  
      CHARACTER*24 DDINCC      
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C        
      INCPLU = INCPLZ
      INCMOI = INCMOZ
      DDINCC = DDINCZ   
C
      CALL DISMOI('F','NB_EQUA',NUMEDD,'NUME_DDL',NEQ,K8BID,IRET) 
      CALL JEVEUO(INCMOI(1:19)//'.VALE','L',JDEPM)
      CALL JEVEUO(INCPLU(1:19)//'.VALE','E',JDEPP)
      CALL JEVEUO(DDINCC(1:19)//'.VALE','L',JDDEPL)
C
C --- MISE A JOUR 
C  
      CALL MAJOUR(NEQ   ,LGROT    ,LENDO    ,SDNUME,ZR(JDEPM) ,
     &            ZR(JDDEPL),COEF  ,ZR(JDEPP),ORDRE)
C
      CALL JEDEMA()
      END
