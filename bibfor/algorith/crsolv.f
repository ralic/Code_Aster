C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
C     CREATION D'UNE STRUCTURE SOLVEUR
C
C-----------------------------------------------------------------------
       INTEGER ISLVI,ISLVK,ISLVR,NPREC
       REAL*8  RESIRE
C-----------------------------------------------------------------------
C
      INTEGER            SDSOLV,ZSLVK ,ZSLVR ,ZSLVI
      REAL*8             JEVTBL,EPSMAT
      CHARACTER*1        BASE
      CHARACTER*3        SYME
      CHARACTER*8        PRECO
      CHARACTER*19       SOLVEU
C
C ----------------------------------------------------------------------
C
C --- INITIALISATIONS
C
      CALL JEMARQ()
C
      SOLVEU = SOLVE
      BASE = BAS
C
      PRECO  = 'SANS'
      SYME   = 'NON'
C
      RESIRE = 1.D-6
      NPREC = 8
      EPSMAT=-1.D0
C
C --- CREATION DES DIFFERENTS ATTRIBUTS DE LA S.D. SOLVEUR
C
      ZSLVK = SDSOLV('ZSLVK')
      ZSLVR = SDSOLV('ZSLVR')
      ZSLVI = SDSOLV('ZSLVI')
      CALL WKVECT(SOLVEU//'.SLVK',BASE//' V K24',ZSLVK ,ISLVK )
      CALL WKVECT(SOLVEU//'.SLVR',BASE//' V R'  ,ZSLVR ,ISLVR )
      CALL WKVECT(SOLVEU//'.SLVI',BASE//' V I'  ,ZSLVI ,ISLVI )
C
C --- REMPLISSAGE DE LA S.D. SOLVEUR
C
      ZK24(ISLVK-1+1) = METHOD
      IF ((METHOD.EQ.'MULT_FRONT').OR.(METHOD.EQ.'LDLT')) THEN
        ZK24(ISLVK-1+2) = 'XXXX'
      ELSE
        ZK24(ISLVK-1+2) = PRECO
      ENDIF
      IF (METHOD.EQ.'MUMPS') THEN
        ZK24(ISLVK-1+3) = 'AUTO'
        ZK24(ISLVK-1+6) = 'OUI'
      ELSE
        ZK24(ISLVK-1+3) = 'XXXX'
        ZK24(ISLVK-1+6) = 'XXXX'
      ENDIF
      ZK24(ISLVK-1+4) = RENUM
      ZK24(ISLVK-1+5) = SYME
      ZK24(ISLVK-1+7) = 'XXXX'
      ZK24(ISLVK-1+8) = 'XXXX'
      ZK24(ISLVK-1+9) = 'XXXX'
      ZK24(ISLVK-1+10) = 'XXXX'
      ZK24(ISLVK-1+11) = 'XXXX'
      ZK24(ISLVK-1+12) = 'XXXX'
C
      ZR(ISLVR-1+1) = EPSMAT
      ZR(ISLVR-1+2) = RESIRE
      ZR(ISLVR-1+3) = JEVTBL('TAILLE_BLOC')
      ZR(ISLVR-1+4) = 0.D0
C
      ZI(ISLVI-1+1) = NPREC
      ZI(ISLVI-1+2) =-9999
      ZI(ISLVI-1+3) =-9999
      ZI(ISLVI-1+4) =-9999
      ZI(ISLVI-1+5) =-9999
      ZI(ISLVI-1+6) =-9999
      ZI(ISLVI-1+7) =-9999
      ZI(ISLVI-1+8) = 0
C
      CALL JEDEMA()
C
      END
