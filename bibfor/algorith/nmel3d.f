       SUBROUTINE  NMEL3D(FAMI,POUM,NNO, NPG, IPOIDS, IVF, IDFDE,
     &             GEOM, TYPMOD, OPTION, IMATE, COMPOR, LGPG, CRIT,
     &             DEPL,ANGMAS, DFDI, PFF, DEF, SIG, VI,
     &             MATUU, VECTU, CODRET )
