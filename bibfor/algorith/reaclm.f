      IMPLICIT NONE
      CHARACTER*8  NOMA
      CHARACTER*24 DEPPLU,RESOCO,DEFICO
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - FROTTEMENT)
C
C REACTUALISATION DES SEUILS DE FROTTEMENT PAR LES MULTIPLICATEURS
C DE CONTACT
C      
C ----------------------------------------------------------------------
C
C 
C IN  NOMA   : NOM DU MAILLAGE
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE RESOLUTION DU CONTACT
C IN  DEPPLU : DEPLACEMENT APRES CONVERGENCE DE NEWTON
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFMMVD,ZTABF,ZMAES
      INTEGER      NTMAE,NTPC,IMAE,POSMAE,NBPC,IPC
      REAL*8       LAMBDA,KSIPC1,KSIPC2
      CHARACTER*24 MAESCL,TABFIN
      INTEGER      JMAESC,JTABF
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- RECUPERATION DES QCQS DONNEES
C
      MAESCL = DEFICO(1:16) // '.MAESCL'
      TABFIN = DEFICO(1:16) // '.TABFIN'
      CALL JEVEUO(MAESCL,'L',JMAESC)
      CALL JEVEUO(TABFIN,'E',JTABF)
C
      ZTABF = CFMMVD('ZTABF')
      ZMAES = CFMMVD('ZMAES')
C
C --- BOUCLE SUR LES POINTS DE CONTACT
C
      NTMAE = ZI(JMAESC)
      NTPC  = 0
      DO 20 IMAE = 1,NTMAE
        POSMAE = ZI(JMAESC+ZMAES*(IMAE-1)+1)
        NBPC   = ZI(JMAESC+ZMAES*(IMAE-1)+3)
        DO 10 IPC = 1,NBPC
          KSIPC1    = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+3)
          KSIPC2    = ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+12)  
          CALL CALLAM(NOMA  ,DEFICO,RESOCO,DEPPLU,POSMAE,
     &                KSIPC1,KSIPC2,LAMBDA)       
          ZR(JTABF+ZTABF*NTPC+ZTABF*(IPC-1)+14) = LAMBDA
 10     CONTINUE
        NTPC = NTPC + NBPC
 20   CONTINUE
C 
      CALL JEDEMA()
      END
