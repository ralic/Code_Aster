C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*24 RESOCO
      CHARACTER*19 NORMMA
      INTEGER      NBNOM
      INTEGER      POSNO(10)         
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT - MAIT/ESCL)
C
C CREATION DE L'OBJET NORMMA POUR NORMALES LISSEES
C
C ----------------------------------------------------------------------
C
C
C TOUTES LES NORMALES SONT CALCULEES DANS CFNORM ET STOCKEES DANS JNRINI
C CETTE ROUTINE EXTRAIT LES NORMALES AUX NOEUDS MAITRES POUR CREER
C NORMMA
C
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  NBNOM  : NOMBRE DE NOEUDS MAITRES CONCERNES (MAX: 9)
C IN  POSNO  : INDICES DANS CONTNO DU NOEUD ESCLAVE ET DES NOEUDS 
C               MAITRES (MAX: 1+9=10)
C OUT NORMMA : NOM DE L'OBJET CONTENANT LES NORMALES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER JLISSA,NOEUD,INO
      INTEGER      JNRINI
      CHARACTER*24 NORINI
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- LECTURE DES SD 
C
      NORINI = RESOCO(1:14)//'.NORINI'  
      CALL JEVEUO(NORINI,'L',JNRINI)      
C
C --- INITIALISATIONS
C      
      NORMMA = '&&CFLISS.NORMMA'
      CALL JEDETR(NORMMA)
      IF (NBNOM.GT.9) THEN
        CALL U2MESS('F','CONTACT_74')
      END IF      
C
C --- CREATION DE L'OBJET
C              
      CALL WKVECT(NORMMA,'V V R',NBNOM*3,JLISSA)
C
C --- RECOPIE DES NORMALES MAITRES
C      
      DO 61 INO = 1, NBNOM
        NOEUD = POSNO(INO+1)
        ZR(JLISSA+(INO-1)*3)   = ZR(JNRINI+3*(NOEUD-1))
        ZR(JLISSA+(INO-1)*3+1) = ZR(JNRINI+3*(NOEUD-1)+1)
        ZR(JLISSA+(INO-1)*3+2) = ZR(JNRINI+3*(NOEUD-1)+2)
 61   CONTINUE
C
      CALL JEDEMA()
      END
