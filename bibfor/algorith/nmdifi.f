      IMPLICIT     NONE
      CHARACTER*16 MOTFAC
      CHARACTER*19 PROVLI
      REAL*8       TOLE
      INTEGER      NUMFIN,NBINST,IOCC
C
C ----------------------------------------------------------------------
C
C ROUTINE *_NON_LINE (STRUCTURES DE DONNES - DISCRETISATION)
C
C DETERMINATION DU NUMERO D'ORDRE FINAL
C
C ----------------------------------------------------------------------
C
C
C IN  MOTFAC : MOT-CLEF FACTEUR
C IN  IOCC   : OCCURRENCE DU MOT-CLEF FACTEUR MOTFAC
C IN  PROVLI : NOM DE LA LISTE D'INSTANT PROVISOIRE
C IN  TOLE   : TOLERANCE POUR RECHERCHE DANS LISTE D'INSTANTS
C IN  NBINST : NOMBRE D'INSTANTS DANS LA LISTE
C OUT NUMFIN : NUMERO D'ORDRE FINAL
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      INTEGER      ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8       ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16   ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL      ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8  ZK8
      CHARACTER*16    ZK16
      CHARACTER*24        ZK24
      CHARACTER*32            ZK32
      CHARACTER*80                ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER      N1,N2
      REAL*8       INST
      INTEGER      JINST
      INTEGER      IARG
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NUMFIN = 0
C
C --- NOM SD_DISC
C
      CALL JEVEUO(PROVLI,'L',JINST )
C
C --- LECTURE MOTS-CLEFS
C
      CALL GETVIS(MOTFAC,'NUME_INST_FIN',IOCC,IARG,1,NUMFIN,N1)
      CALL GETVR8(MOTFAC,'INST_FIN'     ,IOCC,IARG,1,INST  ,N2)
C
C --- PAS D'OCCURENCE DES MOTS-CLES -> NUMERO INITIAL
C
      IF (N1+N2 .EQ. 0) THEN
        NUMFIN = NBINST-1
C
C --- MOTS-CLES INST_FIN
C
      ELSE IF (N1 .EQ. 0) THEN
        CALL UTACLI(INST,ZR(JINST),NBINST,TOLE,NUMFIN)
      ENDIF
C
C --- VERIFICATIONS
C
      IF (NUMFIN.LT.0 .OR. NUMFIN.GT.(NBINST-1)) THEN
        CALL U2MESS('F','DISCRETISATION_94')
        NUMFIN = NBINST - 1
      END IF
C
      CALL JEDEMA()

      END
