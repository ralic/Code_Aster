C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME PRINCIPAL)
C
C GESTION DES ERREURS ET EXCEPTIONS
C      
C ----------------------------------------------------------------------
C
C
C IN  MTCPUI : .TRUE. SI TEMPS DEPASSE POUR NOUVELLE ITERATION
C IN  MTCPUP : .TRUE. SI TEMPS DEPASSE POUR NOUVEAU PAS DE TEMPS
C IN  ITEMAX : .TRUE. SI NBRE MAXI ITERATION DE NEWTON DEPASSE 
C IN  NUMINS : NUMERO DU PAS DE TEMPS
C IN  ITERAT : NUMERO DE L'ITERATION DE NEWTON
C IN  ECHLDC : SI ECHEC LORS INTEGRATION LOI COMPORTEMENT
C IN  ECHEQU : SI MATRICE SINGULIERE
C IN  ECHCON : 1 SI ECHEC LORS TRAITEMENT DE CONTACT
C              2 SI MATRICE DE CONTACT NON INVERSIBLE
C IN  TPSITE : TEMPS MOYEN PAR ITERATION DE NEWTON
C IN  TPSPAS : TEMPS MOYEN PAR PAS DE TEMPS
C IN  TPSRES : TEMPS CPU RESTANT
C                         
C ----------------------------------------------------------------------
C
      REAL*8       RTAB(2) 
      INTEGER      ITAB(2)    
      CHARACTER*8  K8BID
      CHARACTER*16 KTAB(2) 
C      
C ----------------------------------------------------------------------
C
      IF (MTCPUI) THEN
        ITAB(1) = NUMINS
        ITAB(2) = ITERAT
        RTAB(1) = TPSITE
        RTAB(2) = TPSRES
        CALL UTEXCM(28,'MECANONLINE_79',0,K8BID,2,ITAB  ,2,RTAB)
      ELSE IF (MTCPUP) THEN
        ITAB(1) = NUMINS
        KTAB(1) = 'increment de charge'
        RTAB(1) = TPSPAS
        RTAB(2) = TPSRES
        CALL UTEXCM(28,'MECANONLINE_80',1,KTAB ,1,ITAB  ,2,RTAB)
      ELSE IF (ECHLDC) THEN
        CALL UTEXCP(23,'COMPOR1_9')
      ELSE IF (ECHEQU) THEN
        CALL UTEXCP(25,'MECANONLINE_82')
      ELSE IF (ECHCON(1)) THEN
        CALL UTEXCP(26,'CONTACT2_1')
      ELSE IF (ECHCON(2)) THEN
        CALL UTEXCP(27,'CONTACT2_2')
      ELSE IF (ITEMAX) THEN
        CALL UTEXCP(22,'MECANONLINE_83')
      ELSE IF (ECHPIL) THEN
        CALL UTEXCP(22,'MECANONLINE_84')
      ELSE
        CALL U2MESS('F','MECANONLINE_86')
      ENDIF
C   
      END
