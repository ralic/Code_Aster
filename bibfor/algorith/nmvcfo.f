C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS

      IMPLICIT NONE
      CHARACTER*(*) MODELZ
      CHARACTER*19  CNVCFO
      CHARACTER*24  NUMEDD, MATE, COMREF, CARELE
      CHARACTER*24  COMPLU

C ----------------------------------------------------------------------
C    ESTIMATION D'UNE FORCE DE REFERENCE LIEE A L'ACTION DES V. COM.
C ----------------------------------------------------------------------
C
C IN       MODELE K8  MODELE
C IN       NUMEDD K24 NUME_DDL
C IN       MATE   K24 CHAMP MATERIAU
C IN       CARELE K24 CARACTERISTIQUES DES ELEMENTS DE STRUCTURE
C IN       COMREF K24 VARI_COM DE REFERENCE
C IN       COMPLU K24 VARI_COM EN T+ (TEMPERATURE POUR LES POUTRES)
C IN/JXOUT CNVCFO K19 FORCE DE REFERENCE (DFINT/DA * A)
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------

      LOGICAL      EXITEM, EXIHYD, EXISEC, EXIEPA, EXIPHA
      LOGICAL      EXIGEO, EXICAR,LBID
      INTEGER      NBRENV, NBR, IRET, IBID
      INTEGER      JVCP
      CHARACTER*6  MASQUE
      CHARACTER*8  VECEL, LPAIN(20), LPAOUT(1), PARTPP, NOMGD, K8BID
      CHARACTER*8  MODELE
      CHARACTER*16 OPTION
      CHARACTER*24 CHTREF, CHVREF
      CHARACTER*24 TEMPLU, VRCPLU, SECPLU, PHAPLU, INSPLU
      CHARACTER*24 LIGRMO, CHGEOM, CHCARA(17), LCHIN(20), LCHOUT(1)

      DATA  VECEL /'&&VEVCFO'/
C ----------------------------------------------------------------------
      CALL JEMARQ()
      MODELE=MODELZ


C -- PREPARATION DES VECT_ELEM

      NBRENV = 4
      MASQUE = '.VEXXX'
      CALL JEEXIN(VECEL // '.LISTE_RESU',IRET)
      IF (IRET.EQ.0) THEN
        CALL MEMARE('V',VECEL,MODELE,MATE,CARELE,'CHAR_MECA')
        CALL WKVECT(VECEL // '.LISTE_RESU','V V K24',NBRENV,JVCP)
      ELSE
        CALL JEVEUO(VECEL // '.LISTE_RESU','E',JVCP)
      END IF


C -- EXTRACTION DES VARIABLES DE COMMANDE

      CALL NMVCEX('TEMP',COMREF,CHTREF)
      CALL NMVCEX('TOUT',COMREF,CHVREF)
      CALL NMVCEX('TEMP',COMPLU,TEMPLU)
      CALL NMVCEX('TOUT',COMPLU,VRCPLU)
      CALL NMVCEX('PHAS',COMPLU,PHAPLU)
      CALL NMVCEX('INST',COMPLU,INSPLU)
C    VARIABLES DE COMMANDE PRESENTES
      EXITEM = TEMPLU .NE. ' '
      IF (EXITEM) CALL NMVCDE('TEMP',COMPLU,EXITEM)
      CALL NMVCD2('HYDR',MATE,EXIHYD,LBID)
      CALL NMVCD2('SECH',MATE,EXISEC,LBID)
      CALL NMVCD2('EPSA',MATE,EXIEPA,LBID)
C -- ON TESTE LA NATURE DU CHAMP DE TEMPERATURE (FONCTION OU CHAMP)

      CALL DISMOI('F','NOM_GD',TEMPLU,'CHAMP',IBID,NOMGD,IRET)
      IF (NOMGD.EQ.'TEMP_R') THEN
         PARTPP  = 'PTEMPER'
      ELSE IF (NOMGD.EQ.'TEMP_F') THEN
         PARTPP  = 'PTEMPEF'
      ELSE
         CALL U2MESS('F','ALGORITH8_60')
      END IF


C -- CHAMPS DES OPTIONS

      LIGRMO = MODELE//'.MODELE'
      CALL MEGEOM(MODELE,' ' , EXIGEO, CHGEOM)
      CALL MECARA(CARELE(1:8), EXICAR, CHCARA)

      LPAIN(1)  = 'PTEREF'
      LCHIN(1)  =  CHTREF
      LPAIN(2)  = 'PGEOMER'
      LCHIN(2)  =  CHGEOM
      LPAIN(3)  = 'PMATERC'
      LCHIN(3)  =  MATE
      LPAIN(4)  = 'PCACOQU'
      LCHIN(4)  =  CHCARA(7)
      LPAIN(5)  = 'PCAGNPO'
      LCHIN(5)  =  CHCARA(6)
      LPAIN(6)  = 'PCAORIE'
      LCHIN(6)  =  CHCARA(1)
      LPAIN(7)  = 'PCAGNBA'
      LCHIN(7)  =  CHCARA(11)
      LPAIN(8)  = 'PCAARPO'
      LCHIN(8)  =  CHCARA(9)
      LPAIN(9)  = 'PCAMASS'
      LCHIN(9)  =  CHCARA(12)
      LPAIN(10) = 'PCAGEPO'
      LCHIN(10) =  CHCARA(5)
      LPAIN(11)  = 'PTEMPSR'
      LCHIN(11)  =  INSPLU
      LPAIN(12)  =  PARTPP
      LCHIN(12)  =  TEMPLU
      LPAIN(13)  = 'PVARCPR'
      LCHIN(13)  =  VRCPLU
      LPAIN(14)  = 'PVARCRR'
      LCHIN(14)  =  CHVREF
      LPAIN(15)  = 'PPHASRR'
      LCHIN(15)  =  PHAPLU
      LPAIN(16)  = 'PNBSP_I'
      LCHIN(16)  = CHCARA(1) (1:8)//'.CANBSP'
      LPAIN(17) = 'PFIBRES'
      LCHIN(17) =  CHCARA(1) (1:8)//'.CAFIBR'


C -- CALCUL DES OPTIONS EN T+

      NBR = 0

C    THERMIQUE
      IF (EXITEM) THEN
        NBR = NBR+1
        CALL CODENT (NBR,'D0',MASQUE(4:6))
        LPAOUT(1) = 'PVECTUR'
        LCHOUT(1) =  VECEL // MASQUE
        OPTION = 'CHAR_MECA_TEMP_R'
       CALL CALCUL('S',OPTION,LIGRMO,17,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V')
        ZK24(JVCP-1 + NBR) = LCHOUT(1)
      END IF

C    HYDRATATION
      IF (EXIHYD) THEN
        NBR = NBR+1
        CALL CODENT (NBR,'D0',MASQUE(4:6))
        LPAOUT(1) = 'PVECTUR'
        LCHOUT(1) =  VECEL // MASQUE
        OPTION = 'CHAR_MECA_HYDR_R'
       CALL CALCUL('S',OPTION,LIGRMO,17,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V')
        ZK24(JVCP-1 + NBR) = LCHOUT(1)
      END IF

C    SECHAGE
      IF (EXISEC) THEN
        NBR = NBR+1
        CALL CODENT (NBR,'D0',MASQUE(4:6))
        LPAOUT(1) = 'PVECTUR'
        LCHOUT(1) =  VECEL // MASQUE
        OPTION = 'CHAR_MECA_SECH_R'
       CALL CALCUL('S',OPTION,LIGRMO,17,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V')
        ZK24(JVCP-1 + NBR) = LCHOUT(1)
      END IF

C    DEFORMATION ANELASTIQUE
      IF (EXIEPA) THEN
        NBR = NBR+1
        CALL CODENT (NBR,'D0',MASQUE(4:6))
        LPAOUT(1) = 'PVECTUR'
        LCHOUT(1) =  VECEL // MASQUE
        OPTION = 'CHAR_MECA_EPSA_R'
       CALL CALCUL('S',OPTION,LIGRMO,17,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V')
        ZK24(JVCP-1 + NBR) = LCHOUT(1)
      END IF


C -- ASSEMBLAGE

      CALL JEECRA(VECEL // '.LISTE_RESU','LONUTI',NBR,K8BID)
      CALL ASSVEC ('V',CNVCFO,1,VECEL,1.D0,NUMEDD,' ','ZERO',1)


      CALL JEDEMA()
      END
