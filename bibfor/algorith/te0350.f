      SUBROUTINE TE0350(OPTION,NOMTE)
      
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 13/08/2003   AUTEUR JMBHH01 J.M.PROIX 
