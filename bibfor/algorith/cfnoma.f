      IMPLICIT     NONE
      INTEGER      IZONE 
      CHARACTER*24 DEFICO
      CHARACTER*4  MATYP
      INTEGER      NDIM,MOYEN
      REAL*8       COORDA(3),COORDB(3),COORDC(3)
      REAL*8       MNORM(3),UNORM(3),ENORM(3)
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT - MAIT/ESCL)
C
C PREPARATION DES NORMALES
C  - CALCUL DE LA NORMALE A LA MAILLE MAITRE 
C  - CALCUL DE LA NORMALE MOYENNE MAITRE/ESCLAVE
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE CONTACT (DEFINITION)
C IN  IZONE  : ZONE DE CONTACT
C IN  NDIM   : DIMENSION DE L'ESPACE (2 OU 3)
C IN  MATYP  : TYPE DE LA MAILLE MAITRE
C IN  MOYEN  : NORMALES D'APPARIEMENT
C               0 MAIT
C               1 MAIT_ESCL
C IN  COORDA : COORDONNEES DU SOMMET A 
C IN  COORDB : COORDONNEES DU SOMMET B
C IN  COORDC : COORDONNEES DU SOMMET C
C IN  ENORM  : NORMALE AU NOEUD ESCLAVE
C OUT MNORM  : NORMALE A LA MAILLE MAITRE
C OUT UNORM  : NORMALE MOYENNE MAITRE/ESCLAVE (SI MOYEN = 1)  
C
C ----------------------------------------------------------------------
C
      MNORM(1) = 0.D0
      MNORM(2) = 0.D0
      MNORM(3) = 0.D0      
C
C --- VECTEUR NORMAL MAILLE MAITRE
C
      IF (MATYP(1:3).EQ.'SEG') THEN
        CALL CFNOSE(DEFICO,IZONE ,NDIM  ,COORDA,COORDB,
     &              MNORM ) 
      ELSE IF (MATYP(1:3).EQ.'TRI') THEN        
        CALL CFNOTR(COORDA,COORDB,COORDC,MNORM )
      ELSE IF (MATYP(1:3).EQ.'QUA') THEN
C
C --- ON DECOUPE EN QUATRE TRIANGLES: SERA RAPPELLEE ULTERIEUREMENT
C           
      ELSE  
        CALL U2MESS('F','CONTACT_30')
      END IF            
C      
C --- CALCUL DE LA NORMALE MOYENNE      
C
      IF (MATYP(1:3).EQ.'QUA') THEN
C
C --- INITIALISE A ZERO, SERA RECALCULEE AU MOMENT DU DECOUPAGE
C --- EN TRIANGLE (PROJTT RE-APPELLE CFNOMA)
C      
        UNORM(1) = 0.D0
        UNORM(2) = 0.D0
        UNORM(3) = 0.D0       
      ELSE
        CALL CFMOYN('MAIT_ESCL',MOYEN,MNORM,ENORM,UNORM)   
      ENDIF
C
      END
