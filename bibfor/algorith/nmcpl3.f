C     CONTRAINTES PLANES PAR LA METHODE DE BORST / CONDENSATION STATIQUE
C     POUR LES COMPORTEMENTS QUI N'INTEGRENT PAS LES CONTRAINTES PLANES
C     ATTENTION : POUR BIEN CONVERGER, IL FAUT REACTUALISER LA MATRICE
C     TANGENTE. DE PLUS, IL FAUT AJOUTER 4 VARIABLES INTERNES
C
C IN  CRIT    : CRITERES DE CONVERGENCE LOCAUX
C                               (3) = VALEUR TOLERANCE DE CONVERGENCE
C                                     (RESI_INTE_RELA == RESCREL)
C     DEPS    : INCREMENT DE DEFORMATION TOTALE :
C               DEPS(T) = DEPS(MECANIQUE(T)) + DEPS(DILATATION(T))
C     ICP     : NUMERO DE L'ITERATION
C VAR DSIDEP  : MATRICE TANGENTE CARREE
C VAR SIGP    : CONTRAINTES A L'INSTANT ACTUEL
C VAR VIP     : LES 4 DERNIERES SONT RELATIVES A LA METHODE DE BORST 

      IMPLICIT NONE
      CHARACTER*16 OPTION
      INTEGER      K,L,NDIM,NCPMAX,ICP,CPL
      LOGICAL      CONV,VECTEU
      REAL*8       VIP(*),DEPS(*),CRIT(*),DSIDEP(6,6),SIGP(4),SIGPEQ
      REAL*8       PREC,SIGNUL,PRECR,DDEZZ

      VECTEU = OPTION(1:9) .EQ. 'FULL_MECA' .OR. 
     &         OPTION(1:9) .EQ. 'RAPH_MECA'
     
      NCPMAX = NINT(CRIT(9))

      SIGNUL=CRIT(3)
      PREC=CRIT(3)

      CONV = .TRUE.
      
      IF (VECTEU) THEN
      
        SIGPEQ=0.D0
        DO 141 K = 1,2*NDIM
          SIGPEQ = SIGPEQ + SIGP(K)**2
 141    CONTINUE
        SIGPEQ = SQRT(SIGPEQ)

        IF (SIGPEQ.LT.SIGNUL) THEN
          PRECR=PREC
        ELSE
          PRECR=PREC*SIGPEQ
        ENDIF

        CONV = (ICP.GE.NCPMAX .OR. ABS(SIGP(3)).LT.PRECR)   

        IF(.NOT. CONV) THEN
          IF((CPL .EQ. 2) .AND. ABS(DSIDEP(3,3)).GT.PRECR) THEN 

           DEPS(3) = DEPS(3) - SIGP(3)/DSIDEP(3,3)
          
          ELSEIF(ABS(DSIDEP(2,2)).GT.PRECR) THEN 
          
           CALL ASSERT(CPL .EQ. 1)
           DDEZZ = -(SIGP(3) - DSIDEP(3,2)/DSIDEP(2,2)*SIGP(2))
     &              /(DSIDEP(3,3)- DSIDEP(3,2)*DSIDEP(2,3)/DSIDEP(2,2))
           DEPS(3) = DEPS(3) + DDEZZ
           DEPS(2) = DEPS(2) - (SIGP(2) + DSIDEP(2,3)*DDEZZ)/DSIDEP(2,2)
          
          ENDIF  
        ENDIF     
      
      ENDIF
      
      END
