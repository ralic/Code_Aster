      IMPLICIT     NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 MOTFAC
      INTEGER      IEXCI
      CHARACTER*8  CHARGE
C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (LISTE_CHARGES)
C
C LECTURE DU NOM DE LA CHARGE (PROVENANT DE AFFE_CHAR_*)
C
C ----------------------------------------------------------------------
C
C
C IN  MOTFAC : MOT-CLEF FACTEUR DES EXCITATIONS
C IN  IEXCI  : OCCURRENCE DE L'EXCITATION
C OUT CHARGE : NOM DE LA CHARGE (OU DU VECT_ASSE[_GENE])
C
      INTEGER      NVAL,IARG
      INTEGER      EXIMVE,EXIMVG,GETEXM
      CHARACTER*24 K24BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      CHARGE = ' '
C
C --- CHARGE SPECIFIQUE VECT_ASSE OU VECT_ASSE_GENE
C
      EXIMVE = GETEXM(MOTFAC,'VECT_ASSE')
      IF (EXIMVE.EQ.1) THEN
        CALL GETVID(MOTFAC,'VECT_ASSE',IEXCI,IARG,1,
     &              CHARGE,NVAL)
        CALL ASSERT(NVAL.GE.0)
      ENDIF

      EXIMVG = GETEXM(MOTFAC,'VECT_ASSE_GENE')
      IF (EXIMVG.EQ.1) THEN
        CALL GETVID(MOTFAC,'VECT_ASSE_GENE',IEXCI,IARG,1,
     &              CHARGE,NVAL)
        CALL ASSERT(NVAL.GE.0)
      ENDIF
C
C --- CHARGE STANDARD
C
      CALL GETVID(MOTFAC,'CHARGE'   ,IEXCI,IARG,1,
     &            CHARGE,NVAL)
      CALL ASSERT(NVAL.GE.0)
C
      CALL JEDEMA()
      END
