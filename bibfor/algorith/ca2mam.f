C ROUTINE CALCULANT LES MATRICES DES DERIVEES
C DE FONCTIONS DE FORME SUR LE MODELE INTERFACE

C IN : MOINT : MODELE INTERFACE
C IN : MATE : MATERIAU CODE
C IN : LIGRMO : LIGREL DU MODELE
C IN: LCHIN,LPAIN,LPAOUT : PARAMETRES DU CALCUL ELEMENTAIRE
C IN : NUM : NUMEROTATION DES DDLS THERMIQUES D INTERFACE

C OUT : MADE : MATRICE DES DERIVEES

C---------------------------------------------------------------------

      INCLUDE 'jeveux.h'
      CHARACTER*(*) MOINT
      CHARACTER*3 INCR
      CHARACTER*8 K8BID,LPAIN(2),LPAOUT(1)
      CHARACTER*14 NUM
      CHARACTER*16 OPTION
      CHARACTER*19 MATEL,NU19
      CHARACTER*24 LCHOUT(1),LCHIN(2),LIGRMO
      CHARACTER*24 MADE,MADEEL

C ----- CREATION DE LA MATR_ELEM DES DERIVEES DES DN(I)*DN(J)
C--------------SUR LE MODELE THERMIQUE------------------------------
C--------------------D'INTERFACE -----------------------------------

      CALL JEMARQ()
      OPTION = 'AMOR_AJOU'
      LPAOUT(1) = 'PMATTTR'
      MATEL = '&&B'//INCR(1:3)
      MADEEL = MATEL//'.RELR'
      CALL MEMARE('V',MATEL,MOINT(1:8),' ',' ','AMOR_AJOU ')
      CALL WKVECT(MADEEL,'V V K24',1,JLVA)

      LCHOUT(1) = MATEL(1:8)//'.ME000'
      CALL CODENT(1,'D0',LCHOUT(1) (12:14))
      CALL CALCUL('S',OPTION,LIGRMO,2,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V',
     &               'OUI')
      ZK24(JLVA) = LCHOUT(1)
      CALL JEECRA(MADEEL,'LONUTI',1,K8BID)


C-------------------- NUMEROTATION ----------------------------------

      NUM = 'NUM'//INCR
      CALL NUMDDL(NUM,'VV',1,MATEL,'RCMK')
      CALL PROMOR(NUM,'V')
      CALL CRNSLV(NUM,'MULT_FRONT','METIS','V')

C---------------ASSEMBLAGE DES MATRICES  DES DN(I)DN(J)--------------

      MADE = 'MA'//INCR

      CALL ASSMAM('V',MADE,1,MATEL,1.D0,NUM,'ZERO',1)


      NU19=NUM
      CALL JEDETR(NU19//'.ADLI')
      CALL JEDETR(NU19//'.ADNE')
      CALL JEDETC('V',MATEL,1)


      CALL JEDEMA()
      END
