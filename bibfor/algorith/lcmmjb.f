     &     NUECOU,NSFV,NSFA,IR,IS,NBSYS,NFS,NSG,HSR,VIND,DY,IEXP,EXPBP,
     &            ITMAX,TOLER,DGSDTS,DKSDTS,DGRDBS,DKRDBS,IRET)
      IMPLICIT NONE
