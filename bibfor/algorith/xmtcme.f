C ======================================================================
C RESPONSABLE NISTOR I.NISTOR

      IMPLICIT NONE
      CHARACTER*8  MODELE
      CHARACTER*24 DEPMOI,DEPDEL,RESOCO
      
C ----------------------------------------------------------------------
C        CALCUL DES MATRICES ELEMENTAIRES ET DES SECONDS MEMBRES 
C             DE TYPE CONTACT FROTTANT DANS LE CADRE D'X-FEM
C                       'GRANDS GLISSEMENTS'
C ----------------------------------------------------------------------
C ROUTINE SPECIFIQUE A L'APPROCHE <<GRANDS GLISSEMENTS AVEC XFEM>>,
C TRAVAIL EFFECTUE EN COLLABORATION AVEC I.F.P.
C ----------------------------------------------------------------------

C IN MODELE : NOM DU MODELE
C IN LIGRCF : NOM DE LIGREL (DES ELEMENTS TARDIFS DE CONTACT)
C IN CARTCF : NOM DE LA CARTE DE CONTACT
C IN  DEPMOI : DÉPLACEMENT À L'ÉQUILIBRE PRÉCÉDENT
C IN  DEPDEL : INCRÉMENT DE DÉPLACEMENT DEPUIS L'ÉQUILIBRE PRÉCÉDENT
C IN/JXOUT MCONEL : MATR_ELEM : MATRICES ELEMENTAIRES
C IN/JXOUT SCONEL : VECT_ELEM : S M ELEMENTAIRES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      NBOUT,NBIN
      PARAMETER    (NBOUT=1, NBIN=7)
      CHARACTER*8  LPAOUT(NBOUT),LPAIN(NBIN)
      CHARACTER*19 LCHOUT(NBOUT),LCHIN(NBIN)
C
      CHARACTER*8  K8BID
      CHARACTER*8  MCONEL,SCONEL       
      CHARACTER*19 VECCON,VECFRO
      CHARACTER*19 CHGEOM,LIGRCF,MCON19,SCON19
      CHARACTER*19 CPOINT,CPINTE,CAINTE,CCFACE       
      CHARACTER*24 MELEME
      INTEGER      JLIRES,IFM,NIV
      INTEGER      JMELEM,JLICON,JLIFRO
      LOGICAL      EXIGEO
C
C ----------------------------------------------------------------------
C           
      CALL JEMARQ()
      CALL INFDBG('XFEM',IFM,NIV)
C      
C --- INITIALISATIONS     
C         
      MELEME = RESOCO(1:14)//'.MELEM'
      CALL JEVEUO(MELEME,'L',JMELEM)
      MCONEL = ZK8(JMELEM+1-1)
      MCON19 = MCONEL
      SCONEL = ZK8(JMELEM+2-1)
      SCON19 = SCONEL
      CPOINT = RESOCO(1:14)//'.XFPO'
      CPINTE = RESOCO(1:14)//'.XFPI'
      CAINTE = RESOCO(1:14)//'.XFAI'
      CCFACE = RESOCO(1:14)//'.XFCF' 
      VECCON = '&&MMCCON'
      VECFRO = '&&MMCFRO'
      LIGRCF = RESOCO(1:14)//'.LIGR'
C
C --- INITIALISATION DES CHAMPS POUR CALCUL
C
      CALL INICAL(NBIN  ,LPAIN ,LCHIN ,
     &            NBOUT ,LPAOUT,LCHOUT)          
C      
C --- RECUPERATION DE LA GEOMETRIE
C
      CALL MEGEOM(MODELE,' ',EXIGEO,CHGEOM)  
C       
C --- CREATION DES LISTES DES CHAMPS IN
C
      LPAIN(1)  = 'PGEOMER'
      LCHIN(1)  = CHGEOM
      LPAIN(2)  = 'PDEPL_M'
      LCHIN(2)  = DEPMOI
      LPAIN(3)  = 'PDEPL_P'
      LCHIN(3)  = DEPDEL
      LPAIN(4)  = 'PCAR_PT'
      LCHIN(4)  = CPOINT
      LPAIN(5)  = 'PCAR_PI'
      LCHIN(5)  = CPINTE
      LPAIN(6)  = 'PCAR_AI'
      LCHIN(6)  = CAINTE
      LPAIN(7)  = 'PCAR_CF'
      LCHIN(7)  = CCFACE      
C        
C --- CALCUL DE MCONEL :
C    
      CALL DETRSD('MATR_ELEM',MCON19) 
      CALL MEMARE('V',MCON19,MODELE,' ',' ','RIGI_MECA')
      
      LPAOUT(1) = 'PMATUUR'
      LCHOUT(1) = MCON19(1:8)//'.ME001'
      CALL CALCUL('S','RIGI_CONT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &            'V')
      CALL REAJRE(MCON19,LCHOUT(1),'V')
      
      LCHOUT(1) = MCON19(1:8)//'.ME002'
      CALL CALCUL('S','RIGI_FROT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &            'V')
      CALL REAJRE(MCON19,LCHOUT(1),'V')
C
C --- CALCUL DE SCONEL :
C     
      CALL DETRSD('VECT_ELEM',SCON19)
      CALL MEMARE('V',SCON19,MODELE,' ',' ','CHAR_MECA')

      CALL DETRSD('VECT_ELEM',VECCON)
      CALL MEMARE('V',VECCON,MODELE,' ',' ','CHAR_MECA')

      CALL DETRSD('VECT_ELEM',VECFRO)
      CALL MEMARE('V',VECFRO,MODELE,' ',' ','CHAR_MECA')

      LPAOUT(1) = 'PVECTUR'
      LCHOUT(1) = SCON19(1:8)//'.VE001'
      CALL CALCUL('S','CHAR_MECA_CONT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,
     &            LPAOUT,'V')
      CALL REAJRE(SCON19,LCHOUT(1),'V')
      CALL REAJRE(VECCON,LCHOUT(1),'V')

      LCHOUT(1) = SCON19(1:8)//'.VE002'
      CALL CALCUL('S','CHAR_MECA_FROT',LIGRCF,7,LCHIN,LPAIN,1,LCHOUT,
     &            LPAOUT,'V')
      CALL REAJRE(SCON19,LCHOUT(1),'V')
      CALL REAJRE(VECFRO,LCHOUT(1),'V')

      CALL JEDEMA()
      END
