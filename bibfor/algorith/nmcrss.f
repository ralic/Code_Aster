C
C IN  SDSENS : SD SENSIBILITE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*8   BASENO
      CHARACTER*13  INPSCO 
      CHARACTER*24  SENSNB,SENSBA,SENSIN
      INTEGER       JSENSN,JSENSB,JSENSI 
      INTEGER       IFM,NIV         
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> ... CREATION SD SENSIBILITE'
      ENDIF      
C
C --- CREATION SD SENSIBILITE
C
      SENSNB = SDSENS(1:16)//'.NBPASE '
      SENSBA = SDSENS(1:16)//'.BASENO '
      SENSIN = SDSENS(1:16)//'.INPSCO '
      CALL WKVECT(SENSNB,'V V I'  ,1,JSENSN)
      CALL WKVECT(SENSBA,'V V K8' ,1,JSENSB)
      CALL WKVECT(SENSIN,'V V K16',1,JSENSI)
      BASENO = '&&OP0070'
      INPSCO = '&&OP0070_PSCO'       
      ZK8(JSENSB+1-1)  = BASENO
      ZK16(JSENSI+1-1) = INPSCO
C
      CALL JEDEMA()      
C
      END
