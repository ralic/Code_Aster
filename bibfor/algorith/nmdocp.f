      TXCP='ANALYTIQUE'      
      EXICP = GETEXM(MOCLEF,'ALGO_C_PLAN')
      EXI1D = GETEXM(MOCLEF,'ALGO_1D')
      IF (EXICP .EQ. 1) CALL GETVTX(MOCLEF,'ALGO_C_PLAN',K,IARG,1,
     &                              TXCP,N1)
