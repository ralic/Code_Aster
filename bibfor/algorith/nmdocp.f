C IN/OUT  NCOMEL  : NOMBRE TOTAL DE COMPORTEMENTS ELEMENTAIRES
C IN/OUT  LCOMEL  : NOMS DES COMPORTEMENTS ELEMENTAIRES
C OUT     TXCP    : TYPE DE CONTRAINTES PLANES : ANALYTIQUE OU DEBORST
C ----------------------------------------------------------------------
