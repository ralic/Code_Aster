      IF (TX1D.EQ.'DEBORST') THEN
         IF(TXCP.EQ.'DEBORST')THEN
            CALL U2MESS('F','ALGORITH7_58')
         ELSE
            TXCP=TX1D
         ENDIF
      ENDIF
      IF (TXCP.EQ.'DEBORST') THEN
         NCOMEL=NCOMEL+1
         LCOMEL(NCOMEL)='DEBORST'                
      ENDIF
      END
