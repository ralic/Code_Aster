C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT NONE
      INTEGER      POSMA
      REAL*8       LTANG1(3),LTANG2(3)
      CHARACTER*8  NOMA
      CHARACTER*24 DEFICO
      REAL*8       XPG,YPG
C
C ---------------------------------------------------------------------
C ROUTINE APPELEE PAR : MAPPAR
C ---------------------------------------------------------------------
C
C CALCUL DES VECTEURS TANGENTS LOCAUX AU POINT DE CONTACT
C ON PROCEDE PAR INTERPOLATION LINEAIRE DES VECTEURS TANGENTS 
C AUX NOEUDS DE LA MAILLE MAITRE SUR LAQUELLE LE POINT DE CONTACT
C SE PROJETE
C
C ACTIVE PAR L'OPTION LISSAGE
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  POSMA  : MAILLE MAITRE QUI RECOIT LA PROJECTION
C IN  XPG    : COORDONNEE X DU POINT ESCLAVE PROJETE
C IN  YPG    : COORDONNEE Y DU POINT ESCLAVE PROJETE
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C OUT LTANG1 : PREMIER VECTEUR TANGENT LOCAL AU POINT ESCLAVE PROJETE
C OUT LTANG2 : SECOND VECTEUR TANGENT LOCAL AU POINT ESCLAVE PROJETE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFMMVD,ZNOES
      CHARACTER*24 CONTMA,NOMACO,PNOMA,NOESCL      
      INTEGER      JMACO,JNOMA,JPONO,JNOESC
      INTEGER      NNO,JDEC,INO,POSNNO(9),I,J,IBID,NUMA
      REAL*8       FF(9)
      REAL*8       VECTA1(27),VECTA2(27)
      CHARACTER*8  ALIAS
C
C-----------------------------------------------------------------------
C
      CALL JEMARQ()
C      
      CONTMA = DEFICO(1:16)//'.MAILCO'
      NOMACO = DEFICO(1:16)//'.NOMACO'
      PNOMA  = DEFICO(1:16)//'.PNOMACO'
      NOESCL = DEFICO(1:16)//'.NOESCL'
      CALL JEVEUO(NOESCL,'L',JNOESC)
      CALL JEVEUO(CONTMA,'L',JMACO)
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(PNOMA, 'L',JPONO)
C
      ZNOES = CFMMVD('ZNOES')
C
      NUMA = ZI(JMACO+POSMA-1)
      CALL MMELTY(NOMA,NUMA,ALIAS,NNO,IBID)
C      
C --- VECTEURS TANGENTS LOCAUX AUX NOEUDS DE LA MAILLE MAITRE
C
      JDEC = ZI(JPONO+POSMA-1)
      DO 10 INO = 1,NNO
        POSNNO(INO) = ZI(JNOMA+JDEC+INO-1)
        VECTA1(3*(INO-1)+1) = ZR(JNOESC+ZNOES*(POSNNO(INO)-1)+1+4)
        VECTA1(3*(INO-1)+2) = ZR(JNOESC+ZNOES*(POSNNO(INO)-1)+1+5)
        VECTA1(3*(INO-1)+3) = ZR(JNOESC+ZNOES*(POSNNO(INO)-1)+1+6)
        VECTA2(3*(INO-1)+1) = ZR(JNOESC+ZNOES*(POSNNO(INO)-1)+1+7)  
        VECTA2(3*(INO-1)+2) = ZR(JNOESC+ZNOES*(POSNNO(INO)-1)+1+8)
        VECTA2(3*(INO-1)+3) = ZR(JNOESC+ZNOES*(POSNNO(INO)-1)+1+9)
   10 CONTINUE
C
C --- VECTEURS TANGENTS LOCAUX AU POINT SUR LA MAILLE MAITRE
C
      DO 40 I=1,3
        LTANG1(I)=0.D00
        LTANG2(I)=0.D00
   40 CONTINUE

      CALL CALFFX(ALIAS,XPG,YPG,FF)

      DO 61 I = 1,3
        DO 51 J = 1,NNO
          LTANG1(I) = FF(J)*VECTA1((J-1)*3+I) + LTANG1(I)
          LTANG2(I) = FF(J)*VECTA2((J-1)*3+I) + LTANG2(I)
   51   CONTINUE
   61 CONTINUE
C
      CALL JEDEMA()
      END
