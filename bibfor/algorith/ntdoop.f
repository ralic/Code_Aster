      IMPLICIT NONE
      INTEGER      NOPT
      CHARACTER*24 LISOPT
C ----------------------------------------------------------------------
C
C LIT LES OPTIONS DE CALCUL THERMIQUE.
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C
      INTEGER      N1,IER,JOPT
      CHARACTER*8  K8BID
      CHARACTER*16 NOMOPT
      INTEGER      IARG
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      NOMOPT = 'OPTION'
C
C --- RECUPERATION DE LA LISTE DES OPTIONS DE CALCUL
C
      CALL GETVTX (' ',NOMOPT,0,IARG,0,K8BID,N1)
      NOPT = -N1
      LISOPT = ' '
      IF (NOPT.EQ.0) GOTO 9999
      LISOPT = '&&NTDOOP.OPTION'
      CALL JEEXIN (LISOPT,IER)
      IF (IER.NE.0) CALL JEDETR (LISOPT)
      CALL WKVECT (LISOPT,'V V K16',NOPT,JOPT)
      CALL GETVTX(' ',NOMOPT,1,IARG,NOPT,ZK16(JOPT),N1)
C
C ----------------------------------------------------------------------
 9999 CONTINUE
      CALL JEDEMA()
      END
