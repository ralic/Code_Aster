C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT     NONE
      CHARACTER*24 RESOCO
      CHARACTER*16 PRECON
      INTEGER      NEQ,NBLIAI
      CHARACTER*19 MATASS,SOLVEU
      INTEGER      PREMAX
      REAL*8       TOLE,EPSI,RELA
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (RESOLUTION - GCP)
C
C PRECONDITIONNEMENT
C
C ----------------------------------------------------------------------
C 
C
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  SOLVEU : SD SOLVEUR
C IN  MATASS : NOM DE LA MATRICE DU PREMIER MEMBRE ASSEMBLEE
C IN  NBLIAI : NOMBRE DE LIAISONS DE CONTACT
C IN  NEQ    : NOMBRE D'EQUATIONS
C IN  PRECON : TYPE DE PRECONDITIONNEMENT
C              'DIRICHLET'
C              'SANS'
C IN  TOLE   : TOLERANCE POUR DETECTER PRESSION NULLE
C IN  PREMAX : NOMBRE MAXI D'ITERATIONS DU SOLVEUR ITERATIF
C IN  RELA   : COEF_RESI
C IN  EPSI   : RESI_ABSO
C
C --------------- DEBUT DECLARATIONS NORMALISEES JEVEUX ---------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      REAL*8       EPSIPC
      CHARACTER*19 SGRADP,SGRPRP
      INTEGER      JSGRAP,JSGPRP
      CHARACTER*24 APCOEF,APDDL,APPOIN
      INTEGER      JAPCOE,JAPDDL,JAPPTR
      CHARACTER*19 MU ,LIAC
      INTEGER      JMU,JLIAC
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      EPSIPC = EPSI*RELA
C
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C
      APPOIN = RESOCO(1:14)//'.APPOIN'
      APCOEF = RESOCO(1:14)//'.APCOEF'
      APDDL  = RESOCO(1:14)//'.APDDL'
      MU     = RESOCO(1:14)//'.MU'
      LIAC   = RESOCO(1:14)//'.LIAC'
      SGRADP = RESOCO(1:14)//'.SGDP'
      SGRPRP = RESOCO(1:14)//'.SGPP'
      CALL JEVEUO(APPOIN,'L',JAPPTR)
      CALL JEVEUO(APCOEF,'L',JAPCOE)
      CALL JEVEUO(APDDL ,'L',JAPDDL)
      CALL JEVEUO(MU    ,'L',JMU   )
      CALL JEVEUO(LIAC  ,'L',JLIAC )
      CALL JEVEUO(SGRADP,'L',JSGRAP)
      CALL JEVEUO(SGRPRP,'E',JSGPRP)
C
C --- INITIALISATION DU PRECONDITIONNEUR
C
      CALL R8INIR(NBLIAI,0.D0,ZR(JSGPRP),1)
C
C --- PRECONDITIONNEMENT (OU PAS)
C
      IF (PRECON.EQ.'DIRICHLET') THEN
        CALL PREGCP(RESOCO ,NEQ       ,NBLIAI    ,TOLE      ,EPSIPC   ,
     &              ZR(JMU),ZR(JAPCOE),ZI(JAPDDL),ZI(JAPPTR),ZI(JLIAC),
     &              MATASS ,SOLVEU    ,PREMAX    ,ZR(JSGRAP),ZR(JSGPRP))
      ELSEIF (PRECON.EQ.'SANS') THEN
        CALL DCOPY(NBLIAI,ZR(JSGRAP),1,ZR(JSGPRP),1)
      ELSE
        CALL ASSERT(.FALSE.)
      END IF 
C
      CALL JEDEMA()
C
      END
