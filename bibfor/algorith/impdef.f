C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT NONE
      INTEGER      ZFON
      INTEGER      ZDEF
      LOGICAL      FONACT(ZFON)
      INTEGER      INFCMP(4)
      INTEGER      NBSUIV
      INTEGER      JIMCOL
      INTEGER      NBCOL
      CHARACTER*8  MODE
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : IMPMIN/IMPSTD
C ----------------------------------------------------------------------
C
C DEFINITION DES COLONNES ACTIVEES POUR LES CAS STANDARD ET MINIMUM
C
C IN  ZDEF   : NOMBRE MAXI DE COLONNES DISPONIBLES POUR L'AFFICHAGE
C IN  ZFON   : LONGUEUR MAXI DU VECTEUR FONACT
C IN  FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C            FONACT(1):  RECHERCHE LINEAIRE
C            FONACT(2):  PILOTAGE
C            FONACT(3):  LOIS NON LOCALES
C            FONACT(4):  CONTACT DISCRET
C            FONACT(5):  CONTACT CONTINU
C            FONACT(6):  METHODE XFEM
C            FONACT(7):  ALGORITHME DE DE BORST
C            FONACT(8):  CONVERGENCE PAR RESIDU DE REFERENCE
C            FONACT(9):  METHODE XFEM AVEC CONTACT
C            FONACT(10): CONTACT/FROTTEMENT CONTINU
C            FONACT(11): FETI
C IN  INFCMP : VAUT 1 SI ON DOIT AFFICHER DES INFOS SUR LE NOEUD OU
C              LES RESIDUS SONT EVALUES
C            INFCMP(1):  RESI_RELA
C            INFCMP(2):  RESI_MAXI
C            INFCMP(3):  RESI_REFE
C            INFCMP(4):  CTCD_GEOM
C IN  NBSUIV : NOMBRE DE SUIVIS EN TEMPS REEL
C IN  MODE   : MODE DE DEFINITION DES COLONNES
C            'STANDARD': TOUTES LES COLONNES ATTACHEES A DES 
C                          FONCTIONNALITES ACTIVEES
C                         + DETAILS SUR LES RESIDUS (INFCMP)
C                         + SUIVIS EN TEMPS REEL (NBSUIV)
C            'MINIMUM' : ITERATION DE NEWTON
C                          RESIDUS PRINCIPAUX RESI_RELA ET RESI_MAXI
C IN  JIMCOL : ADRESSE JEVEUX DU VECTEUR CONTENANT LE CODE DE LA 
C              COLONNE ACTIVEE
C OUT NBCOL  : NOMBRE DE COLONNES ACTIVEES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      ISUI,ICOL
      LOGICAL      RECLIN,PILOTA,NONLOC,CTCDIS,CTCCON,DEBORS,RESREF,
     &             LFETI
      CHARACTER*16 NOMSUI
      CHARACTER*1  FOR1
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- FONCTIONNALITES ACTIVEES
C
      RECLIN = FONACT(1)
      PILOTA = FONACT(2)
      NONLOC = FONACT(3)
      CTCDIS = FONACT(4)
      CTCCON = FONACT(5)
      DEBORS = FONACT(7)
      RESREF = FONACT(8)
      LFETI  = FONACT(11)
C
C ---
C --- MODE STANDARD
C ---
C
      IF (MODE.EQ.'STANDARD') THEN
        ICOL = 1
C
C --- CONTACT CONTINU
C  
        IF (CTCCON) THEN
          CALL IMPDEA(ZDEF,'CTCC_GEOM',JIMCOL,ICOL)
          IF (FONACT(10)) THEN
            CALL IMPDEA(ZDEF,'CTCC_FROT',JIMCOL,ICOL)
          ENDIF
          CALL IMPDEA(ZDEF,'CTCC_CONT',JIMCOL,ICOL)
        ENDIF 
C
C --- ITERATIONS DE NEWTON
C
        CALL IMPDEA(ZDEF,'ITER_NEWT',JIMCOL,ICOL)
C
C --- RESI_GLOB_RELA
C
        CALL IMPDEA(ZDEF,'RESI_RELA',JIMCOL,ICOL)
        IF (INFCMP(1).EQ.1) THEN
          CALL IMPDEA(ZDEF,'RELA_NOEU',JIMCOL,ICOL)
        ENDIF
C
C --- RESI_GLOB_MAXI
C
        CALL IMPDEA(ZDEF,'RESI_MAXI',JIMCOL,ICOL)
        IF (INFCMP(2).EQ.1) THEN
          CALL IMPDEA(ZDEF,'MAXI_NOEU',JIMCOL,ICOL)
        ENDIF
C
C --- RESIDU PAR REFERENCE
C
        IF (RESREF) THEN
          CALL IMPDEA(ZDEF,'RESI_REFE',JIMCOL,ICOL)
          IF (INFCMP(3).EQ.1) THEN
            CALL IMPDEA(ZDEF,'REFE_NOEU',JIMCOL,ICOL)
          ENDIF
        ENDIF
C
C --- METHODE DE BORST
C
        IF (DEBORS) THEN
          CALL IMPDEA(ZDEF,'ITER_DEBO',JIMCOL,ICOL)
        ENDIF
C
C --- RECHERCHE LINEAIRE
C
        IF (RECLIN) THEN
          CALL IMPDEA(ZDEF,'RELI_ITER',JIMCOL,ICOL)
          CALL IMPDEA(ZDEF,'RELI_COEF',JIMCOL,ICOL)
        ENDIF 
C
C --- PILOTAGE
C
        IF (PILOTA) THEN
          CALL IMPDEA(ZDEF,'PILO_PARA',JIMCOL,ICOL)
        ENDIF 
C
C --- LOIS NON LOCALES
C
        IF (NONLOC) THEN
          CALL IMPDEA(ZDEF,'LAGR_ECAR',JIMCOL,ICOL)
          CALL IMPDEA(ZDEF,'LAGR_INCR',JIMCOL,ICOL)
          CALL IMPDEA(ZDEF,'LAGR_ITER',JIMCOL,ICOL)
        ENDIF 
C
C --- OPTION D'ASSEMBLAGE
C
        CALL IMPDEA(ZDEF,'MATR_ASSE',JIMCOL,ICOL)
C
C --- CONTACT DISCRET
C  
        IF (CTCDIS) THEN
          CALL IMPDEA(ZDEF,'CTCD_ITER',JIMCOL,ICOL)
          CALL IMPDEA(ZDEF,'CTCD_INFO',JIMCOL,ICOL)
          CALL IMPDEA(ZDEF,'CTCD_GEOM',JIMCOL,ICOL)
          IF (INFCMP(4).EQ.1) THEN
            CALL IMPDEA(ZDEF,'CTCD_NOEU',JIMCOL,ICOL)
          ENDIF
        ENDIF

C
C --- FETI
C
        IF (LFETI) CALL IMPDEA(ZDEF,'ITER_FETI',JIMCOL,ICOL)
C
C --- SUIVIS EN TEMPS REEL
C
        IF (NBSUIV.GT.4) THEN
          CALL UTMESS('F','IMPDEF',
     &     'TROP DE COLONNES DE SUIVI (LIMITE A QUATRE)')
        ENDIF
    
        DO 10 ISUI = 1, NBSUIV
          WRITE (FOR1,'(I1)') ISUI
          NOMSUI = 'SUIV_'//FOR1
          CALL IMPDEA(ZDEF,NOMSUI(1:9),JIMCOL,ICOL)
   10   CONTINUE
C
C ---
C --- MODE MINIMUM
C ---
C
      ELSE IF (MODE.EQ.'MINIMUM ') THEN
        ICOL = 1
C
C --- ITERATIONS DE NEWTON
C
        CALL IMPDEA(ZDEF,'ITER_NEWT',JIMCOL,ICOL)
C
C --- RESI_GLOB_RELA
C
        CALL IMPDEA(ZDEF,'RESI_RELA',JIMCOL,ICOL)
        IF (INFCMP(1).EQ.1) THEN
          CALL IMPDEA(ZDEF,'RELA_NOEU',JIMCOL,ICOL)
        ENDIF
C
C --- RESI_GLOB_MAXI
C
        CALL IMPDEA(ZDEF,'RESI_MAXI',JIMCOL,ICOL)
        IF (INFCMP(2).EQ.1) THEN
          CALL IMPDEA(ZDEF,'MAXI_NOEU',JIMCOL,ICOL)
        ENDIF
C
C --- FETI
C
        IF (LFETI) CALL IMPDEA(ZDEF,'ITER_FETI',JIMCOL,ICOL)

      ELSE
        CALL UTMESS('F','IMPDEF',
     &              'MODE INCORRECT (DVLP)')
      ENDIF
C
      NBCOL = ICOL - 1
C
      CALL JEDEMA()

      END
