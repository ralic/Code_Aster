      SUBROUTINE DIINIT(INSTAN, PARTPS, DERNIE, RESULT,NBOBSE,NUMINI,
     &                 LOBSER,NUOBSE,NOMTAB,MAILL2,NBOBAR,LISINS,LISOBS)

