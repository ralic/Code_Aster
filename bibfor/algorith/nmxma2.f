     &                  SDDISC,FONACT,NUMINS,VALINC,SOLALG,
     &                  LISCHA,DEFICO,RESOCO,SOLVEU,NUMEDD,
     &                  NBMATR,LTYPMA,LOPTME,LOPTMA,LCALME,
     &                  LASSME,MEELEM,MEASSE)
