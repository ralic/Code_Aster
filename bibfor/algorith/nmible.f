C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE MABBAS M.ABBAS
C TOLE CRP_21
C
      IMPLICIT NONE
      LOGICAL      PREMIE
      INTEGER      NIVEAU
      CHARACTER*8  NOMA
      CHARACTER*19 LISCHA,SOLVEU,MATASS
      CHARACTER*24 DEFICO,RESOCO
      CHARACTER*24 MODELE,NUMEDD
      CHARACTER*19 MEELEM(8)
      CHARACTER*19 MEASSE(8)
      CHARACTER*24 VALMOI(8),VALPLU(8),DEPALG(8)
      CHARACTER*19 SDDYNA
      REAL*8       INST(*)
      LOGICAL      FONACT(*)
C      
C ----------------------------------------------------------------------
C
C   STAT_NON_LINE : ROUTINE D'AIGUILLAGE DES ITERATIONS SE SITUANT
C                   ENTRE LES PAS DE TEMPS ET L'EQUILIBRE
C
C                   POUR LE MOMENT, SERT A LA METHODE CONTINUE DE
C                   CONTACT
C
C                   SERVIRA DANS L'AVENIR POUR LES CALCULS DE
C                   SENSIBILITE
C
C                   LES ITERATIONS ONT LIEU ENTRE CETTE ROUTINE
C                   (NMIBLE) ET SA COUSINE (NMTBLE) QUI COMMUNIQUENT
C                   POUR LE MOMENT PAR LA VARIABLE NIVEAU
C
C   IN  NIVEAU : INDICATEUR D'UTILISATION DE LA BOUCLE 
C        -1     ON N'UTILISE PAS CETTE BOUCLE
C         4     BOUCLE CONTACT METHODE CONTINUE
C                 INITIALISATION 
C         3     BOUCLE CONTACT METHODE CONTINUE
C                 BOUCLE GEOMETRIE
C         2     BOUCLE CONTACT METHODE CONTINUE
C                 BOUCLE SEUILS DE FROTTEMENT
C         1     BOUCLE CONTACT METHODE CONTINUE
C                 BOUCLE CONTRAINTES ACTIVES
C
C ----------------------------------------------------------------------
C
      CHARACTER*8  NOMO
      INTEGER      MMITGO,MMITCA,MMITFR,ITERG
      CHARACTER*24 K24BLA,K24BID
      REAL*8       R8BID
      INTEGER      IBID
      LOGICAL      ISFONC,LFROTT,LXFEM,LCTCC,LBID  
C
C ----------------------------------------------------------------------
C 
      NOMO   = MODELE   
      K24BLA = ' '             
C     
      IF (NIVEAU.LT.0) THEN 
        GOTO 9999
      ENDIF
C
C --- INFOS SUR LE CONTACT
C
      CALL MMINFP(0    ,DEFICO,K24BLA,'FROTTEMENT',
     &            IBID ,R8BID ,K24BID,LFROTT)
      LXFEM  = ISFONC(FONACT,'XFEM')  
      LCTCC  = ISFONC(FONACT,'CONT_CONTINU') 
C       
      GOTO (101,102,103,104) NIVEAU
C
C --- NIVEAU: 4   BOUCLE CONTACT METHODE CONTINUE
C ---             INITIALISATION
C
 104  CONTINUE
      CALL MMINIT(DEFICO,RESOCO,SDDYNA,VALMOI,VALPLU)
      CALL MMBOUC(RESOCO,'GEOM','INIT',MMITGO)
C
C --- NIVEAU: 3   BOUCLE CONTACT METHODE CONTINUE
C ---             BOUCLE GEOMETRIE
C      
 103  CONTINUE
      NIVEAU = 3
      CALL MMBOUC(RESOCO,'GEOM','INCR',MMITGO)
      IF (LXFEM.AND.LCTCC) THEN
        CALL MMINFP(0    ,DEFICO,K24BLA,'ITER_GEOM_MAXI',
     &              ITERG,R8BID ,K24BID,LBID)
        IF (ITERG.GT.0) THEN
          CALL NMIMPR('IMPR','BCL_GEOME',' ',0.D0,MMITGO)
C
C ---  REACTUALISATION DES FACETTES DE CONTACT 
C     
          CALL XREACG(NOMO  ,NOMA  ,DEFICO,RESOCO)
C
C ---  APPARIEMENT (CAS X-FEM)
C
          CALL XAPPAR(PREMIE,NOMA  ,NOMO  ,DEFICO)

        ENDIF      
      
      ELSEIF (.NOT.LXFEM) THEN   
         
        CALL NMIMPR('IMPR','BCL_GEOME',' ',0.D0,MMITGO)    
C
C --- APPARIEMENT
C
        CALL MAPPAR(PREMIE,NOMA  ,NUMEDD,DEFICO,RESOCO)
      END IF
      CALL MMBOUC(RESOCO,'FROT','INIT',MMITFR)
C
C --- SI PAS DE FROTTEMENT -> PAS DE BOUCLE SUR SEUIL DE FROTTEMENT
C      
      IF (.NOT.LFROTT) THEN 
        GOTO 666
      ENDIF  
C
C --- NIVEAU: 2   BOUCLE CONTACT METHODE CONTINUE
C ---             BOUCLE SEUILS DE FROTTEMENT
C 
 102  CONTINUE
      NIVEAU = 2
      CALL MMBOUC(RESOCO,'FROT','INCR',MMITFR)
      CALL NMIMPR('IMPR','BCL_SEUIL',' ',0.D0,MMITFR)
C
C -- BOUCLE DE CONTRAINTES ACTIVES (CONTACT ECP)
C
 666  CONTINUE
      CALL MMBOUC(RESOCO,'CONT','INIT',MMITCA)
C
C --- NIVEAU: 1   BOUCLE CONTACT METHODE CONTINUE
C ---             BOUCLE CONTRAINTES ACTIVES
C      
 101  CONTINUE
      NIVEAU = 1
      CALL MMBOUC(RESOCO,'CONT','INCR',MMITCA)
      CALL NMIMPR('IMPR','BCL_CTACT',' ',0.D0,MMITCA)
C
C --- CREATION ELEMENTS TARDIFS ET CARTE
C 
      IF (LXFEM) THEN
        CALL XCONLI(NOMA  ,NOMO  ,PREMIE,DEFICO,RESOCO,
     &              LISCHA,SOLVEU,NUMEDD,VALMOI,VALPLU,
     &              DEPALG)
      ELSE
        CALL CONLIG(NOMA  ,MODELE,NUMEDD,DEFICO,RESOCO,
     &              LISCHA,SOLVEU,MATASS,INST  ,SDDYNA,
     &              VALMOI,VALPLU,DEPALG,MEELEM,MEASSE) 
      END IF        
C
 9999 CONTINUE
C
      END
