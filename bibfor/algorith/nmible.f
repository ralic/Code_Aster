      SUBROUTINE NMIBLE(NIVEAU,
     &                  PREMIE,MODELE,NOMA  ,DEFICO,RESOCO,
     &                  FONACT,LISCHA,SOLVEU,NUMEDD,MATASS,
     &                  INST  ,SDDYNA,VALMOI,VALPLU,DEPALG,
     &                  MEELEM,MEASSE)
