      SUBROUTINE PIPEF3(NDIM, NNO, NDDL,NPG,LGPG,WREF, VFF,
     &                  DFDE,   MATE,   GEOM,   VIM,
     &                  DDEPL,  DEPLM,  DDEPL0, DDEPL1,
     &                  DTAU,   COPILO, TYPMOD             )

