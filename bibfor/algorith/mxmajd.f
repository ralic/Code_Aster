C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*19 SDDYNA
      REAL*8       DELTAT
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME - EXPLICITE)
C
C MET A JOUR LES VITESSES ET LES DEPL/VITE/ACCE GENERALISEES
C (PROJ_MODAL)
C      
C ----------------------------------------------------------------------
C
C
C IN  SDDYNA : SD DEDIEE A LA DYNAMIQUE (CF NDLECT)
C IN  DELTAT : INCREMENT DE TEMPS 
C
C
C
C
      CHARACTER*19 DEPGEP,VITGEP,ACCGEP
      INTEGER      JDEPGP,JVITGP,JACCGP       
      CHARACTER*19 DEPGEM,VITGEM,ACCGEM
      INTEGER      JDEPGM,JVITGM,JACCGM
      INTEGER      IMODE
      REAL*8       COEVI1,COEVI2 
      LOGICAL      NDYNLO
      INTEGER      NDYNIN,NBMODP
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- COEFFICIENTS
C
      IF (NDYNLO(SDDYNA,'DIFF_CENT')) THEN
        COEVI1 = 0.5D0*DELTAT
        COEVI2 = 0.5D0*DELTAT
      ELSEIF (NDYNLO(SDDYNA,'TCHAMWA')) THEN
        COEVI1 = 0.D0
        COEVI2 = DELTAT
      ENDIF       
C
C --- NOM DES OBJETS POUR PROJECTION MODALE
C
      CALL NDYNKK(SDDYNA,'PRMO_DEPGEM',DEPGEM)
      CALL NDYNKK(SDDYNA,'PRMO_VITGEM',VITGEM)
      CALL NDYNKK(SDDYNA,'PRMO_ACCGEM',ACCGEM)
      CALL NDYNKK(SDDYNA,'PRMO_DEPGEP',DEPGEP)
      CALL NDYNKK(SDDYNA,'PRMO_VITGEP',VITGEP)
      CALL NDYNKK(SDDYNA,'PRMO_ACCGEP',ACCGEP)
      NBMODP = NDYNIN(SDDYNA,'NBRE_MODE_PROJ')
                   
      CALL JEVEUO(DEPGEP,'E',JDEPGP)      
      CALL JEVEUO(DEPGEM,'E',JDEPGM)
      CALL JEVEUO(VITGEP,'E',JVITGP)      
      CALL JEVEUO(VITGEM,'E',JVITGM)
      CALL JEVEUO(ACCGEP,'E',JACCGP)      
      CALL JEVEUO(ACCGEM,'E',JACCGM)            

      DO 11 IMODE = 1,NBMODP
        ZR(JVITGP+IMODE-1) = ZR(JVITGM+IMODE-1) + 
     &                       COEVI1*ZR(JACCGP+IMODE-1) +
     &                       COEVI2*ZR(JACCGM+IMODE-1)
        ZR(JDEPGM+IMODE-1) = ZR(JDEPGP+IMODE-1)
        ZR(JVITGM+IMODE-1) = ZR(JVITGP+IMODE-1)
        ZR(JACCGM+IMODE-1) = ZR(JACCGP+IMODE-1)
                        
        
11    CONTINUE

C
      CALL JEDEMA()
      END
