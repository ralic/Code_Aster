C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
      INTEGER      IIN
      CHARACTER*8  NTYMA1
      CHARACTER*8  NTYMA2
      INTEGER      NNDEL
      INTEGER      IORDR
C
C ----------------------------------------------------------------------
C ROUTINE APPELLEE PAR : XMLIGR
C ----------------------------------------------------------------------
C ROUTINE SPECIFIQUE A L'APPROCHE <<GRANDS GLISSEMENTS AVEC XFEM>>,
C TRAVAIL EFFECTUE EN COLLABORATION AVEC I.F.P.
C ----------------------------------------------------------------------
C RETOURNE DES INFOS SUR LES ELEMENTS DE CONTACT FORMES ENTRE
C DEUX ELEMENTS DE SURFACE
C
C IN  IIN    : NUMERO D'ORDRE EN ENTREE
C               SI ZERO: ON UTILISE NTYMA1/NTYMA2
C IN  NTYMA1 : PREMIERE MAILLE
C IN  NTYMA2 : SECONDE  MAILLE
C OUT IORDR  : ORDRE DANS LA LISTE DES ELEMENTS
C OUT NNDEL  : NOMBRE DE NOEUDS DE L'ELEMENT DE CONTACT
C
C
C ----------------------------------------------------------------------
C
      INTEGER      NBTYP
      PARAMETER    (NBTYP=15)
      CHARACTER*8  CPL(NBTYP,12)
      INTEGER      NPL(NBTYP)
      INTEGER      K
C
      DATA (CPL(K,1),K=1,NBTYP) /
     &      'QUAD8','TRIA6','QUAD4','TRIA3','QUAD4','TRIA3',
     &      'QUAD8','TRIA6','QUAD4','TRIA3','QUAD4','TRIA3',
     &      'HEXA8','PENTA6','TETRA4'/
      DATA (CPL(K,2),K=1,NBTYP) /
     &      'QUAD8','TRIA6','QUAD4','TRIA3','TRIA3','QUAD4',
     &      'QUAD8','TRIA6','QUAD4','TRIA3','TRIA3','QUAD4',
     &      'HEXA8','PENTA6','TETRA4'/
      DATA (NPL(K),K=1,NBTYP) /
     &      12 ,9 ,8 , 6, 7, 7, 12 ,9 ,8 , 6, 7, 7, 16, 12, 8/
C
C ----------------------------------------------------------------------
C
      IORDR = 0
      NNDEL = 0
C
      IF (IIN.EQ.0) THEN
        DO 10 K=1,NBTYP
          IF (NTYMA1.EQ.CPL(K,1)) THEN
            IF (NTYMA2.EQ.CPL(K,2)) THEN
              NNDEL = NPL(K)
              IORDR = K
            ENDIF
          ENDIF
  10    CONTINUE
      ELSEIF ((IIN.GT.0).AND.(IIN.LE.NBTYP)) THEN
        IORDR = IIN
        NNDEL = NPL(IIN)
      ELSE
        IORDR = 0
      ENDIF
      CALL ASSERT(IORDR.NE.0)

      END
