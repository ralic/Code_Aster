      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER       FONACT(*)
      CHARACTER*19  SDDYNA,SDNUME
      CHARACTER*19  LISCHA
      CHARACTER*24  MODELE,MATE,CARELE, NUMEDD
      CHARACTER*24  COMPOR,CARCRI,COMREF
      CHARACTER*24  SDTIME,SDSTAT
      CHARACTER*19  SDDISC
      REAL*8        PARCON(*)
      CHARACTER*24  DEFICO,RESOCO,RESOCU
      CHARACTER*19  SOLALG(*),VEASSE(*),VALINC(*)
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C INITIALISATIONS SPECIFIQUES POUR SCHEMAS MULTIPAS EN POURSUITE
C
C ----------------------------------------------------------------------
C
C IN  SDDYNA : SD DYNAMIQUE
C IN  SDTIME : SD TIMER
C IN  SDSTAT : SD STATISTIQUES
C
C
C
C
      INTEGER      IFM,NIV
      INTEGER      NOCC,IRET
      LOGICAL      EVONOL
      CHARACTER*19 COMMOI,INSMOI
      CHARACTER*24 K24BID
      INTEGER      IARG
C
C ----------------------------------------------------------------------
C
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> INITIALISATION MULTI-PAS'
      ENDIF
C
      CALL GETVID('ETAT_INIT','EVOL_NOLI',1,IARG,1,K24BID,NOCC)
      EVONOL = NOCC .GT. 0
C
C --- DECOMPACTION DES VARIABLES CHAPEAUX
C
      CALL NMCHEX(VALINC,'VALINC','COMMOI',COMMOI)
      IF (EVONOL) THEN
        CALL NMVCEX('INST',COMMOI,INSMOI)
        CALL EXISD('CHAMP_GD',INSMOI,IRET)
        IF ( IRET .NE. 0 ) THEN
          CALL NMCHHT(MODELE,NUMEDD,MATE  ,COMPOR,CARELE,
     &                LISCHA,CARCRI,COMREF,FONACT,SDSTAT,
     &                SDDYNA,SDTIME,DEFICO,RESOCO,RESOCU,
     &                VALINC,SDDISC,PARCON,SOLALG,VEASSE,
     &                SDNUME)
        ENDIF
      ENDIF

      CALL JEDEMA()
      END
