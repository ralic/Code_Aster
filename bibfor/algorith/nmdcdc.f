      IMPLICIT     NONE
      CHARACTER*19 SDDISC
      CHARACTER*24 NOMLIS
      INTEGER      NBRPAS,NUMINS
C
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (GESTION DES EVENEMENTS - DECOUPE)
C
C MISE A JOUR DES SD APRES DECOUPE
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  NUMINS : NUMERO D'INSTANTS
C IN  NOMLIS : NOM DE LA LISTE DES INSTANTS A AJOUTER
C IN  NBRPAS : NOMBRE D'INSTANTS A AJOUTER
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      JINST
      INTEGER      NBINS,NBINI
      REAL*8       R8BID,DT0
      CHARACTER*8  K8BID
      INTEGER      IBID
      CHARACTER*16 METLIS
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES OBJET INST
C
      CALL JEVEUO(NOMLIS,'L',JINST )
C
C --- GESTION DE LA LISTE D'INSTANT
C
      CALL UTDIDT('L'   ,SDDISC,'LIST',IBID  ,'METHODE',
     &            R8BID ,IBID  ,METLIS)
C
C --- LONGUEUR INITIALE DE LA LISTE D'INSTANTS
C
      CALL UTDIDT('L'   ,SDDISC,'LIST',IBID  ,'NBINST' ,
     &            R8BID ,NBINI ,K8BID )
C
C --- NOMBRE D'INSTANTS A AJOUTER
C
      IF (METLIS.EQ.'AUTO') THEN
        NBINS = 1
      ELSEIF (METLIS.EQ.'MANUEL') THEN
        NBINS = NBRPAS - 1
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- EXTENSION DE LA LISTE D'INSTANTS
C
      CALL NMDCEI(SDDISC,NUMINS,ZR(JINST),NBINI ,NBINS ,
     &            'DECO',DT0   )
C
C --- EXTENSION DE LA LISTE DES NIVEAUX DE DECOUPAGE
C
      CALL NMDCEN(SDDISC,NUMINS,NBINI ,NBINS )
C
C --- ENREGISTREMENT INFOS
C
      CALL UTDIDT('E'   ,SDDISC,'LIST',IBID  ,'DT-',
     &            DT0   ,IBID  ,K8BID )
C
      CALL JEDEMA()
      END
