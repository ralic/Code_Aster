C  BUT: COMPARER DEUX LISTES DE NUMEROS DE NOEUDS PAR ORDRE CROISSANT ET
      IMPLICIT NONE
C         DETECTER LES ELEMENTS COMMUNS
C         ARRET EN CAS D'INTERSECTION NON VIDE
C-----------------------------------------------------------------------
C
C NBL      /I/: NOMBRE DE NOEUDS INTERFACE LIBRE
C NBB      /I/: NOMBRE DE NOEUDS INTERFACE BLOQUEE
C NOL      /I/: VECTEUR DES NUMEROS DES NOEUDS LIBRES
C NOB      /I/: VECTEUR DES NUMEROS DES NOEUDS BLOQUES
C MAILLAGE /I/: NOM DU UTILISATEUR DU MAILLAGE
C
C
C
C
C
      INCLUDE 'jeveux.h'
      CHARACTER*24 VALK(3)
      CHARACTER*8 NOMNOE,MAILLA,TYPL,TYPB
      INTEGER NOL(NBL),NOB(NBB)
      LOGICAL DOUBLE
      INTEGER I ,JF ,LCOU ,LP ,NBB ,NBL
C-----------------------------------------------------------------------
C
C
      IF (NBL.EQ.0 .OR. NBB.EQ.0) GOTO 9999
C
      DOUBLE = .FALSE.
      JF = 1
      DO 10 I = 1,NBL
        JF = JF - 1
        LCOU = NOL(I)
        LP = 0
   20   CONTINUE
        IF (LP.LT.LCOU .AND. JF.LT.NBB) THEN
          JF = JF + 1
          LP = NOB(JF)
          IF (LP.EQ.LCOU) THEN
            DOUBLE = .TRUE.
            CALL JENUNO(JEXNUM(MAILLA//'.NOMNOE',LP),NOMNOE)
            VALK(1) = NOMNOE
            VALK(2) = TYPL
            VALK(3) = TYPB
           CALL U2MESG('E', 'ALGORITH13_69',3,VALK,0,0,0,0.D0)
          END IF

          GO TO 20

        ELSE
          GO TO 10

        END IF

   10 CONTINUE
C
      GOTO 9999

 9999 CONTINUE
      END
