C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*8   NOMA
      CHARACTER*4   TYPNOE      
      CHARACTER*24  DEFICO
      INTEGER       IZONE,NUMMAI
      REAL*8        TAU1(3),TAU2(3)
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (TOUTES METHODES - APPARIEMENT)
C
C ORIENTATION DES TANGENTES DANS LE CAS DES POUTRES
C      
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  NUMMAI : NUMERO ABSOLU DE LA MAILLE
C IN  IZONE  : NUMERO DE LA ZONE DE CONTACT
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  TYPNOE : TYPE DU NOEUD 'MAIT' OU 'ESCL'
C OUT TAU1   : PREMIERE TANGENTE (NON NORMALISEE)
C OUT TAU2   : SECONDE TANGENTE (NON NORMALISEE)
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*32 JEXNUM
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      ITYPE
      CHARACTER*8  NOMMAI      
      CHARACTER*24 K24BLA,K24BID,TYPNOR   
      REAL*8       VECTOR(3),NORME
      LOGICAL      LBID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C       
      K24BLA = ' '
      CALL JENUNO(JEXNUM(NOMA//'.NOMMAI',NUMMAI),NOMMAI)
C
C --- TYPE DE NORMALE
C
      IF (TYPNOE.EQ.'ESCL') THEN
        TYPNOR = 'VECT_ESCL'
      ELSEIF (TYPNOE.EQ.'MAIT') THEN
        TYPNOR = 'VECT_MAIT'
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- CHOIX DE LA NORMALE SUIVANT UTILISATEUR
C      
      CALL MMINFP(IZONE ,DEFICO,K24BLA,TYPNOR,
     &            ITYPE ,VECTOR,K24BID,LBID)
      CALL NORMEV(VECTOR,NORME)                 
C
C --- REDEFINITION SI BASE LOCALE DANS LE CAS DES POUTRES
C
      IF (ITYPE.EQ.0) THEN
        CALL U2MESS('I','CONTACT3_33')                
        CALL U2MESK('F','CONTACT_60',1,NOMMAI)
      ELSEIF (ITYPE.EQ.1) THEN
        CALL PROVEC(VECTOR,TAU1,TAU2)   
      ELSEIF (ITYPE.EQ.2) THEN
        CALL DCOPY(3,VECTOR,1,TAU2,1)        
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF  
C
      CALL JEDEMA()
      END
