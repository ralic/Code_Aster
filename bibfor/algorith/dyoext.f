      IMPLICIT     NONE
      CHARACTER*8  MAILLA
      INTEGER      TYPCHA
      CHARACTER*8  TOPO
      INTEGER      SUBTOP
      CHARACTER*8  CMP
      INTEGER      VARINT
      CHARACTER*24 DEPPLU
      CHARACTER*24 VITPLU
      CHARACTER*24 ACCPLU
      CHARACTER*24 SIGPLU
      CHARACTER*24 VARPLU
      CHARACTER*24 DEPENT
      CHARACTER*24 VITENT
      CHARACTER*24 ACCENT
      INTEGER      ICTC
      CHARACTER*19 CNSINZ
      INTEGER      ISND
      CHARACTER*24 CNFEDO
      CHARACTER*24 CNFEPI
      CHARACTER*24 CNFSDO
      CHARACTER*24 CNFSPI
      INTEGER      INOEUD
      INTEGER      ICMP
      REAL*8       VALR
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : DYOBAR/DYOSUI
C ----------------------------------------------------------------------
C
C REALISATION D'UNE OBSERVATION OU D'UN SUIVI
C RECUPERATION DE LA COMPOSANTE
C
C IN  MAILLA : NOM DU MAILLAGE
C IN  TYPCHA : TYPE DE CHAMP
C           TYPCHA = 1      'DEPL_ABSOLU'  
C           TYPCHA = 2      'VITE_ABSOLU'   
C           TYPCHA = 3      'ACCE_ABSOLU' 
C           TYPCHA = 4      'DEPL'   
C           TYPCHA = 5      'VALE_CONT'
C           TYPCHA = 6      'VITE'
C           TYPCHA = 7      'ACCE'
C           TYPCHA = 8      'SIEF_ELGA'
C           TYPCHA = 9      'VARI_ELGA'
C           TYPCHA = 10     'FORC_NODA'
C IN  TOPO   : ENTITE TOPOLOGIQUE PRINCIPALE (NOEUD OU MAILLE)
C IN  SUBTOP : NUMERO DU POINT DE GAUSS SI ENTITE PRINCIPALE EST MAILLE
C IN  CMP    : COMPOSANTE A OBSERVER
C IN  VARINT : VARIABLE INTERNE A OBSERVER SI TYPCHA=9
C IN  DEPPLU : DEPLACEMENTS A L'INSTANT ACTUEL
C IN  VITPLU : VITESSES A L'INSTANT ACTUEL
C IN  ACCPLU : ACCELERATIONS A L'INSTANT ACTUEL
C IN  SIGPLU : CONTRAINTES A L'INSTANT ACTUEL
C IN  VARPLU : VARIABLES INTERNES A L'INSTANT ACTUEL
C IN  DEPENT : DEPLACEMENTS DE REFERENCE POUR LES DEPLACEMENTS ABSOLUS
C IN  VITENT : VITESSES DE REFERENCE POUR LES VITESSES ABSOLUS
C IN  ACCENT : ACCELERATIONS DE REFERENCE POUR LES ACCELERATIONS
C              ABSOLUS
C IN  ICTC   : VAUT ZERO SI LE CHAM_NO_S DE CONTACT N'EXISTE PAS
C IN  CNSINZ : CHAM_NO CONTENANT LES INFOS SUR LE CONTACT
C IN  ISND   : VAUT ZERO SI LE SECOND MEMBRE N'EXISTE PAS
C IN  CNFEDO : FORCE DONNEE FIXE (SECOND MEMBRE)
C IN  CNFEPI : FORCE PILOTEE FIXE (SECOND MEMBRE)
C IN  CNFSDO : FORCE DONNEE SUIVEUSE (SECOND MEMBRE)
C IN  CNFSPI : FORCE PILOTEE SUIVEUSE (SECOND MEMBRE)
C OUT INOEUD : NUMERO ABSOLU DU NOEUD
C OUT ICMP   : NUMERO ABSOLU DE LA COMPOSANTE
C OUT VALR   : VALEUR REELLE DE LA COMPOSANTE POUR SIEF_ELGA 
C                ET VARI_ELGA
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      COMPLEX*16   CBID
      INTEGER      IRET,IFEDO,IFEPI,IFSDO,IFSPI
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C ---
C
      VALR = 0.D0
      IF     (TYPCHA.EQ.1) THEN
        CALL POSDDL('CHAM_NO',DEPPLU,TOPO,CMP,INOEUD,ICMP)
        CALL POSDDL('CHAM_NO',DEPENT,TOPO,CMP,INOEUD,ICMP)
      ELSEIF (TYPCHA.EQ.2) THEN
        CALL POSDDL('CHAM_NO',VITPLU,TOPO,CMP,INOEUD,ICMP)
        CALL POSDDL('CHAM_NO',VITENT,TOPO,CMP,INOEUD,ICMP)
      ELSEIF (TYPCHA.EQ.3) THEN
        CALL POSDDL('CHAM_NO',ACCPLU,TOPO,CMP,INOEUD,ICMP)
        CALL POSDDL('CHAM_NO',ACCENT,TOPO,CMP,INOEUD,ICMP)
      ELSEIF (TYPCHA.EQ.4) THEN
        CALL POSDDL('CHAM_NO',DEPPLU,TOPO,CMP,INOEUD,ICMP)
      ELSEIF (TYPCHA.EQ.5) THEN
        IF (ICTC.NE.0) THEN
          CALL POSDDL('CHAM_NO',CNSINZ,TOPO,CMP,INOEUD,ICMP)
        ELSE
          ICMP = 0
        ENDIF
      ELSEIF (TYPCHA.EQ.6) THEN
        CALL POSDDL('CHAM_NO',VITPLU,TOPO,CMP,INOEUD,ICMP)
      ELSEIF (TYPCHA.EQ.7) THEN
        CALL POSDDL('CHAM_NO',ACCPLU,TOPO,CMP,INOEUD,ICMP)
      ELSEIF (TYPCHA.EQ.8) THEN
        CALL UTCH19(SIGPLU(1:19),MAILLA,TOPO,' ',SUBTOP,
     &              0,1,CMP,'R',VALR,CBID,IRET)
      ELSEIF (TYPCHA.EQ.9) THEN
        CALL UTCH19(VARPLU(1:19),MAILLA,TOPO,' ',SUBTOP,
     &              0,VARINT,'VARI','R',VALR,CBID,IRET)
      ELSEIF (TYPCHA.EQ.10) THEN

        IF (ISND.NE.0) THEN
          CALL POSDDL('CHAM_NO',DEPPLU,TOPO,CMP,INOEUD,ICMP)

          CALL JEEXIN(CNFEDO(1:19) // '.VALE',IRET)
          IF (IRET.NE.0) THEN
            CALL JEVEUO(CNFEDO(1:19) // '.VALE','L',IFEDO)
            VALR = VALR+ZR(IFEDO-1+ICMP)
          ENDIF

          CALL JEEXIN(CNFSDO(1:19) // '.VALE',IRET)
          IF (IRET.NE.0) THEN
            CALL JEVEUO(CNFSDO(1:19) // '.VALE','L',IFSDO)
            VALR = VALR+ZR(IFSDO-1+ICMP)
          ENDIF

          CALL JEEXIN(CNFEPI(1:19) // '.VALE',IRET)
          IF (IRET.NE.0) THEN
            CALL JEVEUO(CNFEPI(1:19) // '.VALE','L',IFEPI)
            VALR = VALR+ZR(IFEPI-1+ICMP)
          ENDIF

          CALL JEEXIN(CNFSPI(1:19) // '.VALE',IRET)
          IF (IRET.NE.0) THEN
            CALL JEVEUO(CNFSPI(1:19) // '.VALE','L',IFSPI)
            VALR = VALR+ZR(IFSPI-1+ICMP)
          ENDIF

        ELSE
          ICMP = 0
        ENDIF        

      ELSE 
        CALL UTMESS('F','DYOEXT','TYPE DE CHAMP INCONNU')
      ENDIF
C
      CALL JEDEMA()
      END
