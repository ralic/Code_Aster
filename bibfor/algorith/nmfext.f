      CHARACTER*19  CNFEXT
      CHARACTER*24  SECMBR,NUMEDD
      CHARACTER*24  RESOCO
      LOGICAL       FONACT(*)
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (ALGORITHME)
C
C RESULTANTE DES EFFORTS EXTERIEURS
C      
C ----------------------------------------------------------------------
C
C
C     IN   NUMEDD : NUME_DDL
C     IN   ETA    : COEFFICIENT DE PILOTAGE
C     IN   SECMBR : VECTEURS ASSEMBLES DES CHARGEMENTS
C     IN   RESOCO : SD CONTACT (POUR REACTIONS DE CONTACT)
C     IN   DEFICO : SD CONTACT (POUR REACTIONS DE CONTACT)
C IN/JXOUT CNFEXT : RESULTANTE DES EFFORTS EXTERIEURS
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      IFM,NIV
      INTEGER      NBPROC,NIVMPI
      INTEGER      IBID,NEQ
      REAL*8       R8BID
      CHARACTER*8  K8BID
      CHARACTER*24 K24BID
      LOGICAL      LFETI
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE> CALCUL DES FORCES EXTERNES' 
      ENDIF      
C      
C --- PREPARATION FETI
C
      CALL NMFETI(NUMEDD,IFM   ,LFETI ,NIVMPI,NBPROC)
C
C --- ASSEMBLAGE DES FORCES EXTERIEURES
C
      CALL NMASSF(FONACT,RESOCO,ETA   ,SECMBR,CNFEXT)
C
C --- SI FETI PARALLELE, ON COMMUNIQUE A CHAQUE PROC LA SOMME DES
C --- CHAM_NOS GLOBAUX PARTIELLEMENT CALCULES
C --- A OPTIMISER VIA UN DES CRITERES LOCAUX
C
      IF ((LFETI).AND.(NBPROC.GT.1)) THEN
        CALL JELIRA(CNFEXT(1:19)//'.VALE','LONMAX',NEQ,K8BID)
        K24BID = CNFEXT(1:19)//'.VALE'
        CALL FETMPI(71    ,NEQ   ,IFM   ,NIVMPI,IBID  ,
     &              IBID  ,K24BID,K24BID,K24BID,R8BID )
      ENDIF

      CALL JEDEMA()
      END
