      SUBROUTINE CONLIG(NEQ   ,DEPDEL,DDEPLA,DEPMOI,DEPPLU,
     &                  DEFICO,MAILLA,LIGRCF,CARTCF,MODELE,
     &                  LISCHA,SOLVEU,NUMEDD,MCONEL,SCONEL,
     &                  MATASS,MEMASS,MASSE ,PREMIE,INST  ,
     &                  VITMOI,ACCMOI,USUMOI,USUPLU,USUINI,
     &                  USUFIX,SDDYNA)
C     
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 30/04/2007   AUTEUR ABBAS M.ABBAS 
C ======================================================================
