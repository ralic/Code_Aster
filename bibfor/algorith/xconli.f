C ROUTINE XFEM (METHODE XFEM - CREATION OBJETS)
C
C CREATION DES OBJETS ELEMENTAIRES:
C  - CHAM_ELEM
C  - VECT_ELEM
C  - MATR_ELEM
C      
C ----------------------------------------------------------------------
