      SUBROUTINE XCONLI(NEQ,DEPDEL,DDEPLA,DEPMOI,DEPPLU,PREMIE,MODELE,
     &                  MA,DEFICO,MCONEL,SCONEL)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 28/02/2006   AUTEUR MASSIN P.MASSIN 
C ======================================================================
