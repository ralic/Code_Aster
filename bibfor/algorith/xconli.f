C
C IN  PREMIE : VAUT .TRUE. SI PREMIER PAS DE TEMPS
C IN  NOMO   : NOM DE L'OBJET MODÈLE
C IN  NOMA   : NOM DE L'OBJET MAILLAGE 
C IN  RESOCO : SD CONTACT RESULTAT
C IN  DEFICO : SD CONTACT DEFINITION
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV   
      INTEGER      IBID
      REAL*8       R8BID
      LOGICAL      LTFCM     
      CHARACTER*24 K24BLA,K24BID
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('XFEM',IFM,NIV)      
C
C --- AFFICHAGE
C      
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<XFEM   > CREATION ET INITIALISATION'//
     &        ' DES OBJETS *_ELEM' 
      ENDIF 
C
C --- INITIALISATIONS
C
      K24BLA = ' '                 
C
C --- CREATION DES CHAM_ELEM SI PREMIER PAS DE TEMPS
C  
      IF (PREMIE) THEN
        CALL XMELEM(NOMA  ,NOMO  ,DEFICO,RESOCO)
      ENDIF
C
C --- GRANDS GLISSEMENTS ?
C
      CALL MMINFP(0    ,DEFICO,K24BLA,'XFEM_GG',
     &            IBID ,R8BID ,K24BID,LTFCM)
C
C --- CREATION DU LIGREL DES ELEMENTS DE CONTACT     
C
      IF (LTFCM) THEN     
        CALL XMLIGR(NOMA,DEFICO,RESOCO)
        CALL XMCART(NOMA,DEFICO,NOMO  ,RESOCO) 
      ENDIF
C
      CALL JEDEMA()
      END
