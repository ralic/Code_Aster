      SUBROUTINE VEFNME(MODELE,SIGMA,CARA,DEPMOI,DEPDEL,VECELZ,MATCOD,
     &                  COMPOR,NH,FNOEVO,PARTPS,CARCRI,CHTEMP,LIGREZ)

      IMPLICIT NONE
      LOGICAL FNOEVO
      REAL*8 PARTPS(3)
      CHARACTER*(*) MODELE,LIGREZ
      CHARACTER*(*) SIGMA,CARA,DEPMOI,DEPDEL,VECELZ
      CHARACTER*(*) MATCOD,COMPOR,CARCRI,CHTEMP
      CHARACTER*8 VECELE,NEWNOM

