      IMPLICIT NONE
      CHARACTER*16 MCFACT
      INTEGER      NECHEC,NERREU
C
C ----------------------------------------------------------------------
C
C OPERATEUR DEFI_LIST_INST
C
C LECTURE DES ECHECS
C
C DECOMPTE DES OCCURRENCES MOT-CLEF ECHEC
C
C ----------------------------------------------------------------------
C
C IN  MCFACT : MOT-CLEF FACTEUR POUR LIRE L'ECHEC
C OUT NECHEC : NB OCCURRENCES MOT-CLEF ECHEC
C OUT NERREU : NB OCCURRENCES EVENEMENT ='ERREUR'
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IBID
      INTEGER      IECHEC
      CHARACTER*16 EVEN
      INTEGER      IARG
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NERREU = 0
C
C --- NECHEC: NOMBRE D'OCCURRENCES DU MOT-CLE FACTEUR 'ECHEC'
C
      CALL GETFAC(MCFACT,NECHEC)
C
C --- NOMBRE D'OCCURRENCES DES EVENEMENTS
C
      DO 100 IECHEC = 1,NECHEC
        CALL GETVTX(MCFACT,'EVENEMENT',IECHEC,IARG,1,EVEN,IBID)
        IF (EVEN.EQ.'ERREUR')    NERREU = NERREU+1
 100  CONTINUE
C
C --- ON NE PEUT DEFINIR QU'UNE SEULE OCCURRENCE AVEC 'ERREUR'
C
      IF (NERREU.GT.1) CALL U2MESS('F','DISCRETISATION_10')     
C
      CALL JEDEMA()
      END
