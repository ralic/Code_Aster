C
C ROUTINE MECA_NON_LINE (UTILITAIRE)
C
C PROCHAIN INSTANT DE PASSAGE DANS LA LISTE DES JALONS
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  INST   : INSTANT RECHERCHE
C IN  PREC   : PRECISION
C OUT JALON  : VALEUR DE L'INSTANT JALON TROUVE
C              VAUT R8VIDE SI L'INSTANT EST AU DELA DE LA LSITE
C
C
C
C
      CHARACTER*24 TPSIPO
      INTEGER      JIPO
      CHARACTER*8  K8BID
      INTEGER      IPO,NIPO
      REAL*8       R8VIDE
      LOGICAL      COMPR8
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      JALON = R8VIDE()
C
C --- LISTE DES JALONS
C
      TPSIPO = SDDISC(1:19)//'.LIPO'
      CALL JELIRA(TPSIPO,'LONMAX',NIPO,K8BID)
      CALL JEVEUO(TPSIPO,'L',JIPO)
C
C --- RECHERCHE PROCHAIN JALON
C
      DO 10 IPO = 1,NIPO
        IF ( COMPR8(ZR(JIPO-1+IPO),'GT',INST,PREC,1) ) THEN
          JALON = ZR(JIPO-1+IPO)
          GOTO 20
        ENDIF
 10   CONTINUE
 20   CONTINUE
C
      CALL JEDEMA()
      END
