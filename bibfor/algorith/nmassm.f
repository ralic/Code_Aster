C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*19  LISCHA,SOLVEU
      CHARACTER*24  NUMEDD,DEFICO
      CHARACTER*6   TYPMAT
      CHARACTER*(*) OPTASZ      
      CHARACTER*19  MEELEM(8)
      CHARACTER*19  MATASS
      LOGICAL       FONACT(*)      
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (CALCUL)
C
C ASSEMBLAGE DES MATRICES ELEMENTAIRES
C      
C ----------------------------------------------------------------------
C
C
C IN  FONACT : FONCTIONNALITES ACTIVEES (VOIR NMFONC)
C IN  DEFICO : SD CONTACT (DEFINITION)
C IN  LISCHA : LISTE DES CHARGEMENTS
C IN  OPTASS : OPTION D'ASSEMBLAGE
C IN  NUMEDD : NUME_DDL
C IN  SOLVEU : SOLVEUR
C IN  MEELEM : ARIABLE CHAPEAU POUR NOM DES MATR_ELEM
C OUT MATASS : MATR_ASSE CALCULEE
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*19 NMCHEX,MEDIRI,MEMASS,MEAMOR,MESSTR
      INTEGER      IFM,NIV   
      CHARACTER*16 OPTASS 
      LOGICAL      LMELE       
C      
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('MECA_NON_LINE',IFM,NIV)
C
C --- INITIALISATIONS
C   
      OPTASS = OPTASZ  
C
C --- AFFICHAGE
C
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<MECANONLINE><MATR> ASSEMBLAGE DES MATR_ELEM' //
     &                ' DE TYPE <',TYPMAT,'>'     
      ENDIF
C
C --- INDICATEUR PRESENCE VARIABLES CHAPEAUX
C
      LMELE     = .FALSE. 
C
C --- DECOMPACTION DES VARIABLES CHAPEAUX
C 
      IF (MEELEM(1)(1:1).NE.' ') THEN 
        MEDIRI = NMCHEX(MEELEM,'MEELEM','MEDIRI')
        MEMASS = NMCHEX(MEELEM,'MEELEM','MEMASS')
        MEAMOR = NMCHEX(MEELEM,'MEELEM','MEAMOR')
        MESSTR = NMCHEX(MEELEM,'MEELEM','MESSTR') 
        LMELE  = .TRUE.            
      ENDIF                                  
C
C --- ASSEMBLAGE MATRICES ELEMENTAIRES
C
      IF (TYPMAT.EQ.'MERIGI') THEN
        IF (.NOT.LMELE) CALL ASSERT(.FALSE.)      
        CALL ASMARI(FONACT,DEFICO,MEELEM,NUMEDD,SOLVEU,
     &              LISCHA,MATASS)   
      ELSEIF (TYPMAT.EQ.'MEAMOR') THEN  
        IF (.NOT.LMELE) CALL ASSERT(.FALSE.)               
        CALL ASMAAM(MEAMOR,NUMEDD,SOLVEU,LISCHA,MATASS)
        CALL MTDSCR(MATASS)        
      ELSEIF (TYPMAT.EQ.'MEMASS') THEN 
        IF (.NOT.LMELE) CALL ASSERT(.FALSE.)    
        IF (OPTASS.EQ.' ') THEN      
          CALL ASMAMA(MEMASS,' '   ,NUMEDD,SOLVEU,LISCHA,
     &                MATASS)
        ELSEIF (OPTASS.EQ.'AVEC_DIRICHLET') THEN 
          CALL ASMAMA(MEMASS,MEDIRI,NUMEDD,SOLVEU,LISCHA,
     &                MATASS) 
        ENDIF    
      ELSEIF (TYPMAT.EQ.'MESSTR') THEN
        IF (.NOT.LMELE) CALL ASSERT(.FALSE.)          
        CALL ASMATR(1     ,MESSTR,' '   ,NUMEDD,SOLVEU,
     &              LISCHA,'ZERO','V'   ,1     ,MATASS)  
        CALL MTDSCR(MATASS)               
      ELSE  
        CALL ASSERT(.FALSE.)
      ENDIF         
C
C --- DEBUG
C
      IF (NIV.EQ.2) THEN
        CALL NMDEBG('MATA',MATASS,IFM   )
      ENDIF

C
      CALL JEDEMA()      
C
      END
