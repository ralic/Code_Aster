C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      IMPLICIT NONE
      
      INTEGER      ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8       ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16   ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL      ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8  ZK8
      CHARACTER*16    ZK16
      CHARACTER*24        ZK24
      CHARACTER*32            ZK32
      CHARACTER*80                ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------
C
      INTEGER      IBID,JCESD,JCESL,JCESV,N1,I
      INTEGER      NBMA,IMA,IAD,JPAS,NBDT,JREPE
      INTEGER      NBMODE
      INTEGER      JMASG,JRAIG,IOROL, JORDM
      REAL*8       DTCOU,VALEUR,PHI,DT,R8DEPI
      CHARACTER*8  K8BID,STOCFL,MECA,K8B
      CHARACTER*14 K14B
      CHARACTER*19 CHAMS,LISINS
      CHARACTER*19 NOMSTM,NOMSTK
      CHARACTER*24 NUMK24,NUMM24      

      CALL JEMARQ()
            
C     INITIALISATION DE DTCOU
      
      DTCOU = -1.D0

C     --- RECUPERATION DES FREQUENCES PROPRES

      CALL JELIRA(  MECA//'           .ORDR','LONUTI',NBMODE,K8B)
      CALL JEVEUO(  MECA//'           .ORDR','L',JORDM)
      IOROL = ZI(JORDM)
      CALL RSADPA(  MECA, 'L',1,'OMEGA2',IOROL,0, IAD,K8B)
      DTCOU = R8DEPI() / SQRT(ZR(IAD))
      DO 21 I = 1,NBMODE-1
       IOROL = ZI(JORDM+I)
       CALL RSADPA(  MECA, 'L',1,'OMEGA2',IOROL,0, IAD,K8B)
       DT = R8DEPI() / SQRT(ZR(IAD))
       IF (DT.LT.DTCOU) DTCOU = DT
   21 CONTINUE

      CALL GETVID(' ','STOP_CFL',1,1,1,STOCFL,N1)
      
C     VERIFICATION DE LA CONFORMITE DE LA LISTE D'INSTANTS

      CALL GETVID('INCREMENT','LIST_INST',1,1,1,LISINS,N1)
      CALL JEVEUO(LISINS//'.LPAS','L',JPAS)

      CALL JELIRA(LISINS//'.LPAS','LONMAX',NBDT,K8BID)
            
      CALL GETFAC('DIFF_CENT',N1)
      IF (N1.EQ.1) THEN
        DTCOU =DTCOU/(2.D0)        
        CALL UTDEBM('I','PASCOU','PAS DE TEMPS MAX POUR DIFF_CENT :')
        CALL UTIMPR('S',' ',1,DTCOU)
        CALL UTFINM()
      ELSE
        CALL GETFAC('TCHAMWA',N1)
        IF (N1.EQ.1) THEN
          CALL GETVR8('TCHAMWA','PHI',1,1,1,PHI,N1)
          DTCOU = DTCOU/(PHI*2.D0)
          CALL UTDEBM('I','PASCOU','PAS DE TEMPS MAX POUR TCHAMWA :')
          CALL UTIMPR('S',' ',1,DTCOU)
          CALL UTFINM()
        ELSE
          CALL UTMESS('F','PASCOU','SCHEMA INCONNU')
        ENDIF
      ENDIF
        
      DO 20 I=1,NBDT
        IF (ZR(JPAS-1+I).GT.DTCOU) THEN
          IF (STOCFL(1:3).EQ.'OUI') THEN
            CALL UTMESS('F','PASCOU','LA LISTE D''INSTANTS'
     &   //  ' FOURNIE NE RESPECTE PAS LA CONDITION DE STABILITE.')
          ELSE
            CALL UTMESS('A','PASCOU','LA LISTE D''INSTANTS'
     &   //  ' FOURNIE NE RESPECTE PAS LA CONDITION DE STABILITE.')
          ENDIF
        ENDIF
 20   CONTINUE
  
      CALL JEDEMA()

      END
