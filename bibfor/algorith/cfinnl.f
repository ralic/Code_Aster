C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*24 DEFICO,RESOCO
      INTEGER      NBLIAC,LLF,LLF1,LLF2
      LOGICAL      REAGEO
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE DISCRETE - ALGORITHME)
C
C NOMBRE DE LIAISONS INITIALES
C
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD DE DEFINITION DU CONTACT
C IN  RESOCO : SD DE TRAITEMENT NUMERIQUE DU CONTACT
C IN  REAGEO : .TRUE. SI ON VIENT DE FAIRE UN NOUVEL APPARIEMENT
C OUT NBLIAC : NOMBRE DE LIAISONS ACTIVES
C OUT LLF    : NOMBRE DE LIAISON DE FROTTEMENT (DEUX DIRECTIONS)
C OUT LLF1   : NOMBRE DE LIAISON DE FROTTEMENT (1ERE DIRECTION ) 
C OUT LLF2   : NOMBRE DE LIAISON DE FROTTEMENT (2EME DIRECTION ) 
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER CFDISD
      LOGICAL CFDISL,LPENAF,LLAGRC
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- PARAMETRES
C
      LPENAF = CFDISL(DEFICO,'FROT_PENA')
      LLAGRC = CFDISL(DEFICO,'CONT_LAGR')
C
C --- NOMBRE DE LIAISONS INITIALES
C --- POUR LES METHODES PUREMENT LAGRANGIENNES, ON GARDE
C --- LA MEMOIRE DES LIAISONS PRECEDEMMENT ACTIVES
C
      IF (LLAGRC) THEN
        IF (REAGEO) THEN
          NBLIAC = 0
          LLF    = 0
          LLF1   = 0
          LLF2   = 0
        ELSE
          NBLIAC = CFDISD(RESOCO,'NBLIAC'  )
          IF (LPENAF) THEN
            LLF    = 0
            LLF1   = 0
            LLF2   = 0
          ELSE
            LLF    = CFDISD(RESOCO,'LLF'     )
            LLF1   = CFDISD(RESOCO,'LLF1'    )
            LLF2   = CFDISD(RESOCO,'LLF2'    )
          ENDIF
        ENDIF
      ELSE
        NBLIAC = 0
        LLF    = 0
        LLF1   = 0
        LLF2   = 0
      ENDIF
C
      CALL JEDEMA()
C
      END
