      SUBROUTINE CHSTNL (CHAINE, MODELE, NUMEDD, MATE,   CARELE,
     &                    COMPLU, DEPMOI, DEPDEL, VERESI,
     &                    LISCHA, INSTAP, INSTAM, COMPOR, CARCRI,
     &                    VECELE, VECASS, VECCHA, VITES, ACCEL)
