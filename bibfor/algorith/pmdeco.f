C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE PROIX J-M.PROIX
C
      IMPLICIT NONE
      INTEGER      ACTION,ITERAT
      INTEGER      NUMINS
      CHARACTER*19 SDDISC   
      REAL*8       PARCRI(*)  
      LOGICAL      ITEMAX,ERROR,ECHGAU
C
C ----------------------------------------------------------------------
C
C ROUTINE OP0033 (CALC_POINT_MAT)
C
C GESTION DE LA DECOUPE DU PAS DE TEMPS, IDEM NMDECO MAIS SANS SDTIME
C      
C ----------------------------------------------------------------------
C
C
C IN  PARCRI : CRITERES DE CONVERGENCE
C IN  SDDISC : SD DISCRETISATION TEMPORELLE
C IN  ITERAT : NUMERO D'ITERATION DE NEWTON
C IN  NUMINS : NUMERO D'INSTANTS
C IN  ITEMAX : NOMBRE D'ITERATIONS MAXI ATTEINT
C IN  ECHLDC : VRAI Si ECHEC INTEGRATION LOI DE COMPORTEMENT
C IN  ECHGAU : VRAI Si ECHEC MGAUSS
C OUT ACTION : CODE RETOUR ACTION
C               0 ARRET DU CALCUL
C               1 NOUVEAU PAS DE TEMPS
C               2 ON FAIT DES ITERATIONS DE NEWTON EN PLUS
C               3 ON FINIT LE PAS DE TEMPS
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C 
      INTEGER  RETOUR,IBID,NOCC,IOCC,IFM,NIV
      LOGICAL  ARRET,LBID,LCRIT,DICREC,ECHLDC
      REAL*8   R8BID
      CHARACTER*8 K8BID
C 
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFMAJ
      CALL INFNIV(IFM,NIV)
C
C --- INITIALISATIONS
C
      ARRET  = (NINT(PARCRI(4)).EQ.0)
      ACTION = 0
C
C --- DECOUPE DU PAS DE TEMPS

      CALL UTDIDT('L',SDDISC,'ECHE',IBID,'NB_OCC',R8BID,NOCC,K8BID)

C     A LA 1ERE CAUSE D'ECHEC VERIFIEE, ON SORT DE LA BOUCLE
      LCRIT = .FALSE.
      ERROR=.FALSE.
      IF (ECHLDC) THEN
         ERROR=.TRUE.
         CALL U2MESS('I','COMPOR1_9')
      ENDIF
      IF (ECHGAU) THEN
         ERROR=.TRUE.
         CALL U2MESS('I','COMPOR2_4')
      ENDIF
      DO 10 IOCC =1,NOCC
        LCRIT = DICREC(SDDISC,'VERIF_CRIT_ECHE',IOCC,ITEMAX,ERROR)
        IF (LCRIT) GOTO 20
 10   CONTINUE

 20   CONTINUE
      CALL ASSERT(LCRIT)

C     DECOUPAGE DU PAS DE TEMPS
      CALL DIDECO(SDDISC,NUMINS,ITERAT,IOCC,RETOUR)
C
      IF (RETOUR.EQ.0)  THEN
C      
C ---   LA SUBDIVISION S'EST BIEN PASSEE
        ACTION = 1

      ELSE IF (RETOUR.EQ.3) THEN
C      
C ---   AUTORISATION DE FAIRE DES ITERATIONS DE NEWTON EN PLUS  
        ACTION = 2
        ITEMAX = .FALSE.

      ELSEIF ((RETOUR.EQ.1).AND.(.NOT.ARRET).AND.ITEMAX) THEN
C
C ---   ON VA A LA SUBDIVISION OU AU PAS DE TEMPS SUIVANT
        ACTION = 3

      ELSEIF ((RETOUR.EQ.2).AND.(.NOT.ARRET)) THEN
C
C ---   ON VA A LA SUBDIVISION OU AU PAS DE TEMPS SUIVANT 
        ACTION = 3

      ELSE
C
C ---  ARRET DU CALCUL        
        ACTION = 0
        CALL U2MESS('A','MECANONLINE_83')        
      ENDIF      
C
      CALL JEDEMA()
      END
