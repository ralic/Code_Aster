      IMPLICIT REAL*8 (A-H,O-Z)

C ROUTINE CALCULANT LES MATRICES AX ET AY SUR LE MODELE INTERFACE
C G. ROUSSEAU
C IN : MOINT : MODELE INTERFACE
C IN : MATE : MATERIAU CODE
C IN : DIR : DIRECTION X OU Y
C IN : LIGRMO : LIGREL DU MODELE
C IN: LCHIN,LPAIN,LPAOUT : PARAMETRES DU CALCUL ELEMENTAIRE
C IN : NUM : NUMEROTATION DES DDLS THERMIQUES D INTERFACE

C OUT : MAA : MATRICE AX OU AY
C---------------------------------------------------------------------
C--------- DEBUT DES COMMUNS JEVEUX ----------------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXR8,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C--------- FIN DES COMMUNS JEVEUX ------------------------------------

      CHARACTER*(*) MOINT,MATE
      CHARACTER*1 DIR
      CHARACTER*8 MATEL,K8BID,LPAIN(1),LPAOUT(1)
      CHARACTER*14 NUM
      CHARACTER*16 OPTION
      CHARACTER*19 MAA
      CHARACTER*24 LCHOUT(1),LCHIN(1),LIGRMO
      REAL*8  RTBLOC,JEVTBL



C ----- CREATION DE LA MATR_ELEM AX OU AY DES N(I)*N(J)*NX OU NY----
C--------------SUR LE MODELE THERMIQUE------------------------------
C--------------------D'INTERFACE -----------------------------------

      CALL JEMARQ()
      OPTION = 'FLUX_FLUI_'//DIR
      MATEL = '&&CA.MA'//DIR
      MAA = MATEL//'.LISTE_RESU'
      CALL MEMARE('V',MATEL,MOINT(1:8),MATE,' ','FLUX_FLUI_ '//DIR)
      CALL WKVECT(MAA,'V V K24',1,JLVA)
      LCHOUT(1) = MATEL//'.ME000'
      CALL CODENT(1,'D0',LCHOUT(1) (12:14))
      CALL CALCUL('S',OPTION,LIGRMO,1,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V')
      ZK24(JLVA) = LCHOUT(1)
      CALL JEECRA(MAA,'LONUTI',1,K8BID)


C-------------------- NUMEROTATION ----------------------------------

      NUM = '&&CALMAA.NUM'//DIR
      CALL NUMDDL(NUM,'V',1,MATEL,'RCMK')
      CALL PROMOR(NUM,'V')
      CALL CRNSLV(NUM,'MULT_FRO','METIS','V')

C---------------ASSEMBLAGE DES MATRICES AX OU AY DES N(I)N(J)NX OU NY

      MAA = '&&CA.AA'//DIR

      CALL ASSMAM('V',MAA,1,LCHOUT,1.D0,NUM,'ZERO',1)


   10 CONTINUE
      CALL JEDEMA()
      END
