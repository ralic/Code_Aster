      IMPLICIT NONE
      CHARACTER*24 DEFICO
      INTEGER      IPC,IZONE,POSMAE
      LOGICAL      EXNOEC
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - APPARIEMENT)
C
C EXCLUSION DES NOEUDS DONNES DANS SANS_GROUP_NO
C      
C ----------------------------------------------------------------------
C
C
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  IZONE  : NUMERO DE LA ZONE DE CONTACT
C IN  POSMAE : NUMERO DE LA MAILLE ESCLAVE
C IN  IPC    : NUMERO DU NOEUD ESCLAVE
C OUT EXNOEC : .TRUE. SI CE NOEUD EST A EXCLURE
C
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      POSNOE,NUMNOE
      INTEGER      JNOMA,JNOCO,JPONO
      CHARACTER*24 NOMACO,CONTNO,PNOMA
      INTEGER      SUPPOK
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ACCES OBJETS
C  
      NOMACO = DEFICO(1:16)//'.NOMACO'
      CONTNO = DEFICO(1:16)//'.NOEUCO'
      PNOMA  = DEFICO(1:16)//'.PNOMACO'          
      CALL JEVEUO(NOMACO,'L',JNOMA)
      CALL JEVEUO(CONTNO,'L',JNOCO)     
      CALL JEVEUO(PNOMA ,'L',JPONO)  
C
C --- INITIALISATIONS 
C
      POSNOE = ZI(JNOMA+ZI(JPONO+POSMAE-1)+IPC-1)
      NUMNOE = ZI(JNOCO+POSNOE-1)           
C
C --- REPERAGE SI LE NOEUD IPC EST UN NOEUD A EXCLURE (SUPPOK = 1)
C
      CALL CFMMEX(DEFICO,'CONT',IZONE ,NUMNOE,SUPPOK)
      IF (SUPPOK .EQ. 1) THEN
        EXNOEC = .TRUE.
      ENDIF
C
      CALL JEDEMA()      
      END
