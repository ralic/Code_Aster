      IMPLICIT NONE
      CHARACTER*13     INPSCO
      CHARACTER*16     CMD 
      CHARACTER*19     FOINER
      CHARACTER*24     DEPPLU, DEPDEL, DEPOLD, DDEPLA, DEPGEO, DEPPIL(2)
      CHARACTER*24     DEPLAM, DEPKM1, VITKM1, ACCKM1, ROMKM1, ROMK 
      CHARACTER*24     ACCENM, VITMOI, ACCMOI, NUMEDD, VITPLU, ACCPLU
      CHARACTER*24     DEPENT, VITENT, ACCENT, DEPENM, VITENM, VITINI
      CHARACTER*24     ACCINI      
      INTEGER           NEQ,    NBPASE, NBMODS
           
C ----------------------------------------------------------------------
C     CREATION DES VECTEURS D'INCONNUS
C 
C ----------------------------------------------------------------------
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------

      INTEGER     NRPASE,IAUX,JAUX,JDEPEN,JVITEN,JACCEN


      CALL JEMARQ()

C -- CREATION DES CHAMPS DERIVES +
      DO 6 NRPASE=NBPASE,0,-1
        IAUX = NRPASE
        JAUX = 4
        CALL PSNSLE(INPSCO,IAUX,JAUX,DEPPLU)
        CALL VTCREB(DEPPLU,NUMEDD,'V','R',NEQ)
        JAUX = 18
        CALL PSNSLE(INPSCO,IAUX,JAUX,DEPENT)
        CALL VTCREB(DEPENT,NUMEDD,'V','R',NEQ)
        JAUX = 20
        CALL PSNSLE(INPSCO,IAUX,JAUX,VITENT)
        CALL VTCREB(VITENT,NUMEDD,'V','R',NEQ)
        JAUX = 22
        CALL PSNSLE(INPSCO,IAUX,JAUX,ACCENT)
        CALL VTCREB(ACCENT,NUMEDD,'V','R',NEQ)
 6    CONTINUE

      CALL VTCREB(DEPDEL,NUMEDD,'V','R',NEQ)
      CALL VTCREB(DEPOLD,NUMEDD,'V','R',NEQ)
      CALL VTCREB(DDEPLA,NUMEDD,'V','R',NEQ)
      CALL VTCREB(DEPPIL(1),NUMEDD,'V','R',NEQ)
      CALL VTCREB(DEPPIL(2),NUMEDD,'V','R',NEQ)
      CALL VTCREB(DEPGEO,NUMEDD,'V','R',NEQ)
      CALL VTCREB(DEPLAM,NUMEDD,'V','R',NEQ)
      CALL VTCREB(DEPKM1,NUMEDD,'V','R',NEQ)
      CALL VTCREB(VITKM1,NUMEDD,'V','R',NEQ)
      CALL VTCREB(ACCKM1,NUMEDD,'V','R',NEQ)
      CALL VTCREB(ROMKM1,NUMEDD,'V','R',NEQ)
      CALL VTCREB(ROMK  ,NUMEDD,'V','R',NEQ)
      CALL VTCREB(FOINER,NUMEDD,'V','R',NEQ)

C -- VECTEUR POUR LA DYNAMIQUE A L INSTANT MOINS
C -- UTILE UNIQUEMENT AFIN D ARCHIVER LE DERNIER INSTANT CALCULE
C -- SI PLANTE POUR LE NOUVEAU PAS DE TEMPS DANS
C -- LES ITERATIONS DE NEWTON

      IF (CMD(1:4).EQ.'DYNA') THEN
       CALL VTCREB(DEPENM,NUMEDD,'V','R',NEQ)
       CALL VTCREB(VITENM,NUMEDD,'V','R',NEQ)
       CALL VTCREB(ACCENM,NUMEDD,'V','R',NEQ)
       CALL VTCREB(VITMOI,NUMEDD,'V','R',NEQ)
       CALL VTCREB(ACCMOI,NUMEDD,'V','R',NEQ)
       CALL COPISD('CHAMP_GD','V',VITPLU,VITMOI)
       CALL COPISD('CHAMP_GD','V',ACCPLU,ACCMOI)
       IF (NBMODS.NE.0) THEN
        CALL JEVEUO(DEPENM(1:19)//'.VALE','E',JDEPEN)
        CALL JEVEUO(VITENM(1:19)//'.VALE','E',JVITEN)
        CALL JEVEUO(ACCENM(1:19)//'.VALE','E',JACCEN)
        CALL R8INIR(NEQ,0.D0,ZR(JDEPEN),1)
        CALL R8INIR(NEQ,0.D0,ZR(JVITEN),1)
        CALL R8INIR(NEQ,0.D0,ZR(JACCEN),1)
       ENDIF
      ENDIF

      IF (CMD(1:13).EQ.'DYNA_NON_LINE') THEN
        CALL VTCREB(VITINI,NUMEDD,'V','R',NEQ)
        CALL VTCREB(ACCINI,NUMEDD,'V','R',NEQ)
       ENDIF
C
C
C -- CREATION DE CHAMPS NODAUX PARTAGES (PASSES EN SOUTERRAIN)
C      OBJECTIFS :
C         NE PAS FRAGMENTER LA MEMOIRE
C      REGLES :
C         CNZERO : LECTURE SEULE -> IL VAUT TJRS 0
C         CNTMPX : NE TRANSITENT PAS D'UNE ROUTINE A L'AUTRE

      CALL VTCREB ('&&CNPART.ZERO',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNPART.CHP1',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNPART.CHP2',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNPART.CHP3',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNPART.CHP4',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNREPL.CHP1',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNREPL.CHP2',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNREPL.CHP3',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNREPL.CHP4',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNCETA.CHP0',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNCETA.CHP1',NUMEDD,'V','R',NEQ)
      CALL VTCREB ('&&CNCETA.CHP2',NUMEDD,'V','R',NEQ)


      CALL JEDEMA()
      END
