C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
      IMPLICIT NONE
      CHARACTER*24  SDSENS,SDIETO
      INTEGER       NUMINS
      REAL*8        PARA(*)
      LOGICAL       LNONL,FORCE
      CHARACTER*19  SDDISC,SDCRIT
      CHARACTER*24  MODELE,MATE  ,CARELE
      CHARACTER*19  LISCH2
C
C ----------------------------------------------------------------------
C
C ROUTINE THER_* (ALGORITHME)
C
C ARCHIVAGE
C
C ----------------------------------------------------------------------
C
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      LOGICAL      DIDERN,LSENS
      REAL*8       INSTAM,INSTAN
      REAL*8       DIINST
      INTEGER      IRET
      INTEGER      NUMARC
      INTEGER      JINST
      INTEGER      NMSENN,NBPASE,NRPASE
      CHARACTER*19 K19BID
      CHARACTER*8  K8BID,NOPASE,RESULT    
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- FONCTIONNALITES ACTIVEES
C    
      NBPASE = NMSENN(SDSENS)
      LSENS  = NBPASE.GT.0
C
C --- IMPRESSION EVENTUELLE DES MESURES DE TEMPS
C
      CALL UTTCPG('IMPR','INCR')      
C
C --- DERNIER PAS : ON ARCHIVE TOUS LES CHAMPS
C
      IF (DIDERN(SDDISC,NUMINS)) THEN
        FORCE = .TRUE.
      ENDIF 
C
C --- NUMERO D'ARCHIVAGE
C
      CALL DINUAR(SDDISC,NUMINS,FORCE ,NUMARC)
C
C --- INSTANT COURANT
C
      INSTAN = DIINST(SDDISC,NUMINS)
C
C ----------------------------------------------------------------------
C
      IF (NUMARC.GE.0) THEN
C
C ----- NOM SD RESULTAT
C
        CALL NTNSLE(SDSENS,0,'RESULT',RESULT)
C
C ----- INSTANT DEJA ARCHIVE ?
C
        IF (NUMARC.GE.2) THEN
          CALL RSADPA(RESULT,'L',1,'INST',NUMARC-1,0,JINST,K8BID)
          INSTAM = ZR(JINST)
          IF (INSTAN.EQ.INSTAM) THEN
            GOTO 999
          ENDIF
        ENDIF
C
C ----- AFFICHAGE
C
        CALL U2MESS('I','ARCHIVAGE_5')
C
C ----- EXTENSION DE RESULT SI TROP PETIT (DOUBLEMENT)
C
        CALL RSEXCH(RESULT,'TEMP',NUMARC,K19BID,IRET  )
        IF (IRET.EQ.110) THEN
          CALL RSAGSD(RESULT,0)
        ENDIF   
C
C ----- ARCHIVAGE DES PARAMETRES
C
        CALL NTARC0(RESULT,MODELE,MATE  ,CARELE,SDCRIT,
     &              LISCH2,LNONL ,PARA  ,NUMARC,INSTAN)        
C
C ----- ARCHIVAGE DES CHAMPS
C
        NRPASE = 0
        CALL NMARCE(SDSENS,SDIETO,RESULT,SDDISC,INSTAN,
     &              NUMARC,NRPASE,FORCE )       
C
C ----- CAS SENSIBLE
C
        IF (LSENS) THEN
C
C ------- BOUCLE SUR LES PARAMETRES SENSIBLES
C
          DO 10 NRPASE = 1,NBPASE  
C
C --------- NOM DES CHAMPS SENSIBLES
C
            CALL NTNSLE(SDSENS,NRPASE,'NOPASE',NOPASE)
            CALL NTNSLE(SDSENS,NRPASE,'RESULT',RESULT)
C
C --------- AFFICHAGE
C
            CALL U2MESK('I','ARCHIVAGE_7',1,NOPASE)
C
C --------- ARCHIVAGE DES PARAMETRES
C
            CALL NTARC0(RESULT,MODELE,MATE  ,CARELE,SDCRIT,
     &                  LISCH2,LNONL ,PARA  ,NUMARC,INSTAN)
C
C --------- ARCHIVAGE DES CHAMPS SENSIBLES
C
            CALL NMARCE(SDSENS,SDIETO,RESULT,SDDISC,INSTAN,
     &                  NUMARC,NRPASE,FORCE )     
  10      CONTINUE              
        ENDIF
      ENDIF
C
  999 CONTINUE
C      
      CALL JEDEMA()

      END
