     &                     CPMONO,NVI,    NMAT,   VINI,
     &                     COTHE, COEFF, DCOTHE, DCOEFF,PGL, E,
     &                     NU,    ALPHA, COEL,X,   DTIME,  SIGI,
     &                     EPSD,  DETOT, TPERD,  DTPER,  TPEREF,
     &                     DVIN,  BZ )
        IMPLICIT NONE
