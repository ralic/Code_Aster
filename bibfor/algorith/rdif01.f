     &                     CPMONO,NBFSYM,TOUTMS, NVI,    NMAT, VINI,
     &                     COTHE, COEFF, DCOTHE, DCOEFF,PGL,NBPHAS, E,
     &                     NU,    ALPHA, COEL,X,   DTIME,  SIGI,
     &                     EPSD,  DETOT,
     &                     DVIN, HSR, ITMAX, TOLER, IRET )
