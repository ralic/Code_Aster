C-----------------------------------------------------------------------
      CALL JEMARQ()
      CHAVAL = CHAVAZ
      RESU  = RESUZ
      TYPCH = TYPCHZ
      CALL RSEXCH(RESU,TYPCH,IORD,CHACOU,IER)
      IF (IER.NE.0) THEN
        VALK = RESU
        VALI = IORD
        CALL U2MESG('F', 'ALGORITH12_76',1,VALK,1,VALI,0,0.D0)
      END IF

      CHAVAL = CHACOU//'.VALE'
      CALL JEVEUO(CHAVAL,'L',IAD)
C
      CHAVAZ = CHAVAL
      GOTO 9999

 9999 CONTINUE
      CALL JEDEMA()
      END
