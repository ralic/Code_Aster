C     EVOLUTION DE LA VARIABLE GUIDANT LES PROPORTIONS TANGENTE ET
C     SECANTE DE LA MATRICE (LOCALE) UTILISEE PAR NEWTON (GLOBAL)

C IN  ITERAT   : ITERATION GLOBALE DE NEWTON
C IN  VIET     : VARIABLE INTERNE INDIQUANT L ETAT (ELAS=0, ENDO>0)
C IN/OUT  VITS : VARIABLE INTERNE A TRAITER

        IF((VITS .GE. 1.0D0) .AND. (VIET .EQ. 0.0D0)) THEN
          VITS = -VITS
        ELSEIF((VITS .LT. 0.0D0) .AND. (VIET.GT.0.0D0)) THEN
          VITS = -VITS + 1.0D0
        ELSEIF((VITS .GT. TSEUIL) .AND. (VIET .GT. 0.0D0)) THEN
          VITS = VITS - TSRETU
          IF(VITS .LT. TSEUIL) VITS = TSEUIL
        ELSEIF((VITS .EQ. 0.0D0) .AND. (VIET .GT. 0.0D0)) THEN
          VITS = 1.0D0
        ENDIF
      END
