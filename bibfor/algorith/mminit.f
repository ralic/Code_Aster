      IMPLICIT NONE
      CHARACTER*24 DEFICO,RESOCO     
      CHARACTER*24 VALMOI(8),VALPLU(8)
      CHARACTER*19 SDDYNA     
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - ALGORITHME)
C
C INITIALISATIONS
C      
C ----------------------------------------------------------------------
C     
C
C IN  DEFICO : SD POUR LA DEFINITION DE CONTACT
C IN  RESOCO : SD POUR LA RESOLUTION DE CONTACT
C IN  SDDYNA : SD DEDIEE A LA DYNAMIQUE
C IN  DEPMOI : DEPLACEMENTS A L'INSTANT MOINS
C IN  VITPLU : VITESSES A L'INSTANT PLUS
C IN  VITPLU : ACCELERATIONS A L'INSTANT PLUS
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IFM,NIV
      LOGICAL      NDYNLO,LDYNA
      CHARACTER*24 DEPGEO,DEPLAM 
      CHARACTER*24 VITINI,ACCINI
      CHARACTER*24 ACCPLU,VITPLU
      CHARACTER*24 DEPMOI,K24BID      
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('CONTACT',IFM,NIV)    
C
C --- AFFICHAGE
C  
      IF (NIV.GE.2) THEN
        WRITE (IFM,*) '<CONTACT> INITIALISATIONS' 
      ENDIF
C 
C --- FONCTIONNALITES ACTIVEES
C     
      LDYNA  = NDYNLO(SDDYNA,'DYNAMIQUE')      
C
C --- DECOMPACTION DES VARIABLES CHAPEAUX
C       
      CALL DESAGG(VALMOI,DEPMOI,K24BID,K24BID,K24BID,
     &            K24BID,K24BID,K24BID,K24BID)
      CALL DESAGG(VALPLU,K24BID,K24BID,K24BID,K24BID,
     &            VITPLU,ACCPLU,K24BID,K24BID)           
C
C --- AFIN QUE LE VECTEUR DES FORCES D'INERTIE NE SOIT PAS MODIFIE AU
C --- COURS DE LA BOUCLE DES CONTRAINTES ACTIVES PAR L'APPEL A OP0070
C --- ON LE DUPLIQUE ET ON UTILISE CETTE COPIE FIXE (VITINI,ACCINI)
C
      VITINI = RESOCO(1:14)//'.VITI'
      ACCINI = RESOCO(1:14)//'.ACCI'
      IF (LDYNA) THEN
        CALL COPISD('CHAMP_GD','V',VITPLU,VITINI)
        CALL COPISD('CHAMP_GD','V',ACCPLU,ACCINI)
      END IF          
C
C --- SAUVEGARDE DEPLACEMENTS A L'INSTANT MOINS PR BCL GEOMETRIE
C
      DEPGEO = RESOCO(1:14)//'.DEPG'
      CALL COPISD('CHAMP_GD','V',DEPMOI,DEPGEO)
C
C --- SAUVEGARDE DEPLACEMENTS A L'INSTANT MOINS PR BCL FROTTEMENT
C
      DEPLAM = RESOCO(1:14)//'.DEPF'
      CALL COPISD('CHAMP_GD','V',DEPMOI,DEPLAM)      
C
      CALL JEDEMA()      
      END
