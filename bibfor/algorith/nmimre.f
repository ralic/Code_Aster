C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY  
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR     
C (AT YOUR OPTION) ANY LATER VERSION.                                   
C                                                                       
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT   
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*24 NUMEDD,SDIMPR
      INTEGER      IRELA,IMAXI,IREFE,INNODA
      REAL*8       VRELA,VMAXI,VREFE,VNODA
      CHARACTER*8  NODDLM
C 
C ----------------------------------------------------------------------
C
C ROUTINE MECA_NON_LINE (UTILITAIRE)
C
C IMPRESSION DES INFORMATIONS SUR LES RESIDUS DANS LE TABLEAU DE
C CONVERGENCE
C      
C ----------------------------------------------------------------------
C
C
C IN  NUMEDD : NUMEROTATION NUME_DDL
C IN  SDIMPR : SD AFFICHAGE
C IN  IRELA  : NUMERO DU DDL SUR LAQUELLE NORME_MAX (RESI_GLOB_RELA)
C IN  VRELA  : VALEUR NORME_MAX (RESI_GLOB_RELA)
C IN  IMAXI  : NUMERO DU DDL SUR LAQUELLE NORME_MAX (RESI_GLOB_MAXI)
C IN  VMAXI  : VALEUR NORME_MAX (RESI_GLOB_MAXI)
C IN  IREFE  : NUMERO DU DDL SUR LAQUELLE NORME_MAX (RESI_GLOB_REFE)
C IN  VREFE  : VALEUR NORME_MAX (RESI_GLOB_REFE)
C IN  INNODA : NUMERO DU NOEUD SUR LAQUELLE NORME_MAX (RESI_COMP_RELA)
C IN  VNODA  : VALEUR NORME_MAX (RESI_COMP_RELA)
C
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 IMPCNT,IMPCNL,IMPCNV,IMPCNA
      INTEGER      JIMPCT,JIMPCL,JIMPCV,JIMPCA  
      CHARACTER*16 NRELA,NMAXI,NREFE,NNODA
      REAL*8       R8BID
      INTEGER      IBID
      CHARACTER*16 K16BID
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()      
C
C --- ACCES SDIMPR
C      
      IMPCNT = SDIMPR(1:14)//'.CONV.TYP'
      IMPCNL = SDIMPR(1:14)//'.CONV.LIE'
      IMPCNV = SDIMPR(1:14)//'.CONV.VAL'
      IMPCNA = SDIMPR(1:14)//'.CONV.ACT'      
      CALL JEVEUO(IMPCNT,'E',JIMPCT)
      CALL JEVEUO(IMPCNL,'E',JIMPCL)
      CALL JEVEUO(IMPCNV,'E',JIMPCV)
      CALL JEVEUO(IMPCNA,'E',JIMPCA)  
C
C --- NOM DES DDLS      
C      
      CALL IMPCMP(IRELA ,NUMEDD,NRELA )
      CALL IMPCMP(IMAXI ,NUMEDD,NMAXI )
      CALL IMPCMP(IREFE ,NUMEDD,NREFE ) 
C 
      CALL IMPCOM(INNODA,NODDLM,NNODA)
C
C --- SAUVEGARDE DANS SDIMPR
C      
      ZK16(JIMPCT-1+1) = 'RESI_GLOB_RELA'
      ZR(JIMPCV-1+1)   = VRELA
      ZK16(JIMPCL-1+1) = NRELA
      ZK16(JIMPCT-1+2) = 'RESI_GLOB_MAXI'
      ZR(JIMPCV-1+2)   = VMAXI
      ZK16(JIMPCL-1+2) = NMAXI
      ZK16(JIMPCT-1+3) = 'RESI_GLOB_REFE'
      ZR(JIMPCV-1+3)   = VREFE
      ZK16(JIMPCL-1+3) = NREFE
      ZK16(JIMPCT-1+4) = 'RESI_COMP_RELA'
      ZR(JIMPCV-1+4)   = VNODA
      ZK16(JIMPCL-1+4) = NNODA
C
C --- MODIFICATIONS DES COLONNES
C
      CALL IMPSDR(SDIMPR,'RESI_RELA',K16BID,VRELA ,IBID  )
      CALL IMPSDR(SDIMPR,'RELA_NOEU',NRELA ,R8BID ,IBID  )
      CALL IMPSDR(SDIMPR,'RESI_MAXI',K16BID,VMAXI ,IBID  )
      CALL IMPSDR(SDIMPR,'MAXI_NOEU',NMAXI ,R8BID ,IBID  )
      CALL IMPSDR(SDIMPR,'RESI_REFE',K16BID,VREFE ,IBID  )
      CALL IMPSDR(SDIMPR,'REFE_NOEU',NREFE ,R8BID ,IBID  ) 
      CALL IMPSDR(SDIMPR,'RESI_COMP',K16BID,VNODA,IBID)      
      CALL IMPSDR(SDIMPR,'COMP_NOEU',NNODA,R8BID,IBID)      
C
      CALL JEDEMA()
      END
