      SUBROUTINE NMIMRE(NUMEDD,SDIMPR,IRELA ,IMAXI ,IREFE ,
     &                  VRELA ,VMAXI ,VREFE, VNODA, NODDLM,INNODA )     
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 26/07/2011   AUTEUR ABBAS M.ABBAS 
C ======================================================================
