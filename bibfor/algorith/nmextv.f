C
C ----------------------------------------------------------------------
C
C ROUTINE *_NON_LINE (EXTRACTION - UTILITAIRE)
C
C EXTRAIRE LES VALEURS DES COMPOSANTES - APPLICATION DE LA FORMULE
C
C ----------------------------------------------------------------------
C
C
C IN  NEFF   : NOMBRE EFFECTIF DE COMPOSANTES
C IN  FORMUL : NOM DE LA FORMULE (' ' SI PAS FORMULE)
C IN  NOMCMP : NOM DES COMPOSANTES
C IN  VALCMP : VALEURS DES COMPOSANTES
C OUT NVALCP : NOMBRE EFFECTIF DE COMPOSANTES
C OUT VALRES : RESULTAT DE LA FORMULE OU DE L'EXTRACTION
C
C
C
C
      INTEGER      IEFF,ICODE
      REAL*8       VALR
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NVALCP = 0
C
C --- EVALUATION DE LA FONCTION
C
      IF (FORMUL.EQ.' ') THEN
        DO 25 IEFF = 1,NEFF
          VALRES(IEFF) = VALCMP(IEFF)
  25    CONTINUE
        NVALCP = NEFF
      ELSE
        CALL FOINTE('FM'  ,FORMUL,NEFF  ,NOMCMP,
     &                     VALCMP,VALR  ,ICODE )          
        VALRES(1) = VALR
        NVALCP    = 1  
      ENDIF 
C
      CALL JEDEMA()
C
      END
