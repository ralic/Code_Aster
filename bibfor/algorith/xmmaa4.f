      SUBROUTINE XMMAA4(NNOL ,NNOF,PLA,IPGF,IVFF  ,FFC,
     &                  JAC, NOEUD  ,CSTACO,MMAT )

      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      INTEGER     NNOL,NNOF,IVFF,IPGF,PLA(27)
      REAL*8      MMAT(216,216)
      
      REAL*8      FFC(8),JAC,CSTACO
      LOGICAL     NOEUD

