      SUBROUTINE NMEXT0(TYPCHA,NBMA  ,NBNO  ,NBPI  ,NBSPI ,
     &                  NBCMP ,CHNOEU,CHGAUS,CHELGA,EXTRGA,
     &                  EXTRCH)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 11/09/2012   AUTEUR LADIER A.LADIER 
C ======================================================================
