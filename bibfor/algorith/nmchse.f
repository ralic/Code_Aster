      SUBROUTINE NMCHSE(MODE  ,MODELE,NUMEDD,MATE  ,CARELE,
     &                  COMPOR,LISCHA,CARCRI,INST  ,FONACT,
     &                  PARMET,SOLVEU,VALMOI,VALPLU,POUGD ,
     &                  SECMBR,DEPALG,VEELEM,MEELEM,MEASSE,
     &                  SDSENS,SDDYNA,NRPASE)
C
