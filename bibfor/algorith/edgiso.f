C ----------------------------------------------------------------------
C     MODELE VISCOPLASTIQUE SANS SEUIL DE EDGAR
C    CALCUL DE LA FONCTION SEUIL ET DE SA DERIVEE
C    SEUIL(DP)=EQSITR-3*MU*DP-GAMMA(K)*((PM+DP)**M(K))*(DP**N(K))
C  IN  DP      : INCREMENT DE DEFORMATION PLASTIQUE CUMULEE
C  IN  PM      : DEFORMATION PLASTIQUE CUMULEE A L INSTANT MOINS
C  IN  EQSITR : CONTRAINTE EQUIVALENTE ESSAI
C  IN  MU      : COEFFICIENT DE MATERIAU ELASTIQUE
C  IN  GAMMA   : COEFFICIENT VISQUEUX A MULTIPLIER PAR 2*MU
C  IN  M       : COEFFICIENT VISQUEUX
C  IN  N       : COEFFICIENT VISQUEUX

C  OUT SEUIL   : FONCTION SEUIL
C  OUT DSEUIL  : DERIVEE DE SEUIL
C ----------------------------------------------------------------------

      INTEGER  K
      
C 1 - FONCTION F

C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
      SEUIL = EQSITR-3.D0*MU*DP
      DO 10 K=1,3
        SEUIL=SEUIL-2.D0*MU*GAMMA(K)*((PM+DP)**M(K))*(DP**N(K))
 10   CONTINUE

C 2 - DERIVEE DE LA FONCTION F
 
      DSEUIL = -3.D0*MU
      DO 20 K=1,3
        DSEUIL=DSEUIL
     1       -2.D0*MU*GAMMA(K)*M(K)*((PM+DP)**(M(K)-1.D0))*(DP**N(K))
     2       -2.D0*MU*GAMMA(K)*N(K)*((PM+DP)**M(K))*(DP**(N(K)-1.D0))
 20   CONTINUE
 
      END
