      CHARACTER*8    K8B
C
C---------CONSTITUTION DE LA LISTE COMPLETE DES ET DDLS EN CAS D EFFORT
C---------RECUPERATION MODELE ET MAILLA
C
      CALL JEMARQ()
C
      CALL JEVEUO(MODMEC//'           .REFE','L',IAD1)
      MTRMAS = ZK24(IAD1)(1:8)
      CALL DISMOI('F','NB_EQUA'     ,MTRMAS,'MATR_ASSE',NBDDL,K8B  ,IE)
      CALL DISMOI('F','NOM_NUME_DDL',MTRMAS,'MATR_ASSE',IBID,NUMER ,IE)
      CALL DISMOI('F','NOM_MAILLA'  ,MTRMAS,'MATR_ASSE',IBID,MAILLA,IE)
      CALL DISMOI('F','CHAM_MATER'  ,MTRMAS,'MATR_ASSE',IBID,CHAMAT,IE)
      CALL DISMOI('F','CARA_ELEM'   ,MTRMAS,'MATR_ASSE',IBID,CELEM ,IE)
C
      CALL JEDEMA()
      END
