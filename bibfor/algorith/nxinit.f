      SUBROUTINE NXINIT(RESULT,MODELE,MATE  ,CARELE,COMPOR,
     &                  LISCHA,LISCH2,SOLVEU,PARA  ,NUMEDD,
     &                  LOSTAT,LEVOL ,LNONL ,SDDISC,
     &                  SDIETO,VHYDR ,SDOBSE,MAILLA,SDCRIT,
     &                  TIME  )
