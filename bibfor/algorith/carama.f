      SUBROUTINE CARAMA(NOMA  ,DEFICO,RESOCO,NEWGEO,POSMA ,
     &                  IZONE ,POSNO ,MATYP ,NBNOM ,COOR  ,
     &                  ZONESY,MOYEN ,LISSA ,NORMMA,TOLEIN,
     &                  TOLEOU,DIAGNO)
