        INTEGER NMAT,NBCOMM(NMAT,3),NVI,IROTA
        REAL*8 PGL(3,3),TOUTMS(5,24,6),VIND(*)
        CHARACTER*16 CPMONO(5*NMAT+1),COMP(*)

