      SUBROUTINE XMILAR(NDIM,JPTINT,JTABAR,AREINT,MILARA,MILARB)
      IMPLICIT NONE 

      INTEGER       NDIM,JTABAR,AREINT,JPTINT
      REAL*8        MILARA(NDIM),MILARB(NDIM)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGORITH  DATE 16/06/2010   AUTEUR CARON A.CARON 
C ======================================================================
