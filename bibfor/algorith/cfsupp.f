C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT     NONE
      CHARACTER*8  NOMA
      INTEGER      IZONE,IESCL
      INTEGER      JAPPAR,JAPMEM
      INTEGER      JNOMA,JPONO,JNOCO,JMACO,JDIM
      CHARACTER*3  PROJOP    
      INTEGER      POSESC,POSMIN,PROMIN  
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES DISCRETES - APPARIEMENT - MAIT/ESCL)
C
C DECIDE DE CE QUE L'ON FAIT DE LA LIAISON
C
C ----------------------------------------------------------------------
C
C
C IN  NOMA   : NOM DU MAILLAGE
C IN  JAPPAR : POINTEUR VERS RESOCO(1:14)//'.APPARI'
C IN  JAPMEM : POINTEUR VERS RESOCO(1:14)//'.APMEMO'
C IN  JNOMA  : POINTEUR VERS DEFICO(1:16)//'.NOMACO'
C IN  JPONO  : POINTEUR VERS DEFICO(1:16)//'.PNOMACO'
C IN  JNOCO  : POINTEUR VERS DEFICO(1:16)//'.NOEUCO'
C IN  JMACO  : POINTEUR VERS DEFICO(1:16)//'.MAILCO'
C IN  JDIM   : POINTEUR VERS DEFICO(1:16)//'.NDIMCO'
C IN  IZONE  : NUMERO DE LA ZONE DE CONTACT COURANTE
C IN  IESCL  : INDICE ABSOLU DU NOEUD ESCLAVE
C IN  POSESC : INDICE DANS CONTNO DU NOEUD ESCLAVE 
C IN  POSMIN : MAILLE MAITRE CORRESPONDANT AU JEU MINIMUM 
C               (INDICE DANS CONTMA)
C IN  PROMIN : TYPE DE PROJECTION CORRESPONDANT AU JEU MINIMUM
C               0     PROJECTION NORMALE
C              -1000  PROJECTION EN DEHORS
C                   DE LA MAILLE MAITRE (TOLERANCE TOLEOU ET DEBORD) 
C              -30X   PROJECTION SUR
C                   ENTITE GEOMETRIQUE NOEUD
C                   DE LA MAILLE MAITRE (TOLERANCE TOLEIN ET NOEUD)
C                      X EST LE NUMERO DE NOEUD (1 A 4)
C              -20X   PROJECTION SUR
C                   ENTITE GEOMETRIQUE ARETE
C                   DE LA MAILLE MAITRE (TOLERANCE TOLEIN ET ARETE)
C                      X EST LE NUMERO D'ARETE (1 A 4)
C              -10X   PROJECTION SUR
C                   ENTITE GEOMETRIQUE DIAG (PSEUDO SUR QUADRANGLE)
C                   DE LA MAILLE MAITRE (TOLERANCE TOLEIN ET DIAG)
C                      X EST LE NUMERO DE DIAGONALE (1 A 2)
C IN  PROJOP : OPERATION DE PROJECTION 
C              'NOP' ON NE FAIT RIEN
C              'ADD' ON AJOUTE L'APPARIEMENT DE CE NOEUD ESCLAVE
C              'SUP' ON SUPPRIME L'APPARIEMENT DE CE NOEUD ESCLAVE
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      TYPSUP,POSSUP
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()     
C
      IF (PROJOP.EQ.'NOP') THEN
        TYPSUP = 0
      ELSE IF (PROJOP.EQ.'ADD') THEN
        TYPSUP = 0
      ELSE IF (PROJOP.EQ.'SUP') THEN
C
C --- LE NOEUD ESCLAVE EST EXCLU (VA AU COIN !)
C
        TYPSUP = -3
        POSSUP = POSESC
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- EXCLUSION DU NOEUD ESCLAVE
C
      IF (TYPSUP.NE.0) THEN
        CALL CFSUPM(NOMA  ,IZONE ,IESCL ,JDIM  ,JNOCO,
     &              JMACO ,JAPMEM,JAPPAR,POSSUP,TYPSUP)
      ENDIF
C 
      CALL JEDEMA()
      END
