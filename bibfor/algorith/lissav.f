C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C
      IMPLICIT     NONE
      INCLUDE 'jeveux.h'
      CHARACTER*19 LISCHA
      CHARACTER*13 PREFOB
      INTEGER      ICHAR
      CHARACTER*8  CHARGE
      CHARACTER*16 TYPAPP,TYPFCT
      INTEGER      CODCHA
      CHARACTER*8  TYPECH,NOMFCT
      REAL*8       PHASE
      INTEGER      NPUIS
C
C ----------------------------------------------------------------------
C
C ROUTINE UTILITAIRE (LISTE_CHARGES)
C
C SAUVEGARDE DANS LA SD LISTE_CHARGES
C
C ----------------------------------------------------------------------
C
C
C IN  LISCHA : NOM DE LA SD LISTE_CHARGES
C IN  ICHAR  : INDICE DE LA CHARGE
C IN  CHARGE : NOM DE LA CHARGE (AFFE_CHAR_*)
C IN  TYPECH : TYPE DE LA CHARGE
C               'REEL'    - CHARGE CONSTANTE REELLE
C               'COMP'    - CHARGE CONSTANTE COMPLEXE
C               'FONC_F0' - CHARGE FONCTION QUELCONQUE
C               'FONC_FT' - CHARGE FONCTION DU TEMPS
C IN  CODCHA : CODE (ENTIER CODE) CONTENANT LES GENRES
C IN  PREFOB : PREFIXE DE L'OBJET DE LA CHARGE
C IN  TYPAPP : TYPE D'APPLICATION DE LA CHARGE
C              FIXE_CSTE
C              FIXE_PILO
C              SUIV
C              DIDI
C IN  NOMFCT : NOM DE LA FONCTION MULTIPLICATRICE
C IN  TYPFCT : TYPE DE LA FONCTION MULTIPLICATRICE
C              'FONCT_REEL' FONCTION MULTIPLICATRICE REELLE
C              'FONCT_COMP' FONCTION MULTIPLICATRICE COMPLEXE
C              'CONST_REEL' FONCTION MULTIPLICATRICE CONSTANTE REELLE
C              'CONST_COMP' FONCTION MULTIPLICATRICE CONSTANTE COMPLEXE
C IN  PHASE  : PHASE POUR LES FONCTIONS MULTIPLICATRICES COMPLEXES
C IN  NPUIS  : PUISSANCE POUR LES FONCTIONS MULTIPLICATRICES COMPLEXES
C
C
C
C
      CHARACTER*24 NOMCHA,CODECH,TYPCHA,TYPEAP,PRECHA
      INTEGER      JNCHA,JCODC,JTYPC,JTYPA,JPREC
      CHARACTER*24 NOMFON,TYPFON,VALFON
      INTEGER      JNFON,JTFON,JVFON
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NOMCHA = LISCHA(1:19)//'.NCHA'
      CODECH = LISCHA(1:19)//'.CODC'
      TYPCHA = LISCHA(1:19)//'.TYPC'
      TYPEAP = LISCHA(1:19)//'.TYPA'
      PRECHA = LISCHA(1:19)//'.PREO'
      NOMFON = LISCHA(1:19)//'.NFON'
      TYPFON = LISCHA(1:19)//'.TFON'
      VALFON = LISCHA(1:19)//'.VFON'
C
      CALL JEVEUO(NOMCHA,'E',JNCHA)
      CALL JEVEUO(CODECH,'E',JCODC)
      CALL JEVEUO(TYPCHA,'E',JTYPC)
      CALL JEVEUO(TYPEAP,'E',JTYPA)
      CALL JEVEUO(PRECHA,'E',JPREC)
      CALL JEVEUO(NOMFON,'E',JNFON)
      CALL JEVEUO(TYPFON,'E',JTFON)
      CALL JEVEUO(VALFON,'E',JVFON)
C
      ZK8 (JNCHA-1+ICHAR) = CHARGE
      ZI  (JCODC-1+ICHAR) = CODCHA
      ZK8 (JTYPC-1+ICHAR) = TYPECH
      ZK16(JTYPA-1+ICHAR) = TYPAPP
      ZK24(JPREC-1+ICHAR) = PREFOB
      ZK8 (JNFON-1+ICHAR) = NOMFCT
      ZK16(JTFON-1+ICHAR) = TYPFCT
      ZR  (JVFON-1+2*(ICHAR-1)+1) = PHASE
      ZR  (JVFON-1+2*(ICHAR-1)+2) = NPUIS
C
      CALL JEDEMA()
      END
