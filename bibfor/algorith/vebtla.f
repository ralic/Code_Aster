      IMPLICIT NONE
      CHARACTER*(*) MODELZ, DEPLAZ, VEDIRZ
      CHARACTER*8   MODELE, VEDIRI
      CHARACTER*19  LISCHA, DEPLA

C ----------------------------------------------------------------------
C     CALCUL DES VECTEURS ELEMENTAIRES BT.LAMBDA
C
C IN       MODELE K8   NOM DU MODELE
C IN       DEPLA  K19  CHAMP DE DEPLACEMENTS
C IN       LISCHA K19  SD L_CHARGES
C IN/JXOUT VEDIRI K8   LISTE DE VECTEURS ELEMENTAIRES
C
C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------
C
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------

      CHARACTER*6  MASQUE
      CHARACTER*8  NOMCHA, LPAIN(2), LPAOUT(1), K8BID
      CHARACTER*16 OPTION
      CHARACTER*24 LIGRCH, LCHIN(2), LCHOUT(1)
      INTEGER      IRET, NCHAR, JDIR, NDIR, JCHAR, JINF, ICHA
C ----------------------------------------------------------------------


      CALL JEMARQ()
      MODELE = MODELZ
      DEPLA  = DEPLAZ
      VEDIRI = VEDIRZ

      CALL JEEXIN(LISCHA // '.LCHA',IRET)
      IF ( IRET .EQ. 0 ) GOTO 9999
      CALL JELIRA(LISCHA // '.LCHA','LONMAX',NCHAR,K8BID)
      CALL JEVEUO(LISCHA // '.LCHA','L',JCHAR)
      CALL JEVEUO(LISCHA // '.INFC','L',JINF)


C -- CREATION DU VECT_ELEM SI BESOIN

      CALL JEEXIN(VEDIRI // '.LISTE_RESU' ,IRET)
      IF ( IRET .EQ. 0 ) THEN
        CALL MEMARE('V', VEDIRI, MODELE, ' ', ' ', 'CHAR_MECA')
        CALL WKVECT(VEDIRI // '.LISTE_RESU','V V K24',NCHAR,JDIR)
      ELSE
        CALL JEVEUO(VEDIRI // '.LISTE_RESU','E',JDIR)
      ENDIF
      MASQUE = '.REXXX'


C --- CALCUL DE L'OPTION BTLAMBDA

      OPTION = 'MECA_BTLA_R'
      NDIR  = 0
      DO 10 ICHA = 1,NCHAR
        IF ( ZI(JINF+ICHA) .LE. 0 ) GOTO 10
        NOMCHA = ZK24(JCHAR+ICHA-1)(1:8)
        LIGRCH = NOMCHA // '.CHME.LIGRE'

        NDIR = NDIR + 1
        CALL CODENT(NDIR,'D0',MASQUE(4:6))

        LPAIN(1)  = 'PDDLMUR'
        LCHIN(1)  =  NOMCHA//'.CHME.CMULT'
        LPAIN(2)  = 'PLAGRAR'
        LCHIN(2)  =  DEPLA
        LPAOUT(1) = 'PVECTUR'
        LCHOUT(1) =  VEDIRI // MASQUE
        CALL CALCUL('S',OPTION,LIGRCH,2,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V')
        ZK24(JDIR-1 + NDIR) = LCHOUT(1)
 10   CONTINUE
      CALL JEECRA(VEDIRI // '.LISTE_RESU','LONUTI',NDIR,K8BID)


 9999 CONTINUE
      CALL JEDEMA()
      END
