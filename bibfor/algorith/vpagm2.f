C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C TOLE CRP_7
      IMPLICIT NONE
      REAL*8   X
      REAL*8   VPAGM2     
C
CDEB
C---------------------------------------------------------------
C     FONCTION A(X)  :  CAS DE LA LOI GATT-MONERIE
C---------------------------------------------------------------
C IN  X     :R: ARGUMENT RECHERCHE LORS DE LA RESOLUTION SCALAIRE
C---------------------------------------------------------------
C     L'ETAPE LOCALE DU CALCUL VISCOPLASTIQUE (CALCUL DU TERME
C       ELEMENTAIRE DE LA MATRICE DE RIGIDITE TANGENTE) COMPORTE
C       LA RESOLUTION D'UNE EQUATION SCALAIRE NON LINEAIRE:
C
C           A(X) = 0
C
C       (DPC,SIELEQ,DEUXMU,DELTAT JOUENT LE ROLE DE PARAMETRES)
C---------------------------------------------------------------
CFIN
C     COMMON POUR LES PARAMETRES DES LOIS VISCOPLASTIQUES
      COMMON / NMPAVP / SIELEQ,DEUXMU,TROISK,DELTAT,TSCHEM,PREC,THETA,
     &                  NITER
      REAL*8            SIELEQ,DEUXMU,TROISK,DELTAT,TSCHEM,PREC,THETA,
     &                  NITER
     
C     COMMON POUR LES PARAMETRES DE LA LOI GATT-MONERIE
      COMMON / NMPAGM /AK1,AK2,XN1,XN2,EXPA1,EXPA2,EXPAB1,EXPAB2,A1,A2,
     &                 B1,B2,XW,XQ,XH,SIGE,SIGH,SIGH0,POROM,SGD
      REAL*8           AK1,AK2,XN1,XN2,EXPA1,EXPA2,EXPAB1,EXPAB2,A1,A2,
     &                 B1,B2,XW,XQ,XH,SIGE,SIGH,SIGH0,POROM,SGD
      
      REAL*8   EXP1,EXP2,EX1,EX2,TK,TT,XTANH,CTHETA,CTHETP,
     &         PSI1,PSI2,PSI1P,PSI2P,ZZ1,ZZ2,G,PHIP,INV1

C---- DEBUT INSERTION C3
C     POTENTIEL BASSE CONTRAINTE (PSI1)
      EXP1= XN1 + 1.D0
      EX1 = 0.5D0*EXP1
      ZZ1 = A1*((1.5D0*SIGH)**2) + B1*(X**2)
      PSI1= (AK1/EXP1)*(ZZ1**EX1)
C     POTENTIEL FORTE CONTRAINTE (PSI12)
      EXP2= XN2 + 1.D0
      EX2 = 0.5D0*EXP2
      ZZ2 = A2*((1.5D0*SIGH)**2) + B2*(X**2)
C     RESTONS RAISONNABLE
      IF (ZZ2.GE.1.D8) THEN
        ZZ2 = 1.0D8
      ENDIF
C     
      PSI2=(AK2/EXP2)*(ZZ2**EX2)
C     FONCTION COUPLAGE (CTHETA)
      TK = TSCHEM + 273.D0
      INV1 = SQRT(2.D0*X*X/3.D0+3.D0*SIGH*SIGH)
      TT = XW*(INV1**XQ)
      XTANH = TANH( (TK-TT)/XH )
      CTHETA = 0.5D0*(1 + XTANH )
C     CALCUL DE G
      PSI1P = X*AK1*B1*(ZZ1**(EX1-1.D0))
      PSI2P = X*AK2*B2*(ZZ2**(EX2-1.D0))
      PHIP = - 2.D0*XW*XQ*X*(INV1**(XQ-2.D0))/(3.D0*XH)
      CTHETP = 0.5D0*(1.D0-XTANH**2)*PHIP
      IF (X.EQ.0.D0) THEN
        G = 0.D0
      ELSE
        G = (1.D0-CTHETA)*PSI1P + CTHETA*PSI2P + (PSI2-PSI1)*CTHETP
      ENDIF
C---- FIN INSERTION C3
      G = G*THETA
      VPAGM2  = 1.5D0*DEUXMU*DELTAT*G + X - SIELEQ
      END
