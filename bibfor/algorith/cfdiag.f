      IMPLICIT     NONE
      INTEGER      LMAT
      REAL*8       XMAX
C
C VALEUR MAXI SUR LA DIAGONALE DE LA MATR_ASSE DU SYSTEME MECANIQUE
C UTILISE POUR LA DETECTION DE PIVOT NUL
C
C IN  LMAT   : DESCRIPTEUR DE LA MATR_ASSE DU SYSTEME MECANIQUE
C OUT XMAX   : VALEUR DU PIVOT MAX
C
C --------------- DEBUT DECLARATIONS NORMALISEES JEVEUX ---------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
      CHARACTER*32       JEXNUM
C
C ---------- FIN  DECLARATIONS  NORMALISEES  JEVEUX --------------------
C
      INTEGER      JSMDI,JVALM,IEQ,IADIA,NEQ,IBID
      CHARACTER*19 MAT
      CHARACTER*4 KMPIC
C ----------------------------------------------------------------------


      CALL JEMARQ()
      XMAX = 0.D0
      MAT  = ZK24(ZI(LMAT+1))
      CALL MTDSC2 ( MAT, 'SMDI', 'L', JSMDI )
      CALL DISMOI('F','MPI_COMPLET',MAT,'MATR_ASSE',IBID,KMPIC,IBID)
      IF (KMPIC.NE.'OUI') CALL U2MESS('F','CALCULEL6_54')
      CALL JEVEUO ( JEXNUM(MAT//'.VALM',1), 'L', JVALM )

      NEQ=ZI(LMAT+2)
      DO 20 IEQ = 1 , NEQ
          IADIA = JVALM + ZI(JSMDI+IEQ-1) - 1
          XMAX = MAX ( XMAX , ZR(IADIA) )
 20   CONTINUE
      CALL JEDEMA()
      END
