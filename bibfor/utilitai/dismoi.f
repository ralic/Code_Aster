      SUBROUTINE DISMOI(ARRET,QUESTI,NOMOB,TYPECO,REPI,REPK,IERD)
      IMPLICIT NONE
      INTEGER                                      REPI,     IERD
      CHARACTER*(*)     ARRET,QUESTI,NOMOB,TYPECO,     REPK
C     ------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 29/03/2010   AUTEUR PELLET J.PELLET 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.
C
C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE                            VABHHTS J.PELLET
C     ------------------------------------------------------------------
C     IN:
C       ARRET  : 'F' : ERREUR FATALE SI IERD /=0
C                'C' : ON CONTINUE MEME SI IERD /=0
C       QUESTI : TEXTE PRECISANT LA QUESTION POSEE
C       NOMOB  : NOM D'UN OBJET DE CONCEPT DONNE
C       TYPECO : TYPE DU CONCEPT NOMOB
C     OUT:
C       REPI   : REPONSE ( SI ENTIERE )
C       REPK   : REPONSE ( SI CHAINE DE CARACTERES )
C       IERD   : CODE RETOUR (0--> OK, 1--> PB )
C
C     ------------------------------------------------------------------
C     VARIABLES LOCALES:
C     ------------------
      CHARACTER*32 NOMO1,REPK1
      CHARACTER*24 TYPEC1
      CHARACTER*1 ARRET2
C --------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C --------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------
      INTEGER REPI1, IERD1
C
C DEB-------------------------------------------------------------------
C
      ARRET2=ARRET
      CALL ASSERT(ARRET2.EQ.'F'.OR.ARRET2.EQ.'C')

      REPI1=99999
      IERD1=0
      REPK1='????????'
C
C     --ON RECOPIE LES ARGUMENTS "CARACTERE" EN ENTREE
C       DANS DES VARIABLES DE LONGUEUR FIXE (K24) :
C
      TYPEC1 = TYPECO
      NOMO1  = NOMOB
C
C     -- SUIVANT LE TYPE DU CONCEPT, ON APPELLE DIFFERENTS DISMIJ:
C
      IF (TYPEC1.EQ.'MATR_ASSE') THEN
         CALL DISMMS(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'RESULTAT') THEN
         CALL DISMRS(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'CATALOGUE') THEN
         CALL DISMCT(QUESTI,NOMO1(1:1),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'INCONNU') THEN
         CALL DISMIC(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'MACR_ELEM_STAT') THEN
         CALL DISMML(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'CHAM_MATER') THEN
         CALL DISMCM(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'CHAM_NO') THEN
         CALL DISMCN(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'CARTE') THEN
         CALL DISMCA(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'CHAMP') THEN
         CALL DISMCP(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'GRANDEUR') THEN
         CALL DISMGD(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'PHENOMENE') THEN
         CALL DISMPH(QUESTI,NOMO1(1:16),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'PHEN_MODE') THEN
         CALL DISMPM(QUESTI,NOMO1(1:32),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'NUME_DDL') THEN
         CALL DISMNU(QUESTI,NOMO1(1:14),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'PROF_CHNO') THEN
         CALL DISMPN(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'MATR_ELEM') THEN
         CALL DISMME(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'VECT_ELEM') THEN
         CALL DISMME(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'LIGREL') THEN
         CALL DISMLG(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'MAILLAGE') THEN
         CALL DISMMA(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'CHARGE') THEN
         CALL DISMCH(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'MODELE') THEN
         CALL DISMMO(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'CHAM_ELEM') THEN
         CALL DISMCE(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'RESUELEM') THEN
         CALL DISMRE(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'INTERF_DYNA') THEN
         CALL DISMLI(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'NUME_EQUA') THEN
         CALL DISMNE(QUESTI,NOMO1(1:19),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'TYPE_ELEM') THEN
         CALL DISMTE(QUESTI,NOMO1(1:16),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'TYPE_MAILLE') THEN
         CALL DISMTM(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE IF (TYPEC1.EQ.'FISS_XFEM') THEN
         CALL DISMXF(QUESTI,NOMO1(1:8),REPI1,REPK1,IERD1)
      ELSE
         IF (ARRET2.EQ.'F') THEN
           REPK1 = TYPECO
           CALL U2MESK('F','UTILITAI_65',1,REPK1)
         ENDIF
      END IF

 9999 CONTINUE

C     -- ON NE DOIT PAS SORTIR DE DISMOI SI IERD1/=0 ET ARRET='F'
      IF (IERD1.NE.0.AND.ARRET2.EQ.'F') CALL ASSERT(.FALSE.)

      REPK = REPK1
      REPI = REPI1
      IERD = IERD1
      END
