C FONCTION: INITIALISE LE CHAM_NO CHAMIN A:
C           . SI REAROT=FALSE:  ZERO
C           . SI REAROT=TRUE :  ZERO POUR LES DDL DE TRANSLATION OU DE
C                               PETITE ROTATION
C                               LA VALEUR DE MME RANG EXTRAITE DE CHAMRO
C                               POUR LES DDL DE GRANDE ROTATION
C
