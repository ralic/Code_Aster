C
C ROUTINE MECA_NON_LINE (SDDYNA - UTILITAIRE)
C
C INTERROGE SDDYNA POUR RENVOYER UNE CHAINE
C      
C ----------------------------------------------------------------------
C
C
C OUT NDYNKK : NOM DE LA SD DEDIEE 
C               - AU MULTI_APPUI OU
C               - A L'AMORTISSEMENT MODAL OU
C               - A LA PROJECTION MODALE
C IN  SDDYNA : NOM DE LA SD DEDIEE A LA DYNAMIQUE
C IN  CHAINE :  = / 'MULTI_APPUI' 
C                 / 'AMOR_MODAL'
C                 / 'PROJ_MODAL'
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      JNSD
      CHARACTER*19 VALK(1)
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      CALL JEVEUO(SDDYNA(1:15)//'.NOM_SD','L',JNSD)
C
      IF(CHAINE(1:11).EQ.'MULTI_APPUI')THEN
        NDYNKK = ZK24(JNSD+1-1)
      ELSEIF(CHAINE(1:10).EQ.'AMOR_MODAL')THEN
        NDYNKK = ZK24(JNSD+2-1)
      ELSEIF(CHAINE(1:10).EQ.'PROJ_MODAL')THEN
        NDYNKK = ZK24(JNSD+3-1)
      ELSE
        VALK(1) = CHAINE
        CALL U2MESK('F','UTILITAI7_7',1,VALK)
      ENDIF
C
      CALL JEDEMA()

      END
