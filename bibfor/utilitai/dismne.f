      REPK=' '
      IF (QUESTI.EQ.'NOM_MAILLA') THEN
         CALL JEVEUO(NOMOB(1:19)//'.REFN','L',IAREFE)
         REPK= ZK24(IAREFE)(1:8)
      ELSEIF (QUESTI.EQ.'NOM_GD') THEN
         CALL JEVEUO(NOMOB(1:19)//'.REFN','L',IAREFE)
         REPK= ZK24(IAREFE+1)(1:8)
      ELSE
         IERD=1
      END IF
C
      REPKZ = REPK
      CALL JEDEMA()
      END
