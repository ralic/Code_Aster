C        DEBUT/MESURE_TEMPS/NIVE_DETAIL
C
C  ACTION = 'INIT' : ON INITIALISE LES DIFFERENTES MESURES
C  ACTION = 'IMPR' : ON IMPRIME LES DIFFERENTES MESURES
