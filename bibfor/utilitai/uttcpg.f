C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------

C     -- COMMONS POUR MESURE DE TEMPS :
C        MTPNIV : NIVEAU D'IMPRESSION DES MESURES DE TEMPS DEMANDE
C                 PAR L'UTILISATEUR
C        MTPSTA : IMPRESSION DES STATISTIQUES OU NON EN PARALLELE
      INTEGER  MTPNIV,MTPSTA,INDMAX
      PARAMETER (INDMAX=5)
      CHARACTER*80 SNOLON(INDMAX)
      REAL*8 VALMES(INDMAX*7),VALMEI(INDMAX*7)
      COMMON /MESTP1/ MTPNIV,MTPSTA
      COMMON /MESTP2/ SNOLON
      COMMON /MESTP3/ VALMES,VALMEI

      INTEGER NDIM,NBMESU
      PARAMETER (NDIM=30)
      INTEGER IFM,K
      CHARACTER*1 PRPAL(NDIM)
      CHARACTER*24 NOMC(NDIM)
      CHARACTER*80 NOML(NDIM)
C----------------------------------------------------------------------

      CALL UTTCPL(NDIM,NBMESU,NOMC,NOML,PRPAL)

      IF (ACTION.EQ.'INIT') THEN
         DO 1, K=1,NBMESU
           CALL UTTCPU (NOMC(K),'INIT',NOML(K))
 1       CONTINUE

      ELSEIF (ACTION.EQ.'IMPR') THEN
        IFM=6

        IF (MTPNIV.EQ.1) THEN
          DO 2, K=1,NBMESU
            IF (PRPAL(K).EQ.'S') GOTO 2
            IF (TYPIMP.EQ.'INCR') GOTO 2
            CALL UTTCPI (NOMC(K),IFM,TYPIMP)
 2        CONTINUE

        ELSEIF (MTPNIV.EQ.2) THEN
          DO 3, K=1,NBMESU
            IF (TYPIMP.EQ.'INCR') GOTO 3
            CALL UTTCPI (NOMC(K),IFM,TYPIMP)
 3        CONTINUE

        ELSEIF (MTPNIV.EQ.3) THEN
          DO 4, K=1,NBMESU
            CALL UTTCPI (NOMC(K),IFM,TYPIMP)
 4        CONTINUE

        ENDIF

      ENDIF

      END
