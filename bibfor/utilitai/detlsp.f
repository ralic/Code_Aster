C
C ----------------------------------------------------------------------
C
C  DETRUIRE LES INSTANCES MUMPS DU PRECONDITIONNEUR LDLT_SP
C  ***                                              *    **
C
C ----------------------------------------------------------------------
C
C IN  MATASS : MATRICE ASSEMBLEE
C IN  SOLVEU : SD SOLVEUR
C
