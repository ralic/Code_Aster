C     ----------
      INCLUDE 'jeveux.h'
      INTEGER REPI,IERD
      CHARACTER*(*) QUESTI
      CHARACTER*32  REPK
      CHARACTER*8   NOMOB
      CHARACTER*(*) NOMOBZ,REPKZ
C ----------------------------------------------------------------------
C    IN:
C       QUESTI : TEXTE PRECISANT LA QUESTION POSEE
C       NOMOBZ : NOM D'UN OBJET DE TYPE LIGREL
C    OUT:
C       REPI   : REPONSE ( SI ENTIERE )
C       REPKZ  : REPONSE ( SI CHAINE DE CARACTERES )
C       IERD   : CODE RETOUR (0--> OK, 1 --> PB)
C
C ----------------------------------------------------------------------
C     VARIABLES LOCALES:
C     ------------------
      INTEGER JREFM
      CHARACTER*8 MODELE

C
C
      CALL JEMARQ()
      REPK=' '
      NOMOB = NOMOBZ

      CALL JEVEUO(NOMOB//'.REFM','L',JREFM)
      IF (QUESTI.EQ.'NOM_MAILLA') THEN
         REPK= ZK8(JREFM-1+2)
      ELSE IF (QUESTI.EQ.'NOM_MODELE') THEN
         REPK= ZK8(JREFM-1+1)
      ELSE IF (QUESTI.EQ.'NOM_NUME_DDL') THEN
         REPK= ZK8(JREFM-1+5)
      ELSE IF (QUESTI.EQ.'NOM_PROJ_MESU') THEN
         REPK= ZK8(JREFM-1+9)

      ELSE IF (QUESTI.EQ.'DIM_GEOM') THEN
         MODELE=ZK8(JREFM)
         CALL DISMMO(QUESTI,MODELE,REPI,REPK,IERD)
      ELSE
         IERD=1
      END IF
C
      REPKZ = REPK
      CALL JEDEMA()
      END
