C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C     STOCKAGE DANS LA COLLECTION CHVAL DES FONCTIONS COMPOSANT LA NAPPE
C     ACCES PAR NOM OU NUMERO DANS LA COLLECTION
C     ------------------------------------------------------------------
C IN  CHVAL : NOM JEVEUX DE LA COLLECTION
C IN  VECNOM: NOMS DES FONCTIONS, ISSUS DES COMMANDES UTILISATEUR
C IN  NBFONC: NOMBRE DE FONCTIONS
C     ------------------------------------------------------------------
C     OBJETS SIMPLES LUS
C         CHFVAL=VECNOM(I)//'.VALE'
C     OBJETS SIMPLES CREES
C         JEXNUM(CHVAL,I)
C     ----------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON/IVARJE/ZI(1)
      COMMON/RVARJE/ZR(1)
      COMMON/CVARJE/ZC(1)
      COMMON/LVARJE/ZL(1)
      COMMON/KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8  ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      CHARACTER*32 JEXNUM
      CHARACTER*1 K1BID
C     ------------------------------------------------------------------
      INTEGER      I,J,NUMFON,NBP,LVALN,LVALF,NBPT,JPRO
      CHARACTER*24 CHFVAL,CHFPRO
C     ------------------------------------------------------------------
      CALL JEMARQ()
      NBPT=0
      CHFVAL(20:24) = '.VALE'
      DO 1 I=1,NBFONC
         CHFVAL(1:19) = VECNOM(I)
         CALL JELIRA(CHFVAL,'LONUTI',NBP,K1BID)
         NBPT=NBPT+NBP
    1 CONTINUE
      CALL JEECRA(CHVAL,'LONT',NBPT,' ')
      DO 3 I=1,NBFONC
         CHFVAL(1:19) = VECNOM(I)
         CALL JEVEUO(CHFVAL,'L',LVALF)
         CALL JELIRA(CHFVAL,'LONUTI',NBP,K1BID)
         CALL JECROC(JEXNUM(CHVAL,I))
         CALL JEECRA(JEXNUM(CHVAL,I),'LONMAX',NBP,' ')
         CALL JEECRA(JEXNUM(CHVAL,I),'LONUTI',NBP,' ')
         CALL JEVEUO(JEXNUM(CHVAL,I),'E',LVALN)
         DO 2 J=1,NBP
            ZR(LVALN+J-1)=ZR(LVALF+J-1)
    2    CONTINUE
         CALL JELIBE(CHFVAL)
    3 CONTINUE
      CALL JEDEMA()
      END
