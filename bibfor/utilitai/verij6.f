C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE VABHHTS J.PELLET
C TOLE CRP_20
C ----------------------------------------------------------------------
C  BUT : "COCHER" DANS VCO LES OBJETS POTENTIELLEMENT
C         CREES DANS LA SD NOMSD DE TYPE TYPESD

C IN K*   COUI   :
C    /'C'     : NOMSD EST LIE A LA SD SUP. PAR COMPOSITION
C    /'I_XXX' : NOMSD EST LIE A LA SD SUP. PAR L'INDIRECTION XXX
C IN K*   TYPESD : TYPE DE LA STRUCTURE DE DONNEE A COCHER
C IN K8   NOMSD  : NOM DE LA STRUCTURE DE DONNEE A COCHER
C IN K24  PN     : POINTEUR DE NOM CONTENANT LES OBJETS REELLEMENT CREES
C IN/JXVAR M80   : NOM DE L'OBJET MEMORISANT LES SD VISITEES
C                  (POUR EVITER LES RECURSIONS INFINIES)
C IN/OUT  NVU    : NOMBRE DE SD DEJA VISITEES
C OUT V(I) VCO   : VECTEUR QUE L'ON COCHE (1-> COCHE)
C IN/OUT  NTROU  : NOMBRE D'OJB EXISTANT (CUMULE)

C ----------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32,JEXNOM,JEXNUM
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------

      INTEGER J1,I1,N1,N2,K,K2,IBID,IEXI,J2,NUORD
      CHARACTER*4 TYCH
      CHARACTER*5 SUFFIX
      CHARACTER*8 CH8,KBID,FONC,TYMAT,FISS1
      CHARACTER*13 CH13
      CHARACTER*14 CH14
      CHARACTER*16 TYP2SD,CH16
      CHARACTER*17 CH17,TABLE
      CHARACTER*19 CH19,COMPO1,CHAMP
      CHARACTER*24 CH24

C     COMMON VERIJK : POUR ETABLIR LE CATALOGUE A PARTIR DE LA REALITE
C                     DES TESTS.
C     PRCATA : 'OUI' : IL FAUT IMPRIMER LES CARACTERISTIQUES DES
C                      OBJETS TROUVES.
C              'NON' : ON N'IMPRIME RIEN
C     DERNSD : TYPE DE LA SD DE PLUS BAS NIVEAU (PARCOURS DE L'ARBRE)
      CHARACTER*16 DERNSD,PRCATA
      COMMON /VERIJK/DERNSD,PRCATA


C -DEB------------------------------------------------------------------

      CALL JEMARQ()
      IF (NOMSD.EQ.' ') GOTO 380

      TYP2SD = TYPESD


      IF (TYP2SD.EQ.'MAILLAGE') THEN
C     ------------------------------
        CH8 = NOMSD
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH8//'.CONNEX',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.DIME',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.GROUPEMA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.GROUPENO',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.NOMACR',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.NOMMAI',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.NOMNOE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.PARA_R',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.SUPMAIL',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.TYPL',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.TYPMAIL',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.ADAPTATION',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.FORM',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.TITR',PN,VCO,M80,NVU,NTROU)

        CALL VERIJB('C','CHAM_NO',CH8//'.COORDO',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','CARTE',CH8//'.ABS_CURV',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','L_TABLE',CH8,PN,VCO,M80,NVU,NTROU)


      ELSEIF (TYP2SD.EQ.'MELASFLU') THEN
C     ------------------------------
        CH19 = NOMSD
        CH8 = NOMSD
        CALL VERIJB('C','TABLE',CH19,PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','L_TABLE',CH19,PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.NUMO',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.VITE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.FREQ',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.REMF',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.DESC',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.MASG',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.FACT',PN,VCO,M80,NVU,NTROU)

        CALL JEEXIN(CH19//'.0001',IEXI)
        IF (IEXI.EQ.0) GOTO 20
        CALL JEVEUO(CH19//'.0001','L',J1)
        CALL JELIRA(CH19//'.0001','LONMAX',N1,KBID)
        DO 10,K = 1,N1
          CHAMP = ZK24(J1-1+K)
          IF (CHAMP.EQ.' ') GOTO 10
          CALL VERIJB('I_001','CHAMP',CHAMP,PN,VCO,M80,NVU,NTROU)
   10   CONTINUE
   20   CONTINUE

        CALL JEEXIN(CH8//'.TB000000  .0003',IEXI)
        IF (IEXI.EQ.0) GOTO 40
        CALL JEVEUO(CH8//'.TB000000  .0003','L',J1)
        CALL JELIRA(CH8//'.TB000000  .0003','LONMAX',N1,KBID)
        DO 30,K = 1,N1
          IF (ZK24(J1-1+K).EQ.' ') GOTO 30
          CALL VERIJB('I_TB03','MATR_ASSE',ZK24(J1-1+K),PN,VCO,M80,NVU,
     &                NTROU)
   30   CONTINUE
   40   CONTINUE

        CALL JEEXIN(CH8//'.TB000000  .0004',IEXI)
        IF (IEXI.EQ.0) GOTO 40
        CALL JEVEUO(CH8//'.TB000000  .0004','L',J1)
        CALL JELIRA(CH8//'.TB000000  .0004','LONMAX',N1,KBID)
        DO 50,K = 1,N1
          IF (ZK24(J1-1+K).EQ.' ') GOTO 50
          CALL VERIJB('I_TB04','MATR_ASSE',ZK24(J1-1+K),PN,VCO,M80,NVU,
     &                NTROU)
   50   CONTINUE
   60   CONTINUE

        CALL JEEXIN(CH8//'.TB000000  .0005',IEXI)
        IF (IEXI.EQ.0) GOTO 40
        CALL JEVEUO(CH8//'.TB000000  .0005','L',J1)
        CALL JELIRA(CH8//'.TB000000  .0005','LONMAX',N1,KBID)
        DO 70,K = 1,N1
          IF (ZK24(J1-1+K).EQ.' ') GOTO 70
          CALL VERIJB('I_TB05','MATR_ASSE',ZK24(J1-1+K),PN,VCO,M80,NVU,
     &                NTROU)
   70   CONTINUE
   80   CONTINUE
   90   CONTINUE


      ELSEIF (TYP2SD.EQ.'L_TABLE') THEN
C     ------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH19//'.LTNS',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.LTNT',PN,VCO,M80,NVU,NTROU)

        CALL JEEXIN(CH19//'.LTNS',IEXI)
        IF (IEXI.EQ.0) GOTO 110
        CALL JEVEUO(CH19//'.LTNS','L',J1)
        CALL JELIRA(CH19//'.LTNS','LONMAX',N1,KBID)
        DO 100,K = 1,N1
          TABLE = ZK24(J1-1+K)
          IF (TABLE.EQ.' ') GOTO 100
          CALL VERIJB('I_LTNS','TABLE',TABLE,PN,VCO,M80,NVU,NTROU)
  100   CONTINUE
  110   CONTINUE


      ELSEIF (TYP2SD.EQ.'TABLE') THEN
C     ------------------------------
        CH17 = NOMSD
        CALL VERIJB('C','OJB',CH17//'  .TBNP',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH17//'  .TBLP',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH17//'  .TBBA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH17//'  .TITR',PN,VCO,M80,NVU,NTROU)

        CALL JEEXIN(CH17//'  .TBLP',IEXI)
        IF (IEXI.EQ.0) GOTO 150
        CALL JEVEUO(CH17//'  .TBLP','L',J1)
        CALL JELIRA(CH17//'  .TBLP','LONMAX',N1,KBID)
        N2 = N1/4
        CALL ASSERT(N1.EQ.N2*4)
        DO 140,K = 1,N2
          CALL VERIJB('I_TBLP3','OJB',ZK24(J1-1+4* (K-1)+3),PN,VCO,M80,
     &                NVU,NTROU)
          CALL VERIJB('I_TBLP4','OJB',ZK24(J1-1+4* (K-1)+4),PN,VCO,M80,
     &                NVU,NTROU)

C         -- PARFOIS ON CREE DES FONCTIONS EN SOUS-TERRAIN :
          IF (ZK24(J1-1+4* (K-1)+2).NE.'K24') GOTO 130

          CALL JEVEUO(ZK24(J1-1+4* (K-1)+3),'L',J2)
          CALL JELIRA(ZK24(J1-1+4* (K-1)+3),'LONMAX',N2,KBID)
          DO 120,K2 = 1,N2
            CALL VERIJB('I_TBLPF','FONCTION',ZK24(J2-1+K2),PN,VCO,M80,
     &                  NVU,NTROU)
  120     CONTINUE
  130     CONTINUE

  140   CONTINUE
  150   CONTINUE


      ELSEIF (TYP2SD.EQ.'LIGREL') THEN
C     ------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH19//'.NBNO',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.NOMA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.LIEL',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.REPE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.NEMA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.PRNM',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.PRNS',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.LGNS',PN,VCO,M80,NVU,NTROU)

        CALL JEEXIN(CH19//'.NOMA',IEXI)
        IF (IEXI.GT.0) THEN
          CALL JEVEUO(CH19//'.NOMA','L',J1)
          CALL VERIJB('I_NOMA','MAILLAGE',ZK8(J1-1+1),PN,VCO,M80,NVU,
     &                NTROU)
        ENDIF


      ELSEIF (TYP2SD.EQ.'MODELE') THEN
C     ------------------------------
        CH8 = NOMSD
        CALL VERIJB('C','LIGREL',CH8//'.MODELE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.NOEUD_UTIL',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.MAILLE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.NOEUD',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.SSSA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.FISS',PN,VCO,M80,NVU,NTROU)

        CALL JEEXIN(CH8//'.FISS',IEXI)
        IF (IEXI.EQ.0) GOTO 177
        CALL JEVEUO(CH8//'.FISS','L',J1)
        CALL JELIRA(CH8//'.FISS','LONMAX',N1,KBID)
        CALL ASSERT(N1.EQ.1)
        FISS1 = ZK8(J1-1+1)
        IF (FISS1.EQ.' ') GOTO 177
        CALL VERIJB('I_FISS','FISS_XFEM',FISS1,PN,VCO,M80,NVU,NTROU)
  177   CONTINUE


      ELSEIF (TYP2SD.EQ.'MACR_ELEM_STAT') THEN
C     -------------------------------------
        CH8 = NOMSD
        CH14 = NOMSD
        CALL VERIJB('C','OJB',CH8//'.CONX  ',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.DESM  ',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.KP_EE ',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.LICA  ',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.LICH  ',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.LINO  ',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.MP_EE ',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.PHI_IE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.REFM  ',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.VARM  ',PN,VCO,M80,NVU,NTROU)

        CALL VERIJB('C','NUME_DDL',CH14,PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','MATR_ASSE',CH8//'.MASSMECA',PN,VCO,M80,NVU,
     &              NTROU)
        CALL VERIJB('C','MATR_ASSE',CH8//'.RIGIMECA',PN,VCO,M80,NVU,
     &              NTROU)


      ELSEIF (TYP2SD.EQ.'MATER') THEN
C     ------------------------------
        CH8 = NOMSD
        CALL VERIJB('C','OJB',CH8//'.MATERIAU.NOMRC',PN,VCO,M80,NVU,
     &              NTROU)
        CALL VERIJB('C','FONCTION',CH8//'.&&RDEP',PN,VCO,M80,NVU,NTROU)

        CALL JEEXIN(CH8//'.MATERIAU.NOMRC',IEXI)
        IF (IEXI.EQ.0) GOTO 170
        CALL JEVEUO(CH8//'.MATERIAU.NOMRC','L',J1)
        CALL JELIRA(CH8//'.MATERIAU.NOMRC','LONMAX',N1,KBID)
        DO 160,K = 1,N1
          COMPO1 = CH8//'.'//ZK16(J1-1+K) (1:10)
          IF (COMPO1.EQ.' ') GOTO 160
          CALL VERIJB('I_NOMRC','COMPOR1',COMPO1,PN,VCO,M80,NVU,NTROU)
  160   CONTINUE
  170   CONTINUE


      ELSEIF (TYP2SD.EQ.'LISTR8' .OR. TYP2SD.EQ.'LISTIS') THEN
C     --------------------------------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH19//'.BINT',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.LPAS',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.NBPA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.VALE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.TITR',PN,VCO,M80,NVU,NTROU)


      ELSEIF (TYP2SD.EQ.'CHAR_CINE') THEN
C     ------------------------------
        CH8 = NOMSD
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH8//'.TYPE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.CIME.MODEL.NOMO',PN,VCO,M80,NVU,
     &              NTROU)
        CALL VERIJB('C','OJB',CH8//'.CITH.MODEL.NOMO',PN,VCO,M80,NVU,
     &              NTROU)
        CALL VERIJB('C','OJB',CH8//'.CIAC.MODEL.NOMO',PN,VCO,M80,NVU,
     &              NTROU)
        CALL VERIJB('C','OJB',CH19//'.DEFI',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.VALE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.VALF',PN,VCO,M80,NVU,NTROU)


      ELSEIF (TYP2SD.EQ.'PARA_SENSI') THEN
C     ------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','FONCTION',CH19,PN,VCO,M80,NVU,NTROU)


      ELSEIF (TYP2SD.EQ.'FORMULE') THEN
C     ------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH19//'.PROL',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.NOVA',PN,VCO,M80,NVU,NTROU)


      ELSEIF (TYP2SD.EQ.'L_CHARGES') THEN
C     ---------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH19//'.LCHA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.FCHA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.INFC',PN,VCO,M80,NVU,NTROU)


      ELSEIF (TYP2SD.EQ.'FONCTION') THEN
C     ------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH19//'.PROL',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.VALE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.PARA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.TITR',PN,VCO,M80,NVU,NTROU)


      ELSEIF (TYP2SD.EQ.'COMPOR1') THEN
C     ------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH19//'.VALK',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.VALR',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.VALC',PN,VCO,M80,NVU,NTROU)

        CALL JEEXIN(CH19//'.VALK',IEXI)
        IF (IEXI.EQ.0) GOTO 190
        CALL JEVEUO(CH19//'.VALK','L',J1)
        CALL JELIRA(CH19//'.VALK','LONMAX',N1,KBID)
        DO 180,K = 1,N1/2
          FONC = ZK8(J1-1+N1/2+K)
          IF (FONC.EQ.' ') GOTO 180
          CALL VERIJB('I_VALK','FONCTION',FONC,PN,VCO,M80,NVU,NTROU)
  180   CONTINUE
  190   CONTINUE


      ELSEIF (TYP2SD.EQ.'COMPOR') THEN
C     ------------------------------
        CH8 = NOMSD
        CALL VERIJB('C','OJB',CH8//'.CPRK',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH8//'.CPRI',PN,VCO,M80,NVU,NTROU)



      ELSEIF (TYP2SD.EQ.'CHAMP') THEN
C     --------------------------------
        CH19 = NOMSD
        CALL DISMOI('F','TYPE_CHAMP',CH19,'CHAMP',IBID,TYCH,IBID)
        IF (TYCH.EQ.'NOEU') THEN
          CALL VERIJB('I_CHNO','CHAM_NO',CH19,PN,VCO,M80,NVU,NTROU)

        ELSEIF (TYCH.EQ.'CART') THEN
          CALL VERIJB('I_CART','CARTE',CH19,PN,VCO,M80,NVU,NTROU)

        ELSEIF (TYCH(1:2).EQ.'EL') THEN
          CALL VERIJB('I_CHEL','CHAM_ELEM',CH19,PN,VCO,M80,NVU,NTROU)

        ELSEIF (TYCH.EQ.'RESL') THEN
          CALL VERIJB('I_RESL','RESUELEM',CH19,PN,VCO,M80,NVU,NTROU)

        ELSEIF (TYCH.EQ.'VGEN') THEN
          CALL VERIJB('I_VGEN','CHAM_GENE',CH19,PN,VCO,M80,NVU,NTROU)

        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF


      ELSEIF (TYP2SD.EQ.'CARTE') THEN
C     --------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH19//'.NOMA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.NOLI',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.DESC',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.LIMA',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.VALE',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.TITR',PN,VCO,M80,NVU,NTROU)

        CALL JEEXIN(CH19//'.NOMA',IEXI)
        IF (IEXI.GT.0) THEN
          CALL JEVEUO(CH19//'.NOMA','L',J1)
          CALL VERIJB('I_NOMA','MAILLAGE',ZK8(J1-1+1),PN,VCO,M80,NVU,
     &                NTROU)
        ENDIF


      ELSEIF (TYP2SD.EQ.'CHAM_ELEM') THEN
C     --------------------------------
        CH19 = NOMSD
        CALL VERIJB('C','OJB',CH19//'.CELD',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.CELK',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.CELV',PN,VCO,M80,NVU,NTROU)
        CALL VERIJB('C','OJB',CH19//'.TITR',PN,VCO,M80,NVU,NTROU)

        CALL JEEXIN(CH19//'.CELK',IEXI)
        IF (IEXI.GT.0) THEN
          CALL JEVEUO(CH19//'.CELK','L',J1)
          CALL VERIJB('I_CELK1','LIGREL',ZK24(J1-1+1),PN,VCO,M80,NVU,
     &                NTROU)
