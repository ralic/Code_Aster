      IMPLICIT REAL*8 (A-H,O-Z)
C     --     DISMOI(PROF_CHNO)
C     ARGUMENTS:
C     ----------
      INTEGER REPI,IERD
      CHARACTER*(*) QUESTI,CODMES
      CHARACTER*(*) NOMOBZ,REPKZ
      CHARACTER*32 REPK
      CHARACTER*19 NOMOB
C ----------------------------------------------------------------------
C     IN:
C       CODMES : CODE DES MESSAGES A EMETTRE : 'F', 'A', ...
C       QUESTI : TEXTE PRECISANT LA QUESTION POSEE
C       NOMOBZ : NOM D'UN OBJET DE TYPE PROF_CHNO
C     OUT:
C       REPI   : REPONSE ( SI ENTIERE )
C       REPKZ  : REPONSE ( SI CHAINE DE CARACTERES )
C       IERD   : CODE RETOUR (0--> OK, 1 --> PB)
C
C     LISTE DES QUESTIONS ADMISSIBLES:
C        'NB_DDLACT'
C ----------------------------------------------------------------------
C     VARIABLES LOCALES:
C     ------------------
C --------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C --------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------
      CHARACTER*19 NOLIGR
      CHARACTER*8 KBID
C
C
C
      CALL JEMARQ()
      NOMOB = NOMOBZ
      REPK  = REPKZ

      IF (QUESTI.EQ.'NB_DDLACT') THEN
C     --------------------------------
         CALL JELIRA(NOMOB//'.NUEQ','LONMAX',NEQU,KBID)
         CALL JELIRA(NOMOB//'.LILI','NUTIOC',NLILI,KBID)
         NBDDLB=0
         DO 10 I=2,NLILI
            CALL JENUNO(JEXNUM(NOMOB//'.LILI',I),NOLIGR)
            CALL DISMLG(CODMES,'NB_NO_SUP',NOLIGR,NBNOS,REPK,IERD)
            NBDDLB=NBDDLB+ NBNOS
 10      CONTINUE
         REPI=NEQU-3*(NBDDLB/2)

      ELSE
         REPK = QUESTI
         CALL UTMESS(CODMES,'DISMPN:',
     +               'LA QUESTION : "'//REPK//'" EST INCONNUE')
         IERD=1
         GO TO 9999
      END IF
C
 9999 CONTINUE
      REPKZ = REPK
      CALL JEDEMA()
      END
