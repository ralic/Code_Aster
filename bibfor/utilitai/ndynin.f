C
C ROUTINE MECA_NON_LINE (UTILITAIRE)
C
C INTERROGE SDDYNA POUR RENVOYER UN ENTIER
C      
C ----------------------------------------------------------------------
C
C
C OUT NDYNIN : TYPE DE FORMULATION
C            -> 1 : FORMULATION EN DEPLACEMENT
C            -> 2 : FORMULATION EN VITESSE
C            -> 3 : FORMULATION EN ACCELERATION
C IN  SDDYNA : NOM DE LA SD DEDIEE A LA DYNAMIQUE
C IN  CHAINE :  = / 'FORMUL_CONTACT' 
C                 / 'FORMUL_DYNAMIQUE'
C 
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      INTEGER      JTFOR,JNCHA
      CHARACTER*24 TFOR ,NCHA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      NDYNIN = 0
C
C --- INTERROGATION
C      
      TFOR = SDDYNA(1:15)//'.TYPE_FOR'
      NCHA = SDDYNA(1:15)//'.NBRE_CHA'
      CALL JEVEUO(TFOR,'L',JTFOR)
      CALL JEVEUO(NCHA,'L',JNCHA)
      IF (CHAINE.EQ.'FORMUL_DYNAMIQUE')THEN
        NDYNIN = ZI(JTFOR+1-1)
      ELSEIF (CHAINE.EQ.'FORMUL_CONTACT')THEN
        NDYNIN = ZI(JTFOR+2-1)
      ELSEIF (CHAINE.EQ.'NBRE_EXCIT')THEN
        NDYNIN = ZI(JNCHA+1-1)         
      ELSEIF (CHAINE.EQ.'NBRE_ONDE_PLANE')THEN
        NDYNIN = ZI(JNCHA+2-1)     
      ELSEIF (CHAINE.EQ.'NBRE_EXCIT_GENE')THEN
        NDYNIN = ZI(JNCHA+3-1)    
      ELSEIF (CHAINE.EQ.'NBRE_MODE_AMOR')THEN
        NDYNIN = ZI(JNCHA+4-1)  
      ELSEIF (CHAINE.EQ.'NBRE_MODE_PROJ')THEN
        NDYNIN = ZI(JNCHA+5-1)                              
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
      CALL JEDEMA()
      END
