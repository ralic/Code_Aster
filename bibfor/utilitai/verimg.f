      SUBROUTINE VERIMG(NOM)
      IMPLICIT NONE
      CHARACTER*(*) NOM
      CHARACTER*32 JEXNUM
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF UTILITAI  DATE 29/09/2006   AUTEUR VABHHTS J.PELLET 
C ======================================================================
