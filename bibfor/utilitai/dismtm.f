      IMPLICIT NONE
C     --     DISMOI(TYPE_MAILLE)
C     ARGUMENTS:
C     ----------
      INTEGER REPI,IERD
      CHARACTER*(*) QUESTI,CODMES
      CHARACTER*(*) NOMOBZ,REPKZ
      CHARACTER*32 REPK
      CHARACTER*8 NOMOB
C ----------------------------------------------------------------------
C    IN:
C       CODMES : CODE DES MESSAGES A EMETTRE : 'F', 'A', ...
C       QUESTI : TEXTE PRECISANT LA QUESTION POSEE
C       NOMOBZ : NOM D'UN OBJET DE TYPE TYPE_MAILLE (K8)
C    OUT:
C       REPI   : REPONSE ( SI ENTIERE )
C       REPKZ  : REPONSE ( SI CHAINE DE CARACTERES )
C       IERD   : CODE RETOUR (0--> OK, 1 --> PB)

C ----------------------------------------------------------------------
C     VARIABLES LOCALES:
C     ------------------
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR,JEXR8
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      CHARACTER*8 KBID
C---------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------
      INTEGER ITM,IANBNO



      CALL JEMARQ()
      NOMOB = NOMOBZ
      REPK = ' '
      REPI = 0
      CALL JENONU(JEXNOM('&CATA.TM.NOMTM',NOMOB),ITM)
      IF (ITM.EQ.0) CALL U2MESS(CODMES,'UTILITAI_70')


      IF (QUESTI.EQ.'NUM_TYPMAIL') THEN
C     ----------------------------------
        REPI = ITM


      ELSE IF (QUESTI.EQ.'NBNO_TYPMAIL') THEN
C     ----------------------------------
        CALL JEVEUO(JEXNUM('&CATA.TM.NBNO',ITM),'L',IANBNO)
        REPI = ZI(IANBNO)


      ELSE IF (QUESTI.EQ.'DIM_TOPO') THEN
C     ----------------------------------
        CALL JEVEUO(JEXNUM('&CATA.TM.TMDIM',ITM),'L',IANBNO)
        REPI = ZI(IANBNO)


      ELSE IF (QUESTI.EQ.'TYPE_TYPMAIL') THEN
C     ----------------------------------
        CALL JEVEUO(JEXNUM('&CATA.TM.TMDIM',ITM),'L',IANBNO)
        REPI = ZI(IANBNO)
        IF (REPI.EQ.0) THEN
          REPK = 'POIN'
        ELSE IF (REPI.EQ.1) THEN
          REPK = 'LIGN'
        ELSE IF (REPI.EQ.2) THEN
          REPK = 'SURF'
        ELSE IF (REPI.EQ.3) THEN
          REPK = 'VOLU'
        ELSE
          CALL ASSERT(.FALSE.)
        END IF


      ELSE
        REPK = QUESTI
        CALL U2MESK(CODMES,'UTILITAI_49',1,REPK)
        IERD = 1
        GO TO 10
      END IF

   10 CONTINUE
      REPKZ = REPK
      CALL JEDEMA()
      END
