      SUBROUTINE RSEXC1(NOMSD,NOMSY,IORDR,CHEXTR)
      IMPLICIT NONE
      INTEGER IORDR
      CHARACTER*(*) NOMSD,NOMSY,CHEXTR
      CHARACTER*16  NOMCMD,OPTION,TYSD
      CHARACTER*8  CONCEP
      CHARACTER*16 TYPCON
      CHARACTER*24 VALK(2)
      INTEGER VALI
