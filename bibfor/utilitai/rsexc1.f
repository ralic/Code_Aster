C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C      RECUPERATION DU NOM DU CHAMP-GD  CORRESPONDANT A:
C          NOMSD(IORDR,NOMSY).
C      IL S'AGIT D'UN APPEL A RSEXCH COMPLETE PAR DES VERIFICATIONS
C      NOTAMMENT SI L'OPTION A DEJA ETE CALCULEE
C      DANS CE CAS, LES OBJETS JEVEUX PRECEDENTS SONT DETRUITS
C      AFIN DE POUVOIR REFAIRE LE CALCUL DE L'OPTION
C      CE MODULE EST DESTINE A ETRE APPELE PAR DES OPERATEURS
C      PAR EXEMPLE OP0058 AFIN DE PREPARER LES NOMS DE CHAMP-GD
C      AVANT L'APPEL A MECALC
C ----------------------------------------------------------------------
C IN  : NOMSD  : NOM DE LA STRUCTURE "RESULTAT"
C IN  : NOMSY  : NOM SYMBOLIQUE DU CHAMP A CHERCHER.
C IN  : IORDR  : NUMERO D'ORDRE DU CHAMP A CHERCHER.
C OUT : CHEXTR : NOM DU CHAMP EXTRAIT.
C ----------------------------------------------------------------------
C-----------------------------------------------------------------------
      INTEGER ICODE 
C-----------------------------------------------------------------------
      OPTION=NOMSY
      CALL RSEXCH(NOMSD,NOMSY,IORDR,CHEXTR,ICODE)
C --- SI L'OPTION A DEJA ETE CALCULEE, ON LA RECALCULE EN
C     EMETTANT UN MESSAGE D'ALARME
      IF (ICODE.EQ.0) THEN
         CALL GETRES(CONCEP,TYPCON,NOMCMD)
         VALK (1) = OPTION
         VALI     = IORDR
         CALL U2MESG('A','UTILITAI8_31',1,VALK,1,VALI,0,0.D0)
         CALL DETRSD ('CHAM_ELEM',CHEXTR(1:19))
      ELSEIF (ICODE.GT.100) THEN
         CALL GETRES(CONCEP,TYPCON,NOMCMD)
         CALL GETTCO(NOMSD,TYSD)
          VALK(1) = TYSD
          VALK(2) = OPTION
          CALL U2MESK('F','CALCULEL3_27', 2 ,VALK)
      ENDIF
      END
