      OB1 = OBIN
C
      CALL JEEXIN(OB1,IRET)
      IF (IRET.LE.0) THEN
         CALL UTMESS('A','UTIMOS','ERREUR 1')
         GO TO 9999
      END IF
C
      IF (LATTR) THEN
         CALL JEIMPA(FICOU,OB1,' ')
      END IF
C
      IF (LCONT) THEN
         CALL JEIMPO(FICOU,OB1,' ',' ')
      END IF
C
