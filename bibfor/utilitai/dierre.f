C
C ENREGISTRE LES ERREURS A ITERAT
C
C ----------------------------------------------------------------------
C
C
C IN  SDDISC : SD DISCRETISATION
C IN  SDCRIT : SD CRITERE
C IN  ITERAT : NUMERO ITERATION NEWTON
C
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
C

      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C -------------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ----------------
C
      CHARACTER*24 TPSERR
      INTEGER      JERRE
      INTEGER      IRET,JCRR,XXITER,NBVAL
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- ENREGISTRE LES ERREURS A CETTE ITERATION
C
      TPSERR = SDDISC(1:19)//'.ERRE'
      CALL JEEXIN(SDCRIT(1:19)//'.CRTR',IRET)
      IF (IRET.NE.0) THEN
         CALL JEVEUO(SDCRIT(1:19)//'.CRTR','L',JCRR)
C        STOCKAGE DES INFOS SUR LA CONVERGENCE
         CALL JEVEUO(TPSERR,'E',JERRE)
         XXITER = NINT(ZR(JERRE+3))
         IF ( XXITER .NE. 0 ) THEN
            CALL ASSERT( (ITERAT .LE. XXITER+1) )
            NBVAL = NINT(ZR(JERRE))
C           !!!! LE NUMERO DES ITERATIONS COMMENCE A ZERO
            ZR(JERRE + NBVAL + ITERAT*2     ) = ZR(JCRR+2)
            ZR(JERRE + NBVAL + ITERAT*2 + 1 ) = ZR(JCRR+3)
         ENDIF
      ENDIF
C
      CALL JEDEMA()
      END
