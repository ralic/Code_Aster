C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
C     --     DISMOI(RESUELEM)
C
C       QUESTI : TEXTE PRECISANT LA QUESTION POSEE
C       NOMOBZ : NOM D'UN OBJET DE TYPE LIGREL
C
C OUT : REPI   : REPONSE ( SI ENTIERE )
C       REPKZ  : REPONSE ( SI CHAINE DE CARACTERES )
C       IERD   : CODE RETOUR (0--> OK, -1 --> CHAMP INEXISTANT)
C
C ----------------------------------------------------------------------
C
      INTEGER       IBID, IRET, GD, IADESC, JNOLI, L, LXLGUT
      CHARACTER*8   K8BID, NOGD
      CHARACTER*19  NOMOB
      CHARACTER*24 QUESTL,K24
      CHARACTER*32  REPK
      LOGICAL ZEROSD
C DEB-------------------------------------------------------------------
C
      CALL JEMARQ()
C
      IERD   = 0
      NOMOB  = NOMOBZ
      REPK   = ' '
      QUESTL = QUESTI

      CALL JEEXIN ( NOMOB//'.DESC', IRET )
      IF ( IRET .EQ. 0 ) THEN
          IERD = 1
          GOTO 9999
      END IF
C
      CALL JEVEUO (NOMOB//'.DESC', 'L', IADESC )
      GD = ZI(IADESC)
      CALL JENUNO ( JEXNUM('&CATA.GD.NOMGD',GD), NOGD )
C
      IF ( QUESTI .EQ. 'TYPE_CHAMP' ) THEN
         CALL JELIRA ( NOMOB//'.DESC', 'DOCU', IBID, K8BID )
         CALL ASSERT(K8BID(1:4) .EQ. 'RESL')
         REPK = 'RESL'
C
      ELSEIF ( QUESTI .EQ. 'TYPE_SUPERVIS' ) THEN
         CALL JELIRA ( NOMOB//'.DESC', 'DOCU', IBID, K8BID )
         CALL ASSERT( K8BID(1:4) .EQ. 'RESL' )
         REPK = '????'
C
      ELSEIF ( QUESTI .EQ. 'TYPE_MATRICE' ) THEN
         CALL DISMGD (  QUESTI, NOGD, REPI, REPK, IERD )
C
      ELSEIF ( QUESTI .EQ. 'NOM_OPTION' ) THEN
         CALL JEVEUO ( NOMOB//'.NOLI', 'L', JNOLI )
         REPK = ZK24(JNOLI-1+2)(1:16)
C
      ELSEIF ( QUESTI .EQ. 'NOM_MAILLA') THEN
         CALL JEVEUO ( NOMOB//'.NOLI', 'L', JNOLI )
         CALL DISMLG (  QUESTI, ZK24(JNOLI), REPI, REPK, IERD )
C
      ELSEIF ( QUESTL(1:6) .EQ. 'NUM_GD' ) THEN
         REPI = GD
C
      ELSEIF ( QUESTL(1:6) .EQ. 'NOM_GD' ) THEN
         REPK = NOGD
C
      ELSEIF ( QUESTI .EQ. 'NOM_LIGREL' ) THEN
         CALL JEVEUO ( NOMOB//'.NOLI', 'L', JNOLI )
         REPK = ZK24(JNOLI)
C
      ELSEIF ( QUESTI .EQ. 'PARTITION' ) THEN
         CALL JEVEUO ( NOMOB//'.NOLI', 'L', JNOLI )
         CALL DISMLG (  QUESTI, ZK24(JNOLI), REPI, REPK, IERD )
C
      ELSEIF ( QUESTI .EQ. 'NOM_MODELE' ) THEN
         CALL JEVEUO ( NOMOB//'.NOLI','L', JNOLI )
         CALL DISMLG (  QUESTI, ZK24(JNOLI), REPI, REPK, IERD )
C
      ELSEIF ( QUESTI .EQ. 'TYPE_SCA' ) THEN
          L    = LXLGUT(NOGD)
          REPK = NOGD(L:L)
C
      ELSEIF ( QUESTI .EQ. 'MPI_COMPLET' ) THEN
         CALL JEVEUO ( NOMOB//'.NOLI', 'L', JNOLI )
         K24 = ZK24(JNOLI-1+3)
         CALL ASSERT(K24.EQ.'MPI_COMPLET'.OR.K24.EQ.'MPI_INCOMPLET')
         IF (K24.EQ.'MPI_COMPLET')THEN
            REPK='OUI'
         ELSE
            REPK='NON'
         ENDIF
C
      ELSEIF ( QUESTI .EQ. 'ZERO' ) THEN
         IF (ZEROSD('RESUELEM',NOMOB))  THEN
            REPK='OUI'
         ELSE
            REPK='NON'
         ENDIF

      ELSE
         IERD = 1
      ENDIF
C
 9999 CONTINUE
      REPKZ = REPK
C
      CALL JEDEMA()
      END
