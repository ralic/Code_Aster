      IMPLICIT REAL*8 (A-H,O-Z)
      INTEGER      IFMIS, IFREQ, NFREQ
      REAL*8       FREQ
C
      CHARACTER*72 TEXTE
      REAL*8 A(3)
C
      REWIND IFMIS
      READ(IFMIS,'(A72)') TEXTE
      READ(IFMIS,'(A72)') TEXTE
      NFREQ = 0
    1 CONTINUE
      NFREQ = NFREQ + 1
      READ(IFMIS,'(A72)') TEXTE
      IF (TEXTE(1:3).EQ.'CHA') THEN
        NFREQ = NFREQ -1 
      ELSE
        GOTO 1
      ENDIF
      REWIND IFMIS
      READ(IFMIS,'(A72)') TEXTE
      READ(IFMIS,'(A72)') TEXTE
      DO 3 I = 1, NFREQ
        READ(IFMIS,*) (A(J),J=1,3)
        A(1) = A(1) + 1.D-6
        IF (FREQ.LE.A(1)) THEN
          IFREQ = I
          GOTO 4
        ENDIF
    3 CONTINUE
      IFREQ = NFREQ
    4 CONTINUE
      END      
