C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT      NONE
      CHARACTER*8   CHAR
      CHARACTER*16  MOTFAC
      CHARACTER*8   NOMA
      CHARACTER*(*) MOTCLE
      INTEGER       IZONE
      INTEGER       JTRAV
      INTEGER       NBMA,NBNO,NBNOQU
      INTEGER       IPMA,IPNO,IPNOQU
      INTEGER       LISTMA(NBMA)
      INTEGER       LISTNO(NBNO)
      INTEGER       LISTQU(3*NBNOQU)
C     
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (TOUTES METHODES - LECTURE DONNEES)
C
C REMPLISSAGE DE LA LISTE DES MAILLES, DES NOEUDS ET DES NOEUDS
C QUADRATIQUES
C      
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  MOTFAC : MOT-CLE FACTEUR (VALANT 'CONTACT')
C IN  NOMA   : NOM DU MAILLAGE
C IN  MOTCLE : MOT-CLE (GROUP_MA ou MAILLE)
C IN  IZONE  : INDICE POUR LIRE LES DONNEES DANS AFFE_CHAR_MECA
C IN  JTRAV  : POINTEUR VERS VECTEUR DE TRAVAIL 'BIDON'
C IN  NBMA   : NOMBRE DE MAILLES DANS LA ZONE IZONE
C IN  NBNO   : NOMBRE DE NOEUDS DANS LA ZONE IZONE
C IN  NBNOQU : NOMBRE DE NOEUDS QUADRATIQUES DANS LA ZONE IZONE
C I/O IPMA   : INDICE POUR LA LISTE DES NUMEROS DES MAILLES DE CONTACT
C I/O IPNO   : INDICE POUR LA LISTE DES NUMEROS DES NOEUDS DE CONTACT
C I/O IPNOQU : INDICE POUR LA LISTE DES NUMEROS DES NOEUDS QUADRATIQUES
C              DE CONTACT
C I/O LISTMA : LISTE DES NUMEROS DES MAILLES DE CONTACT
C I/O LISTNO : LISTE DES NUMEROS DES NOEUDS DE CONTACT
C I/O LISTQU : LISTE DES NUMEROS DES NOEUDS QUADRATIQUES DE CONTACT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*8    TYPENT,K8BID
      INTEGER        NB,NBENT,NMA,NGRMA
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      IF (MOTCLE(1:6).EQ.'MAILLE') THEN
        TYPENT = 'MAILLE'
      ELSE IF (MOTCLE(1:8).EQ.'GROUP_MA') THEN
        TYPENT = 'GROUP_MA'
      ELSE
        CALL ASSERT(.FALSE.)
      ENDIF
C
C --- LECTURE COMMANDE
C
      CALL GETVEM(NOMA,TYPENT,MOTFAC,MOTCLE,
     &            IZONE,1,0,K8BID,NBENT)
     
      IF (NBENT.NE.0) THEN
        NBENT   = -NBENT
        CALL GETVEM(NOMA,TYPENT,MOTFAC,MOTCLE,
     &              IZONE,1,NBENT,ZK8(JTRAV),NB)
        IF (TYPENT.EQ.'MAILLE') THEN
          NMA   = NB
          NGRMA = 0
        ELSE   
          NMA   = 0
          NGRMA = NB
        ENDIF       
        CALL EXNOEL(CHAR      ,NOMA  ,TYPENT,NGRMA ,NMA   ,
     &              ZK8(JTRAV),NBMA  ,NBNO  ,NBNOQU,LISTMA,
     &              LISTNO    ,LISTQU,IPMA  ,IPNO  ,IPNOQU)     
      END IF
C
C ----------------------------------------------------------------------
C
      CALL JEDEMA()
      END
