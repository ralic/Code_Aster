C
C --- CARA_TORSION ---
C
      CALL CATORS ( CHAR )
C
      CALL JEEXIN ( LIGRCH//'.NOMA', IRET )
      IF (IRET.NE.0) THEN
          CALL INITEL ( LIGRCH )
      ENDIF
C
C --- MISE A JOUR DU LIGREL DE CHARGE SI IL EXISTE EN FONCTION
C     DE LA TAILLE MAX DES .RESL
C
      CALL JEEXIN ( LIGRCH//'.NOMA', IRET )
      IF (IRET.NE.0) THEN
        CALL ADALIG ( LIGRCH )
      ENDIF
C ---------------------------------------------------------------------
      CALL JEDETC('V',CHAR,1)
      END
