     +         TYPELE.EQ.'HHM_D_PLAN_SE3' ) THEN
         TYPEMO = TYPEM1

      ELSEIF ( TYPELE.EQ.'HH_D_PLAN_SE3') THEN
         TYPEMO = TYPEM1

      ELSEIF ( TYPELE.EQ.'HH_AXIS_SE3' ) THEN
         TYPEMO = TYPEM1

      ELSEIF ( TYPELE.EQ.'HH_FACE8' .OR.
     +         TYPELE.EQ.'HH_FACE6' ) THEN
         TYPEMO = TYPEM1

      ENDIF
