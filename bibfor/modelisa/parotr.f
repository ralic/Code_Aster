C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C     BUT: DONNER LA LISTE DES COORDONNEES DES NBNO 1ERS NOEUDS DE LA
C          MAILLE IMA DU MAILLAGE NOMMA APRES UNE ROTATION (O,MROT)
C          ET UNE TRANSLATION T OU DU NOEUD IMA SI NBNO=0
C     VERIFICTION : NBNO < OU = NBRE DE NOUDS DE LA MAILLE
C ARGUMENTS D'ENTREE:
C IN   NOMMA  K8  : NOM DU MAILLAGE
C IN   IAGEOM I   : ADRESSE DE L'OBJET MAILLAGE//'.COORDO   .VALE'
C IN   IMA    I   : NUMERO DE LA MAILLE OU DU NOEUD SI NBNO=0
C IN   NBNO   I   : NOMBRE DE NOEUDS DE LA MAILLE A EXTRAIRE OU 0
C IN   O      R(3): CENTRE DE ROTATION
C IN   MROT   R(9): MATRICE DE ROTATION
C IN   T      R(3): VECTEUR DE TRANSLATION
C OUT  COOR   R(*): COORDONNEES DES NBNO 1ERS NOEUDS DE LA MAILLE
C                   APRES TRANSFORMATION OU DU NOEUD
C                   POUR INO = 1,NBNO OU INO = IMA SI NBNO=0
C                   COOR(3*(INO-1)+1)= X1'(INO)
C                   COOR(3*(INO-1)+2)= X2'(INO)
C                   COOR(3*(INO-1)+3)= X3'(INO) ( EN 2D 0)
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER           ZI
      COMMON / IVARJE / ZI(1)
      REAL*8            ZR
      COMMON / RVARJE / ZR(1)
      COMPLEX*16        ZC
      COMMON / CVARJE / ZC(1)
      LOGICAL           ZL
      COMMON / LVARJE / ZL(1)
      CHARACTER*8       ZK8
      CHARACTER*16              ZK16
      CHARACTER*24                       ZK24
      CHARACTER*32                                ZK32
      CHARACTER*80                                         ZK80
      COMMON / KVARJE / ZK8(1), ZK16(1), ZK24(1), ZK32(1), ZK80(1)
C     ------- FIN COMMUNS NORMALISES  JEVEUX  --------------------------
      REAL*8 X(3)
C --- DEBUT
      IF (NBNO.EQ.0) THEN
        NBNOT = 1
        COOR(1)=ZR(IAGEOM-1+3*(IMA-1)+1)
        COOR(2)=ZR(IAGEOM-1+3*(IMA-1)+2)
        COOR(3)=ZR(IAGEOM-1+3*(IMA-1)+3)
      ELSE
        NBNOT = NBNO
        CALL PACOOR(NOMMA,IMA,NBNO,COOR)
      END IF

      DO 1 INO = 1,NBNOT
        ICOOR = 3*(INO-1)
        DO 2 I = 1,3
          X(I) = 0.D0
          DO 3 J = 1,3
            X(I) = X(I) + MROT(J,I)*(COOR(ICOOR+J)-O(J))
3         CONTINUE
2       CONTINUE
        DO 4 I = 1,3
          COOR(ICOOR+I) = X(I)+O(I)+T(I)
4       CONTINUE
1     CONTINUE
      END
