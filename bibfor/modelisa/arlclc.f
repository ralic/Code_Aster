C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MEUNIER S.MEUNIER
C
      IMPLICIT NONE
      CHARACTER*8  MODARL
      INTEGER      NBCHEL
      CHARACTER*19 CHAMEL(NBCHEL)
      CHARACTER*8  MARLEL
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C APPEL A CALCUL - ARLQ_MATR
C
C ----------------------------------------------------------------------
C
C
C IN  MODARL : NOM DU PSEUDO-MODELE
C IN  NBCHEL : NOMBRE DE CHAM_ELEM CREES
C IN  CHAMEL : LISTE DES CHAM_ELEM D'ENTREES
C I/O MARLEL : IN  -> NOM DES MATR_ELEM A CREER
C              OUT -> MATR_ELEM
C
C
C ----------------------------------------------------------------------
C
      INTEGER      NBOUT,NBIN
      PARAMETER    (NBOUT=2, NBIN=7)
      CHARACTER*8  LPAOUT(NBOUT),LPAIN(NBIN)
      CHARACTER*19 LCHOUT(NBOUT),LCHIN(NBIN)
C
      CHARACTER*19 LIGRMO
      CHARACTER*16 OPTION
      LOGICAL      DEBUG
      INTEGER      IFM,NIV,IFMDBG,NIVDBG
      CHARACTER*19 CTFAMI,CTINFO,CTREF1,CTCOO1,CTREF2,CTCOO2,CHGEOM
      LOGICAL      LBID
      INTEGER      JARLM1, JARLM2

      INTEGER      NBVLR,NBVLK,NBVLI
      PARAMETER   ( NBVLR = 1 , NBVLK = 1 , NBVLI = 1)
      INTEGER      VALI(NBVLI)
      REAL*8       VALR(NBVLR)
      CHARACTER*24 VALK(NBVLK)

      CHARACTER*6  NOMPRO
      PARAMETER   (NOMPRO='ARLCLC')
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('ARLEQUIN',IFM,NIV)
      CALL INFDBG('PRE_CALCUL',IFMDBG,NIVDBG)
C
C --- AFFICHAGE
C
      CALL ARLDBG(NOMPRO,NIV,IFM,1,NBVLI,VALI,NBVLR,VALR,NBVLK,VALK)
C
C --- INITIALISATIONS
C
      LIGRMO = MODARL(1:8)//'.MODELE'
      OPTION = 'ARLQ_MATR'
      IF (NIVDBG.GE.2) THEN
        DEBUG  = .TRUE.
      ELSE
        DEBUG  = .FALSE.
      ENDIF
C
      CTFAMI = CHAMEL(1)
      CTINFO = CHAMEL(2)
      CTREF1 = CHAMEL(3)
      CTCOO1 = CHAMEL(4)
      CTREF2 = CHAMEL(5)
      CTCOO2 = CHAMEL(6)
C
C --- INITIALISATION DES CHAMPS POUR CALCUL
C
      CALL INICAL(NBIN  ,LPAIN ,LCHIN ,
     &            NBOUT ,LPAOUT,LCHOUT)
C
C --- ON DETRUIT MARLEL
C
      CALL DETRSD(' ',MARLEL)
C
C --- CREATION DES LISTES DES CHAMPS IN
C
      CALL MEGEOM(MODARL,' ',LBID  ,CHGEOM)
      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      LPAIN(2) = 'PFAMILK'
      LCHIN(2) = CTFAMI
      LPAIN(3) = 'PINFORR'
      LCHIN(3) = CTINFO
      LPAIN(4) = 'PREFE1K'
      LCHIN(4) = CTREF1
      LPAIN(5) = 'PCOOR1R'
      LCHIN(5) = CTCOO1
      LPAIN(6) = 'PREFE2K'
      LCHIN(6) = CTREF2
      LPAIN(7) = 'PCOOR2R'
      LCHIN(7) = CTCOO2
C
C --- CALCUL DE MARLEL : MATRICES ELEMENTAIRES
C
      CALL WKVECT(MARLEL(1:8)//'.ARLMT1','V V K24',1,JARLM1)
      CALL WKVECT(MARLEL(1:8)//'.ARLMT2','V V K24',1,JARLM2)
C
C --- CREATION DES LISTES DES CHAMPS OUT
C
      LPAOUT(1) = 'PMATUN1'
      LCHOUT(1) = MARLEL(1:8)//'.ARLMT1'
      LPAOUT(2) = 'PMATUN2'
      LCHOUT(2) = MARLEL(1:8)//'.ARLMT2'
C
C --- APPEL A CALCUL
C
      CALL CALCUL('S',OPTION,LIGRMO,NBIN  ,LCHIN ,LPAIN,
     &                              NBOUT ,LCHOUT,LPAOUT,'V')
C
      IF (DEBUG) THEN
        CALL DBGCAL(OPTION,IFMDBG,
     &              NBIN  ,LPAIN ,LCHIN ,
     &              NBOUT ,LPAOUT,LCHOUT)
      ENDIF
C
C --- AFFICHAGE
C
      CALL ARLDBG(NOMPRO,NIV,IFM,2,NBVLI,VALI,NBVLR,VALR,NBVLK,VALK)
C
      CALL JEDEMA()
      END
