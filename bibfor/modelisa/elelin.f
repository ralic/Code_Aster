      INTEGER      NDIM,NBFPG,NBPG(20)
      REAL*8       X(3*27),VOL
      CHARACTER*8  FAPG(20)

      CALL JEMARQ()

      IF(NCONTA.EQ.3) THEN
        IF (ELREF1.EQ.'QU8') THEN
          ELREF2='QU4'
          NNOP = 8
          NNOPS= 4
        ELSEIF (ELREF1.EQ.'TR6') THEN
          ELREF2='TR3'
          NNOP = 6
          NNOPS= 3
        ELSEIF (ELREF1.EQ.'SE3') THEN
          ELREF2='SE2'
          NNOP = 3
          NNOPS= 2
        ELSEIF (ELREF1.EQ.'H20') THEN
          ELREF2='HE8'
          NNOP = 20
          NNOPS= 8
        ELSEIF (ELREF1.EQ.'P15') THEN
          ELREF2='PE6'
          NNOP = 15
          NNOPS= 6
        ELSEIF (ELREF1.EQ.'P13') THEN
          ELREF2='PY5'
          NNOP = 13
          NNOPS= 5
        ELSEIF (ELREF1.EQ.'T10') THEN
          ELREF2='TE4'
          NNOP = 10
          NNOPS= 4
        ELSE
          ELREF2=ELREF1
          CALL ELRACA(ELREF1,NDIM,NNOP,NNOPS,NBFPG,FAPG,NBPG,X,VOL)
        ENDIF
      ELSE
        ELREF2=ELREF1
        CALL ELRACA(ELREF1,NDIM,NNOP,NNOPS,NBFPG,FAPG,NBPG,X,VOL)
      ENDIF

      CALL JEDEMA()
      END
