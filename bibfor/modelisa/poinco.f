      CHARACTER*8  CHAR
      CHARACTER*16 MOTFAC
      CHARACTER*8  NOMA
      INTEGER      NZOCO
      INTEGER      NSUCO
C
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODES MAILLEES - LECTURE DONNEES)
C
C DETERMINATION DU NOMBRE DE ZONES DE CONTACT ET DU NOMBRE TOTAL DE
C MAILLES ET DE NOEUDS DE CONTACT.
C REMPLISSAGE DES POINTEURS ASSOCIES: PZONE,PSURMA,PSURNO,PNOQUA
C
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  MOTFAC : MOT-CLE FACTEUR
C IN  NOMA   : NOM DU MAILLAGE
C IN  NZOCO  : NOMBRE DE ZONES DE CONTACT
C OUT NSUCO  : NOMBRE TOTAL DE SURFACES DE CONTACT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      IZONE
      CHARACTER*24 PZONE,PSURMA,PSURNO
      INTEGER      JZONE,JSUMA,JSUNO
      CHARACTER*24 DEFICO
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATIONS
C
      DEFICO = CHAR(1:8)//'.CONTACT'         
C
C --- ACCES AUX STRUCTURES DE DONNEES DE CONTACT
C
      PZONE  = DEFICO(1:16)//'.PZONECO'
      PSURMA = DEFICO(1:16)//'.PSUMACO'
      PSURNO = DEFICO(1:16)//'.PSUNOCO'
C
C --- INITIALISATIONS
C
      NSUCO  = 0
C
C --- DETERMINATION DU NOMBRE DE SURFACES
C
      CALL WKVECT(PZONE,'G V I',NZOCO+1,JZONE)
      DO 2 IZONE = 1,NZOCO
        CALL NBZOCO(MOTFAC,NOMA  ,IZONE ,JZONE ,NSUCO )
  2   CONTINUE
C
C --- TABLEAUX DU NOMBRE DE MAILLES ET DE NOEUDS DE CONTACT 
C
      CALL WKVECT(PSURMA,'G V I',NSUCO+1,JSUMA )
      CALL WKVECT(PSURNO,'G V I',NSUCO+1,JSUNO )
C
      CALL JEDEMA()
      END
