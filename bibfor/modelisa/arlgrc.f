      CHARACTER*10 NOM
      CHARACTER*8  MAIL
      CHARACTER*16 TYPMAI
      INTEGER      IMA,DIME
      INTEGER      CONNEX(*),LONCUM(*)
      REAL*8       COORD(3,*),NORMAL(DIME,*)
      INTEGER      NUMMAI
      CHARACTER*8  TM,NOMMAI
      REAL*8       H
      INTEGER      NBNO
      REAL*8       CNOEUD(DIME,*)
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C DONNE LES COORDONNEES D'UNE MAILLE D'UN GROUPE
C
C ----------------------------------------------------------------------
C
C
C IN  NOMGRP : NOM DU GROUPE DE MAILLES ARLEQUIN
C IN  MAIL   : NOM DU MAILLAGE
C IN  TYPMAI : SD CONTENANT NOM DES TYPES ELEMENTS (&&CATA.NOMTM)
C IN  NOM    : NOM DE LA SD DE STOCKAGE DES MAILLES ARLEQUIN GROUP_MA_*
C IN  DIME   : DIMENSION DE L'ESPACE
C IN  IMA    : NUMERO D'ORDRE DE LA MAILLE DANS LE GROUPE ARLEQUIN
C IN  CONNEX : CONNEXITE DES MAILLES
C IN  LONCUM : LONGUEUR CUMULEE DE CONNEX
C IN  NORMAL : COORDONNEES DES NORMALES (CF LISNOR)
C IN  COORD  : COORDONNEES DES NOEUDS
C OUT NUMMAI : NUMERO ABSOLU DE LA MAILLE DANS LE MAILLAGE
C OUT NOMMAI : NOM DE LA MAILLE
C OUT TM     : TYPE GEOMETRIQUE DE LA MAILLE
C OUT H      : DIMENSION CARACTERISTIQUE DE LA BOITE AUTOUR DE LA MAILLE
C OUT NBNO   : NOMBRE DE NOEUDS
C OUT CNOEUD : COORD DES NOEUDS (X1, [Y1, Z1], X2, ...)
C
C ----------------------------------------------------------------------
C
      INTEGER       LCOQUE
      CHARACTER*32  JEXNUM
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INFOS SUR LA MAILLE
C
      CALL ARLGRI(MAIL  ,TYPMAI,NOM   ,DIME  ,IMA  ,
     &            NUMMAI,TM    ,H)
      CALL JENUNO(JEXNUM(MAIL(1:8)//'.NOMMAI',NUMMAI),NOMMAI)
C
C --- EXTENSION SI COQUE
C
      CALL TMACOQ(TM    ,DIME  ,LCOQUE)
C
C --- COORDONNEES DE LA MAILLE
C
      CALL CONOEU(NUMMAI,CONNEX,LONCUM,COORD ,NORMAL,
     &            DIME  ,LCOQUE,CNOEUD,NBNO)
C
      CALL JEDEMA()

      END
