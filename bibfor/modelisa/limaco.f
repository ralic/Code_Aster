C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C
      IMPLICIT NONE
      CHARACTER*8  CHAR
      CHARACTER*8  NOMA
      CHARACTER*8  NOMO
      CHARACTER*16 MOTFAC 
      CHARACTER*4  FONREE       
      INTEGER      NZOCP,NZOCO,NDIM        
C     
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (TOUTES METHODES - LECTURE DONNEES)
C
C LECTURE DES MAILLES DE CONTACT
C      
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  NOMA   : NOM DU MAILLAGE
C IN  NOMO   : NOM DU MODELE
C IN  MOTFAC : MOT-CLE FACTEUR (VALANT 'CONTACT')
C IN  FONREE : FONC OU REEL SUIVANT L'OPERATEUR
C IN  NDIM   : NOMBRE DE DIMENSIONS DU PROBLEME
C IN  NZOCO  : NOMBRE DE ZONES DE CONTACT
C IN  NZOCP  : NOMBRE DE ZONES D ECONTACT PRINCIPALES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      CHARACTER*24 FORMCO
      INTEGER      JFORM
      INTEGER      IZONE,FORM,ORDSTC,INDQUA
      LOGICAL      LMAIL
      INTEGER      NSUCO,NMACO,NNOCO,NNOQUA,NTRAV
      INTEGER      NMANO,NNOMA,NMAMA  
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C 
C --- LECTURE DES STRUCTURES DE DONNEES DE CONTACT
C 
      FORMCO = CHAR(1:8)//'.CONTACT.FORMCO'             
      CALL JEVEUO(FORMCO,'L',JFORM)
C
C --- INITIALISATIONS
C      
      ORDSTC = 0  
      LMAIL  = .FALSE.     
C
C --- TYPE DE FORMULATION (DISCRETE/CONTINUE/XFEM)
C
      IZONE  = 1
      FORM   = ZI(JFORM-1+IZONE) 
      LMAIL  = (FORM.EQ.1).OR.(FORM.EQ.2)     
C
C --- ORDRE DE STOCKAGE DANS LES VECTEURS 
C --- GLUTE DUE A METHODE CONTINUE !
C
      IF (FORM.EQ.2) THEN
        ORDSTC = 1
      ELSE
        ORDSTC = 0
      ENDIF
C
C --- LECTURE DES MAILLES/NOEUDS DE CONTACT
C
      IF (LMAIL) THEN
C
C --- TRAITEMENT DU CAS DES MAILLES QUADRATIQUES 
C --- INDQUA VAUT 0 SI LINEARISATION DES MAILLES QUADRATIQUES (CACOEQ)
C
        CALL QUADCO(CHAR  ,MOTFAC,NZOCP ,INDQUA)      
C      
C --- PREMIERE PASSE
C --- DETERMINATION DU NOMBRE TOTAL DE SURFACES, DE MAILLES ET DE NOEUDS
C --- REMPLISSAGE DES POINTEURS PZONE, PSURMA, PSURNO ET PNOQUA      
C
        CALL POINCO(CHAR  ,MOTFAC,NOMA  ,NZOCO ,ORDSTC,
     &              INDQUA,NSUCO ,NMACO ,NNOCO ,NNOQUA,
     &              NTRAV)
C     
C --- DEUXIEME PASSE
C --- STOCKAGE DES MAILLES ET NOEUDS DE CONTACT   
C  
        CALL LISTCO(CHAR  ,MOTFAC,NOMA  ,NTRAV ,NZOCO ,
     &              ORDSTC,NMACO ,NNOCO ,NNOQUA)
C     
C --- TROISIEME PASSE
C --- ELIMINATION DES MAILLES ET NOEUDS DE CONTACT EN DOUBLE  
C      
        CALL ELIMCO(CHAR  ,NOMA  ,NZOCO ,NSUCO ,NMACO ,
     &              NNOCO ,NNOQUA) 
C
C --- TRAITEMENT MOT-CLEF SANS_GROUP_NO
C
        CALL SANSCO(CHAR  ,MOTFAC ,NOMA  ,NZOCO ,NNOCO ,
     &              FORM)
C
C --- TRAITEMENT MOT-CLEFS SPECIFIQUES FORMULATION CONTINUE
C     
        IF (FORM.EQ.2) THEN
          CALL SANSCC(CHAR  ,MOTFAC ,NOMA  ,NZOCO ,NNOCO ,
     &                NMACO)
        ENDIF
C 
C --- CONSTRUCTION DES CONNECTIVITES INVERSES
C 
        CALL TABLCO(CHAR  ,NOMA  ,NZOCO ,NSUCO ,NMACO ,
     &              NNOCO ,NMANO ,NNOMA ,NMAMA)
C
C --- CONSTRUCTION DU VECTEUR D'INFORMATION SUR LES LONGUEURS
C 
        CALL DIMECO(CHAR  ,NOMA  ,NDIM  ,NZOCO ,NSUCO ,
     &              NMACO ,NNOCO ,NMANO ,NNOMA ,NMAMA ) 
C
C --- TRAITEMENT MOT-CLEFS SPECIFIQUES FORMULATION DISCRETE
C     
        IF (FORM.EQ.1) THEN
C
C --- CONSTRUCTION DES TABLEAUX CONTENANT LES INFORMATIONS 
C --- POUR METHODES "PENALISATION" ET "LAGRANGIEN"
C     
          CALL FROTCO(CHAR)    
C         
C --- CREATION DES RELATIONS LINEAIRES POUR MAILLES QUADRATIQUES 
C 
          CALL CACOEQ(FONREE,CHAR,NOMA)                   
        ENDIF
C 
C --- RECUPERATION DES CARACTERISTIQUES DE POUTRE
C 
        CALL CAPOCO(CHAR,MOTFAC,NOMA)
C
C --- RECUPERATION DES CARACTERISTIQUES DE COQUE
C 
        CALL CACOCO(CHAR,MOTFAC,NOMA)  
C
C --- CREATION DES SDS SPECIFIQUES FORMULATION CONTINUE
C     
        IF (FORM.EQ.2) THEN
          CALL MMACON(CHAR  ,NOMA)
        ENDIF     
                       
      ELSE
        IF (FORM.EQ.3) THEN
          CALL LIMACX(CHAR  ,MOTFAC,FONREE,NDIM  ,NZOCO)
          CALL XMACON(CHAR  ,NOMA  ,NOMO)
          CALL XCONTA(CHAR  ,NOMA  ,NOMO  ,NDIM  ) 
        ELSE
          CALL ASSERT(.FALSE.)
        ENDIF
      ENDIF

              
C      
      CALL JEDEMA()
      END
