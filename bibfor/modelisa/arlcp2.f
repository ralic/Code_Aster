C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE MEUNIER S.MEUNIER
C
      IMPLICIT NONE
      CHARACTER*16 TYPMAI
      CHARACTER*8  MAIL,NOMO
      CHARACTER*8  NOMARL
      CHARACTER*10 NOM1,NOM2
      CHARACTER*10 QUADRA
      REAL*8       LCARA
      INTEGER      NDIM,ULPMAI
      CHARACTER*8  MARLEL,MODARL
      CHARACTER*24 TABCPL
C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C CALCUL DES MATRICES DE COUPLAGE ARLEQUIN
C ASSEMBLAGE DANS LES MATRICES ARLEQUIN MORSES
C VERSION AVEC APPEL DE CALCUL
C
C ----------------------------------------------------------------------
C
C
C IN  MAIL   : NOM DU MAILLAGE
C IN  ULPMAI : UNITE LOGIQUE POUR L'IMPRESSION DU PSEUDO-MAILLAGE
C IN  NOMO   : NOM DU MODELE
C IN  NOMARL : NOM DE LA SD PRINCIPALE ARLEQUIN
C IN  TYPMAI : SD CONTENANT NOM DES TYPES ELEMENTS (&&CATA.NOMTM)
C IN  QUADRA : SD DES QUADRATURES A CALCULER
C IN  NOM1   : NOM DE LA SD DE STOCKAGE PREMIER GROUPE
C IN  NOM2   : NOM DE LA SD DE STOCKAGE SECOND GROUPE
C IN  LCARA  : LONGUEUR CARACTERISTIQUE POUR TERME DE COUPLAGE (PONDERA
C              TION DES TERMES DE COUPLAGE)
C IN  NDIM   : DIMENSION DE L'ESPACE GLOBAL (2 OU 3)
C
C OUT MARLEL : MATR_ELEM CREE POUR CHACUNE DES MATRICES DE COUPLAGE
C OUT MODARL : PSEUDO-MODELE
C OUT TABCPL : TABLEAU DE CORRESPONDANCE
C              POUR CHAQUE PSEUDO-MAILLE -> NUM. COUPLE
C
      INTEGER       IFM,NIV
      CHARACTER*8   MAILAR
      INTEGER       NBCHEL
      PARAMETER    (NBCHEL = 6)
      CHARACTER*19  CHAMES(NBCHEL),CHAMEL(NBCHEL)
      CHARACTER*24  TABCOR
      CHARACTER*19  LIGRMO
      INTEGER       NNCP,IRET
C
      CHARACTER*6  NOMPRO
      PARAMETER   (NOMPRO='ARLCP2')
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('ARLEQUIN',IFM,NIV)
C
C --- CREATION PSEUDO-MAILLAGE
C
      CALL ARLMAI(MAIL  ,ULPMAI,MAILAR,NOMARL,QUADRA,NDIM  ,
     &            TYPMAI,NOM1  ,NOM2  ,TABCOR,TABCPL)
C
C --- CREATION PSEUDO-MODELE
C
      CALL ARLMOD(NOMO,MAILAR,MODARL,TABCOR)

      CALL JEDETR(TABCOR)
C
C --- CREATION DES CHAM_ELEM_S
C
      CHAMES(1) = '&&'//NOMPRO//'.ESFAMI'
      CHAMES(2) = '&&'//NOMPRO//'.ESINFO'
      CHAMES(3) = '&&'//NOMPRO//'.ESREF1'
      CHAMES(4) = '&&'//NOMPRO//'.ESCOO1'
      CHAMES(5) = '&&'//NOMPRO//'.ESREF2'
      CHAMES(6) = '&&'//NOMPRO//'.ESCOO2'
C
      CALL ARLCHI(MAIL  ,NOM1  ,NOM2  ,MAILAR,QUADRA,
     &            TYPMAI,LCARA ,TABCPL,NBCHEL,CHAMES)
C
C --- CREATION DES CHAM_ELEM
C
      CHAMEL(1) = '&&'//NOMPRO//'.ELFAMI'
      CHAMEL(2) = '&&'//NOMPRO//'.ELINFO'
      CHAMEL(3) = '&&'//NOMPRO//'.ELREF1'
      CHAMEL(4) = '&&'//NOMPRO//'.ELCOO1'
      CHAMEL(5) = '&&'//NOMPRO//'.ELREF2'
      CHAMEL(6) = '&&'//NOMPRO//'.ELCOO2'
C
C --- TRANSFORMATION DES CHAM_ELEM_S EN CHAM_ELEM
C --- DESTRUCTION DES CHAM_ELEM_S CORRESPONDANT DES QU'ILS NE SONT PLUS
C --- UTILES
C
      LIGRMO = MODARL(1:8)//'.MODELE'

      CALL CESCEL(CHAMES(1),LIGRMO,'ARLQ_MATR','PFAMILK','NON',
     &            NNCP,'V',CHAMEL(1),'F',IRET)
      CALL DETRSD('CHAM_ELEM_S',CHAMES(1))

      CALL CESCEL(CHAMES(2),LIGRMO,'ARLQ_MATR','PINFORR','NON',
     &            NNCP,'V',CHAMEL(2),'F',IRET)
      CALL DETRSD('CHAM_ELEM_S',CHAMES(2))

      CALL CESCEL(CHAMES(3),LIGRMO,'ARLQ_MATR','PREFE1K','NON',
     &            NNCP,'V',CHAMEL(3),'F',IRET)
      CALL DETRSD('CHAM_ELEM_S',CHAMES(3))

      CALL CESCEL(CHAMES(4),LIGRMO,'ARLQ_MATR','PCOOR1R','NON',
     &            NNCP,'V',CHAMEL(4),'F',IRET)
      CALL DETRSD('CHAM_ELEM_S',CHAMES(4))

      CALL CESCEL(CHAMES(5),LIGRMO,'ARLQ_MATR','PREFE2K','NON',
     &            NNCP,'V',CHAMEL(5),'F',IRET)
      CALL DETRSD('CHAM_ELEM_S',CHAMES(5))

      CALL CESCEL(CHAMES(6),LIGRMO,'ARLQ_MATR','PCOOR2R','NON',
     &            NNCP,'V',CHAMEL(6),'F',IRET)
      CALL DETRSD('CHAM_ELEM_S',CHAMES(6))

C
C --- APPEL A CALCUL
C
      CALL ARLCLC(MODARL,NBCHEL,CHAMEL,MARLEL)
C
C --- MENAGE
C
      CALL JEDETR(CHAMEL(1))
      CALL JEDETR(CHAMEL(2))
      CALL JEDETR(CHAMEL(3))
      CALL JEDETR(CHAMEL(4))
      CALL JEDETR(CHAMEL(5))
      CALL JEDETR(CHAMEL(6))

      CALL DETRSD('MAILLAGE',MAILAR)
C
      CALL JEDEMA()

      END
