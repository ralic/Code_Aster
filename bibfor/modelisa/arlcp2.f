      CHARACTER*8  MAIL,NOMO
      CHARACTER*8  NOMARL
      CHARACTER*10 NOM1,NOM2
      CHARACTER*10 QUADRA
      REAL*8       LCARA
      INTEGER      NDIM
      CHARACTER*8  MARLEL,MODARL
      CHARACTER*24 TABCPL

C
C ----------------------------------------------------------------------
C
C ROUTINE ARLEQUIN
C
C CALCUL DES MATRICES DE COUPLAGE ELEMENTAIRE ARLEQUIN
C ASSEMBLAGE DANS LES MATRICES ARLEQUIN MORSES
C NOUVELLE VERSION (APPEL DE CALCUL)
C
C ----------------------------------------------------------------------
C
C
C IN  MAIL   : NOM DU MAILLAGE
C IN  NOMO   : NOM DU MODELE
C IN  NOMARL : NOM DE LA SD PRINCIPALE ARLEQUIN
C IN  NOM1   : NOM DE LA SD DE STOCKAGE PREMIER GROUPE
C IN  NOM2   : NOM DE LA SD DE STOCKAGE SECOND GROUPE
C IN  TYPMAI : SD CONTENANT NOM DES TYPES ELEMENTS (&&CATA.NOMTM)
C IN  QUADRA : SD DES QUADRATURES A CALCULER
C IN  LCARA  : LONGUEUR CARACTERISTIQUE POUR TERME DE COUPLAGE (PONDERA
C              TION DES TERMES DE COUPLAGE)
C IN  NDIM   : NDIMNSION DE L'ESPACE GLOBAL (2 OU 3)
C
C
C ----------------------------------------------------------------------
C
      INTEGER       IFM,NIV
      CHARACTER*8   MAILAR
      INTEGER       NBCART
      PARAMETER    (NBCART = 6)
      CHARACTER*19  CTARLQ(NBCART)
      CHARACTER*24  TABCOR
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
      CALL INFDBG('ARLEQUIN',IFM,NIV)
C
C --- CREATION PSEUDO-MAILLAGE
C
      CALL ARLMAI(MAIL  ,MAILAR,NOMARL,QUADRA,NDIM  ,
     &            TYPMAI,NOM1  ,NOM2  ,TABCOR,TABCPL)
C
C --- CREATION PSEUDO-MODELE
C
      CALL ARLMOD(NOMO,MAILAR,MODARL,TABCOR)
C
C --- CREATION DES CARTES
C
      CTARLQ(1) = '&&ARLCHI.CTFAMI'
      CTARLQ(2) = '&&ARLCHI.CTINFO'
      CTARLQ(3) = '&&ARLCHI.CTREF1'
      CTARLQ(4) = '&&ARLCHI.CTCOO1'
      CTARLQ(5) = '&&ARLCHI.CTREF2'
      CTARLQ(6) = '&&ARLCHI.CTCOO2'
C
      CALL ARLCHI(MAIL  ,NOM1  ,NOM2  ,MAILAR,QUADRA,
     &            TYPMAI,LCARA ,TABCPL,NBCART,CTARLQ)
C
C --- APPELS A CALCUL
C
      CALL ARLCLC(MODARL,NBCART,CTARLQ,MARLEL)
C
C --- MENAGE
C
      CALL DETRSD(' ',MAILAR)
C
      CALL JEDEMA()

      END
