      INTEGER     NFISS,IFISS,IMA,NNO,NBHEAV
      INTEGER     JCONX1,JCONX2,JSTNL(NFISS),JSTNV(NFISS)
C ----------------------------------------------------------------------
C
C --- ROUTINE XFEM
C
C --- TYPE D'ELEMENT D'INTERSECTION XFEM
C
C ----------------------------------------------------------------------
C
C OUT NBHEAV   : NOMBRE D'ENRICHISSEMENTS HEAVISIDES DANS L'ÉLÉMENT
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM,JEXATR
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER     INO,STNO(NNO),IFIS
      INTEGER     JLCNX,NNGL
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
C --- INITIALISATION
      NBHEAV = 0
      DO 10 INO=1,NNO
        STNO(INO) = 0
 10   CONTINUE
C
C --- BOUCLE SUR LES NOEUDS DE LA MAILLE
C
      DO 20 INO = 1,NNO
        NNGL=ZI(JCONX1-1+ZI(JCONX2+IMA-1)+INO-1)
C
C --- BOUCLE SUR LES FISSURES PRECEDENTES
C
        DO 30 IFIS=1,IFISS
C --- RECUPERATION DES STATUTS DES NOEUDS
          IF(ZL(JSTNL(IFIS)-1+NNGL).AND.ZI(JSTNV(IFIS)-1+NNGL).GT.0)THEN
            STNO(INO) = STNO(INO)+1
          ENDIF
          NBHEAV = MAX(NBHEAV,STNO(INO))
 30     CONTINUE
 20   CONTINUE
      CALL JEDEMA()
      END
