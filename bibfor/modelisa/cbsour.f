      REAL*8        R8BID
      CHARACTER*8   SCALC
      CHARACTER*16  MOTFAC
      CHARACTER*24  CARTE,CHSOUR
C     ------------------------------------------------------------------
C
      CARTE = CHAR//'.CHTH.SOURE'
C
      MOTFAC = 'SOURCE'
      CALL GETFAC ( MOTFAC , NBFAC )
C
      NBCALC = 0
      IF ( FONREE .EQ. 'REEL' ) THEN
         DO 10 IOCC = 1 , NBFAC
            CALL GETVID ( MOTFAC,'SOUR_CALCULEE',IOCC,1,1,CHSOUR,ICALC)
            NBCALC = NBCALC + ICALC
 10      CONTINUE
         IF ( NBCALC .GT. 1 ) THEN
            CALL U2MESS('F','MODELISA3_64')
         ELSE IF (NBCALC.EQ.1) THEN
            CALL COPISD ( 'CHAMP_GD', 'G', CHSOUR(1:19), CARTE(1:19) )
         ENDIF
      ENDIF
C
      IF ( FONREE .EQ. 'REEL' ) THEN
         CALL GETVR8 ( MOTFAC, 'SOUR', 1,1,1, R8BID, ISOUR )
      ELSE
         CALL GETVID ( MOTFAC, 'SOUR', 1,1,1, SCALC, ISOUR )
      ENDIF
      IF ( ISOUR .EQ. 1 ) THEN
         CALL CASOUR ( CHAR, LIGRMO, NOMA, NDIM, FONREE )
      ENDIF
C
      END
