      IMPLICIT NONE
      CHARACTER*8 CHAR
      CHARACTER*8 NOMA
      INTEGER     NNOCO
C
C ----------------------------------------------------------------------
C ROUTINE APPELEE PAR : CALICO
C ----------------------------------------------------------------------
C
C REMPLISSAGE DES TABLEAUX CONTENANT LES INFORMATIONS 
C POUR METHODE "CONTINUE" 
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  NOMA   : NOM DU MAILLAGE
C IN  NNOCO  : NOMBRE TOTAL DE NOEUDS DES SURFACES
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX ----------------
C
      INTEGER      TYPALC,IBID,CFMMVD,ZNOES
      CHARACTER*24 NOESCL,DEFICO
      INTEGER      JNOESC
      CHARACTER*14 K14BLA
      DATA K14BLA /' '/      
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ()
C
      DEFICO = CHAR(1:8)//'.CONTACT'
      CALL CFDISC(DEFICO,K14BLA,TYPALC,IBID,IBID,IBID)
      ZNOES = CFMMVD('ZNOES')
      
      IF (ABS(TYPALC).EQ.3) THEN        
        NOESCL = CHAR(1:8)//'.CONTACT.NOESCL'    
        CALL WKVECT(NOESCL,'G V R',ZNOES*NNOCO+1,JNOESC)
        ZR(JNOESC) = NNOCO
        CALL MMACON(NOMA,DEFICO)
      ENDIF
C
      CALL JEDEMA()
      END
