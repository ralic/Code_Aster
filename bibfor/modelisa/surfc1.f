C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE ABBAS M.ABBAS
C 
      IMPLICIT NONE
      CHARACTER*8 CHAR
      CHARACTER*8 NOMA
      INTEGER     IFM      
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE DISCRETE - AFFICHAGE DONNEES)
C
C AFFICHAGE LES INFOS CONTENUES DANS LA SD CONTACT POUR LA FORMULATION
C DISCRETE
C      
C ----------------------------------------------------------------------
C
C
C IN  CHAR   : NOM UTILISATEUR DU CONCEPT DE CHARGE
C IN  NOMA   : NOM DU MAILLAGE
C IN  IFM    : UNITE D'IMPRESSION
C
C -------------- DEBUT DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER ZI
      COMMON /IVARJE/ ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C ---------------- FIN DECLARATIONS NORMALISEES JEVEUX -----------------
C
      INTEGER      CFMMVD,ZCONV,ZMETH,ZCARF,ZTOLE
      INTEGER      NZOCO
      INTEGER      IZONE 
      CHARACTER*24 DEFICO 
      CHARACTER*24 METHCO
      INTEGER      JMETH            
      CHARACTER*24 NDIMCO,CONTNO,TOLECO 
      INTEGER      JDIM  ,JNOCO ,JTOLE       
      CHARACTER*24 CARFRO,CONVCO
      INTEGER      JCARF ,JCONV 
C
C ----------------------------------------------------------------------
C
      CALL JEMARQ
C
C --- INITIALISATIONS
C
      DEFICO = CHAR(1:8)//'.CONTACT'        
C   
C --- COMMUNS TOUTES FORMULATIONS 
C 
      METHCO = DEFICO(1:16)//'.METHCO'       
      CALL JEVEUO(METHCO,'L',JMETH)  
C
      ZMETH  = CFMMVD('ZMETH')       
C   
C --- COMMUNS AVEC FORM. MAILLEES (DISCRET ET CONTINUE MAIS PAS XFEM)
C  
      NDIMCO = DEFICO(1:16)//'.NDIMCO'
      CONTNO = DEFICO(1:16)//'.NOEUCO' 
      TOLECO = DEFICO(1:16)//'.TOLECO'       
      CALL JEVEUO(NDIMCO,'L',JDIM)
      CALL JEVEUO(CONTNO,'L',JNOCO) 
      CALL JEVEUO(TOLECO,'L',JTOLE) 
C  
      ZTOLE = CFMMVD('ZTOLE')                      
C   
C --- SPECIFIQUES FORM. DISCRETE
C 
      CARFRO = DEFICO(1:16)//'.CARFRO'
      CONVCO = DEFICO(1:16)//'.CONVCO'
C
      CALL JEVEUO(CARFRO,'L',JCARF)
      CALL JEVEUO(CONVCO,'L',JCONV)   
C 
      ZCONV = CFMMVD('ZCONV')      
      ZCARF = CFMMVD('ZCARF') 
C
C --- INITIALISATIONS
C      
      NZOCO  = ZI(JMETH)                                           
C
C --- IMPRESSIONS POUR L'UTILISATEUR
C 
      WRITE (IFM,*)      
      WRITE (IFM,*) '<CONTACT> INFOS SPECIFIQUES SUR LA FORMULATION'//
     &              ' DISCRETE'
      WRITE (IFM,*)           
C
      WRITE (IFM,*) '<CONTACT> ... PARAMETRES GENERAUX '
      IZONE = 1
      WRITE (IFM,1071) 'METHODE         ',ZI(JMETH+ZMETH*(IZONE-1)+6)
C
      WRITE (IFM,*) '<CONTACT> ... PARAMETRES APPARIEMENT'
      DO 9 IZONE = 1,NZOCO 
        WRITE (IFM,*) '<CONTACT> ...... ZONE : ',IZONE         
        WRITE (IFM,1071) 'APPARIEMENT     ',ZI(JMETH+ZMETH*(IZONE-1)+1)
        WRITE (IFM,1071) 'REAC_GEOM       ',ZI(JMETH+ZMETH*(IZONE-1)+7)
  9   CONTINUE               
C 
C --- IMPRESSIONS POUR LES PARAMETRES DE CONVERGENCE
C 
      WRITE (IFM,*) '<CONTACT> ... PARAMETRES DE CONVERGENCE'     
      DO 10 IZONE = 1,NZOCO
        WRITE (IFM,*) '<CONTACT> ...... ZONE : ',IZONE          
        WRITE (IFM,1070) 'STOP_SINGULIER  ',ZI(JCONV+ZCONV*(IZONE-1)+0)
        WRITE (IFM,1070) 'NB_RESOL        ',ZI(JCONV+ZCONV*(IZONE-1)+1)
        WRITE (IFM,1070) 'ITER_MULT_MAXI  ',ZI(JCONV+ZCONV*(IZONE-1)+2)
        WRITE (IFM,1070) 'STOP_INTERP     ',ZI(JCONV+ZCONV*(IZONE-1)+3)
        WRITE (IFM,1070) 'PRE_COND        ',ZI(JCONV+ZCONV*(IZONE-1)+4)
        WRITE (IFM,1070) 'RECH_LINEAIRE   ',ZI(JCONV+ZCONV*(IZONE-1)+5)
 10   CONTINUE 
C 
C --- IMPRESSIONS POUR LES PARAMETRES PENALISATION/FROTTEMENT
C 
      WRITE (IFM,*) '<CONTACT> ... PARAMETRES DE PENAL./FROTTEMENT'     
      DO 50 IZONE = 1,NZOCO
        WRITE (IFM,*) '<CONTACT> ...... ZONE : ',IZONE          
        WRITE (IFM,1071) 'E_N             ',ZR(JCARF+ZCARF*(IZONE-1)+1)
        WRITE (IFM,1071) 'E_T             ',ZR(JCARF+ZCARF*(IZONE-1)+2)
        WRITE (IFM,1071) 'COULOMB         ',ZR(JCARF+ZCARF*(IZONE-1)+3)
        WRITE (IFM,1071) 'COEF_MATR_FROT  ',ZR(JCARF+ZCARF*(IZONE-1)+4)
 50   CONTINUE      
C 
C --- IMPRESSIONS POUR LES PARAMETRES DE TOLERANCE
C
      WRITE (IFM,*) '<CONTACT> ... PARAMETRES DE TOLERANCE '     
      DO 300 IZONE = 1,NZOCO
        WRITE (IFM,*) '<CONTACT> ...... ZONE : ',IZONE          
        WRITE (IFM,1071) 'ALARME_JEU      ',ZR(JTOLE+ZTOLE*(IZONE-1)+2)
        WRITE (IFM,1071) 'TOLE_INTERP     ',ZR(JTOLE+ZTOLE*(IZONE-1)+3)
        WRITE (IFM,1071) 'RESI_ABSO       ',ZR(JTOLE+ZTOLE*(IZONE-1)+4)
        WRITE (IFM,1071) 'COEF_RESI       ',ZR(JTOLE+ZTOLE*(IZONE-1)+5)
 300  CONTINUE 
C
 1070 FORMAT (' <CONTACT> ...... PARAM. : ',A16,' - VAL. : ',I5)   
 1071 FORMAT (' <CONTACT> ...... PARAM. : ',A16,' - VAL. : ',E12.5) 
C     
 999  CONTINUE
      CALL JEDEMA
      END
