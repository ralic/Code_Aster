C  BUT : RECUPERER LE NOM DE LA COMPOSANTE NOMCMP (MOT CLE NOM_CMP)
C        SI NOMGD='VARI_R', RENDRE EN PLUS SON NUMERO :IVARI

C ----------------------------------------------------------------------
C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNOM,JEXNUM
C     ----- FIN COMMUNS NORMALISES  JEVEUX  ----------------------------
      INTEGER IBID,N1,N2,IRET,IANCMP,LGNCMP,IVARI
      CHARACTER*8 K8B
      CHARACTER*16 NOMCMD
C     ------------------------------------------------------------------

      CALL JEMARQ()
      CALL GETRES(K8B,K8B,NOMCMD)
      CALL GETVTX(MCFAC,'NOM_CMP',IOCC,1,1,NOMCMP,N2)


      IF (NOMGD.EQ.'VARI_R') THEN
C     ------------------------------
        CALL LXLIIS(NOMCMP(2:8),IBID,IRET)
        IVARI=IBID

        IF ((NOMCMP(1:1).NE.'V').OR.(IRET.NE.0)) THEN
          CALL UTDEBM('F','UTCMP1','ERREURS DONNEES')
          CALL UTIMPK('L','COMPOSANTE INCONNUE ',1,NOMCMP)
          CALL UTIMPK('S',' POUR LA GRANDEUR ',1,'VARI_R')
          CALL UTFINM()
        END IF
      ELSE
        IVARI=0
        CALL JEVEUO(JEXNOM('&CATA.GD.NOMCMP',NOMGD),'L',IANCMP)
        CALL JELIRA(JEXNOM('&CATA.GD.NOMCMP',NOMGD),'LONMAX',LGNCMP,K8B)
        CALL KNINCL(8,NOMCMP,1,ZK8(IANCMP),LGNCMP,IRET)
      END IF
      CALL JEDEMA()
      END
