C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE                            VABHHTS J.PELLET
      IMPLICIT REAL*8 (A-H,O-Z)
      INTEGER LONG,IAD1,IAD2
      CHARACTER*(*) TPSCAZ
C ----------------------------------------------------------------------
C     IN:
C      LONG: LONGUEUR A RECOPIER
C     TPSCAZ : 'R', 'I', 'K8/16/24/32/80','C'
C      LONG: LONGUEUR A RECOPIER
C      IAD1: ADRESSE DANS LE COMMON ZI (OU ZR...) DE L'OBJET A COPIER.
C      IAD2: ADRESSE DANS LE COMMON ZI (OU ZR...) DE L'OBJET RECOPIE.

C     ----- DEBUT COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNOM,JEXNUM
C     -----  FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
      CHARACTER*3 T
      CHARACTER*8 TYPSCA
C DEB-------------------------------------------------------------------

      TYPSCA = TPSCAZ

      T = TYPSCA(1:3)

C     -- QUELQUES VERIFICATIONS:

      IF (LONG.LT.0) THEN
        CALL U2MESS('F','CALCULEL2_65')
      END IF

C     -- RECOPIE SELON LE TYPE DE SCALAIRE:

      IF (T.EQ.'I  ') THEN
CCDIR$ IVDEP
        DO 10,I = 1,LONG
          ZI(IAD2-1+I) = ZI(IAD1-1+I)
   10   CONTINUE
      ELSE IF (T.EQ.'R  ') THEN
CCDIR$ IVDEP
        DO 20,I = 1,LONG
          ZR(IAD2-1+I) = ZR(IAD1-1+I)
   20   CONTINUE
      ELSE IF (T.EQ.'C  ') THEN
CCDIR$ IVDEP
        DO 30,I = 1,LONG
          ZC(IAD2-1+I) = ZC(IAD1-1+I)
   30   CONTINUE
      ELSE IF (T.EQ.'L  ') THEN
CCDIR$ IVDEP
        DO 40,I = 1,LONG
          ZL(IAD2-1+I) = ZL(IAD1-1+I)
   40   CONTINUE
      ELSE IF (T.EQ.'K8 ') THEN
CCDIR$ IVDEP
        DO 50,I = 1,LONG
          ZK8(IAD2-1+I) = ZK8(IAD1-1+I)
   50   CONTINUE
      ELSE IF (T.EQ.'K16') THEN
CCDIR$ IVDEP
        DO 60,I = 1,LONG
          ZK16(IAD2-1+I) = ZK16(IAD1-1+I)
   60   CONTINUE
      ELSE IF (T.EQ.'K24') THEN
CCDIR$ IVDEP
        DO 70,I = 1,LONG
          ZK24(IAD2-1+I) = ZK24(IAD1-1+I)
   70   CONTINUE
      ELSE IF (T.EQ.'K32') THEN
CCDIR$ IVDEP
        DO 80,I = 1,LONG
          ZK32(IAD2-1+I) = ZK32(IAD1-1+I)
   80   CONTINUE
      ELSE IF (T.EQ.'K80') THEN
CCDIR$ IVDEP
        DO 90,I = 1,LONG
          ZK80(IAD2-1+I) = ZK80(IAD1-1+I)
   90   CONTINUE
      ELSE
        CALL U2MESK('F','CALCULEL2_66',1,TYPSCA)
      END IF


  100 CONTINUE
      END
