C MODIF CALCULEL  DATE 08/09/2003   AUTEUR VABHHTS J.PELLET 
C ======================================================================
C COPYRIGHT (C) 1991 - 2001  EDF R&D                  WWW.CODE-ASTER.ORG
C THIS PROGRAM IS FREE SOFTWARE; YOU CAN REDISTRIBUTE IT AND/OR MODIFY
C IT UNDER THE TERMS OF THE GNU GENERAL PUBLIC LICENSE AS PUBLISHED BY
C THE FREE SOFTWARE FOUNDATION; EITHER VERSION 2 OF THE LICENSE, OR
C (AT YOUR OPTION) ANY LATER VERSION.

C THIS PROGRAM IS DISTRIBUTED IN THE HOPE THAT IT WILL BE USEFUL, BUT
C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.

C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE                            VABHHTS J.PELLET
C     ARGUMENTS:
C     ----------
      INTEGER NIN,NOU
      CHARACTER*(*) LCHIN(*),LCHOU(*),OPTION
C ----------------------------------------------------------------------
C     BUT : IMPRIMER SUR UNE LIGNE LA VALEUR
C           DES CHAMPS "IN" ET "OUT" AFIN DE COMPARER 2 VERSIONS
C ----------------------------------------------------------------------
      CHARACTER*19 CHAMP
      CHARACTER*24 OJB
      CHARACTER*3 TYPE
      INTEGER SOMMI,RESUME,LONMAX,LONUTI,I,IRET
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR,SOMMR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80

C DEB-------------------------------------------------------------------

      CALL JEMARQ()
      CALL JXVERI(' ',' ')

C     1- POUR FAIRE DU DEBUG PAR COMPARAISON DE 2 VERSIONS:
C     -----------------------------------------------------
      WRITE (6,*) 'AJACOT OPTION,LCHIN=',OPTION
      DO 10,I = 1,NIN
        CHAMP = LCHIN(I)
        CALL EXISD('CARTE',CHAMP,IRET)
        IF (IRET.GT.0) OJB = CHAMP//'.VALE'
        CALL EXISD('CHAM_NO',CHAMP,IRET)
        IF (IRET.GT.0) OJB = CHAMP//'.VALE'
        CALL EXISD('CHAM_ELEM',CHAMP,IRET)
        IF (IRET.GT.0) OJB = CHAMP//'.CELV'
        CALL EXISD('RESUELEM',CHAMP,IRET)
        IF (IRET.GT.0) OJB = CHAMP//'.RESL'

        CALL TSTOBJ(OJB,RESUME,SOMMI,SOMMR,LONUTI,LONMAX,TYPE,IRET)
        WRITE (6,1001) 'AJACO|',OJB,LONMAX,LONUTI,TYPE,IRET,RESUME,
     &    SOMMI,SOMMR
   10 CONTINUE


      WRITE (6,*) 'AJACOT OPTION,LCHOUT=',OPTION
      DO 20,I = 1,NOU
        CHAMP = LCHOU(I)
        CALL EXISD('CARTE',CHAMP,IRET)
        IF (IRET.GT.0) OJB = CHAMP//'.VALE'
        CALL EXISD('CHAM_NO',CHAMP,IRET)
        IF (IRET.GT.0) OJB = CHAMP//'.VALE'
        CALL EXISD('CHAM_ELEM',CHAMP,IRET)
        IF (IRET.GT.0) OJB = CHAMP//'.CELV'
        CALL EXISD('RESUELEM',CHAMP,IRET)
        IF (IRET.GT.0) OJB = CHAMP//'.RESL'

        CALL TSTOBJ(OJB,RESUME,SOMMI,SOMMR,LONUTI,LONMAX,TYPE,IRET)
        WRITE (6,1001) 'AJACO|',OJB,LONMAX,LONUTI,TYPE,IRET,RESUME,
     &    SOMMI,SOMMR
   20 CONTINUE



      CALL JEDEMA()

 1001 FORMAT (A10,A25,I8,I8,A4,I7,I24,I24,E17.8)
      END
