C BUT : RENDRE L'ADRESSE DU CHAMP LOCAL (JAD) CORRESPONDANT
C       AU PARAMETRE NOMPAR (COMME LE FAIT JEVECH).
C       - SI LE CHAMP LOCAL EST COMPLETEMENT VIDE, LA ROUTINE
C         L'INITIALISE A LA VALEUR PAR DEFAUT VALDEF.
C       - SI LE CHAMP LOCAL EST PARTIELLEMENT VIDE, LA ROUTINE
C         EMET UNE ERREUR FATALE
C----------------------------------------------------------------
      IMPLICIT NONE
      INTEGER ITAB(8),JAD,LONEL,K,IRET
      CHARACTER*(*) NOMPAR
      REAL*8 VALDEF
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80

C ---------------- FIN COMMUNS NORMALISES  JEVEUX  --------------------
      CHARACTER*16 OPTION,NOMTE,NOMTM,PHENO,MODELI
      COMMON /CAKK01/OPTION,NOMTE,NOMTM,PHENO,MODELI

      CALL TECACH('OON',NOMPAR,8,ITAB,IRET)
      CALL ASSERT((IRET.EQ.0).OR.(IRET.EQ.3))

      JAD=ITAB(1)
      IF (IRET.EQ.3) THEN
        CALL ASSERT(ITAB(5).EQ.1)
        LONEL=ITAB(2)*MAX(1,ITAB(6))*MAX(1,ITAB(7))
        DO 1, K = 1,LONEL
          IF (ZL(ITAB(8)-1+K)) THEN
            CALL UTMESS('F','JEVECD',
     &          'MAILLE PARTIELLEMENT AFFECTEE.')
            CALL CONTEX(OPTION,0,NOMPAR,' ',0)
          END IF
          ZR(JAD-1+K)=VALDEF
 1      CONTINUE
      END IF

      END
