      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 OPTION,NOMTE
C ......................................................................
C    - FONCTION REALISEE:  CALCUL DES VECTEURS ELEMENTAIRES
C                          OPTION : 'CHAR_THER_FLUTNL'

C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT
      CHARACTER*8 COEF
      REAL*8 POIDS,R,TPG
      REAL*8 ALPHA,ALPHAP,NX,NY
      INTEGER NNO,NNOS,JGANO,NDIM,KP,NPG,I,K,ITEMPS,ITEMP,ITEMPI,
     &        IFLUX
      INTEGER IPOIDS,IVF,IDFDE,IGEOM
      INTEGER IVERES
      LOGICAL  LTEATT, LAXI
C
C
      CALL JEMARQ()

      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
      LAXI = .FALSE.
      IF (LTEATT(' ','AXIS','OUI')) LAXI = .TRUE.
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      CALL JEVECH('PTEMPER','L',ITEMP)
      CALL JEVECH('PTEMPEI','L',ITEMPI)
      CALL JEVECH('PFLUXNL','L',IFLUX)
      CALL JEVECH('PRESIDU','E',IVERES)

      COEF = ZK8(IFLUX)
      IF (COEF(1:7).EQ.'&FOZERO') GO TO 40


      DO 30 KP = 1,NPG
        K = (KP-1)*NNO
        CALL VFF2DN(NDIM,NNO,KP,IPOIDS,IDFDE,ZR(IGEOM),NX,NY,POIDS)
        R = 0.D0
        TPG = 0.D0
        DO 10 I = 1,NNO
          R = R + ZR(IGEOM+2* (I-1))*ZR(IVF+K+I-1)
          TPG = TPG + ZR(ITEMPI+I-1)*ZR(IVF+K+I-1)
   10   CONTINUE
        CALL FODERI(COEF,TPG,ALPHA,ALPHAP)
        IF (LAXI) POIDS = POIDS*R


        DO 20 I = 1,NNO
          ZR(IVERES+I-1) = ZR(IVERES+I-1) +
     &                     POIDS*ZR(IVF+K+I-1)* (ALPHA-ALPHAP*TPG)
   20   CONTINUE
   30 CONTINUE
   40 CONTINUE
C FIN ------------------------------------------------------------------
      CALL JEDEMA()
      END
