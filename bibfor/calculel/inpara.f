C     ----------
      CHARACTER*8 NOPARA
      CHARACTER*3 STATUT
      INTEGER OPT,TE
C ----------------------------------------------------------------------
C     ENTREES:
C        OPT    : OPTION
C        TE     : TYPE_ELEMENT
C        STATUT : IN/OUT
C        NOMPARA : NOM D'1 PARAMETRE DE L'OPTION

C     SORTIES:
C        INPARA : NUMERO DU PARAMETRE NOMPARA POUR(OPT,TE)
C                --> REND : 0 SI LE NOMPARA N'EST PAS TROUVE

C ----------------------------------------------------------------------
      COMMON /CAII02/IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS,
     +       IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD

C     FONCTIONS EXTERNES:
C     -------------------

C     VARIABLES LOCALES:
C     ------------------
      INTEGER I,DEB,FIN,TROUVE,JJ,OPTMOD,OPTNOM
      INTEGER IADSGD,IAMLOC,IAOPDS,IAOPMO,IAOPNO,IAOPPA,IAOPTT,ILMLOC
      INTEGER ILOPMO,ILOPNO,LGCO,NPARIN,NPARIO,NUCALC
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80

C DEB-------------------------------------------------------------------

      JJ = ZI(IAOPTT-1+ (TE-1)*LGCO+OPT)
      IF (JJ.EQ.0) THEN
        INPARA = 0
      ELSE
        OPTMOD = IAOPMO + ZI(ILOPMO-1+JJ) - 1
        NUCALC = ZI(OPTMOD-1+1)
        IF (NUCALC.LE.0) THEN
          INPARA = 0
          GO TO 30
        END IF
        OPTNOM = IAOPNO + ZI(ILOPNO-1+JJ) - 1
        IF (STATUT.EQ.'IN ') THEN
          DEB = 1
          FIN = ZI(OPTMOD-1+2)

        ELSE
          DEB = ZI(OPTMOD-1+2) + 1
          FIN = ZI(OPTMOD-1+2) + ZI(OPTMOD-1+3)
        END IF

        TROUVE = 0
        DO 10 I = DEB,FIN
          IF (NOPARA.EQ.ZK8(OPTNOM-1+I)) THEN
            TROUVE = 1
            GO TO 20
          END IF
   10   CONTINUE
   20   CONTINUE

        IF (TROUVE.EQ.0) THEN
          INPARA = 0
          GO TO 30
        ELSE
          INPARA = I - DEB + 1
          GO TO 30
        END IF

      END IF

   30 CONTINUE
      END
