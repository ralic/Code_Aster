      SUBROUTINE MEAMME(OPTIOZ,MODELE,NCHAR ,LCHAR ,MATE  ,
     &                  CARA  ,EXITIM,TIME  ,BASE  ,MERIGI,
     &                  MEMASS,MEAMOR)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF CALCULEL  DATE 19/12/2007   AUTEUR ABBAS M.ABBAS 
C ======================================================================
