      CHARACTER*8  FAPG(NBFAMX) 
      INTEGER      NNOS,NBFPG(NBFAMX),NBPG(NBFAMX)     
C
C ----------------------------------------------------------------------
C       
      CALL ARLT2R(TYPEMA,ELREFE)
      CALL ELRACA(ELREFE,NDIM  ,NNO   ,NNOS  ,NBFPG ,
     &            FAPG  ,NBPG  ,NO    ,VOL   )
      CALL ELRAGA(ELREFE,NOMFAM, NDIM, NG, G, WG )
C      
      END
