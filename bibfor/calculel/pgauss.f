C PAR NGM    : NOMBRE MAXI DE POINTS DE GAUSS PAR MAILLE
C IN  TYPEMA : TYPE DE LA MAILLE
C IN  NOMFAM : NOM DE LA FAMILLE DE POINTS DE GAUSS
