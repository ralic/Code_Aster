      REAL*8         RCMP(3),RBID
      INTEGER        IBID
C
      CHARACTER*8    LICMP(3),NOMF(3),ZERO
      CHARACTER*19   LIGRMO
C
      COMPLEX*16     CBID
C
C   ------------------------------------------------------------------
C
      CHFOR0 = '&&MEFOR0.FORCE_NULLE'
      ZERO   = '&&MEFOR0'
      LIGRMO = NOMO//'.MODELE    '
      LICMP(1) = 'FX'
      LICMP(2) = 'FY'
      LICMP(3) = 'FZ'
      RCMP(1) = 0.D0
      RCMP(2) = 0.D0
      RCMP(3) = 0.D0
      IF (FONC) THEN
        CALL FOZERV(ZERO)
        NOMF(1) = ZERO
        NOMF(2) = ZERO
        NOMF(3) = ZERO
        CALL MECACT('V',CHFOR0,'MODELE',LIGRMO,'FORC_F',3,LICMP,IBID,
     &               RBID,CBID,NOMF)
      ELSE
        CALL MECACT('V',CHFOR0,'MODELE',LIGRMO,'FORC_R',3,LICMP,IBID,
     &               RCMP,CBID,' ')
      ENDIF
