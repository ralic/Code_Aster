      INCLUDE 'jeveux.h'
      CHARACTER*8    NOMO
      CHARACTER*(*)   CHFOR0
C
      LOGICAL        FONC
C
C - CETTE ROUTINE GENERE UN CHAMP DE FORCE NUL (CARTE CONSTANTE)
C       FONC = .TRUE.  FORCE FONCTION
C       FONC = .FALSE. FORCE REELLE
C
