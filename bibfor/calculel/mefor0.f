      IMPLICIT REAL*8 (A-H,O-Z)
C
      CHARACTER*8    NOMO
      CHARACTER*24   CHFOR0
C
      LOGICAL        FONC
C
C - CETTE ROUTINE GENERE UN CHAMP DE FORCE NUL (CARTE CONSTANTE)
C       FONC = .TRUE.  FORCE FONCTION
C       FONC = .FALSE. FORCE REELLE
C
C     ----------- COMMUNS NORMALISES  JEVEUX  ----------------------
      COMMON/IVARJE/ZI(1)
      COMMON/RVARJE/ZR(1)
      COMMON/CVARJE/ZC(1)
      COMMON/LVARJE/ZL(1)
      COMMON/KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8  ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C     ----------- COMMUNS NORMALISES  JEVEUX  ------------------------
C
      REAL*8         RCMP(3),RBID
      INTEGER        IBID
C
      CHARACTER*8    LICMP(3),NOMF(3),ZERO
      CHARACTER*19   LIGRMO
C
      COMPLEX*16     CBID
C
C   ------------------------------------------------------------------
C
      CHFOR0 = '&&MEFOR0.FORCE_NULLE'
      ZERO   = '&&MEFOR0'
      LIGRMO = NOMO//'.MODELE    '
      LICMP(1) = 'FX'
      LICMP(2) = 'FY'
      LICMP(3) = 'FZ'
      RCMP(1) = 0.D0
      RCMP(2) = 0.D0
      RCMP(3) = 0.D0
      IF (FONC) THEN
        CALL FOZERV(ZERO)
        NOMF(1) = ZERO
        NOMF(2) = ZERO
        NOMF(3) = ZERO
        CALL MECACT('V',CHFOR0,'MODELE',LIGRMO,'FORC_F',3,LICMP,IBID,
     &               RBID,CBID,NOMF)
      ELSE
        CALL MECACT('V',CHFOR0,'MODELE',LIGRMO,'FORC_R',3,LICMP,IBID,
     &               RCMP,CBID,' ')
      ENDIF
C
      END
