C ----------------------------------------------------------------------
C
C     ON CHERCHE LES NOMS DES CHAMPS DE CARAC_ELEM DANS 1 CARTE CARA
C
C
C     ENTREES:
C        CARA : NOM DE LA CARTE
C
C     SORTIES:
