      INTEGER II
C
C   1   PCAORIE         CARORIEN
C   2   PCADISK         CARDISCK
C   3   PCADISM         CARDISCM
C   4   PCADISA         CARDISCA
C   5   PCAGEPO         CARGEOPO
C   6   PCAGNPO         CARGENPO
C   7   PCACOQU         CARCOQUE
C   8   PCASECT         CARSECTI
C   9   PCAARPO         CARARCPO
C  10   PCACABL         CARCABLE
C  11   PCAGNBA         CARGENBA
C  12   PCAMASS         CARMASSI
C  13   PCAPOUF         CARPOUFL
C  14   PVENTCX         CVENTCXF
C  15   PCINFDI         CARDINFO
C  16   PNBSP_I         CANBSP
C  17   PFIBRES         CAFIBR

      EXICAR = .FALSE.
      IF (CARA(1:8).NE.'        ') THEN
C        CHAINE DE 19 CARACTERES AU MAXIMUM
C                    12345678    90123456789
         CHCARA(1) = CARA(1:8)//'.CARORIEN'
         CHCARA(2) = CARA(1:8)//'.CARDISCK'
         CHCARA(3) = CARA(1:8)//'.CARDISCM'
         CHCARA(4) = CARA(1:8)//'.CARDISCA'
         CHCARA(5) = CARA(1:8)//'.CARGEOPO'
         CHCARA(6) = CARA(1:8)//'.CARGENPO'
         CHCARA(7) = CARA(1:8)//'.CARCOQUE'
         CHCARA(8) = CARA(1:8)//'.CARSECTI'
         CHCARA(9) = CARA(1:8)//'.CARARCPO'
         CHCARA(10)= CARA(1:8)//'.CARCABLE'
         CHCARA(11)= CARA(1:8)//'.CARGENBA'
         CHCARA(12)= CARA(1:8)//'.CARMASSI'
         CHCARA(13)= CARA(1:8)//'.CARPOUFL'
         CHCARA(14)= CARA(1:8)//'.CVENTCXF'
         CHCARA(15)= CARA(1:8)//'.CARDINFO'
         CHCARA(16)= CARA(1:8)//'.CANBSP'
         CHCARA(17)= CARA(1:8)//'.CAFIBR'
         CHCARA(18)= ' '
         EXICAR = .TRUE.
      ELSE
         DO 1 II = 1,18
            CHCARA(II) = ' '
1        CONTINUE
      ENDIF
C
      END
