      INCLUDE 'jeveux.h'
      CHARACTER*8 MODELE
      CHARACTER*19 MATEL
C ----------------------------------------------------------------------

C     CALCUL DES SECONDS MEMBRES ELEMENTAIRES CONTENANT LES NORMALES
C     AUX ARETES AUX NOEUDS SOMMETS (EN FAIT LA SOMME DES 2 NORMALES)
C             ( VALABLE POUR LE 2D )

