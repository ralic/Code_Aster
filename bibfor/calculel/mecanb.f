C        MODELE : NOM DU MODELE

C ----------------------------------------------------------------------

C     FONCTIONS EXTERNES:
C     -------------------

C     VARIABLES LOCALES:
C     ------------------
      LOGICAL EXIGEO
      CHARACTER*8 LPAIN(1),LPAOUT(1),LCHAR(1)
      CHARACTER*16 OPTION
      CHARACTER*24 CHGEOM,LCHIN(1),LCHOUT(1)
      CHARACTER*24 LIGRMO



C     -- CALCUL DE .RERR:
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
      CALL JEMARQ()
      CALL MEMARE('V',MATEL,MODELE,' ',' ','CHAR_MECA')

      CALL JEDETR(MATEL//'.RELR')

      LCHAR(1) = ' '
      CALL MEGEOM(MODELE,LCHAR(1),EXIGEO,CHGEOM)
      IF (.NOT.EXIGEO) CALL U2MESS('F','CALCULEL2_81')

      LPAIN(1) = 'PGEOMER'
      LCHIN(1) = CHGEOM
      LPAOUT(1) = 'PVECTUR'
      LCHOUT(1) = MATEL(1:8)//'.VE001'
      LIGRMO = MODELE//'.MODELE'
      OPTION = 'CALC_NOEU_BORD'
      CALL CALCUL('S',OPTION,LIGRMO,1,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'V',
     &               'OUI')
      CALL REAJRE(MATEL,LCHOUT(1),'V')

      CALL JEDEMA()
      END
