C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C TOLE CRP_6
C
C     IN
C     --
C     IDPRN1 IDPRN2 : ADRESSES DE PRNO ( OBJET ET POINTEUR DE LONGUEUR)
C     ILI : NUMERO DE LIGREL
C     MODE : MODE
C     NEC  : NBEC(GD)
C
C     N    : NUMERO GLOBAL DU NOEUD
C     K    : NUMERO LOCAL DU NOEUD ( DANS ELEMENT )
C     OUT
C     ---
C
C     NDDLOC : NBRE DE DDL SUPPORTES PAR CE NOEUD SUR L ELEMENT
C     POS    : TABLEAU DE CORRESPONDANCE AVEC LES DDL SUR LE NOEUD
C          EN TANT QUE NOEUD GLOBAL
C ----------------------------------------------------------------------
      INCLUDE 'jeveux.h'
C-----------------------------------------------------------------------
      INTEGER I ,IDPRN1 ,IDPRN2 ,IEC ,IECDG ,IECDL ,ILI 
      INTEGER IN ,K ,L ,MODE ,N ,NBECMX ,NCMP 
      INTEGER NDDLOC ,NEC ,NUNOEL 
C-----------------------------------------------------------------------
      PARAMETER (NBECMX = 10)
      INTEGER IFIN(NBECMX)
      INTEGER PRNO
      INTEGER ADMODL,LCMODL,POS(1)
      INTEGER ECODG,ECODL,ENTCOD
C
C     ECODG : ENTIER CODE DE N
C     ECODL : ENTIER CODE DE K
C
C
C
C
C
C     FONCTION D ACCES A PRNO
C
      PRNO(ILI,NUNOEL,L) = ZI(IDPRN1-1+ZI(IDPRN2+ILI-1)+
     +                     (NUNOEL-1)* (NEC+2)+L-1)
C - DEB ----------------------------------------------------------------

C --- IFIN DONNE POUR CHAQUE ENTIER CODE LE NOMBRE MAX DE DDLS
C --- QUE L'ON PEUT TROUVER SUR CET ENTIER :
C     ------------------------------------
      DO 10 IEC = 1, NEC-1
          IFIN(IEC) = 30
 10   CONTINUE
      IFIN(NEC) = NCMP - 30*(NEC-1)

      IN = 0
      NDDLOC = 0


      DO 20 IEC = 1, NEC
        ECODG = PRNO(ILI,N,2+IEC)
        IF (ECODG.EQ.0) GOTO 20

        ECODL = ENTCOD(ADMODL,LCMODL,NEC,MODE,K,IEC)

        DO 110 I = 1,IFIN(IEC)
          ECODG = ECODG/2
          IF (ECODG.EQ.0) GOTO 20
          ECODL = ECODL/2
          IECDG = IAND(1,ECODG)
          IF (IECDG.GT.0) THEN
            IN = IN + 1
            IECDL = IAND(1,ECODL)
            IF (IECDL.GT.0) THEN
               NDDLOC = NDDLOC + 1
               POS(NDDLOC) = IN
            END IF
          END IF
  110   CONTINUE
  20  CONTINUE

      END
