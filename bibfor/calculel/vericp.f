C     CETTE ROUTINE VERIFIE SI LA COMPOSANTE CMP FAIT PARTIE DES COMPO-
C     SANTES CMPGLO DE LA GRANDEUR
C
C IN :  CMPGLO:  LISTE DES COMPOSANTES DE LA GRANDEUR
C       CMP   :  NOM DE LA COMPOSANTE
C       NBCMP :  NOMBRE DE COMPOSANTES DE LA GRANDEUR
C
C OUT : IRET = 0 LA COMPOSANTE CMP APPARTIENT A LA GRANDEUR
C       IRET = 1 LA COMPOSANTE CMP N'APPARTIENT PAS A LA GRANDEUR
C
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
          CHARACTER*8 CMP,CMPGLO(1)
C
           DO 1 I = 1,NBCMP
               IF(CMP.NE.CMPGLO(I)) THEN
                GO TO 1
               ELSE
                IRET=0
                GOTO 9999
               ENDIF
1           CONTINUE
C
             IRET=1
 9999        CONTINUE
             END
