C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C RESPONSABLE                            VABHHTS J.PELLET
C     ARGUMENTS:
C     ----------
      INTEGER OPT,TE
C ----------------------------------------------------------------------
C     ENTREES:
C      OPT : OPTION
C      TE  : TYPE D'ELEMENT

C     SORTIES:
C      MISE A JOUR DU COMMON  CAII06 (OBJET .IA_CHLOC)

C ----------------------------------------------------------------------
      COMMON /CAII02/IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS,
     +       IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD
      COMMON /CAII06/IAWLOC,IAWTYP,NBELGR,IGR
      INTEGER IADSGD,IAMLOC,IAOPDS,IAOPMO,IAOPNO,IAOPPA,IAOPTT
      INTEGER IAWTYP,ICODE,IGR,ILMLOC,ILOPMO,ILOPNO
      INTEGER IPARG,LGCO,M2,MODLOC,NBELGR,NBPOIN,NBSCAL,NPARA,NPARIN
      INTEGER IAWLOC,IPAR,NPARIO,NBPARA,INDIK8,MODATT
C-----------------------------------------------------------------------
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8,NOMPAR,NOPARA
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80

C DEB-------------------------------------------------------------------


C     -- ON REMET A "-1" LGCATA POUR LES PARAMETRES INCONNUS
C        DU TYPE_ELEMENT (TE)
      DO 10,IPARG = 1,NPARIO
        ZI(IAWLOC-1+7* (IPARG-1)+4) = -1
   10 CONTINUE


      NPARA = NBPARA(OPT,TE,'IN ')
      DO 20 IPAR = 1,NPARA
        NOMPAR = NOPARA(OPT,TE,'IN ',IPAR)
        IPARG = INDIK8(ZK8(IAOPPA),NOMPAR,1,NPARIO)
        M2 = MODATT(OPT,TE,'IN ',IPAR)
        MODLOC = IAMLOC - 1 + ZI(ILMLOC-1+M2)
        ICODE = ZI(MODLOC-1+1)
        NBSCAL = ZI(MODLOC-1+3)
        IF (ICODE.LE.3) THEN
          NBPOIN = ZI(MODLOC-1+4)
        ELSE
          NBPOIN = 0
        END IF

        ZI(IAWLOC-1+7* (IPARG-1)+3) = M2
        ZI(IAWLOC-1+7* (IPARG-1)+4) = NBSCAL
        ZI(IAWLOC-1+7* (IPARG-1)+6) = NBPOIN
   20 CONTINUE


      NPARA = NBPARA(OPT,TE,'OUT')
      DO 30 IPAR = 1,NPARA
        NOMPAR = NOPARA(OPT,TE,'OUT',IPAR)
        IPARG = INDIK8(ZK8(IAOPPA),NOMPAR,1,NPARIO)
        M2 = MODATT(OPT,TE,'OUT',IPAR)
        MODLOC = IAMLOC - 1 + ZI(ILMLOC-1+M2)
        ICODE = ZI(MODLOC-1+1)
        NBSCAL = ZI(MODLOC-1+3)
        IF (ICODE.LE.3) THEN
          NBPOIN = ZI(MODLOC-1+4)
        ELSE
          NBPOIN = 0
        END IF

        ZI(IAWLOC-1+7* (IPARG-1)+3) = M2
        ZI(IAWLOC-1+7* (IPARG-1)+4) = NBSCAL
        ZI(IAWLOC-1+7* (IPARG-1)+6) = NBPOIN
   30 CONTINUE

   40 CONTINUE
      END
