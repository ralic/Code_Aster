      IMPLICIT REAL*8 (A-H,O-Z)
C          TROUVER LE DESCRIPTEUR-GRANDEUR ASSOCIE A UN MODE LOCAL
C          DE CARTE, CHAM_NO, OU CHAMELEM, SOUS FORME "IDEN"
C      ENTREE:
C          MODE  : NUMERO DE MODE LOCAL
C      SORTIE:
C          NEC   : NOMBRE D'ENTIERS-CODES
C          DG    : DESCRIPTEUR GRANDEUR DIMENSIONNE A NEC
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI,DG(*),NEC,MODE
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32,JEXNUM,JEXNOM,JEXR8,JEXATR
      CHARACTER*80 ZK80
C
      CALL JEMARQ()
      NEC = 0
      DG(1) = 0
      CALL JEVEUO(JEXNUM('&CATA.TE.MODELOC',MODE),'L',JMOD)
      NEC = NBEC(ZI(JMOD-1+2))
      IF (ZI(JMOD).LE.3) THEN
         NBPT = ZI(JMOD-1+4)
         IF (ABS(NBPT).LT.10000) THEN
            DO 1 I = 1,NEC
               DG(I) = ZI(JMOD-1+4+I)
    1       CONTINUE
         END IF
      END IF
      CALL JEDEMA()
      END
