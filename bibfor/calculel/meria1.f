C     CALCUL DES MATRICES ELEMENTAIRES DE RIGIDITE ACOUSTIQUE MIXTE
C            ( ISO     , 'RIGI_ACOU'  )

C     LES RESUELEM PRODUITS S'APPELLENT :
C           PREFCH(1:8).ME000I , I=1,NCHAR+1

C     ENTREES:

C     LES NOMS QUI SUIVENT SONT LES PREFIXES UTILISATEUR K8:
C        MODELE : NOM DU MODELE
C        NCHAR  : NOMBRE DE CHARGES
C        LCHAR  : LISTE DES CHARGES
C        MATE   : CARTE DE MATERIAU CODE
C        MATEL  : NOM DU MATELE(N RESUELEM) PRODUIT
C        PREFCH : PREFIXE DES NOMS DES RESUELEM STOCKES DANS MATEL

C     SORTIES:
C        MATEL  : EST REMPLI.

C ----------------------------------------------------------------------

C     FONCTIONS EXTERNES:
C     -------------------

C     VARIABLES LOCALES:
C     ------------------
      CHARACTER*16 OPTION
      CHARACTER*24 CHGEOM,LCHIN(4),LCHOUT(1),LIGRMO,LIGRCH
      CHARACTER*8 LPAIN(4),LPAOUT(1)
      LOGICAL EXIGEO


      CALL JEMARQ()
      CALL MEGEOM(MODELE,LCHAR(1),EXIGEO,CHGEOM)

      CALL JEEXIN(MATEL//'.RERR',IRET)
      IF (IRET.GT.0) THEN
        CALL JEDETR(MATEL//'.RERR')
        CALL JEDETR(MATEL//'.RELR')
      END IF
      CALL MEMARE('V',MATEL,MODELE,MATE,' ','RIGI_ACOU')

      LPAOUT(1) = 'PMATTTC'
      LCHOUT(1) = PREFCH(1:8)//'.ME000'
      ILIRES = 0
      IF (MODELE(1:8).NE.'       ') THEN
        LPAIN(1) = 'PGEOMER'
        LCHIN(1) = CHGEOM
        LPAIN(2) = 'PMATERC'
        LCHIN(2) = MATE

        LIGRMO = MODELE(1:8)//'.MODELE'
        OPTION = 'RIGI_ACOU'
C  CAS CLASSIQUE ET SANS CHARGES (PAS DE DDL IMPOSES)
C         IF (NCHAR.EQ.0) THEN
        ILIRES=ILIRES+1
        CALL CODENT(ILIRES,'D0',LCHOUT(1) (12:14))
        CALL CALCUL('S',OPTION,LIGRMO,2,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'G',
     &                 'OUI')
        CALL REAJRE(MATEL,LCHOUT(1),'G')
C          END IF
C  CAS MIXTE OU CLASSIQUE AVEC CHARGES (PRESSION  IMPOSEE)
      END IF
      IF (LCHAR(1).NE.'        ') THEN
        DO 10 ICHA = 1,NCHAR
          LIGRCH = LCHAR(ICHA)//'.CHAC.LIGRE'
          CALL JEEXIN(LCHAR(ICHA)//'.CHAC.LIGRE.LIEL',IRET)
          IF (IRET.EQ.0) GO TO 10
          LCHIN(1) = LCHAR(ICHA)//'.CHAC.CMULT     '
          CALL EXISD('CHAMP_GD',LCHAR(ICHA)//'.CHAC.CMULT',IRET)
          IF (IRET.EQ.0) GO TO 10
          LPAIN(1) = 'PDDLMUC'
          ILIRES=ILIRES+1
          CALL CODENT(ILIRES,'D0',LCHOUT(1) (12:14))
          OPTION = 'ACOU_DDLM_C'
          CALL CALCUL('S',OPTION,LIGRCH,1,LCHIN,LPAIN,1,LCHOUT,LPAOUT,
     &                'G','OUI')
          CALL REAJRE(MATEL,LCHOUT(1),'G')
   10   CONTINUE
      END IF

      CALL JEDEMA()
      END
