      INCLUDE 'jeveux.h'
      INTEGER NCHAR
      CHARACTER*8 LCHAR(*)
      CHARACTER*19 MATEL,PREFCH
      CHARACTER*(*) MODELE,MATE
C ----------------------------------------------------------------------
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF CALCULEL  DATE 13/06/2012   AUTEUR COURTOIS M.COURTOIS 
