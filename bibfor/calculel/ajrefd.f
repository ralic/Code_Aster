C RESPONSABLE PELLET J.PELLET
      IMPLICIT NONE
      CHARACTER*(*) RESU1,RESU2,ACTION
C----------------------------------------------------------------------
C BUT :
C  AJOUTER SI NECESSAIRE L'OBJET .REFD DANS LA SD_RESULTAT_DYN RESU2

C  SI ACTION=='FORCE' :
C      ON ALLOUE RESU2.REFD VIERGE S'IL N'EXISTE PAS DEJA

C  SI ACTION=='ZERO' OU 'COPIE', ON CREE RESU2.REFD SEULEMENT SI :
C     - RESU1 EST UNE SD_RESULTAT_DYN (I.E. IL POSSEDE .REFD)
C     - RESU2 NE POSSEDE PAS DEJA UN .REFD
C      SI ACTION=='ZERO' :
C         ON ALLOUE .REFD VIERGE
C      SI ACTION=='COPIE' :
C         ON COPIE RESU1.REFD DANS RESU2.REFD
C----------------------------------------------------------------------
C
      INTEGER IEXI1,IEXI2,IBID
      CHARACTER*19 R1,R2

      CALL JEMARQ()

      R1=RESU1
      R2=RESU2

      CALL JEEXIN(R2//'.REFD',IEXI2)
      IF (IEXI2.GT.0) GOTO 9999

      IF (ACTION.EQ.'FORCE') THEN
         CALL WKVECT(R2//'.REFD','G V K24',7,IBID)
         GOTO 9999
      ENDIF

      CALL JEEXIN(R1//'.REFD',IEXI1)
      IF (IEXI1.EQ.0) GOTO 9999

      IF (ACTION.EQ.'ZERO') THEN
         CALL WKVECT(R2//'.REFD','G V K24',7,IBID)
      ELSEIF (ACTION.EQ.'COPIE') THEN
         CALL JEDUPO(R1//'.REFD','G',R2//'.REFD',.FALSE.)
      ELSE
         CALL ASSERT(.FALSE.)
      ENDIF

9999  CONTINUE
      CALL JEDEMA()

      END
