C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
C     RETOURNE LE NOMBRE D ENTIERS CODES POUR UN GRANDEUR DU TYPE :
C     _SIMPLE
C     _ELEMENTAIRE
C
      INCLUDE 'jeveux.h'
      INTEGER GD,CODE
      CHARACTER*8 NOMGD,NOMGD1,NOMGD2
C     INCLUDE($FUNJEV)
C
C     FONCTIONS JEVEUX
C
C
C
      INTEGER VALI(3)
      CHARACTER*32 VALK(3)
C
C     EXCLUDE($FUNJEV)
C
      CHARACTER*8 CBID
C
C-----------------------------------------------------------------------
      INTEGER IDDGD ,IGD1 ,IGD2 
C-----------------------------------------------------------------------
      CALL JEMARQ()
      NBCMP=0
C
      CALL JEVEUO(JEXNUM('&CATA.GD.DESCRIGD',GD),'L',IDDGD)
      CODE = ZI(IDDGD)
      IF ((CODE.EQ.1) .OR. (CODE.EQ.2)) THEN
         CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',GD),'LONMAX',NBCMP,CBID)
         GO TO 9999
      ELSE IF (CODE.EQ.3) THEN
         IGD1 = ZI(IDDGD+3)
         IF (IGD1.GT.0) THEN
            CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',IGD1),'LONMAX',NBCMP,
     +                  CBID)
            GO TO 9999
         ELSE
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
         VALK (1) = 'GRANDEUR LIGNE REFERENCEE PAR'
         VALK (2) = NOMGD//' NULLE'
         VALI (1) = GD
            CALL U2MESG('F', 'CALCULEL6_17',2,VALK,1,VALI,0,0.D0)
         END IF
      ELSE IF ((CODE.EQ.4) .OR. (CODE.EQ.5)) THEN
         IGD1 = ZI(IDDGD+3)
         IF (IGD1.EQ.0) THEN
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
         VALK (1) = 'GRANDEUR LIGNE REFERENCEE PAR'
         VALK (2) = NOMGD//' NULLE'
         VALI (1) = GD
            CALL U2MESG('F', 'CALCULEL6_17',2,VALK,1,VALI,0,0.D0)
         END IF
         IGD2 = ZI(IDDGD+4)
         IF (IGD2.EQ.0) THEN
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
         VALK (1) = 'GRANDEUR COLONNE REFERENCEE PAR'
         VALK (2) = NOMGD//' NULLE'
         VALI (1) = GD
            CALL U2MESG('F', 'CALCULEL6_17',2,VALK,1,VALI,0,0.D0)
         END IF
         IF (IGD1.NE.IGD2) THEN
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',GD),NOMGD)
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',IGD1),NOMGD1)
            CALL JENUNO(JEXNUM('&CATA.GD.DESCRIGD',IGD2),NOMGD2)
         VALI (1) = IGD1
         VALI (2) = IGD2
         VALI (3) = GD
         VALK (1) = NOMGD1//' /= '
         VALK (2) = NOMGD2
         VALK (3) = NOMGD
            CALL U2MESG('F', 'CALCULEL6_20',3,VALK,3,VALI,0,0.D0)
         END IF
         CALL JELIRA(JEXNUM('&CATA.GD.NOMCMP',IGD1),'LONMAX',NBCMP,CBID)
         GO TO 9999
      ELSE
         VALI (1) = GD
         VALI (2) = CODE
         CALL U2MESG('F', 'CALCULEL6_21',0,' ',2,VALI,0,0.D0)
      END IF
 9999 CONTINUE
      CALL JEDEMA()
      END
