      CHARACTER*8 LPAIN(10),LPAOUT(1)
      CHARACTER*16 OPTION
      CHARACTER*24 LCHIN(10),LCHOUT(1),CHGEOM
      LOGICAL EXIGEO

C DEB-------------------------------------------------------------------

      OPTION = OPTIOZ
      CALL MEGEOM(MODELE,'        ',EXIGEO,CHGEOM)


      LCHOUT(1) = CHAMEL

      IF (OPTION.EQ.'PRAC_ELNO') THEN
        LPAOUT(1) = 'PPRAC_R'
        LPAIN(1) = 'PPRESSC'
        LCHIN(1) = PRESS
        CALL CALCUL('S',OPTION,LIGREL,1,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'G',
     &                 'OUI')

      ELSE IF (OPTION.EQ.'INTE_ELNO') THEN
        LPAOUT(1) = 'PINTER'
        LPAIN(1) = 'PPRESSC'
        LCHIN(1) = PRESS
        LPAIN(2) = 'PGEOMER'
        LCHIN(2) = CHGEOM
        LPAIN(3) = 'PMATERC'
        LCHIN(3) = MATE
        CALL CALCUL('S',OPTION,LIGREL,3,LCHIN,LPAIN,1,LCHOUT,LPAOUT,'G',
     &                 'OUI')
      END IF

      END
