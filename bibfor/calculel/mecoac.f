C     - FONCTION REALISEE:  CALCUL DES PRESSIONS EN DECIBELS
C                                  DES INTENSITES ACOUS. ACTIVE,REACTIVE

C     - ARGUMENTS:
C        DONNEES:      OPTIOZ       --> OPTION DE CALCUL
C                      MODELE       --> MODELE
C                      PRESS        --> CHAMNO(PRESS)
C                      MATE         --> CARTE DE MATERIAU CODE
C      RESULTATS:      CHAMEL       <-- CHAMELEM( ) DBEL_R PRES_C ...
C                                                   INTE_R
