      SUBROUTINE PJCORR(NOMO2,CHBID,CNS1Z,BASEZ,CES2Z,
     &     LIGREL,CORRES,NBVARI,OPTION,NOMPAR,IRET)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF CALCULEL  DATE 14/09/2010   AUTEUR BERARD A.BERARD 
C ======================================================================
C COPYRIGHT (C) 1991 - 2010  EDF R&D                  WWW.CODE-ASTER.ORG
