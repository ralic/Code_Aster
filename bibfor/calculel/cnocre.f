C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT NONE
      CHARACTER*(*) MAZ,NOMGDZ,CNOZ,BASEZ
      INTEGER NCMPZ
      CHARACTER*(*) LICMP(NCMPZ)
C ------------------------------------------------------------------
C BUT : CREER UN CHAM_NO A VALEURS NULLES
C ------------------------------------------------------------------
C     ARGUMENTS:
C MAZ     IN/JXIN  K8  : MAILLAGE DE CNOZ
C NOMGDZ  IN       K8  : NOM DE LA GRANDEUR DE CNOZ
C NCMPZ   IN       I   : NOMBRE DE CMPS VOULUES DANS CNOZ
C LICMP   IN       L_K8: NOMS DES CMPS VOULUES DANS CNOZ
C BASEZ   IN       K1  : BASE DE CREATION POUR CNOZ : G/V/L
C CNOZ    IN/JXOUT K19 : SD CHAM_NO A CREER
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR
C     ------------------------------------------------------------------
C     VARIABLES LOCALES:
C     ------------------
      CHARACTER*3 TSCA
      CHARACTER*8 NOMGD
      CHARACTER*19 CNS
      INTEGER IBID,NBNO,JCNSK,JCNSD,INO
      INTEGER K,JCNSL,JCNSV,NCMP
C     ------------------------------------------------------------------

      CALL JEMARQ()

      CNS =  '&&CNOCRE.CNS'
      CALL CNSCRE(MAZ,NOMGDZ,NCMPZ,LICMP,'V',CNS)

      CALL JEVEUO(CNS//'.CNSK','L',JCNSK)
      CALL JEVEUO(CNS//'.CNSD','L',JCNSD)
      CALL JEVEUO(CNS//'.CNSV','L',JCNSV)
      CALL JEVEUO(CNS//'.CNSL','L',JCNSL)

      NOMGD = ZK8(JCNSK-1+2)
      NBNO  = ZI(JCNSD-1+1)
      NCMP  = ZI(JCNSD-1+2)

      CALL DISMOI('F','TYPE_SCA',NOMGD,'GRANDEUR',IBID,TSCA,IBID)
      IF (TSCA.NE.'R') CALL UTMESS('F','CNOCRE',
     &    'SEULEMENT REEL SVP')

      DO 30,K = 1,NCMP
        DO 10,INO = 1,NBNO
          ZL(JCNSL-1+ (INO-1)*NCMP+K) = .TRUE.
          ZR(JCNSV-1+ (INO-1)*NCMP+K) = 0.0D0
   10   CONTINUE
   30 CONTINUE

      CALL CNSCNO(CNS,' ',BASEZ,CNOZ)
      CALL DETRSD('CHAM_NO_S',CNS)

      CALL JEDEMA()
      END
