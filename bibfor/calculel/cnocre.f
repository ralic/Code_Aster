      SUBROUTINE CNOCRE(MAZ,NOMGDZ,NBNOZ,LINOE,NCMPZ,LICMP,CNOCMP,BASEZ,
     &                  PROF,CNOZ)
C RESPONSABLE VABHHTS J.PELLET
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF CALCULEL  DATE 26/04/2011   AUTEUR COURTOIS M.COURTOIS 
C ======================================================================
