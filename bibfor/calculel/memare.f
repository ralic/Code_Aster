C      MATEL (K8)  : NOM DU MATR_ELEM (OU VECT_ELEM)
C      MODELE(K8)  : NOM DU MODELE SUPPORTANT LE MATR_ELEM
C      MATE  (K8)  : NOM DU CHAM_MATER ASSOCIE
