      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
      CHARACTER*32       JEXNUM , JEXNOM , JEXR8 , JEXATR
C     ----- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C
      CHARACTER*1  BAS2
      CHARACTER*8  MATE2, MODE2
C
      CALL JEMARQ()
      BAS2  = BASE
      MATE2 = MATEL
      MODE2 = MODELE
      IF ( MODE2 .EQ. '        ' ) THEN
         CALL U2MESS('F','CALCULEL3_51')
      ENDIF
C
      CALL JEEXIN(MATE2//'.REFE_RESU',IRET)
      IF (IRET.GT.0) CALL JEDETR(MATE2//'.REFE_RESU')
      CALL WKVECT(MATE2//'.REFE_RESU',BAS2//' V K24',5,IAREFE)
      ZK24(IAREFE-1+1) = MODELE
      ZK24(IAREFE-1+2) = SUROPT
      ZK24(IAREFE-1+3) = 'NON_SOUS_STRUC'
      ZK24(IAREFE-1+4) = MATE
      ZK24(IAREFE-1+5) = CARA
C
      CALL JEDEMA()
      END
