C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C RESPONSABLE                            VABHHTS J.PELLET
      IMPLICIT REAL*8 (A-H,O-Z)

C     ARGUMENTS:
C     ----------
      INTEGER OPT,IEL,NOUT,TE
      CHARACTER*19 LCHOUZ
      CHARACTER*(*) LCHOUT(*)
      CHARACTER*8 LPAOUT(*)
C ----------------------------------------------------------------------
C     ENTREES:
C     OPT    : OPTION DE CALCUL
C     TE     : TYPE_ELEMENT
C     IGR    : NUMERO DU GREL DONT ON SAUVE LES CHAMPS LOCAUX (COMMON)

C     SORTIES:
C     MET A JOUR LES CHAMPS GLOBAUX DE SORTIE DE L OPTION OPT
C ----------------------------------------------------------------------
      COMMON /CAII02/IAOPTT,LGCO,IAOPMO,ILOPMO,IAOPNO,ILOPNO,IAOPDS,
     +       IAOPPA,NPARIO,NPARIN,IAMLOC,ILMLOC,IADSGD
      INTEGER        IAWLOC,IAWTYP,NBELGR,IGR,JCTEAT,LCTEAT
      COMMON /CAII06/IAWLOC,IAWTYP,NBELGR,IGR,JCTEAT,LCTEAT
      COMMON /CAII08/IEL
      COMMON /CAII07/IACHOI,IACHOK

C     FONCTIONS EXTERNES:
C     -------------------
      INTEGER NBPARA,GRDEUR,INDIK8,DIGDE2,MODATT
      CHARACTER*8 NOPARA
      CHARACTER*32 JEXNOM,JEXNUM

C     VARIABLES LOCALES:
C     ------------------
      INTEGER IPAR,NP,MOD1,JPAR,GD,IFETI,IRET,IIEL,IAUX1,IAUX2,IAUX0
      INTEGER DESCGD,JCELD
      INTEGER CODE,DEBUGR,NCMPEL,ID1,ID2
      CHARACTER*8 NOMPAR,TYPSCA
      LOGICAL     LFETI
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80

      CALL JEMARQ()

C FETI PARALLELE OR NOT ?
      CALL JEEXIN('&CALCUL.FETI.NUMSD',IRET)
      IF (IRET.NE.0) THEN
        LFETI=.TRUE.
        CALL JEVEUO('&CALCUL.FETI.NUMSD','L',IFETI)
        IFETI=IFETI-1
      ELSE
        LFETI=.FALSE.
      ENDIF

      NP = NBPARA(OPT,TE,'OUT')
      DO 10 IPAR = 1,NP
        NOMPAR = NOPARA(OPT,TE,'OUT',IPAR)
        IPARG = INDIK8(ZK8(IAOPPA),NOMPAR,1,NPARIO)
        IAD1 = ZI(IAWLOC-1+7* (IPARG-1)+1)
        IF (IAD1.EQ.-1) GO TO 10

        GD = GRDEUR(NOMPAR)

        MOD1 = MODATT(OPT,TE,'OUT',IPAR)
        JPAR = INDIK8(LPAOUT,NOMPAR,1,NOUT)


        DESCGD = IADSGD + 7* (GD-1)
        CODE = ZI(DESCGD-1+1)

        TYPSCA = ZK8(IAWTYP-1+IPARG)

        IF (CODE.EQ.1) THEN
C           CAS : CHAM_ELEM
          JCELD = ZI(IACHOI-1+2* (JPAR-1)+1)
          DEBUGR = ZI(JCELD-1+ZI(JCELD-1+4+IGR)+8)
          LGGREL = ZI(JCELD-1+ZI(JCELD-1+4+IGR)+4)
          IARESU = ZI(IACHOI-1+2* (JPAR-1)+2)
          CALL JACOPO(LGGREL,TYPSCA,IAD1,IARESU-1+DEBUGR)
        ELSE
C           CAS : RESUELEM
          NCMPEL = DIGDE2(MOD1)
          LCHOUZ = LCHOUT(JPAR)
          CALL JEVEUO(JEXNUM(LCHOUZ//'.RESL',IGR),'E',IARESU)
C      -- SI FETI, LA MAILLE IIEL EST ELLE CONCERNEE PAR LE PROC COURANT
          IF (LFETI) THEN
            DO 8 IIEL=1,NBELGR
              IF (ZL(IFETI+IIEL)) THEN
                IAUX0=(IIEL-1)*NCMPEL
                IAUX1=IAD1+IAUX0
                IAUX2=IARESU+IAUX0
                CALL JACOPO(NCMPEL,TYPSCA,IAUX1,IAUX2)
              ENDIF
    8       CONTINUE
          ELSE
            CALL JACOPO(NBELGR*NCMPEL,TYPSCA,IAD1,IARESU)
          ENDIF
          CALL JELIBE(JEXNUM(LCHOUZ//'.RESL',IGR))
        END IF
   10 CONTINUE
   20 CONTINUE
      CALL JEDEMA()
      END
