C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C     ENTREES:
C        NOMOBJ : NOM DE L'OBJET JEVEUX QUE L'ON VEUT TESTER
C        TYPE   : TYPE DE VALEUR A TESTER :
C                  /'RESUME' : ENTIER QUI RESUME L'OBJET
C                  /'S_I'    : SOMME ENTIERE DE L'OBJET
C                  /'S_R'    : SOMME REELLE DE L'OBJET
C        REFI   : VALEUR ENTIERE ATTENDUE POUR L'OBJET
C        REFR   : VALEUR REELLE ATTENDUE POUR L'OBJET
C        CRIT   : 'RELATIF' OU 'ABSOLU'(PRECISION RELATIVE OU ABSOLUE).
C        EPSI   : PRECISION ESPEREE
C        IFIC   : NUMERO LOGIQUE DU FICHIER DE SORTIE
C     SORTIES:
C      LISTING ...
C ----------------------------------------------------------------------
C     VARIABLES LOCALES:
      CHARACTER*3 TYSC
      COMPLEX*16 CBID
      REAL*8 RBID,SOMMR
      INTEGER IBID,RESUME,SOMMI,LONUTI,LONMAX,NI
C
      CALL TSTOBJ(NOMOBJ,'NON',RESUME,SOMMI,SOMMR,LONUTI,LONMAX,TYSC,
     &            IRET,NI)
      IF (IRET.EQ.0) THEN
        IF (TYPE.EQ.'RESUME') THEN
          CALL UTITES(TYPE,' ','I',REFI,RBID,CBID,RESUME,RBID,CBID,EPSI,
     +                CRIT,IFIC)
        ELSE IF (TYPE.EQ.'S_I') THEN
          CALL UTITES(TYPE,' ','I',REFI,RBID,CBID,SOMMI,RBID,CBID,EPSI,
     +                CRIT,IFIC)
        ELSE IF (TYPE.EQ.'S_R') THEN
          CALL UTITES(TYPE,' ','R',IBID,REFR,CBID,IBID,SOMMR,CBID,EPSI,
     +                CRIT,IFIC)
        END IF
      ELSE
        CALL JEEXIN(NOMOBJ,IRET2)
        IF (IRET2.LE.0) THEN
          WRITE (IFIC,*) ' NOOK OBJET:'//NOMOBJ//' INEXISTANT.'
        ELSE
          WRITE (IFIC,*) ' NOOK OBJET:'//NOMOBJ//' NON TESTABLE.'
        END IF
      END IF
C
      END
