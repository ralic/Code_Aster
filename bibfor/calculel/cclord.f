      SUBROUTINE CCLORD(NUOPLO,NBORDR,LISORD,NOBASE,OPTDEM,
     &                  MINORD,MAXORD,RESUIN,RESUOU,LISOUT)
      IMPLICIT NONE
C     --- ARGUMENTS ---
      LOGICAL      OPTDEM
      INTEGER      NUOPLO,NBORDR,MINORD,MAXORD
      CHARACTER*8  RESUIN,RESUOU,NOBASE
      CHARACTER*19 LISORD
      CHARACTER*24 LISOUT
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF CALCULEL  DATE 15/11/2011   AUTEUR DELMAS J.DELMAS 
