      CHARACTER*2     BASE
      CHARACTER*(*)   NU,TLIMAT(*) ,METHOD
      INTEGER                   NBMAT
C ----------------------------------------------------------------------
C --- DESCRIPTION DES PARAMETRES
C OUT K*14 NU     : LE CHAMP .NUME DE S.D. NUME_DDL_EQUA DE L'OBJET NU
C                   DE S.D. NUME_DDL EST CREE ET REMPLI.
C IN  K*14 NU     : NOM D'UN NUMERO_DDL(SI UN OBJET DE NOM NU ET
C                   DE S.D. NUME_DDL EXISTE ON L'ECRASE)
C IN  K2   BASE    : BASE(1:1) : BASE POUR CREER LE NUME_DDL
C                    (SAUF LE PROF_CHNO)
C                  : BASE(2:2) : BASE POUR CREER LE PROF_CHNO
C IN  I    NBMAT  : NOMBRE DE MATELE PASSES DANS TLIMAT
C IN  K19  TLIMAT : LISTE DES MATELE DEFINISSANT LA NUMEROTATION
C IN  K*4  METHOD : METHODE DE RENUMEROTATION DES NOEUDS:
C                   'SANS' --> ON NE RENUMEROTE PAS
C                   'RCMK' --> 'REVERSE CUTHIL MAC-KEE'
