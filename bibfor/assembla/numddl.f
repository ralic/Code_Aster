      INTEGER IBID
      CHARACTER*19 K19BID
      CHARACTER*24 TLIMA2(NBMAT)
C
C------RECUPERATION DU NIVEAU D'IMPRESSION
      CALL INFNIV(IFM,NIV)

      NUPGM = NU
      K19BID = ' '
C
      DO 10 I=1,NBMAT
        TLIMA2(I)=TLIMAT(I)
 10   CONTINUE
C---- CREATION D'UNE LISTE DE LIGRELS A PARTIR DE LA LISTE DE MATR_ELEM
C
      CALL NUMOCH(TLIMA2,NBMAT,'V',NUPGM//'.&LMODCHAR')
      CALL NUEFFE(NUPGM//'.&LMODCHAR',BASE,NUPGM,METHOD,' ',
     &  K19BID,IBID)
      CALL JEDETC(' ',NUPGM//'.&LMODCHAR',1)
C
      END
