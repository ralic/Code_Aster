      SUBROUTINE ASMATR(NBMAT,TLIMAT,LICOEF,NU,SOLVEU,INFCHA,MOTCLE,
     &                  BASE,TYPE,MATAS)
C MODIF ASSEMBLA  DATE 11/07/2005   AUTEUR LAMARCHE S.LAMARCHE 
C RESPONSABLE VABHHTS J.PELLET
C            CONFIGURATION MANAGEMENT OF EDF VERSION
