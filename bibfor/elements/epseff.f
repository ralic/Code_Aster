      CHARACTER*6 APPLIC
      INTEGER     NB1
      REAL*8      BTILD(5,*),DEPL(*),EPSI(*),SGMTD(*),EFFINB(42)
      REAL*8      WGT,EFFINT(*)
C
C-----------------------------------------------------------------------
      INTEGER I ,K ,NDDLE 
C-----------------------------------------------------------------------
      NDDLE=5*NB1+2
      IF (APPLIC .EQ. 'DEFORM') THEN
C
C     CALCULS DES COMPOSANTES DE DEFORMATIONS TRIDIMENSIONNELLES :
C     EPSXX, EPSYY, EPSXY, EPSXZ, EPSYZ (CE SONT LES COMPOSANTES TILDE)
C
      DO 10 I=1,5
         EPSI(I)=0.D0
      DO 20 K=1,NDDLE
         EPSI(I)=EPSI(I)+BTILD(I,K)*DEPL(K)
 20   CONTINUE
 10   CONTINUE
C
      ELSE IF (APPLIC .EQ. 'EFFORI') THEN
C
C     CALCULS DES EFFORTS INTERIEURS
C
      DO 30 I=1,NDDLE
         EFFINB(I)=0.D0
      DO 40 K=1,5
         EFFINB(I)=EFFINB(I)+BTILD(K,I)*SGMTD(K)
 40   CONTINUE
         EFFINT(I)=EFFINT(I)+WGT*EFFINB(I)
 30   CONTINUE
C
      ENDIF
      END
