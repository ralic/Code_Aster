C*     -----------------------------------------------------------------
C*      CALCUL DE LA MATRICE B AU POINT D INTEGRATION DONNE
C*
C*                                                    V-D.TRINH     2006
C*     -----------------------------------------------------------------
C*
C*  ENTREES :
C*     XNOE(45)     : COORDONNEES DES NOEUDS
C*     IPINT        : NO DU POINT D INTEGRATION
C*     MOT          : NOM DE L ELEMENT FINI
C*  SORTIE
C*     B(NBLIB,NBN) : MATRICE B
C*     AJAC         : JACOBIEN
C*
      IMPLICIT NONE
C*
      REAL *8 XNOE(45),BKSIP(3,15)
      REAL *8 B(3,15),AJAC
C*
C*----   VARIABLES LOCALES
C*
      REAL *8 DJ(3,3),UJ(3,3)
      INTEGER  I,J,K
C*
C*---   DJ = BKSIP * TRANSPOSE(XNOE)
C*
      CALL MATINI(3,3,0.D0,DJ)
C
      DO 50 I=1,3
        DO 40 J=1,3
           DO 30 K=1,15
             DJ(J,I)=DJ(J,I)+BKSIP(J,K)*XNOE((K-1)*3+I)
   30      CONTINUE
   40   CONTINUE
   50 CONTINUE
C
C-----   UJ(J,I)  MATRICE INVERSE DE DJ(J,I)
C
      CALL MATINV('S',3,DJ,UJ,AJAC)
C TEST SI ELEMENT TROP DEFORME: CROISEMENT
      AJAC=ABS(AJAC)
C*
C*-----   MATRICE ( B ) = UJ * BKSIP
C*
      CALL MATINI(3,15,0.D0,B)
C
      DO 100 K=1,3
        DO 90 J=1,3
          DO 80 I=1,15
            B(J,I)=B(J,I)+UJ(J,K)*BKSIP(K,I)
   80     CONTINUE
   90   CONTINUE
  100 CONTINUE
C*
      END
