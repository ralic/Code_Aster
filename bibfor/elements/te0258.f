      IMPLICIT NONE

C     BUT: CALCUL DES MATRICES ELEMENTAIRES EN MECANIQUE
C          CORRESPONDANT A UNE IMPEDANCE IMPOSEE
C          SUR DES FACES D'ELEMENTS ISOPARAMETRIQUES 1D

C          OPTION : 'IMPE_MECA'

C     ENTREES  ---> OPTION : OPTION DE CALCUL
C          ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................

      INCLUDE 'jeveux.h'
      INTEGER ICODRE
      CHARACTER*8 FAMI,POUM
      CHARACTER*16 NOMTE,OPTION
      REAL*8 NX,NY,POIDS,RHO,RHO2
      INTEGER IPOIDS,IVF,IDFDE,IGEOM,IMATE
      INTEGER NDI,NNO,KP,NPG,IMATUU,IIMPE
      INTEGER LDEC,KPG,SPT
      LOGICAL LAXI, LTEATT


C-----------------------------------------------------------------------
      INTEGER I ,II ,IJ ,J ,JGANO ,JJ ,NDIM 
      INTEGER NNOS 
      REAL*8 R ,R8B 
C-----------------------------------------------------------------------
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)
      NDI = NNO* (2*NNO+1)

      LAXI = .FALSE.
      IF (LTEATT(' ','AXIS','OUI')) LAXI = .TRUE.

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PIMPEDR','L',IIMPE)
      CALL JEVECH('PMATUUR','E',IMATUU)

      FAMI='FPG1'
      KPG=1
      SPT=1
      POUM='+'

      CALL RCVALB(FAMI,KPG,SPT,POUM,ZI(IMATE),' ','FLUIDE',0,' ',
     &            R8B,1,'RHO',RHO,   ICODRE,1)

C --- INITIALISATION DE LA MATRICE D'IMPEDANCE
      DO 10 I = 1,NDI
        ZR(IMATUU+I-1) = 0.D0
   10 CONTINUE

      IF (ZR(IIMPE).EQ.0.D0) THEN
        GO TO 60
      ELSE

C        BOUCLE SUR LES POINTS DE GAUSS
        RHO2 = -RHO*RHO

        DO 50 KP = 1,NPG

          LDEC = (KP-1)*NNO

          NX = 0.0D0
          NY = 0.0D0

          CALL VFF2DN(NDIM,NNO,KP,IPOIDS,IDFDE,ZR(IGEOM),NX,NY,POIDS)
C%
          IF (LAXI) THEN
            R = 0.D0
            DO 20 I = 1,NNO
              R = R + ZR(IGEOM+2* (I-1))*ZR(IVF+LDEC+I-1)
   20       CONTINUE
            POIDS = POIDS*R
          END IF
C%
          DO 40 I = 1,NNO

            DO 30 J = 1,I
              II = 2*I
              JJ = 2*J
              IJ = (II-1)*II/2 + JJ

              ZR(IMATUU+IJ-1) = ZR(IMATUU+IJ-1) +
     &                          POIDS*RHO2/ZR(IIMPE+KP-1)*
     &                          ZR(IVF+LDEC+I-1)*ZR(IVF+LDEC+J-1)

   30       CONTINUE
   40     CONTINUE

   50   CONTINUE
      END IF
   60 CONTINUE
      END
