C FONCTION: POUR UN ELEMENT DE POUTRE EN GRAND DEPLACEMENT, CALCULE LA
C           CONTRIBUTION DU NOEUD NE A LA MATRICE UPSILON AU POINT DE
C           GAUSS KP. CETTE MATRICE UPSILON INTERVIENT DANS LA RIGIDITE
C           GEOMETRIQUE COMME LA MATRICE DE DEFORMATION B INTERVIENT
C           DANS LA RIGIDITE MATERIELLE.
C
C     IN  : NE        : NUMERO DU NOEUD
C           KP        : NUMERO DU POINT DE GAUSS
C           AJACOB    : JACOBIEN
C           EN        : FONCTIONS DE FORME
C           ENPRIM    : DERIVEES DES FONCTIONS DE FORME
C
C     OUT : UPS       : MATRICE 9*6
C ------------------------------------------------------------------
      IMPLICIT NONE
      REAL*8 EN(3,2),ENPRIM(3,2),UPS(9,6)
C
C-----------------------------------------------------------------------
      INTEGER I ,J ,KP ,NE 
      REAL*8 AJACOB ,FORM ,FORMPR ,UN ,UNSURJ ,ZERO 
C-----------------------------------------------------------------------
      ZERO = 0.D0
      UN   = 1.D0
      DO 2 J=1,6
      DO 1 I=1,9
      UPS(I,J) = ZERO
    1 CONTINUE
    2 CONTINUE
      UNSURJ = UN / AJACOB
      FORM   = EN(NE,KP)
      FORMPR = UNSURJ * ENPRIM(NE,KP)
      DO 3 I=1,6
      UPS(I,I) = FORMPR
    3 CONTINUE
      DO 4 I=1,3
      UPS(6+I,3+I) = FORM
    4 CONTINUE
      END
