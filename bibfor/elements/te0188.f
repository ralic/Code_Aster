C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE   
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C.......................................................................
C
C     BUT: CALCUL DU VECTEUR INTENSITE REACTIVE AUX NOEUDS
C          ELEMENTS ISOPARAMETRIQUES 3D
C
C          OPTION : 'INTE_ELNO_REAC'
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C          ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................
C
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*8        ELREFE
      CHARACTER*2        CODRET
      CHARACTER*16       NOMTE,OPTION
      CHARACTER*24       CHVAL,CHCTE
C
      REAL*8             OMEGA
      REAL*8             OMERHO, PI
      COMPLEX*16         VITX(27),VITY(27),VITZ(27)
C
      REAL*8             DFDX(27),DFDY(27),DFDZ(27),JAC
      INTEGER            IDFDE,IDFDN,IDFDK,IGEOM,IDINO,IPINO
C
      INTEGER            IINTE,IPRES,IMATE,IFREQ
      INTEGER            NNO,INO,I,J
C
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      COMMON /NOMAJE/PGC
      CHARACTER*6 PGC
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
C
      CALL ELREF1(ELREFE)
      CALL JEMARQ()
C
      CHCTE = '&INEL.'//ELREFE//'.CARACTE'
      CALL JEVETE(CHCTE,' ',JIN)
      NNO = ZI(JIN+2-1)
C
      CHVAL = '&INEL.'//ELREFE//'.FFORMES'
      CALL JEVETE(CHVAL,' ',JVAL)
C
      IVF    = JVAL
      IDFDE  = IVF    + NNO*NNO
      IDFDN  = IDFDE  + 1
      IDFDK  = IDFDN  + 1
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PINTERE','E',IINTE)
      CALL JEVECH('PPRESSC','L',IPRES)
      CALL JEVECH('PMATERC','L',IMATE)
C
      MATER= ZI(IMATE)
      CALL RCVALA(MATER,'FLUIDE',0,' ',R8B,1,'RHO',RHO,CODRET,'FM')
C
      PI = R8PI()
      CALL JEVEUO('FREQ.VALE','L',IFREQ)
      OMEGA = 2.D0* PI * ZR(IFREQ)
      OMERHO = OMEGA*RHO
C
       DO 112 I=1,3*NNO
         ZR(IINTE + I -1) = 0.0D0
112    CONTINUE
C
C
C    BOUCLE SUR LES NOEUDS
C
C
      POIDS=1.D0
      DO 101 INO = 1,NNO
C
        IDINO = IINTE + (INO-1)* 3 -1
        IPINO = IPRES + INO -1
        K = (INO-1)*3*NNO
        CALL DFDM3D ( NNO, POIDS,ZR(IDFDE+K),ZR(IDFDN+K),
     &                ZR(IDFDK+K),ZR(IGEOM),DFDX,DFDY,DFDZ,JAC   )
C
        VITX(INO)= (0.0D0 ,0.0D0 )
        VITY(INO)= (0.0D0 ,0.0D0 )
        VITZ(INO)= (0.0D0 ,0.0D0 )
C
        DO 106 I=1 , NNO
C
          VITX(INO) = VITX(INO) + DFDX(I) * ZC(IPRES+I-1)
          VITY(INO) = VITY(INO) + DFDY(I) * ZC(IPRES+I-1)
          VITZ(INO) = VITZ(INO) + DFDZ(I) * ZC(IPRES+I-1)
106     CONTINUE
C
        VITX(INO) = VITX(INO) *(0.D0,1.D0)/OMERHO
        VITY(INO) = VITY(INO) *(0.D0,1.D0)/OMERHO
        VITZ(INO) = VITZ(INO) *(0.D0,1.D0)/OMERHO
C
        ZR(IDINO+1) = 0.5D0* DIMAG(ZC(IPINO)*DCONJG(VITX(INO)))
C
        ZR(IDINO+2) = 0.5D0* DIMAG(ZC(IPINO)*DCONJG(VITY(INO)))
C
        ZR(IDINO+3) = 0.5D0* DIMAG(ZC(IPINO)*DCONJG(VITZ(INO)))
101   CONTINUE
C
      CALL JEDEMA()
      END
