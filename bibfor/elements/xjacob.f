C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C RESPONSABLE GENIAUT S.GENIAUT
C ----------------------------------------------------------------------
C FONCTION REALISEE:  CALCUL DE L'INVERSE DE LA JACONIENNE EN XE
C                        
C    ENTREE:
C        NNO    :  NOMBRE DE NOEUDS DE L'ELEMENT
C        DFF    :  DÉRIVÉES DES FONCTION DES FORMES AU POINT XE
C        IGEOM  :  COORDONNÉES DES NOEUDS DE L'ÉLÉMENT
C    SORTIE:
C        INVJAC : INVERSE DE LA JACONIENNE AU POINT XE
C.......................................................................
C
      INTEGER       I,J,K
      REAL*8        JACOBI(3,3)

C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------

      CALL JEMARQ()

C     JACOBIENNE EN XE
      CALL MATINI(3,3,0.D0,JACOBI)
      DO 100 I=1,3
        DO 110 J=1,3
          DO 120 K=1,NNO
            JACOBI(I,J) = JACOBI(I,J) + DFF(J,K) * ZR(IGEOM-1+3*(K-1)+I)
 120       CONTINUE
 110     CONTINUE
 100   CONTINUE

C     INVERSE DE LA JACOBIENNE
      CALL MATINV(3,JACOBI,INVJAC)  
           
      CALL JEDEMA()
      END
