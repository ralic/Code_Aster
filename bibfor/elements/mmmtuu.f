     &                  MPROJN,MPROJT,HPG   ,FFE   ,FFM   ,
     &                  JACOBI,COEFCP,COEFCR,COEFFP,COEFFF,
     &                  RESE  ,NRESE ,LAMBDA,COEFFR,JEU   ,
     &                  ASPERI,KAPPAN,KAPPAV,DELTAT,BETA  ,
     &                  GAMMA ,CWEAR ,DISSIP,DLAGRC,DELUSU,
     &                  MATREE,MATRMM,MATREM,MATRME)
