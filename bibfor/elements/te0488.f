C ----------------------------------------------------------------------
C     CALCUL DES COORDONNEES DES POINTS DE GAUSS
C     POUR LES ELEMENTS ISOPARAMETRIQUES 3D

C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------------------

      CHARACTER*32 JEXNUM,JEXNOM,JEXR8,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)

C --- FIN DECLARATIONS NORMALISEES JEVEUX ------------------------------

      INTEGER JGANO,NNO,KP
      INTEGER IPOIDS,IVF,IGEOM
      INTEGER NPG,NPG1,NNOS
      INTEGER ICOPG,INO,NDIM,JVAL,IDFDE

      REAL*8 XX,YY,ZZ
C DEB ------------------------------------------------------------------

      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PCOORPG','E',ICOPG)

      DO 20 KP = 1,NPG
        XX = 0.D0
        YY = 0.D0
        ZZ = 0.D0
        DO 10 INO = 1,NNO
          XX = XX + ZR(IGEOM+3* (INO-1)+0)*ZR(IVF+ (KP-1)*NNO+INO-1)
          YY = YY + ZR(IGEOM+3* (INO-1)+1)*ZR(IVF+ (KP-1)*NNO+INO-1)
          ZZ = ZZ + ZR(IGEOM+3* (INO-1)+2)*ZR(IVF+ (KP-1)*NNO+INO-1)
   10   CONTINUE
        ZR(ICOPG+3* (KP-1)+0) = XX
        ZR(ICOPG+3* (KP-1)+1) = YY
        ZR(ICOPG+3* (KP-1)+2) = ZZ
   20 CONTINUE

      END
