C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C
      IMPLICIT   NONE
      CHARACTER*16 OPTION,NOMTE
C ----------------------------------------------------------------------
C  XFEM GRANDS GLISSEMENTS
C  REACTUALISATION DES GEOMETRIES DES FACETTES DE CONTACT
C  (MAITRE ET ESCLAVE)
C
C  OPTION : 'GEOM_FAC' (X-FEM GEOMETRIE DES FACETTES DE CONTACT)
C
C  ENTREES  ---> OPTION : OPTION DE CALCUL
C           ---> NOMTE  : NOM DU TYPE ELEMENT
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX --------------------
C
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR ,DDOT
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX --------------------
C
      CHARACTER*8   TYPMA
      INTEGER       IGEOM,JDEPL,JPINT,JLON,JGEO
      INTEGER       JGES,JGMA
      INTEGER       NDIM,IBID,NINTER
      INTEGER       I,J,IPT,NNO
      REAL*8        PTREF(3),DEPLE(3),DEPLM(3),FF(8)
C
C ---------------------------------------------------------------------

      CALL JEMARQ()
C
C --- RECUPERATION DU TYPE DE MAILLE, DE SA DIMENSION
C --- ET DE SON NOMBRE DE NOEUDS
C
      CALL ELREF1(TYPMA)
      CALL ELREF4(' ','RIGI',NDIM,NNO,IBID,IBID,IBID,IBID,IBID,IBID)
C
C --- RECUPERATION DES ENTRÉES / SORTIE
C
      CALL JEVECH('PDEPLA','L',JDEPL)
      CALL JEVECH('PPINTER','L',JPINT)
      CALL JEVECH('PLONCHA','L',JLON)
C --- UN DES DEUX CHAMPS SUIVANT NE SERT A RIEN,
C --- LES GEOMETRIES MAITRES ET ESCLAVES INITIALES SONT
C --- ET RESTENT LES MEMES
C      CALL JEVECH('PGESCLO','L',JGESO)
C      CALL JEVECH('PGMAITO','L',JGMAO)
C
C --- DONC ON REMPLACE ICI LES 2 LIGNES PRECEDENTES PAR :
      CALL JEVECH('PGESCLO','L',JGEO)
C
      CALL JEVECH('PNEWGES','E',JGES)
      CALL JEVECH('PNEWGEM','E',JGMA)
C
C --- RECUPERATION DU NOMBRE DES POINTS D'INTERSECTION
C
      NINTER=ZI(JLON-1+1)
C    
C --- BOUCLE SUR LES POINTS D'INTERSECTION
C
      DO 100 IPT=1,NINTER
        CALL VECINI(NDIM,0.D0,DEPLE)
        CALL VECINI(NDIM,0.D0,DEPLM)
        DO 110 I=1,NDIM
C
C --- RECUPERATION DES COORDONNEES DE REFERENCE DU POINT D'INTERSECTION
C 
          PTREF(I)=ZR(JPINT-1+NDIM*(IPT-1)+I)
 110    CONTINUE
C
C --- CALCUL DES FONCTIONS DE FORMES DU POINT D'INTERSECTION
C
        CALL ELRFVF(TYPMA,PTREF,NNO,FF,NNO)
C
C --- CALCUL DES DEPLACEMENTS MAITRES ET ESCLAVES
C --- DU POINT D'INTERSECTION
C
        DO 120 I=1,NNO
          DO 130 J=1,NDIM
            DEPLM(J)=DEPLM(J)+FF(I)*ZR(JDEPL-1+3*NDIM*(I-1)+J)
     &                       +FF(I)*ZR(JDEPL-1+3*NDIM*(I-1)+J+NDIM)
            DEPLE(J)=DEPLE(J)+FF(I)*ZR(JDEPL-1+3*NDIM*(I-1)+J)
     &                       -FF(I)*ZR(JDEPL-1+3*NDIM*(I-1)+J+NDIM)
 130      CONTINUE
 120    CONTINUE
C
C --- CALCUL DES NOUVELLES COORDONNEES DES POINTS D'INTERSECTIONS
C --- MAITRES ET ESCLAVES, ON FAIT :
C --- NOUVELLES COORDONNEES = ANCIENNES COORDONEES + DEPLACEMENT
C
        DO 140 I=1,NDIM
          ZR(JGES-1+NDIM*(IPT-1)+I)=ZR(JGEO-1+NDIM*(IPT-1)+I)+DEPLE(I)
          ZR(JGMA-1+NDIM*(IPT-1)+I)=ZR(JGEO-1+NDIM*(IPT-1)+I)+DEPLM(I)
 140    CONTINUE
 100  CONTINUE
C
      CALL JEDEMA()
      END
