      IMPLICIT NONE
      INCLUDE 'jeveux.h'
      CHARACTER*16 OPTION,NOMTE
C ......................................................................
C    - FONCTION REALISEE:  CALCUL DES MATRICES ELEMENTAIRES
C                          OPTION : 'RIGI_THER_COEF_R'
C    - ARGUMENTS:
C        DONNEES:      OPTION       -->  OPTION DE CALCUL
C                      NOMTE        -->  NOM DU TYPE ELEMENT
C ......................................................................

      REAL*8 POIDS,R,NX,NY,THETA
      INTEGER NNO,KP,NPG,IPOIDS,IVF,IDFDE,IGEOM
      INTEGER IMATTT,K,I,J,IJ,LI,LJ,ICOEFH

C-----------------------------------------------------------------------
      INTEGER ITEMPS ,JGANO ,NDIM ,NNOS 
C-----------------------------------------------------------------------
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDE,JGANO)

      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PCOEFHR','L',ICOEFH)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      CALL JEVECH('PMATTTR','E',IMATTT)

      THETA = ZR(ITEMPS+2)

      DO 40 KP = 1,NPG
        K = (KP-1)*NNO
        CALL VFF2DN(NDIM,NNO,KP,IPOIDS,IDFDE,ZR(IGEOM),NX,NY,POIDS)

        R = 0.D0
        DO 10 I = 1,NNO
          R = R + ZR(IGEOM+2*I-2)*ZR(IVF+K+I-1)
   10   CONTINUE
        POIDS = POIDS*R

        IJ = IMATTT - 1
        DO 30 I = 1,NNO
          LI = IVF + K + I - 1
CCDIR$ IVDEP
          DO 20 J = 1,I
            LJ = IVF + K + J - 1
            IJ = IJ + 1
            ZR(IJ) = ZR(IJ) + POIDS*THETA*ZR(LI)*ZR(LJ)*ZR(ICOEFH)
   20     CONTINUE
   30   CONTINUE
   40 CONTINUE
      END
