C VECTEURS DIMENSIONNES POUR  NNO = 8

C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------

      LOGICAL  AXI
      INTEGER  NDIM, NNO, NPG
      INTEGER  ICARAC, IFF, IPOIDS, IVF, IDFDE, IDFDK
      INTEGER  IGEOM, IMATE, IDEPL, ITEMPS, IECHLI
      REAL*8   RBID
      
      CHARACTER*8        ELREFE
      CHARACTER*24 CARAC, FF
C ......................................................................


      AXI  = NOMTE(3:4).EQ.'AX'
      NDIM = 2

      CALL ELREF1(ELREFE)
      CARAC='&INEL.'//ELREFE//'.CARAC'
      CALL JEVETE(CARAC,'L',ICARAC)
      NNO = ZI(ICARAC)
      NPG = ZI(ICARAC+2)

      FF   ='&INEL.'//ELREFE//'.FF'
      CALL JEVETE(FF,'L',IFF)
      IPOIDS = IFF
      IVF    = IPOIDS+ NPG
      IDFDE  = IVF   + NPG*NNO
      IDFDK  = IDFDE + NPG*NNO


      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PDEPLAR','L',IDEPL)
      CALL JEVECH('PTEMPSR','L',ITEMPS)
      CALL JEVECH('PECHLI', 'E',IECHLI)


C - CALCUL DE LA CONTRIBUTION ELEMENTAIRE A LA CHARGE LIMITE

      CALL NMHOLI(NDIM, AXI, NNO, NPG, ZR(IVF),
     &            ZR(IDFDE), RBID, ZR(IDFDK), ZR(IPOIDS), ZI(IMATE),
     &            ZR(ITEMPS), ZR(IGEOM), ZR(IDEPL), ZR(IECHLI))

      END
