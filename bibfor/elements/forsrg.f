C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
      IMPLICIT NONE
C
      INTEGER INTSN,NB1,NB2,INTSX1
      REAL*8 WGT,RNORMC
      REAL*8 XR(*),VECL1(42),CHG(6),CHGSRG(6,8)
      REAL*8 VECTPT(9,3,3)
C
C     EFFORTS SURFACIQUES DONNES DANS LE REPERE GLOBAL
C
C-----------------------------------------------------------------------
      INTEGER I ,I1 ,I2 ,I3 ,I4 ,I5 ,IN 
      INTEGER INTSX2 ,J ,K ,L1 ,L2 
C-----------------------------------------------------------------------
      WGT=XR(127-1+INTSN)
C
      L1=135
      L2=459
      INTSX1=8*(INTSN-1)
      INTSX2=9*(INTSN-1)
C
      I1=L1+INTSX1
      I2=L2+INTSX2
C
C     INTERPOLATION DES CHARGES
C
      DO 10 J=1,6
         CHG(J)=0.D0
      DO 20 IN=1,NB1
         CHG(J)=CHG(J)+CHGSRG(J,IN)*XR(I1+IN)
 20   CONTINUE
 10   CONTINUE
C
      DO 30 I=1,NB1
C
         I3=5*(I-1)
         VECL1(I3+1)=VECL1(I3+1)+WGT*CHG(1)*XR(I1+I)*RNORMC
         VECL1(I3+2)=VECL1(I3+2)+WGT*CHG(2)*XR(I1+I)*RNORMC
         VECL1(I3+3)=VECL1(I3+3)+WGT*CHG(3)*XR(I1+I)*RNORMC
C
      DO 40 K=1,3
         VECL1(I3+4)=VECL1(I3+4)+WGT*CHG(3+K)*(-XR(I2+I))*VECTPT(I,2,K)
         VECL1(I3+5)=VECL1(I3+5)+WGT*CHG(3+K)*  XR(I2+I) *VECTPT(I,1,K)
 40   CONTINUE
 30   CONTINUE
C
         I4=5*NB1+1
         I5=5*NB1+2
      DO 50 K=1,3
         VECL1(I4)  =VECL1(I4)+WGT*CHG(3+K)*(-XR(I2+NB2))*VECTPT(I,2,K)
         VECL1(I5)  =VECL1(I5)+WGT*CHG(3+K)*  XR(I2+NB2)* VECTPT(I,1,K)
 50   CONTINUE
C
      END
