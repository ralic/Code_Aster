      IMPLICIT REAL*8 (A-H,O-Z)
      REAL*8 EPAIS
      REAL*8 VECTG(2,3),DVECTG(2,3),VECTT(3,3),MATJ(3,3),JM1(3,3),DETJ
      REAL*8 HSFM(3,9),HSS(2,9)
      REAL*8 DHSFM(3,9),DHSS(2,9),DHSJ1M(3,9),DHSJ1S(2,9),DJM1(3,3)
C
C     CONSTRUCTION DE J-1 AUX PTS D'INTEGRATION REDUITS.   J = JACOBIEN
C
      CALL JACBM1(EPAIS,VECTG,VECTT,MATJ,JM1,DETJ)
      CALL DJACBM(EPAIS,VECTG,VECTT,DVECTG,DJM1)
C
C     CONSTRUCTION DE HFM * S * JTILD-1  ET  HS * S * JTILD-1
C                                            AUX PTS DE GAUSS REDUITS
C
C     PARTITION EN BLOCS DE HSFM ET HSS
C
C     HFM * S = HSFM = ( HSFM11 , HSFM12 , HSFM13 )  OU   HSFM1J (3,3)
C
C     HS  * S = HSS =  ( HSS11 , HSS12 , HSS13 )     OU   HSS1J (2,3)
C
C               J-1 0  0
C     JTILD-1 = 0  J-1 0                        OU   J-1   (3,3)
C               0   0  J-1
C
C           IB=1,1
      DO 10 JB=1,3
      DO 20  I=1,3
      DO 30  J=1,3
         J1=J+3*(JB-1)
         DHSJ1M(I,J1)=0.D0
         IF (I.LE.2) DHSJ1S(I,J1)=0.D0
      DO 40 K=1,3
         K1=K+3*(JB-1)
         IF (I.LE.2) DHSJ1S(I,J1)=DHSJ1S(I,J1)+HSS(I,K1)*DJM1(K,J)
     &               +DHSS(I,K1)*JM1(K,J)
         DHSJ1M(I,J1)=DHSJ1M(I,J1)+DHSFM(I,K1)*JM1(K,J)
     &               +HSFM(I,K1)*DJM1(K,J)
 40   CONTINUE
 30   CONTINUE
 20   CONTINUE
 10   CONTINUE
C
      END
