      INTEGER     NBRDDL,I,K
      CHARACTER*6 APPLIC
      REAL*8      BTILD(4,*),DEPL(*),EPSI(*),SGMTD(*),EFFINB
      REAL*8      WGT,EFFINT(*)
C
      IF (APPLIC .EQ. 'DEFORM') THEN
C
C     CALCULS DES COMPOSANTES DE DEFORMATIONS TRIDIMENSIONNELLES :
C     EPSXX, EPSYY, EPSXY, EPSXZ (CE SONT LES COMPOSANTES TILDE)
C
      DO 10 I=1,4
         EPSI(I)=0.D0
      DO 20 K=1,NBRDDL
         EPSI(I)=EPSI(I)+BTILD(I,K)*DEPL(K)
 20   CONTINUE
 10   CONTINUE
C
      ELSE IF (APPLIC .EQ. 'EFFORI') THEN
C
C     CALCULS DES EFFORTS INTERIEURS
C
      DO 30 I=1,NBRDDL
         EFFINB=0.D0
         DO 40 K=1,4
            EFFINB=EFFINB+BTILD(K,I)*SGMTD(K)
 40      CONTINUE
         EFFINT(I)=EFFINT(I)+WGT*EFFINB
 30   CONTINUE
C
      ENDIF
      END
