      INTEGER    KK,ZIMAT
      CHARACTER*8 KFONC1,KFONC2,MATER,K8B
      REAL*8     NORMF,X0
      REAL*8     XRAC
      INTEGER IER1,IER2,ITER,ITERMX
      REAL*8 FX1,FX2,FX,DFX1,DFX2,DFX,TOLE,ERR
      CHARACTER*16  PHENOM
      CHARACTER*2   CODRES

      PHENOM = 'GLRC_DAMAGE'
      ITER=0
      ITERMX=100
      XRAC=X0

      K8B = 'X '
      CALL RCVALE(MATER,PHENOM,1,K8B,XRAC,1,KFONC1,FX1,CODRES,'FM')
      CALL RCVALE(MATER,PHENOM,1,K8B,XRAC,1,KFONC2,FX2,CODRES,'FM')

      FX=FX1-FX2
      ERR=ABS(FX)
      TOLE=1.D-8*NORMF

       DO 9, ITER = 1,ITERMX
         IF(ERR .LE. TOLE) GOTO 10
         CALL CDNFO2(MATER,KFONC1,XRAC,1,DFX1,IER1)
         CALL CDNFO2(MATER,KFONC2,XRAC,1,DFX2,IER2)
         DFX=DFX1-DFX2

         IF ( (ABS(DFX) .LT. 1.D-12).OR.(IER1 .GT. 0)
     &        .OR.(IER2 .GT. 0)  ) THEN
           CALL U2MESS('F','ELEMENTS2_27')
         ENDIF

         XRAC=XRAC-FX/DFX
         CALL RCVALE(MATER,PHENOM,1,K8B,XRAC,1,KFONC1,FX1,
     &             CODRES,'FM')
         CALL RCVALE(MATER,PHENOM,1,K8B,XRAC,1,KFONC2,FX2,
     &                CODRES,'FM')
         FX=FX1-FX2
         ERR=ABS(FX)
 9     CONTINUE
       CALL U2MESS('F','ELEMENTS2_27')
       
 10    CONTINUE

      END
