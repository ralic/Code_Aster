      IMPLICIT REAL*8 (A-H,O-Z)
C
C    ESTIMATEUR ZZ (2-EME VERSION 92)
C
C                                  T
C    CETTE ROUTINE CALCULE   SIGMA( P(X   ,Y   ) * P(X   ,Y   ) )
C                             IPG      IPG  IPG       IPG  IPG
C
C    AVEC P(X,Y) = (1,X,Y,XY,X2,Y2,X2Y,XY2,X2Y2)
C
C     X(IPG),Y(IPG) SONT LES COORDONNEES DES PTS DE GAUSS SUR
C     L'ELEMENT COURANT
C
      REAL*8 WI(1),X(1),Y(1),A(9,9),XX,YY,B(9)
      DO 1 IPG = 1,NPG
        XX = 0.D0
        YY = 0.D0
        DO 2 INO = 1,NNO
          XX = XX + WI(NNO*(IPG-1)+INO)*X(INO)
          YY = YY + WI(NNO*(IPG-1)+INO)*Y(INO)
2       CONTINUE
        XX = -1.D0+2.D0*(XX-XMIN)/(XMAX-XMIN)
        YY = -1.D0+2.D0*(YY-YMIN)/(YMAX-YMIN)
        B(1) =  1.D0
        B(2) =  XX
        B(3) =  YY
        B(4) =  XX*YY
        B(5) =  XX*XX
        B(6) =  YY*YY
        B(7) =  YY*B(5)
        B(8) =  XX*B(6)
        B(9) =  B(6)*B(5)
        DO 3 I = 1,NNO
          DO 3 J = 1,I
          A(I,J) = A(I,J) + B(I) * B(J)
3         CONTINUE
        DO 4 I = 1,NNO
          DO 4 J = I+1,NNO
          A(I,J) = A(J,I)
4         CONTINUE
1     CONTINUE
C
      END
