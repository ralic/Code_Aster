C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C ======================================================================
C     CALCUL DES EFFORTS GENERALISES
C     GENERALISES POUR LES ELEMENTS DKTG, ET Q4GG
C     POUR UN MATERIAU ISOTROPE
C         OPTION TRAITEE  ==>  SIEF_ELGA
C
C     IN   K16   OPTION : NOM DE L'OPTION A CALCULER
C     IN   K16   NOMTE  : NOM DU TYPE_ELEMENT
C     ------------------------------------------------------------------
      INTEGER       NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDX,JGANO
      INTEGER       I,IPG,JCARA,IRET
      INTEGER       JDEPG, JEFFG, JGEOM

      REAL*8        PGL(3,3), XYZL(3,4),ALPHA,BETA,R8DGRD
      REAL*8        DEPL(24)
      REAL*8        EFFGT(32)
      REAL*8        T2EV(4), T2VE(4)

      CHARACTER*4   FAMI

C     ------------------------------------------------------------------
C
      FAMI = 'RIGI'
C
      CALL ELREF4(' ',FAMI,NDIM,NNO,NNOS,NPG,IPOIDS,IVF,IDFDX,JGANO)
C
      IF (OPTION.NE.'SIEF_ELGA' ) CALL ASSERT(.FALSE.)
C
      DO 10 I = 1,32
         EFFGT(I) = 0.D0
   10 CONTINUE

      CALL JEVECH('PGEOMER','L',JGEOM)
C
      IF (NNO.EQ.3) THEN
         CALL DXTPGL(ZR(JGEOM),PGL)
      ELSE IF (NNO.EQ.4) THEN
         CALL DXQPGL(ZR(JGEOM),PGL,'S',IRET)
      END IF

      CALL UTPVGL(NNO,3,PGL,ZR(JGEOM),XYZL)
C
      CALL JEVECH ('PCACOQU', 'L', JCARA)
      ALPHA = ZR(JCARA+1) * R8DGRD()
      BETA  = ZR(JCARA+2) * R8DGRD()
      CALL COQREP(PGL,ALPHA,BETA,T2EV,T2VE)
C
      CALL JEVECH('PDEPLAR','L',JDEPG)
      CALL UTPVGL(NNO,6,PGL,ZR(JDEPG),DEPL)
C
C --- CALCUL DES EFFORTS GENERALISES AUX POINTS DE CALCUL
      CALL JEVECH('PCONTRR','E',JEFFG)
      CALL DXEFGV(NOMTE,OPTION,XYZL,PGL,DEPL,EFFGT)
      DO 20 IPG=1,NPG*8
         ZR(JEFFG-1+IPG)=EFFGT(IPG)
20    CONTINUE
C
      END
