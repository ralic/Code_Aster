      SUBROUTINE NMNET2(ZIMAT,NMNBN,CNBN,CPLAS
     &                 ,CZEF,CZEG,CIEF,CDEPS
     &                 ,CDTG,CIER,DC1,DC2,DEPSP2,NORMM)
