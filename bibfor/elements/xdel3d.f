      SUBROUTINE  XDEL3D(ELREFP,INO,NNOP,IGEOM,PINTT,DEPL,HEI,DDLH,NFE,
     &                   DDLT,BASLOC,LSN,LST,DEPLA)
      IMPLICIT NONE
      INTEGER     DDLT,INO, NNOP,IGEOM, HEI, DDLH, NFE
      REAL*8      DEPL(DDLT,NNOP),BASLOC(9*NNOP),LSN(NNOP),LST(NNOP)
      CHARACTER*8 ELREFP

