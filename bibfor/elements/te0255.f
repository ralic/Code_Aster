C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C.......................................................................
      IMPLICIT REAL*8 (A-H,O-Z)
C
C     BUT: CALCUL DES VECTEURS ELEMENTAIRES EN MECANIQUE
C          ELEMENTS ISOPARAMETRIQUES 1D
C
C          OPTION : 'CHAR_MECA_VNOR '
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C          ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................
C
      CHARACTER*2        CODRET
      CHARACTER*8        ELREFE
      CHARACTER*16       NOMTE,OPTION
      CHARACTER*24       CHVAL,CARAC
      REAL*8             POIDS,NX,NY,COEF,R8PI,PI
      INTEGER            IPOIDS,IVF,IDFDE,IGEOM,ICARAC,IVNOR
      INTEGER            NDI,NNO,KP,NPG2,NPG1,IVECTU,IMATE,LDEC
C
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      COMMON /NOMAJE/PGC
      CHARACTER*6 PGC
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
C
      CALL ELREF1(ELREFE)

      CARAC = '&INEL.'//ELREFE//'.CARAC'
      CALL JEVETE(CARAC,'L',ICARAC)
      NNO = ZI(ICARAC)
      NPG1 = ZI(ICARAC+2)
C
      CHVAL = '&INEL.'//ELREFE//'.FF'
      CALL JEVETE(CHVAL,'L',IFF)
      IPOIDS = IFF
      IVF    = IPOIDS + NPG1
      IDFDE  = IVF    + NPG1 * NNO
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PVECTUR','E',IVECTU)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PSOURCR','L',IVNOR)
C
      CALL RCVALA(ZI(IMATE),'FLUIDE',0,' ',R8B,1,'RHO',RHO,CODRET,'FM')
C
      DO 11 I = 1,2*NNO
         ZR(IVECTU+I-1) = 0.0D0
11    CONTINUE
C
C     BOUCLE SUR LES POINTS DE GAUSS
C
      DO 101 KP=1,NPG1
         LDEC=(KP-1)*NNO
C
         NX = 0.0D0
         NY = 0.0D0
C
         CALL VFF2DN ( NNO,ZR(IPOIDS+KP-1),ZR(IDFDE+LDEC),ZR(IGEOM),
     &                 NX,NY,POIDS)
C
        IF ( NOMTE(3:4) .EQ. 'AX' ) THEN
           R = 0.D0
           DO 102 I=1,NNO
             R = R + ZR(IGEOM+2*(I-1))*ZR(IVF+LDEC+I-1)
102        CONTINUE
           POIDS = POIDS*R
        ENDIF
C
       DO 103 I=1,NNO
            II=2*I
            ZR(IVECTU+II-1) = ZR(IVECTU+II-1) -POIDS*
     &         ZR(IVNOR+KP-1) * RHO * ZR(IVF+LDEC+I-1)
103      CONTINUE
C
101   CONTINUE
C
 9999 CONTINUE
      END
