C -------- CALCUL DANS LE CAS 2
      REAL*8 EPS,BETA1,E,D1,SIGF1,BETA2,D2,SIG,ESEC
      REAL*8 FPRIM,TITI,TOTO,UN
      PARAMETER (UN=1.D+0)

C     ESEC EST LE MODULE SECANT DE COMPRESSION
      ESEC = E* (UN-D2)

      TITI = SIGF1 + BETA1*D1* (UN-D2)/ (UN-D1)
      TOTO = BETA1*D1* (UN-D2)/ (UN-D1)
      FPRIM = (ESEC*EPS-BETA2*D2+SIGF1)/TITI
C --- LA SOLUTION EST LA BORNE DROITE
      SIG = ESEC*EPS - BETA2*D2 - TOTO*FPRIM

