      DO 50 J=1,NDDLE
         KTILDI(I,J)=0.D0
      DO 60 K=1,NDIMC
         KTILDI(I,J)=KTILDI(I,J)+DBTILD(K,I)*WMATCB(K,J)
 60   CONTINUE
 50   CONTINUE
 40   CONTINUE
      END
