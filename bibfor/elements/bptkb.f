      REAL*8 WMATC(NDIMC,NDIMC),WMATCB(NDIMC,NDIMX),BTILD(NDIMC,NDIMX)
      REAL*8 KTILDI(NDIMX,NDIMX),DBTILD(NDIMC,NDIMX)
