C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF            
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU      
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.                              
C                                                                       
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE     
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,         
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.         
C ======================================================================
C TOLE CRP_21
C-----------------------------------------------------------------------
C    - FONCTION REALISEE:  CREATION DES OBJETS JEVEUX DE LA STRUCTURE
C      DE DONNEES SOLVEUR. UTILITAIRE DE BAS NIVEAU LANCE PAR CRESOL.
C
C IN SOLVEU   : NOM DE LA SD SOLVEUR
C IN METHOD....NIREMP : SES PARAMETRES, POUR SIGNIFICATION VOIR CRESOL.
C IN IFM,NIV : PARAMETRES POUR MONITORING.
C IN NUMSD   : NUMERO DE SOUS-DOMAINE
C IN NBMA    : NBRE DE MAILLES
C IN VERIF   : PARAMETRE POUR DEMANDER LA VERIFICATION DE SDFETI
C IN TESTCO  : PARAMETRE POUR TESTER LA CONTINUITE
C IN NBREOR/TYREOR : PARAMETRES POUR LA REOTHOGONALISATION DES
C              DIRECTIONS DE DESCENTE AVEC FETI
C IN SCALIN  : PARAMETRE DE SCALING POUR LE PRECONDITIONNEMENT FETI
C IN JLOGI   : ADRESSE DE L'OBJET DE LA COLLECTION .LOGI ASSOCIE AU 
C              SOUS-DOMAINE 
C   -------------------------------------------------------------------
C     SUBROUTINES APPELLEES:
C       MESSAGE:UTIMSD.
C       JEVEUX:WKVECT,JEMARQ,JEDEMA.
C
C     FONCTIONS INTRINSEQUES:
C       NONE.
C   -------------------------------------------------------------------
C     ASTER INFORMATIONS:
C       02/11/03 (OB): CREATION.
C----------------------------------------------------------------------
C RESPONSABLE BOITEAU O.BOITEAU
C CORPS DU PROGRAMME
      IMPLICIT NONE

C DECLARATION PARAMETRES D'APPELS
      INTEGER      NPREC,NMAXIT,ISTOP,NIREMP,IFM,NIV,NUMSD,NBMA,JLOGI,
     &             NBREOR
      REAL*8       EPS,RESIRE,TBLOC,TESTCO
      CHARACTER*3  SYME     
      CHARACTER*8  METHOD,PRECO,RENUM,VERIF,TYREOR,SCALIN
      CHARACTER*19 SOLVEU
      CHARACTER*24 SDFETI

C --- DEBUT DECLARATIONS NORMALISEES JEVEUX ----------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --- FIN DECLARATIONS NORMALISEES JEVEUX --------------------

C DECLARATION VARIABLES LOCALES
      INTEGER      ISLVK,ISLVR,ISLVI,ILOGI,I,NBMASD,IFETA,INTBUF
      CHARACTER*8  K8BID
      CHARACTER*24 K24B,NOMLOG
      CHARACTER*32 JEXNUM
      
C------------------------------------------------------------------
      CALL JEMARQ()

C --------------------------------------------------------------
C CREATION DES DIFFERENTS ATTRIBUTS DE LA S.D. SOLVEUR
C --------------------------------------------------------------
      CALL WKVECT(SOLVEU(1:19)//'.SLVK','V V K24',8,ISLVK)
      CALL WKVECT(SOLVEU(1:19)//'.SLVR','V V R',4,ISLVR)
      CALL WKVECT(SOLVEU(1:19)//'.SLVI','V V I',5,ISLVI)

C --------------------------------------------------------------
C REMPLISSAGE DE LA SD SOLVEUR
C --------------------------------------------------------------
      ZK24(ISLVK)   = METHOD
      ZK24(ISLVK+1) = PRECO
      ZK24(ISLVK+2) = VERIF
      ZK24(ISLVK+3) = RENUM
      ZK24(ISLVK+4) = SYME

C GESTION SD_FETI DIFFERENTE DE VERIF... CAR SD_FETI SERT AUSSI
C AU .LOGI      
      IF (NUMSD.EQ.0) THEN
        ZK24(ISLVK+5) = SDFETI
      ELSE
        ZK24(ISLVK+5) = '????'      
      ENDIF
      ZK24(ISLVK+6) = TYREOR
      ZK24(ISLVK+7) = SCALIN
      
      ZR(ISLVR)   = EPS
      ZR(ISLVR+1) = RESIRE
      ZR(ISLVR+2) = TBLOC
      ZR(ISLVR+3) = TESTCO

      ZI(ISLVI)   = NPREC
      ZI(ISLVI+1) = NMAXIT
      ZI(ISLVI+2) = ISTOP
      ZI(ISLVI+3) = NIREMP
      ZI(ISLVI+4) = NBREOR
      
      
C MONITORING
      IF (NIV.GE.3) THEN
        WRITE(IFM,*)
        WRITE(IFM,*)'DDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDD'
        IF (NUMSD.EQ.0) THEN
          WRITE(IFM,*)'<FETI/CRESO1> DOMAINE GLOBAL'
        ELSE
          WRITE(IFM,*)'<FETI/CRESO1> NUMERO DE SOUS-DOMAINE: ',
     &      NUMSD       
        ENDIF   
        WRITE(IFM,*)'<FETI/CRESO1> CREATION OBJETS JEVEUX ',
     &    SOLVEU(1:19),' SUR LA BASE V' 
        WRITE(IFM,*)'.SLVK: ',METHOD,' ',PRECO,' ',VERIF,' ',
     &    RENUM,' ',SYME,' ',SDFETI(1:8),' ',TYREOR,' ',SCALIN
        WRITE(IFM,*)'.SLVR: ',EPS,' ',RESIRE,' ',TBLOC,' ',
     &     TESTCO
        WRITE(IFM,*)'.SLVI: ',NPREC,' ',NMAXIT,' ',ISTOP,
     &    ' ',NIREMP,' ',NBREOR
        WRITE(IFM,*)
                        
        IF (NIV.GE.4) 
     &    CALL UTIMSD(IFM,2,.FALSE.,.TRUE.,SOLVEU(1:19),1,'V')
      ENDIF     

      IF (NUMSD.NE.0) THEN
        CALL JELIRA(JEXNUM(SDFETI(1:19)//'.FETA',NUMSD),'LONMAX',NBMASD
     &    ,K8BID)
        CALL JEVEUO(JEXNUM(SDFETI(1:19)//'.FETA',NUMSD),'L',IFETA)     
        DO 10 I=1,NBMA
          ZL(JLOGI+I-1)=.FALSE.
   10   CONTINUE
        DO 20 I=1,NBMASD
          INTBUF=ZI(IFETA+I-1)
          ZL(JLOGI+INTBUF-1)=.TRUE.
   20   CONTINUE 
      ENDIF
      IF ((NIV.GE.4).AND.(NUMSD.EQ.0)) THEN
        NOMLOG=SDFETI(1:8)//'.LOGI' 
        CALL UTIMSD(IFM,2,.FALSE.,.TRUE.,NOMLOG(1:19),1,'V')
      ENDIF 
      IF (NIV.GE.3) THEN 
        WRITE(IFM,*)'DDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDDD'        
        WRITE(IFM,*)
      ENDIF             
C FIN ------------------------------------------------------
      CALL JEDEMA()
      END
