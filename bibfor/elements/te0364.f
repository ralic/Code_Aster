      SUBROUTINE TE0364(OPTION,NOMTE )
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 04/07/2011   AUTEUR DESOZA T.DESOZA 
C ======================================================================
