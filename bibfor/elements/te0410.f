C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*(*)   OPTIOZ , NOMTZ

      CHARACTER*16    OPTION , NOMTE
C     ----------------------------------------------------------------
C     CALCUL DES OPTIONS DES ELEMENTS DE COQUE 3D
C                EPSI_ELNO
C                SIEF_ELGA
C                EFGE_ELNO
C
C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      LOGICAL            ZL
      COMMON  / LVARJE / ZL(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      INTEGER    NPGT,NCOUMX,VALI(2)
      PARAMETER (NPGT=10,NCOUMX=10)
      INTEGER    NB1,ITAB(7),IRET
      REAL*8     SIGTOT(6,9),EFFGT(8,9),SIGPG(162*NCOUMX)
      REAL*8     MATEVN(2,2,NPGT), MATEVG(2,2,NPGT),R8BID
C DEB
C
      OPTION=OPTIOZ
      NOMTE=NOMTZ
C
      CALL JEVECH ('PGEOMER' , 'L' , JGEOM)
      CALL JEVETE('&INEL.'//NOMTE(1:8)//'.DESI',' ', LZI )
      NB2  =ZI(LZI-1+2)
      NPGSN=ZI(LZI-1+4)
C
      CALL JEVECH('PNBSP_I','L',JCOU)
      NBCOU=ZI(JCOU)
      VALI(1)=NCOUMX
      VALI(2)=NBCOU
      IF(NBCOU.GT.NCOUMX)CALL U2MESI('F','CALCULEL7_4',2,VALI)
      IF((NBCOU.GT.1).AND.(OPTION(1:9).EQ.'EPSI_ELNO'))THEN
        CALL U2MESG('F','CALCULEL7_5',1,OPTION,1,NBCOU,0,R8BID)
      ENDIF
C     LE TABLEAU SIGPG A ETE ALLOUE DE FACON STATIQUE POUR OPTIMISER
C     LE CPU CAR LES APPELS A WKVECT DANS LES TE SONT COUTEUX.
      CALL VDXSIG(NOMTE,OPTION,ZR(JGEOM),NB1,NPGSR,SIGTOT,
     &            SIGPG,EFFGT)
C
C --- DETERMINATION DES MATRICES DE PASSAGE DES REPERES INTRINSEQUES
C --- AUX NOEUDS ET AUX POINTS D'INTEGRATION DE L'ELEMENT
C --- AU REPERE UTILISATEUR :
C     ---------------------
      CALL VDREPE(NOMTE, MATEVN, MATEVG)
      IF (OPTION(1:9) .EQ. 'EPSI_ELNO') THEN
        CALL JEVECH('PDEFORR','L',JSIGM)
C
C --- PASSAGE DU VECTEUR DES CONTRAINTES OU DU VECTEUR DES DEFORMATIONS
C --- DEFINI AUX NOEUDS DE L'ELEMENT DU REPERE INTRINSEQUE AU
C --- REPERE UTILISATEUR :
C     ------------------
           CALL VDSIRO(NB2,1,MATEVN,'IU','N',SIGTOT,ZR(JSIGM))
C
      ELSEIF ( OPTION(1:9) .EQ. 'EPSI_ELGA') THEN
              CALL TECACH('OOO','PDEFORR',7,ITAB,IRET)
              ICONTR=ITAB(1)
C
C --- STOCKAGE DU VECTEUR DES DEFORMATIONS
C --- 1 COUCHE - 3 PTS DANS L'EPAISSEUR
C---- 162 = NPGSN*6(6 DEFORMATIONS STOCKEES)*NPGE
C     ------------------------------------------------------
C
              DO 11 I = 1,NPGSN*18*NBCOU
                  ZR(ICONTR-1+I)=SIGPG(I)
 11           CONTINUE
C ---         PASSAGE DES DEFORMATIONS DANS LE REPERE UTILISATEUR :
              CALL VDSIRO(ITAB(3),ITAB(7),MATEVG,'IU','G',
     &                    ZR(ICONTR),ZR(ICONTR))
C

      ELSE IF ( OPTION(1:9) .EQ. 'SIEF_ELGA'.OR.
     &          OPTION(1:9) .EQ. 'EFGE_ELNO') THEN
           IF ( OPTION(1:9) .EQ. 'SIEF_ELGA') THEN
              CALL TECACH('OOO','PCONTRR',7,ITAB,IRET)
              ICONTR=ITAB(1)
C
C --- STOCKAGE DU VECTEUR DES CONTRAINTES EN ELASTICITE
C --- 1 COUCHE - 3 PTS DANS L'EPAISSEUR
C---- 162 = NPGSN*6(6 CONTRAINTES STOCKEES)*NPGE
C     ------------------------------------------------------
C
              DO 10 I = 1,NPGSN*18*NBCOU
                  ZR(ICONTR-1+I)=SIGPG(I)
 10           CONTINUE
C ---         PASSAGE DES CONTRAINTES DANS LE REPERE UTILISATEUR :
              CALL VDSIRO(ITAB(3),ITAB(7),MATEVG,'IU','G',
     &                    ZR(ICONTR),ZR(ICONTR))


C
C --- PASSAGE DU VECTEUR DES EFFORTS GENERALISES OU DU VECTEUR
C --- DES DEFORMATIONS-COURBURES DEFINI AUX POINTS D'INTEGRATION
C --- DE L'ELEMENT DU REPERE INTRINSEQUE AU REPERE UTILISATEUR :
C     --------------------------------------------------------
C
           ELSE IF ( OPTION(1:9) .EQ. 'EFGE_ELNO') THEN
              CALL JEVECH('PEFFORR','L',JEFFG)
C
C     CES CHAMPS SONT CALCULES AUX NB2 NOEUDS
C
              NP=NB2
C
C --- PASSAGE DU VECTEUR DES EFFORTS GENERALISES OU DU VECTEUR
C --- DES DEFORMATIONS-COURBURES DEFINI AUX NOEUDS
C --- DE L'ELEMENT DU REPERE INTRINSEQUE AU REPERE UTILISATEUR :
C     --------------------------------------------------------
             CALL VDEFRO(NP, MATEVN, EFFGT, ZR(JEFFG))
           ENDIF
C
      ENDIF
C
      END
