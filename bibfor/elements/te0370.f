C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C.......................................................................
C
C     BUT: CALCUL DES MATRICES DE RIGIDITE  ELEMENTAIRES EN MECANIQUE
C          ELEMENTS 2D DE COUPLAGE PESANTEUR-SURFACE LIBRE D'UN FLUIDE
C
C          OPTION : 'RIGI_MECA'
C
C     ENTREES  ---> OPTION : OPTION DE CALCUL
C          ---> NOMTE  : NOM DU TYPE ELEMENT
C.......................................................................
C
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER*8        ELREFE,MATER
      CHARACTER*2        CODRET
      CHARACTER*16       NOMTE,OPTION
      CHARACTER*24       CHVAL,CHCTE
      REAL*8             VALRES,A(2,2,27,27)
      REAL*8             SZ(27,27),DFDX(27),DFDY(27),RHO
      INTEGER            IGEOM,IMATE
      INTEGER            I,J,K,L,IK,IJKL,IDEC,JDEC,LDEC,KDEC,KCO,INO,JNO
      INTEGER            NBPG(10),NBFPG,NDIM,NNO,NPG1,IPG,NNOS,JGANO
      INTEGER            JIN,JVAL,IPOIDS,IVF,IDFRDE,IDFRDK,IMATUU
      REAL*8                  POIDS,DFRDE(1),DFRDK(1),COOR(1)
      REAL*8             PESA,JAC
      REAL*8             DXDE,DXDK,DYDE,DYDK
C
C
C---------------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
C------------FIN  COMMUNS NORMALISES  JEVEUX  --------------------------
C
      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG2,IPOIDS,IVF,IDFRDE,JGANO)
C
      CALL JEVECH('PGEOMER','L',IGEOM)
      CALL JEVECH('PMATERC','L',IMATE)
      CALL JEVECH('PMATUUR','E',IMATUU)
      CALL JEVECH('PPESANR','L', LPESA)
      PESA = ZR(LPESA)
C
      CALL RCVALA(ZI(IMATE),' ','FLUIDE',0,' ',R8B,1,'RHO',RHO,
     &           CODRET,'FM')
C
C     INITIALISATION DE LA MATRICE
C
      DO 112 K=1,2
         DO 112 L=1,2
            DO 112 I=1,NNO
            DO 112 J=1,I
                A(K,L,I,J) = 0.D0
112   CONTINUE
C
C     BOUCLE SUR LES POINTS DE GAUSS
C
      DO 113 IPG=1,NPG2
C
         KDEC = (IPG-1)*NNO*NDIM
         LDEC = (IPG-1)*NNO
C
         DXDE=0.D0
         DXDK=0.D0
         DYDE=0.D0
         DYDK=0.D0
         DO 100 I=1,NNO
           DXDE=DXDE+ZR(IGEOM+3*(I-1))*ZR(IDFRDE+KDEC+(I-1)*NDIM)
           DXDK=DXDK+ZR(IGEOM+3*(I-1))*ZR(IDFRDE+KDEC+(I-1)*NDIM+1)
           DYDE=DYDE+ZR(IGEOM+3*(I-1)+1)*ZR(IDFRDE+KDEC+(I-1)*NDIM)
           DYDK=DYDK+ZR(IGEOM+3*(I-1)+1)*ZR(IDFRDE+KDEC+(I-1)*NDIM+1)
C
100      CONTINUE
         JAC   = DXDE*DYDK - DXDK*DYDE
         POIDS = ABS(JAC)*ZR(IPOIDS+IPG-1)
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C        CALCUL DU TERME RHO * G * Z * Z   C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
         DO 130 INO=1,NNO
            DO 140 JNO=1,INO
               A(2,2,INO,JNO) = A(2,2,INO,JNO) +
     &                          POIDS * RHO * PESA *
     &                          ZR(IVF+LDEC+INO-1) * ZR(IVF+LDEC+JNO-1)
C
140         CONTINUE
130      CONTINUE
113   CONTINUE
C
C PASSAGE DU STOCKAGE RECTANGULAIRE AU STOCKAGE TRIANGULAIRE
C
      IJKL = 0
      IK = 0
      DO 160 K=1,2
         DO 160 L=1,2
            DO 162 I=1,NNO
                IK = ((2*I+K-3) * (2*I+K-2)) / 2
                DO 164 J=1,I
                   IJKL = IK + 2 * (J-1) + L
                   ZR(IMATUU+IJKL-1) = A(K,L,I,J)
164             CONTINUE
162         CONTINUE
160   CONTINUE
C
 9999 CONTINUE
      END
