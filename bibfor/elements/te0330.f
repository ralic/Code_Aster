      IMPLICIT NONE

C     BUT:
C       POUR TOUS LES ELEMENTS, CALCUL AUX NOEUDS DES
C       DES CONTRAINTES EQUIVALENTES SUIVANTES :
C           . VON MISES            (= 1 VALEUR)
C           . TRESCA               (= 1 VALEUR)
C           . CONTRAINTES PRINCIPALES      (= 3 VALEURS)
C           . VON-MISES * SIGNE (PRESSION) (= 1 VALEUR)
C           . DIRECTION DES CONTRAINTES PRINCIPALES
C                              (=3*3 VALEURS)
C           . TRACE                        (= 1 VALEUR)
C           . TAUX DE TRIAXIALITE          (= 1 VALEUR)
C     OPTIONS :  'SIEQ_ELNO'

C     ENTREES :  OPTION : OPTION DE CALCUL
C                NOMTE  : NOM DU TYPE ELEMENT
C ----------------------------------------------------------------------
      INCLUDE 'jeveux.h'

      INTEGER NNOMAX,NEQMAX
      PARAMETER (NNOMAX=27,NEQMAX=17)

      INTEGER IDFDX ,IPOIDS ,IVF ,JGANO ,NDIM
      INTEGER ICONT,IEQUIF
      INTEGER IDCP,INO,J,I
      INTEGER NNO,NCEQ,NPG,NNOS,NCMP
      INTEGER NDIM1,NBVA
      REAL*8 EQNO(NEQMAX*NNOMAX)
      REAL*8 SIGMA(30)
      CHARACTER*6  TYPMOD
      CHARACTER*16 NOMTE,OPTION



      CALL EQCARA(NOMTE,TYPMOD,NDIM1,NCEQ,NCMP,NBVA)

      CALL ELREF4(' ','RIGI',NDIM,NNO,NNOS,NPG,IPOIDS,
     &            IVF,IDFDX,JGANO)

      CALL JEVECH('PCONTRR','L',ICONT)
      CALL JEVECH('PCONTEQ','E',IEQUIF)

      DO 20 I = 1,NCMP*NNO
        EQNO(I) = 0.0D0
   20 CONTINUE

C -   CONTRAINTES EQUIVALENTES AUX NOEUDS

      IF(TYPMOD.EQ.'2D') THEN
         DO 103 INO = 1,NNO
            IDCP = (INO-1) * NCMP
            DO 109 I = 1,4
                SIGMA(I) = ZR(ICONT+(INO-1)*NBVA+I-1)
 109        CONTINUE
            SIGMA(5) = 0.D0
            SIGMA(6) = 0.D0
            CALL FGEQUI(SIGMA,'SIGM',NDIM1,EQNO(IDCP+1))
 103     CONTINUE


      ELSEIF(TYPMOD.EQ.'3D'.OR.TYPMOD.EQ.'COQUE') THEN
         DO 100 INO = 1,NNO
           IDCP = (INO-1) * NCMP
           IF(TYPMOD.EQ.'COQUE') THEN
              CALL FGEQUI(ZR(ICONT+(INO-1)*NBVA),'SIGM_DIR',NDIM1,
     &                    EQNO(IDCP+1))
           ELSE
              CALL FGEQUI(ZR(ICONT+(INO-1)*NBVA),'SIGM',NDIM1,
     &                    EQNO(IDCP+1))
           ENDIF
  100        CONTINUE
      ENDIF

C -       STOCKAGE

        DO 120 INO = 1,NNO
          DO 110 J = 1,NCMP
            ZR(IEQUIF-1+ (INO-1)*NCMP+J) = EQNO((INO-1)*NCMP+J)
  110     CONTINUE
  120   CONTINUE

      END
