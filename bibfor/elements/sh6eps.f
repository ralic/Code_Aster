      SUBROUTINE SH6EPS(XETEMP,XIDEPP,DEPLOC,DUSDX)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 12/08/2008   AUTEUR DESROCHES X.DESROCHES 
