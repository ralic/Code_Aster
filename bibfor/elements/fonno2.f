      SUBROUTINE FONNO2 (MACOFO,NOMA,NBMAC,NBNOFF,NBNOSE,NBMAX,NOEU,
     &                   TABLEV)
      IMPLICIT NONE
      CHARACTER*8         NOMA, NOEU
      CHARACTER*19        MACOFO
      INTEGER             NBMAC,NBNOFF,NBNOSE,NBMAX,TABLEV(2)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ELEMENTS  DATE 06/09/2011   AUTEUR GENIAUT S.GENIAUT 
C ======================================================================
C COPYRIGHT (C) 1991 - 2011  EDF R&D                  WWW.CODE-ASTER.ORG
