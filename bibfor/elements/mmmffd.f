      IMPLICIT NONE
      CHARACTER*8 ALIAS 
      REAL*8      KSI1
      REAL*8      KSI2   
      INTEGER     TYPBAR
      REAL*8      FF(9)
      REAL*8      DFF(2,9)
      REAL*8      DDFF(3,9)                
C      
C ----------------------------------------------------------------------
C
C ROUTINE CONTACT (METHODE CONTINUE - UTILITAIRE)
C
C CALCUL DES FONCTIONS DE FORME ET DE LEUR DERIVEES EN UN POINT 
C DE L'ELEMENT DE REFERENCE
C MODIFICATIONS PAR BARSOUM
C
C ----------------------------------------------------------------------
C
C
C IN  ALIAS  : NOM D'ALIAS DE L'ELEMENT
C IN  KSI1     : POINT DE CONTACT SUIVANT KSI1 DES
C               FONCTIONS DE FORME ET LEURS DERIVEES
C IN  KSI2     : POINT DE CONTACT SUIVANT KSI2 DES
C               FONCTIONS DE FORME ET LEURS DERIVEES
C IN  TYPBAR : INDICATEUR D'ELEMENT DE BARSOUM
C OUT FF     : FONCTIONS DE FORMES EN KSI1,KSI2
C OUT DFF    : DERIVEES PREMIERES DES FONCTIONS DE FORME EN KSI1 KSI2
C OUT DDFF   : DERIVEES SECONDES DES FONCTIONS DE FORME EN KSI1 KSI2
C
C ----------------------------------------------------------------------
C
C      
C --- FONCTIONS DE FORME
C
      CALL CALFFX(ALIAS ,KSI1  ,KSI2  ,FF    )
C      
C --- DERIVEES PREMIERES DES FONCTIONS DE FORME
C
      CALL CALDFX(ALIAS ,KSI1  ,KSI2  ,DFF   )
C      
C --- DERIVEES SECONDES DES FONCTIONS DE FORME
C
      CALL CAL2FX(ALIAS ,KSI1  ,KSI2  ,DDFF  )
C
C --- MODIFICATIONS DES FF ET DFF POUR ELEMENTS DE BARSOUM
C
      IF (TYPBAR.GE.0) THEN
        CALL MMMFFM(ALIAS ,KSI1  ,KSI2  ,TYPBAR,FF    ,
     &              DFF   )
      ENDIF
      END
