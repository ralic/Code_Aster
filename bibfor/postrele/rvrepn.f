      INCLUDE 'jeveux.h'
      CHARACTER*24  NLSNAC
      CHARACTER*19  SDNEWR
      CHARACTER*8   MAILLA, REPERE
C
C***********************************************************************
C
C  OPERATION REALISEE
C  ------------------
C
C     CALCUL  DU REPERE LOCAL OU POLAIRE LA LONG D' UNE LISTE DE NOEUDS
C
C  ARGUMENTS EN ENTREE
C  -------------------
C
C     REPERE : VAUT 'LOCAL' OU 'POLAIRE'
C     COURBE : NOM DE LISTE DE NOEUDS
C     MAILLA : NOM DU MAILLAGE
C
C  ARGUMENTS EN SORTIE
C  -------------------
C
C     SDNEWR : NOM DE LA SD DU NOUVEAU REPERE
C              (DOC. C.F. RVCHGR)
C
C***********************************************************************
C
C  -----------------------------------------
C
C
C
C  ---------------------------------
C
C  VARIABLES LOCALES
C  -----------------
C
      INTEGER      ACOORD,AVEC1,AVEC2,NBN,ALSNAC
      CHARACTER*1 K1BID
C
C====================== CORPS DE LA ROUTINE ===========================
C
      CALL JEMARQ()
      CALL JELIRA(NLSNAC,'LONMAX',NBN,K1BID)
      CALL JEVEUO(NLSNAC,'L',ALSNAC)
C
      CALL JEVEUO(MAILLA//'.COORDO    .VALE','L',ACOORD)
C
      CALL JECREC(SDNEWR//'.VEC1','V V R','NU','DISPERSE','VARIABLE',1)
      CALL JECREC(SDNEWR//'.VEC2','V V R','NU','DISPERSE','VARIABLE',1)
      CALL JECROC(JEXNUM(SDNEWR//'.VEC1',1))
      CALL JECROC(JEXNUM(SDNEWR//'.VEC2',1))
      CALL JEECRA(JEXNUM(SDNEWR//'.VEC1',1),'LONMAX',2*NBN,' ')
      CALL JEECRA(JEXNUM(SDNEWR//'.VEC2',1),'LONMAX',2*NBN,' ')
      CALL JEVEUO(JEXNUM(SDNEWR//'.VEC1',1),'E',AVEC1)
      CALL JEVEUO(JEXNUM(SDNEWR//'.VEC2',1),'E',AVEC2)
C
      CALL RVRLLN(ZR(ACOORD),ZI(ALSNAC),NBN,REPERE,ZR(AVEC1),ZR(AVEC2))
C
      CALL JEDEMA()
      END
