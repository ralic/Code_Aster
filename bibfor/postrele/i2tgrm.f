C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,       
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.      
C ======================================================================
C
      INCLUDE 'jeveux.h'
      INTEGER NBM,VOISN1(*),VOISN2(*)
      INTEGER NBCHM,STCHM(*),PTCHM(*)
C
      LOGICAL SIMPLE,CYCLE
      INTEGER I,APT,AST,MDPT,APLACE
C
C
C
C
      CALL JEMARQ()
      APT = 1
      AST = 1
C
      MDPT = 0
C
      APLACE = 0
C
      SIMPLE = .TRUE.
      CYCLE  = .TRUE.
C
      CALL JECREO ('&INTPLACE','V V L')
      CALL JEECRA ('&INTPLACE','LONMAX',NBM,' ')
      CALL JEVEUO ('&INTPLACE','E',APLACE)
C
      DO 10, I = 1, NBM, 1
C
         ZL(APLACE + I-1) = .FALSE.
C
10    CONTINUE
C
20    CONTINUE
      IF ( SIMPLE ) THEN
C
         CALL I2FSPL(VOISN2,ZL(APLACE),NBM,SIMPLE,MDPT)
C
         IF ( SIMPLE ) THEN
C
            CALL I2GSPL(MDPT,VOISN1,VOISN2,ZL(APLACE),STCHM,PTCHM,
     +                  AST,APT)
C
         ENDIF
C
         GOTO 20
C
      ENDIF
C
      MDPT = 0
C
30    CONTINUE
      IF ( CYCLE ) THEN
C
         CALL I2FCCL(ZL(APLACE),NBM,CYCLE,MDPT)
C
         IF ( CYCLE ) THEN
C
            CALL I2GCCL(MDPT,VOISN1,VOISN2,ZL(APLACE),STCHM,PTCHM,
     +                  AST,APT)
C
         ENDIF
C
         GOTO 30
C
      ENDIF
C
      PTCHM(APT) = AST
C
      NBCHM = APT - 1
C
      CALL JEDETR('&INTPLACE')
C
      CALL JEDEMA()
      END
