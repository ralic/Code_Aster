      IMPLICIT NONE
      INTEGER N,ADPER(*),AD(*),IER
      REAL*8 FRONTL(*),T1(*),FRONTU(*),T2(*),EPS,ALPHA,BETA
      INTEGER  I,K
      INTEGER SEUIN,SEUIK
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
      PARAMETER(SEUIN=1500,SEUIK=300)
      INTEGER NN,KK,LDA,INCX,INCY
      CHARACTER*1 TRA
      TRA='N'
      ALPHA=-1.D0
      BETA=1.D0
      INCX=1
      INCY=1
      LDA = N
      DO 30 K = 1,N
        DO 10 I = 1,K - 1
          AD(I) = ADPER(I) + K - I        
          T1(I) = FRONTU(AD(I))
          T2(I) = FRONTL(AD(I))
   10   CONTINUE
        IF (K.GT.1) THEN
C
           NN= N-K+1
           KK= K-1
           IF(NN.LT.SEUIN.OR.KK.LT.SEUIK) THEN
          CALL SSPMVB(N-K+1,K-1,FRONTL,AD,T1,FRONTL(ADPER(K)))
          CALL SSPMVB(N-K+1,K-1,FRONTU,AD,T2,FRONTU(ADPER(K)))
          ELSE
          CALL DGEMV(TRA,NN,KK,ALPHA,FRONTL(K),LDA,T1,INCX,BETA,
     &               FRONTL(ADPER(K)),INCY)
          CALL DGEMV(TRA,NN,KK,ALPHA,FRONTU(K),LDA,T2,INCX,BETA,
     &               FRONTU(ADPER(K)),INCY)
          ENDIF
       ENDIF
C         DIVISION PAR LE TERME DIAGONAL DE FRONTL (PAS FRONTU)
        IF (ABS(FRONTL(ADPER(K))).LE.EPS) THEN
          IER = K
          GO TO 40
        END IF
CRAY DIR$ IVDEP DIRECTIVE INHIBEE CAR DEPENDANCE AVANT
        DO 20 I = 1,N - K
          FRONTL(ADPER(K)+I) = FRONTL(ADPER(K)+I)/FRONTL(ADPER(K))
   20   CONTINUE
   30 CONTINUE
   40 CONTINUE
      END
