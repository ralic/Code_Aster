      SUBROUTINE MLTPRE(NU,BASE,RENUM)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGELINE  DATE 07/02/2006   AUTEUR CIBHHLV L.VIVAN 
C RESPONSABLE JFBHHUC C.ROSE
C ======================================================================
