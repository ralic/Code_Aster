      SUBROUTINE DIAGHR (N, A, LDA, EVAL, EVEC, LDEVEC, ACOPY, RWK,
     &                   CWK)
      IMPLICIT REAL*8 (A-H,O-Z)
      INTEGER    N, LDA, LDEVEC
      REAL *8 EVAL(*), RWK(*)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGELINE  DATE 29/09/2006   AUTEUR VABHHTS J.PELLET 
C ======================================================================
