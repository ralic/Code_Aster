      SUBROUTINE PBFLSO(UMOY,RHOF,RMOY,LONG,ICOQ,IMOD,NBM,RKIP,TCOEF,
     &                  S1,S2,HARM,LAMBDA,KCALCU,PASSAG,CONDIT,GAMMA,D,
     &                  YSOL)
