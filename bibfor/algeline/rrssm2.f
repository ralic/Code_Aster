C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      INTEGER SMHCR(*),SMHCI(*),IDLEXC(*)
      INTEGER SMDIR(*),SMDII(*)
      INTEGER NEQ,IDEBL1,IDEBL2,KIN1,KIN2,IEQUA,IFINL1
      INTEGER IFINL2,I,JINDIR,J2,K,I2,I1,J1,IND1
      REAL*8 COEF,VALMI(*),VALMR(*)
C--------------------------------------------------------


C --- CREATION DU TABLEAU D'INDIRECTION DES INDICES DES
C --- TERMES  D'UNE LIGNE DE LA MATRICE A COMBINER
C --- VERS LES INDICES DES TERMES DE LA MEME LIGNE
C --- DE LA MATRICE RESULTANTE :
C     ------------------------
      CALL WKVECT('&&RRSSM2.IND_LIG','V V I',NEQ,JINDIR)

      IDEBL1 = 1
      IDEBL2 = 1
      KIN1 = 0
      KIN2 = 0


      DO 60 IEQUA = 1,NEQ
        IFINL1 = SMDIR(IEQUA)
        IFINL2 = SMDII(IEQUA)
        DO 10 I = 1,NEQ
          ZI(JINDIR-1+I) = 0
   10   CONTINUE

C       -- CALCUL DE .IND_LIG :
C       ------------------------
        DO 40 J2 = IDEBL2,IFINL2
          K = 0
          I2 = SMHCI(J2)
          DO 20 J1 = IDEBL1,IFINL1
            I1 = SMHCR(J1)
            K = K + 1
            IF (I1.EQ.I2) GO TO 30
   20     CONTINUE
   30     CONTINUE
          ZI(JINDIR-1+I2) = K
   40   CONTINUE


C       -- CUMUL DANS LA MATRICE RESULTAT :
C       ------------------------------------
        KIN1 = IDEBL1 - 1
        DO 50 J2 = IDEBL2,IFINL2
          KIN2 = KIN2 + 1
          I2 = SMHCI(J2)
          IND1 = ZI(JINDIR-1+I2)
          VALMR(KIN1+IND1) = VALMR(KIN1+IND1) +
     &                       COEF*VALMI(KIN2)* (1-IDLEXC(I2))*
     &                       (1-IDLEXC(IEQUA))

   50   CONTINUE

        IDEBL1 = SMDIR(IEQUA) + 1
        IDEBL2 = SMDII(IEQUA) + 1
   60 CONTINUE
      CALL JEDETR('&&RRSSM2.IND_LIG')

      END
