      INTEGER ND,NBSN,NBSN1,SUPND(NBSN1),NOUV(ND)
      INTEGER PARENT(ND),PROV(ND),INVSUP(ND)
      INTEGER I,J,SNJP,SNJ
C     CALCUL DE PARENT EN FONCTION DES SUPERNOEUDS
C
C     SAUVEGARDE DE PARENT NODAL
      DO 110 I = 1,ND
          PROV(I) = PARENT(I)
  110 CONTINUE
C     CALCUL DE INVSUP
      DO 130 I = 1,NBSN
          DO 120 J = SUPND(I),SUPND(I+1) - 1
              INVSUP(J) = I
  120     CONTINUE
  130 CONTINUE
      DO 140 I = 1,ND
          PARENT(I) = 0
  140 CONTINUE
      DO 150 I = 1,ND
          IF (PROV(I).NE.0) THEN
              SNJP = INVSUP(NOUV(PROV(I)))
              SNJ = INVSUP(NOUV(I))
              PARENT(SNJ) = SNJP
          END IF
  150 CONTINUE
      END
