C ARGUMENTS:
C-----------
C
C IN/JXIN  K19 MATR   : MATR_ASSE PREMIER MEMBRE DU SYSTEME LINEAIRE
C IN       I   LMAT   : ADRESSE DU DESCRIPTEUR DE LA MATRICE
C
C REMARQUE : ON UTILISE (AU CHOIX) L'UN DES 2 ARGUMENTS MATR OU LMAT
C
C IN       I   NSECM  : NOMBRE DE SECONDS MEMBRES
C IN/OUT   R   RSOLU(*,NSECM)  :
C        EN ENTREE : VECTEUR DE REELS CONTENANT LES SECONDS MEMBRES
C        EN SORTIE : VECTEUR DE REELS CONTENANT LES SOLUTIONS
C IN/OUT   C   CSOLU(*,NSECM)  : IDEM RSOLU POUR LES COMPLEXES.
