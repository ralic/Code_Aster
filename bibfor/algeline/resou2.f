C BUT : RESOUDRE UN SYSTEME LINEAIRE D'EQUATIONS (REEL OU COMPLEXE)
C CETTE ROUTINE EST UN "CHAPEAU" A RESOUD POUR AVOIR MOINS D'ARGUMENTS
C-----------------------------------------------------------------------
