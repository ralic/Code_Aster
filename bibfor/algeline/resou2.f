      IMPLICIT NONE

      CHARACTER*(*) MATR
      INTEGER NSECM,LMAT
      REAL*8 RSOLU(*)
      COMPLEX*16 CSOLU(*)

C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      CHARACTER*32 JEXNUM,JEXNOM,JEXATR
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC,CBID
      COMMON /CVARJE/ZC(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
      CHARACTER*19 MATR2
      INTEGER IBID
C ----------------------------------------------------------------------

      IF (MATR.NE.' ') THEN
        CALL ASSERT(LMAT.EQ.0)
        MATR2=MATR
      ELSE
        CALL ASSERT(LMAT.NE.0)
        MATR2=ZK24(ZI(LMAT+1))
      ENDIF
      CALL ASSERT(MATR2.NE.' ')

      CALL RESOUD(MATR2,' ',' ',' ',' ',' ',' ',
     &                  ' ',NSECM,RSOLU,CSOLU)
      END
