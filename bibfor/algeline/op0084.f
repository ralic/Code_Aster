      CALL GETVR8(' ','RESI_RELA',0,1,1,EPS,IBID)


C     MATRICE ASSEMBLEE MORSE A RESOUDRE :
      CALL GETVID(' ','MATR_ASSE',0,1,1, MATAS,IBID)


