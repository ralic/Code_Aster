      CALL GETVTX(' ','REPRISE',0,1,1,OUINON,L)
      IF (OUINON.EQ.'OUI') IREP = 1

      CALL GETVID(' ','CHAM_NO',0,1,1,VECSM,L)
      VCINE=' '
      CALL GETVID(' ','CHAM_CINE',0,1,1,VCINE,L)


      CRITER = '&&RESGRA_GCPC'
