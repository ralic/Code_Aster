      SUBROUTINE TLDLG2(LMAT,NPREC,NMRIG,VEMRIG,FETI,VEINPN)
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGELINE  DATE 16/04/2012   AUTEUR TARDIEU N.TARDIEU 
