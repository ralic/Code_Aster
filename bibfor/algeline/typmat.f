C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
      IMPLICIT NONE
      CHARACTER*(*) TLIMAT(*)
      INTEGER NBMAT
      CHARACTER*1 TYPMAT
C-----------------------------------------------------------------------
C
C- BUT : CETTE FONCTION RETOURNE LE TYPE SYMETRIQUE : 'S'
C                                     OU PLEINE     : 'N'
C        DE LA MATRICE GLOBALE RESULTANTE DE L'ASSEMBLAGE
C        DES MATR_ELEM TLIMAT
C
C-----------------------------------------------------------------------
C --- DESCRIPTION DES PARAMETRES
C IN  I  NBMAT  : NOMBRE DE MATR_ELEM DE LA LISTE TLIMAT
C IN  K* TLIMAT : LISTE DES MATR_ELEM
C ----------------------------------------------------------------------
      REAL*8 RBID
      COMPLEX*16 CBID
C----------------------------------------------------------------------
      CHARACTER*8 SYM,PARTIT,ZERO
      CHARACTER*19 MATEL
      INTEGER I,ITYMAT
      INTEGER IBID, IERD
      INTEGER IRET
C----------------------------------------------------------------------
C     ITYMAT =  0 -> SYMETRIQUE
C            =  1 -> NON-SYMETRIQUE
C
C     --  PAR DEFAUT LE TYPE DE MATRICE EST SYMETRIQUE
      ITYMAT = 0

      DO 10 I =1, NBMAT
        MATEL = TLIMAT(I)
        CALL JEEXIN(MATEL//'.RELR',IRET)
        IF (IRET.EQ.0) GOTO 10
        CALL DISMOI('F','TYPE_MATRICE',MATEL,'MATR_ELEM',IBID,SYM,IERD)
        IF (SYM.EQ.'NON_SYM') THEN
          CALL DISMOI('F','ZERO',MATEL,'MATR_ELEM',IBID,ZERO,IERD)
          IF (ZERO.EQ.'NON') THEN
            ITYMAT = 1
            GOTO 11
          ENDIF
        ENDIF
   10 CONTINUE
   11 CONTINUE


C     -- SI CALCULS DISTRIBUES, IL FAUT COMMUNIQUER :
      CALL PARTI0(NBMAT,TLIMAT,PARTIT)

      IF (PARTIT.NE.' ') CALL MPICM1('MPI_MAX','I',1,ITYMAT,RBID)

      IF (ITYMAT.EQ.0) THEN
         TYPMAT='S'
      ELSE
         TYPMAT='N'
      ENDIF
      END
