C FONCTION: FORME LA MATRICE ANTISYMETRIQUE 'AMAT' DE VECTEUR AXIAL
C           'AXIAL' ET MULTIPLIE LES ELEMENTS DE LA MATRICE PAR COEF.
C
C     IN  : AXIAL     : VECTEUR D'ORDRE 3
C           COEF      : SCALAIRE
C
C     OUT : AMAT      : MATRICE D'ORDRE 3
C ------------------------------------------------------------------
      IMPLICIT REAL*8(A-H,O-Z)
      REAL*8 AXIAL(3),AMAT(3,3)
C
      ZERO = 0.D0
      DO 2 J=1,3
      DO 1 I=1,3
      AMAT(I,J) = ZERO
    1 CONTINUE
    2 CONTINUE
      AMAT(1,2) = -COEF * AXIAL(3)
      AMAT(1,3) =  COEF * AXIAL(2)
      AMAT(2,1) =  COEF * AXIAL(3)
      AMAT(2,3) = -COEF * AXIAL(1)
      AMAT(3,1) = -COEF * AXIAL(2)
      AMAT(3,2) =  COEF * AXIAL(1)
      END
