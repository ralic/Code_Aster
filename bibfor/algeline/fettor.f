C-----------------------------------------------------------------------
C    - FONCTION REALISEE:  TEST ORTHOGONALITE DU GCPPC DE ALGO FETI
C                          POUR SOULAGER ALFETI.F
C     ------------------------------------------------------------------
C     IN  OPTION : IN   : OPTION DE LA ROUTINE
C     IN  INFOFE : CH19 : CHAINE DE CHARACTERES POUR MONITORING FETI
C     IN  RANG   : IN   : RANG DU PROCESSEUR
C     IN  NBI    : IN   : TAILLE DE L'INTERFACE
C     IN  IRG1   : IN   : ADRESSE DE PRI-1 AVANT MAJ
C     IN  IRG    : IN   : IDEM APRES MAJ
C     IN  IRP    : IN   : ADRESSE DE DI-1
C     IN  IRZ    : IN   : ADRESSE DE FI*DI-1
C     IN  IFM    : IN   : UNITE LOGIQUE D'AFFICHAGE
C----------------------------------------------------------------------
C TOLE CRP_4
C TOLE CRP_21
C RESPONSABLE BOITEAU O.BOITEAU
C CORPS DU PROGRAMME
      IMPLICIT NONE

C DECLARATION PARAMETRES D'APPELS
      INTEGER      OPTION,RANG,NBI,IRG1,IRG,IRP,IRZ,IFM
      CHARACTER*24 INFOFE

C --------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ---------------------
      INTEGER            ZI
      COMMON  / IVARJE / ZI(1)
      REAL*8             ZR
      COMMON  / RVARJE / ZR(1)
      COMPLEX*16         ZC
      COMMON  / CVARJE / ZC(1)
      CHARACTER*8        ZK8
      CHARACTER*16                ZK16
      CHARACTER*24                          ZK24
      CHARACTER*32                                    ZK32
      CHARACTER*80                                              ZK80
      COMMON  / KVARJE / ZK8(1) , ZK16(1) , ZK24(1) , ZK32(1) , ZK80(1)
C --------- FIN  DECLARATIONS  NORMALISEES  JEVEUX ---------------------
C DECLARATION VARIABLES LOCALES
      REAL*8       DDOT,RAUX
      INTEGER*4    NBI4

C CORPS DU PROGRAMME
      CALL JEMARQ()

      IF ((INFOFE(8:8).EQ.'T').AND.(RANG.EQ.0)) THEN
        NBI4=NBI
        IF (OPTION.EQ.1) THEN
C TEST 1 ORTHOGONALITE DU GCPPC
          RAUX=DDOT(NBI4,ZR(IRG),1,ZR(IRG1),1)
          WRITE(IFM,*)'RANG ',RANG,' TEST <PRI,PRI-1>',RAUX
          RAUX=DDOT(NBI4,ZR(IRG),1,ZR(IRP),1)
          WRITE(IFM,*)'RANG ',RANG,' TEST <PRI,DI-1>',RAUX
        ELSE IF (OPTION.EQ.2) THEN
C TEST 2 ORTHOGONALITE DU GCPPC
          RAUX=DDOT(NBI4,ZR(IRP),1,ZR(IRZ),1)
          WRITE(IFM,*)'RANG ',RANG,' TEST <DI,FI*DI-1>',RAUX
        ELSE
          CALL U2MESS('F','ALGELINE_40')
        ENDIF
      ENDIF

      CALL JEDEMA()
      END
