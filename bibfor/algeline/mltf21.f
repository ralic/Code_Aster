C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C    1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C     VERSION MODIFIEE POUR L' APPEL A DGEMV (PRODUITS MATRICE-VECTEUR)
C     LE STOCKAGE DES COLONNES DE LA FACTORISEE EST MODIFIE
      IMPLICIT NONE
      INTEGER P,N,IER
      REAL*8 FRONT(*),T1(*),T2(*),FRN(*),EPS
C                                          VARIABLES LOCALES
      INTEGER DIA1,DIA2,J,L,M,R,DIA21,N1,JJ
      REAL*8 COEF1
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
      M = P/2
      R = P - 2*M
      L = N
      DIA2 = -N
      DO 10 J = 1,M
      JJ=J
C                    TRAVAIL SUR LE BLOC TRIANGULAIRE
C     MODIFS POUR STOCKAGE DGEMV
        DIA1 = DIA2 + N + 1
        DIA2 = DIA1 + N + 1
        DIA21 = DIA2 + 1
        L = L - 2
        COEF1 = FRONT(DIA1+1)
        IF (ABS(FRONT(DIA1)).LE.EPS) THEN
          IER = 1
          GO TO 30
        ELSE
          FRONT(DIA1+1) = FRONT(DIA1+1)/FRONT(DIA1)
          FRONT(DIA2) = FRONT(DIA2) - FRONT(DIA1+1)*COEF1
C                                            TRAVAIL SUR LES 2 COLONNES
          CALL COLNI2(FRONT(DIA1+2),FRONT(DIA21),L,FRONT(DIA1),
     +                FRONT(DIA2),COEF1,T1,T2,EPS,IER)
          IF (IER.NE.0) GO TO 30
C                                    MISE A JOUR DES COLONNES
          N1 = P - 2*J
C     MODIFS POUR STOCKAGE DGEMV
          CALL COL21J(FRONT(DIA1),FRONT(DIA21+N),FRN,J,L,N,N1,T1,T2)
        END IF
   10 CONTINUE
C                          TRAVAIL SUR LE RESTE DES COLONNES
C                                            DU SUPERNOEUD
      IF (R.EQ.1) THEN
        JJ = M + 1
C     MODIFS POUR STOCKAGE DGEMV
        DIA1 = DIA2 + N  + 1
        DIA2 = DIA1 + L
        L = L - 1
        CALL COLNI1(FRONT(DIA1+1),L,FRONT(DIA1),T1,EPS,IER)
        IF (IER.NE.0) GO TO 30
        CALL COL11J(FRONT(DIA1),FRN,L,T1)
      END IF
   30 CONTINUE
      IF (IER.GT.0) IER = IER + 2* (JJ-1)
      END
