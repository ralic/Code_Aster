      SUBROUTINE CMJOIN(MAIN, GNO1, GNO2, PREFIX, MAINIT, NOMGMA,
     &                  NOMA, CONNEC, TYMA, NGMA, GPMA)      
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF ALGELINE  DATE 21/03/2005   AUTEUR LAVERNE J.LAVERNE 
C ======================================================================
