C FONCTION: CUMULE LES ELEMENTS DE LA MATRICE PMAT, MULTIPLIES PAR COEF,
C           A CEUX DE LA MATRICE GMAT, SELON LA FORMULE:
C GMAT((I-1)*6+I1,(J-1)*6+J1) = GMAT((I-1)*6+I1,(J-1)*6+J1) +
C                               PMAT(I1,J1)
C
C     IN  : I,J       : POINTEURS SUR LA MATRICE GMAT
C           PMAT      : MATRICE D'ORDRE 6
C           COEF      : SCALAIRE
C
C     OUT : GMAT      : MATRICE D'ORDRE 18
C ------------------------------------------------------------------
      IMPLICIT NONE
      REAL*8 PMAT(6,6),GMAT(18,18)
C
C-----------------------------------------------------------------------
      INTEGER I ,I1 ,I2 ,II ,J ,J1 ,J2 
      INTEGER JJ 
      REAL*8 COEF 
C-----------------------------------------------------------------------
      I1 = (I-1) * 6
      J1 = (J-1) * 6
      DO 12 J2=1,6
      JJ = J1 + J2
      DO 11 I2=1,6
      II = I1 + I2
      GMAT(II,JJ) = GMAT(II,JJ) + COEF*PMAT(I2,J2)
   11 CONTINUE
   12 CONTINUE
      END
