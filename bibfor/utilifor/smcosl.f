C    SYSTEME MATRICIEL POUR LE CALCUL DES COORDONNEES BARYCENTRIQUES   C
C......................................................................C
      IMPLICIT NONE
      REAL*8   A(6,6),B(6),TRC((3*NBHIST),5),X(5),ZERO,UN
      INTEGER  IND(6),NBHIST,J,I
C
C-----------------------------------------------------------------------
C-----------------------------------------------------------------------
      ZERO=0.D0
        UN=1.D0
      DO 20 J=1,6
        B(J)=ZERO
        DO 10 I=1,6
          A(J,I)=ZERO
 10     CONTINUE
 20   CONTINUE
C
C CONSTRUCTION MATRICE A
C
      DO 40 I=1,3
        DO 30 J=1,6
          A(I,J)=TRC(IND(J),I)
 30     CONTINUE
 40   CONTINUE
      DO 50 J=1,6
        A(4,J)=TRC(IND(J),4)/X(4)
        A(5,J)=TRC(IND(J),5)/X(5)
        A(6,J)=UN
 50   CONTINUE
C
C CONSTRUCTION SECOND MEMBRE
C
      DO 60 I=1,3
        B(I)= X(I)
 60   CONTINUE
      B(4)=UN
      B(5)=UN
      B(6)=UN
      END
