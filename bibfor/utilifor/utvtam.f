      IF( IDF .LE. 0 .OR. IDF .GT. MXUNIT ) THEN
        TAMPON(1) = '<S> ERREUR DE PROGRAMMATION'
        TAMPON(2) = '    APPEL A UTFINM OU UTIMP? ERRONE'
        LIGCOU = 2
        IFIC = UNIT(2,1)
        IF ( IFIC .GT. 0 )
     &           WRITE (IFIC,'(1X,A)') (TAMPON(K),K=1,LIGCOU)
        CALL JXABOR()
      ENDIF
      NBL = MIN(LIGCOU,NT)
      DO 1 L=1, NBUNIT(IDF)
         WRITE(UNIT(IDF,L),'(1X,A)') (TAMPON(K),K=1,NBL)
 1    CONTINUE
      DO 2 L=1,NT
         TAMPON(L) = ' '
 2    CONTINUE
      END
