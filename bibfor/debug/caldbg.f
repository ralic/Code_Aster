      INTEGER NCHAM,I,IRET
      CHARACTER*(*) INOUT
      CHARACTER*19 LCHAM(*)
      CHARACTER*8 LPARAM(*)
      CHARACTER*16 OPTION
C ----------------------------------------------------------------------
C     BUT : IMPRIMER SUR UNE LIGNE LA VALEUR
