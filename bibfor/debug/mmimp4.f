      SUBROUTINE MMIMP4(IFM   ,NOMA  ,NUMMAE,IPTM  ,INDCOI,
     &                  INDCON,INDFRI,INDFRN,LFROT ,LVITES,
     &                  LGLISS,JEU   ,JEUVIT,LAMBDC)
C
C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF DEBUG  DATE 21/02/2012   AUTEUR ABBAS M.ABBAS 
