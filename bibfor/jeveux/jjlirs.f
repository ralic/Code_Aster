      INTEGER IADM,ICLAS,IUS,IST
C ----------------------------------------------------------------------
C     RELIT LES ENTIERS ENCADRANT UN SEGMENT DE VALEURS
C
C IN  IADMI  : ADRESSE DU PREMIER MOT DU SEGMENT DE VALEUR
C IN  ICLAS  : CLASSE DE L'OBJET JEVEUX
C IN  IDOS   : IDENTIFICATEUR D'OBJET SIMPLE OU D'OBJET DE COLLECTION
C IN  IDCO   : IDENTIFICATEUR DE COLLECTION
C OUT IUS    : USAGE DU SEGMENT DE VALEUR
C OUT IST    : STATUT DU SEGMENT DE VALEUR
C ----------------------------------------------------------------------
      CHARACTER*1      K1ZON
      COMMON /KZONJE/  K1ZON(8)
      INTEGER          LK1ZON , JK1ZON , LISZON , JISZON , ISZON(1)
      COMMON /IZONJE/  LK1ZON , JK1ZON , LISZON , JISZON
      EQUIVALENCE    ( ISZON(1) , K1ZON(1) )
C ----------------------------------------------------------------------
      INTEGER          ISTAT
      COMMON /ISTAJE/  ISTAT(4)
C ----------------------------------------------------------------------
      CHARACTER*75 CMESS
      CHARACTER*10 CIADM
C DEB ------------------------------------------------------------------
      ISTA1  = ISZON(JISZON+IADM-1)
      IDATOC = ISZON(JISZON+IADM-2)
      IF (IDATOC.NE.IDOS) THEN
        WRITE (CIADM,'(I10)') IADM
        CMESS = 'ECRASEMENT AMONT POSSIBLE ADRESSE >'//CIADM
        CALL JVMESS('S','JJLIRS01',CMESS)
      END IF
C
      IF (ISTA1.NE.ISTAT(1) .AND. ISTA1.NE.ISTAT(2)) THEN
        WRITE (CIADM,'(I10)') IADM
        CMESS = 'ECRASEMENT AMONT POSSIBLE ADRESSE >'//CIADM
        CALL JVMESS('S','JJLIRS02',CMESS)
      END IF
C
      IS = JISZON+ISZON(JISZON+IADM-4)
      ISTA2 = ISZON(IS-4)
      ICLA2 = ISZON(IS-2)
      IF (ICLA2.NE.ICLAS) THEN
        WRITE (CIADM,'(I10)') IADM
        CMESS = 'ECRASEMENT AVAL  POSSIBLE ADRESSE >'//CIADM
        CALL JVMESS('S','JJLIRS03',CMESS)
      END IF

      IF (ISTA2.NE.ISTAT(3) .AND. ISTA2.NE.ISTAT(4)) THEN
        WRITE (CIADM,'(I10)') IADM
        CMESS = 'ECRASEMENT AVAL  POSSIBLE ADRESSE >'//CIADM
        CALL JVMESS('S','JJLIRS04',CMESS)
      END IF
C
      IUS = ISTA1
      IST = ISTA2
C FIN ------------------------------------------------------------------
      END
