        iprem = i
    else
        if (r8nomb .gt. prem(i)) then
            j = (j + 1) / 2
            i = min( i + j , npre)
        else
            j = (j + 1) / 2
            i = max(i - j,1)
        endif
        iter=iter+1
        if (j .gt. 1) goto 5
        iprem = i
        if (r8nomb .gt. prem(i)) then
            iprem = iprem + 1
        endif
    endif
    jjprem = jprem(iprem)
    if (iprem .eq. npre) iret = 1
