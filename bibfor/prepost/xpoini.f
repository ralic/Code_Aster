      IMPLICIT NONE

      CHARACTER*8   MAXFEM,MODELE,MALINI,RESUCO,RESUX
      CHARACTER*24  LICHAM
      INTEGER       NBCHAM

C
C               RECUPERATION DES ENTREES SORTIES 
C               POUR LES OPERATEURS DE POST-TRAITEMENT X-FEM
C
C
C   OUT
C       MAXFEM : MAILLAGE X-FEM
C       MODELE : MODELE FISSURE
C       MALINI : MAILLAGE SAIN
C       JLICHA : ADRESSE DE LA LISTE DES CHAMPS A POST-TRAITER
C       NBSY   : NOMBRE DE CHAMPS A POST-TRAITER
C       RESUCO : NOM DU CONCEPT RESULTAT DONT ON EXTRAIT LES CHAMPS
C       RESUX  : NOM DU CONCEPT RESULTAT A CREER
C

      INTEGER       IRET,IBID,JORD,JLICHA,N1
      CHARACTER*1   KBID
      CHARACTER*8   K8B
      CHARACTER*16  K16B,NOMCMD
C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNOM,JEXNUM,JEXATR
C---------------- FIN COMMUNS NORMALISES  JEVEUX  ----------------------

      CALL JEMARQ()

C     NOM DE LA COMMANDE (POST_MAIL_XFEM OU POST_CHAM_XFEM)
      CALL GETRES(K8B,K16B,NOMCMD)

C     MODELE FISSURE : MODELE
      CALL GETVID(' ','MODELE',0,1,1,MODELE,IRET)
      CALL JEEXIN(MODELE//'.FISS',IRET)
      IF(IRET.EQ.0) CALL U2MESK('F','XFEM_3',1,MODELE)

C     MAILLAGE INITIAL : MALINI
C     MAILLAGE LINÉIARE OU QUADRATIQUE SUIVANT LES CAS
      CALL GETVID(' ','MAILLAGE_SAIN',0,1,1,MALINI,IRET)
        
      IF (NOMCMD.EQ.'POST_MAIL_XFEM') THEN

C       NOM DU MAILLAGE DE SORTIE : MAXFEM
        CALL GETRES(MAXFEM,K16B,K16B)        
C
      ELSEIF (NOMCMD.EQ.'POST_CHAM_XFEM') THEN

C       NOM ET TYPE DE LA SD RESULTAT EN ENTREE : RESUCO , TYSD
        CALL GETVID(' ','RESULTAT',1,1,1,RESUCO,IBID)

C       NOM DE LA SD RESULTAT A CREER : RESUX
        CALL GETRES(RESUX,K16B,K16B)

C       NOM DU MAILLAGE ET NOM DU CHAMPS D'ENTREE: MA , LICHAM
        CALL GETVID(' ','MAILLAGE_FISS',1,1,1,MAXFEM,IBID)
        CALL GETVTX(' ','NOM_CHAM',1,1,0,K8B,N1)
        NBCHAM=-N1
        CALL WKVECT ( LICHAM, 'V V K16', NBCHAM, JLICHA )
        CALL GETVTX(' ','NOM_CHAM',1,1,NBCHAM,ZK16(JLICHA),IRET)

      ENDIF

      CALL JEDEMA()
      END
