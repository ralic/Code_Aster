      CHARACTER*(*) GD,NOMCGD(*),NOMCMP(*),TYP
      INTEGER       NCMPMX,NBCMP,NBCMPT,JCMP
C ----------------------------------------------------------------------
C     BUT :   TROUVER LE NOMBRE ET LES NOMS DES COMPOSANTES D'UNE LISTE
C             PRESENTENT DANS UNE GRANDEURS
C     ENTREES:
C        GD     : NOM DE LA GRANDEUR
C        NCMPMX : NOMBRE DE COMPOSANTES DE LA GRANDEUR  GD
C        NOMCGD : NOMS DES COMOPOSANTES DE LA GRANDEUR GD
C        NBCMP  : NOMBRE DE COMPOSANTES DE LA LISTE
C        NOMCMP : NOMS DES COMPOSANTES DE LA LISTE
C     SORTIES:
C        NBCMPT : NOMBRE DE COMPOSANTES DE LA LISTE PRESENTES DANS GD
C        JCMP   : ADRESSE OBJET JEVEUX CONTENANT NUMEROS DES COMPOSANTES
C ----------------------------------------------------------------------
C     ----------- COMMUNS NORMALISES  JEVEUX  --------------------------
      COMMON /IVARJE/ZI(1)
      COMMON /RVARJE/ZR(1)
      COMMON /CVARJE/ZC(1)
      COMMON /LVARJE/ZL(1)
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      INTEGER ZI
      REAL*8 ZR
      COMPLEX*16 ZC
      LOGICAL ZL,EXISTE
      CHARACTER*8  ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32,JEXNUM,JEXNOM
      CHARACTER*80 ZK80
C     ------------------------------------------------------------------
C
      NBCMPT=0
C
      DO 10 ICM = 1,NBCMP
        DO 11 ICMPP=1,NCMPMX
           IF(NOMCMP(ICM).EQ.NOMCGD(ICMPP)) THEN
              NBCMPT=NBCMPT+1
              ZI(JCMP-1+NBCMPT) = ICMPP
              GO TO 10
           ENDIF
  11    CONTINUE
        IF ( TYP(1:1) .NE. ' ' ) THEN
            CALL UTDEBM(TYP,'IRCCMP',' ON NE TROUVE PAS LA COMPOSANTE')
            CALL UTIMPK('S',' ',1,NOMCMP(ICM))
            CALL UTIMPK('S',' DANS LA GRANDEUR ',1,GD)
            CALL UTFINM
         ENDIF
  10  CONTINUE
C
      END
