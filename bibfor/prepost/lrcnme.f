      SUBROUTINE LRCNME ( CHANOM, NOCHMD, NOMAMD,
     &                    NOMAAS, NOMGD, TYPENT,
     &                    NBCMPV, NCMPVA, NCMPVM,
     &                    IINST, NUMPT, NUMORD, INST, CRIT, PREC,
     &                    NROFIC, CODRET )
