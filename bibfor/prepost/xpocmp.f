      IMPLICIT NONE

      INTEGER       NDIM,DDLH,NFE,IMA,N,JCONX1,JCONX2,NBCMP,CMP(NBCMP),
     &              DDLC
      CHARACTER*19  CNS1
C
C   DETERMINER LES COMPOSANTES ACTIVES DU CHAMP DE DEPLACEMENT
C
C   IN
C     CNS1   : CHAMP_NO_S DU DEPLACEMENT EN ENTREE
C     IMA    : NUMERO DE MAILLE COURANTE PARENT
C     N      : NOMBRE DE NOEUDS DE L'ELEMENT PARENT
C     JCONX1 : ADRESSE DE LA CONNECTIVITE DU MAILLAGE SAIN
C     JCONX2 : LONGUEUR CUMULEE DE LA CONNECTIVITE DU MAILLAGE SAIN
C     NDIM   : DIMENSION DU MAILLAGE
C     NBCMP  : NOMBRE DE COMPOSANTES DU CHAMP_NO DE DEPL1
C
C   OUT
C     DDLH   : NOMBRE DE DDL HEAVISIDE (PAR NOEUD)
C     NFE    : NOMBRE DE FONCTIONS SINGULIÈRES D'ENRICHISSEMENT (1 A 4)
C     CMP    : POSITION DES DDLS DE DEPL X-FEM DANS LE CHAMP_NO DE DEPL1

C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNOM,JEXNUM,JEXATR
C---------------- FIN COMMUNS NORMALISES  JEVEUX  ----------------------

      INTEGER       JCNSD1,JCNSC1,JCNSL1,I,J,INO,ICMP,NDC,IPOS
      LOGICAL       EXIST(N,NBCMP)
      CHARACTER*8   NOMCMP

      CALL JEMARQ()

C     COMPOSANTES DU CHAMP DE DEPLACEMENT 1
      CALL JEVEUO(CNS1//'.CNSC','L',JCNSC1)
      CALL JEVEUO(CNS1//'.CNSL','L',JCNSL1)

      DO 110 J=1,N
C       INO : NUMÉRO DU NOEUD DANS MALINI
        INO=ZI(JCONX1-1+ZI(JCONX2+IMA-1)+J-1)
        DO 111 ICMP = 1,NBCMP
          EXIST(J,ICMP)= ZL(JCNSL1-1+(INO-1)*NBCMP + ICMP)
 111    CONTINUE
 110  CONTINUE
 
C     ON REGARDE LES COMPOSANTES ACTIVES POUR TOUS LES NOEUDS 
      IPOS = 0
      NDC  = 0
      DDLH  = 0
      NFE  = 0
      DDLC=0
      DO 21 I = 1,NBCMP
        NOMCMP = ZK8(JCNSC1-1+I)
        DO 22,J=1,N
           IF (.NOT.EXIST(J,I)) GOTO 21   
 22     CONTINUE 
        IF (NOMCMP(1:1).EQ.'D') THEN
          IPOS = IPOS +1
          NDC = NDC +1
          CMP(IPOS)=I
        ENDIF
        IF (NOMCMP(1:2).EQ.'H1') THEN
          IPOS = IPOS +1
          DDLH = DDLH +1
          CMP(IPOS)=I
        ENDIF
        IF (NOMCMP(1:2).EQ.'E1'.OR.NOMCMP(1:2).EQ.'E2'.OR.
     &      NOMCMP(1:2).EQ.'E3'.OR.NOMCMP(1:2).EQ.'E4') THEN
          IPOS = IPOS +1
          NFE = NFE +1
          CMP(IPOS)=I
        ENDIF
        IF (NOMCMP(1:4).EQ.'LAGS') THEN
           IPOS=IPOS+1
           DDLC=DDLC+1
           CMP(IPOS)=I
        ENDIF

 21   CONTINUE 

      NFE = NFE/NDIM 

      CALL ASSERT(NDIM.EQ.NDC)
      CALL ASSERT(DDLH.EQ.0.OR.DDLH.EQ.NDIM)
 
      CALL JEDEMA()
      END
