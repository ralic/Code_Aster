C WITHOUT ANY WARRANTY; WITHOUT EVEN THE IMPLIED WARRANTY OF
C MERCHANTABILITY OR FITNESS FOR A PARTICULAR PURPOSE. SEE THE GNU
C GENERAL PUBLIC LICENSE FOR MORE DETAILS.
C
C YOU SHOULD HAVE RECEIVED A COPY OF THE GNU GENERAL PUBLIC LICENSE
C ALONG WITH THIS PROGRAM; IF NOT, WRITE TO EDF R&D CODE_ASTER,
C   1 AVENUE DU GENERAL DE GAULLE, 92141 CLAMART CEDEX, FRANCE.
C ======================================================================
C              RECUPERATION DES COORDONNEES RELLES ET
C                 CALCUL DES LEVEL SET DE L'ENTITE
C
C   IN
C     ELREFP : ÉLÉMENT DE RÉFÉRENCE PARENT
C     INO   : NUMERO DU POINT D'INTER OU DU NOEUD
C     N      : NOMBRE DE NOEUDS DE L'ÉLÉMENT PARENT
C     JLSN   : ADRESSE DU CHAM_NO_S DE LA LEVEL NORMALE
C     JLST   : ADRESSE DU CHAM_NO_S DE LA LEVEL TANGENTE
C     IMA    : NUMERO DE MAILLE COURANTE PARENT
C     IAD    : POINTEUR DES COORDONÉES DE INO
C     IGEOM  : COORDONNÉES DES NOEUDS DE L'ÉLÉMENT PARENT
C     NFISS  : NOMBRE DE FISSURES "VUES" PAR L'ÉLÉMENT PARENT
C     NDIME  : DIMENSION TOPOLOGIQUE DE LA MAILLE PARENT
C     NDIM   : DIMENSION DU MAILLAGE
C     JCONX1 : ADRESSE DE LA CONNECTIVITE DU MAILLAGE SAIN
C              (CONNECTIVITE QUADRATIQUE SI LAGRANGES DE CONTACT
C              AUX ARETES)
C     JCONX2 : LONGUEUR CUMULEE DE LA CONNECTIVITE DU MAILLAGE SAIN
C              (CONNECTIVITE QUADRATIQUE SI LAGRANGES DE CONTACT
C              AUX ARETES)
C   OUT
C     CO     : COORDONNEES DU NOEUD OU DU POINT
C     LSN    : LEVEL SET NORMALE DU NOEUD OU DU POINT
C     LST    : LEVEL SET TANGENTE DU NOEUD OU DU POINT
C
      REAL*8       R8PREM,FF(N)
      INTEGER      I,IFISS

      CALL JEMARQ()
      CALL VECINI(3,0.D0,CO)

C     CAS D'UN NOEUD
      IF (INO.LT.1000) THEN
        I=ZI(JCONX1-1+ZI(JCONX2+IMA-1)+INO-1)
        CO(1)=ZR(IAD-1+3*(I-1)+1)
        CO(2)=ZR(IAD-1+3*(I-1)+2)
        CO(3)=ZR(IAD-1+3*(I-1)+3)

        DO 23 IFISS=1,NFISS
          LSN(IFISS) = ZR(JLSN-1+NFISS*(INO-1)+IFISS)
          LST(IFISS) = ZR(JLST-1+NFISS*(INO-1)+IFISS)
 23     CONTINUE

C     CAS D'UN POINT D'INTERSECTION OU D'UN POINT MILIEU
      ELSEIF (INO.GT.1000) THEN
        DO 11 I=1,NDIM
          CO(I)=ZR(IAD-1+I)
 11     CONTINUE

C       FF : FONCTIONS DE FORMES
        CALL XPOFFO(NDIM,NDIME,ELREFP,N,IGEOM,CO,FF)

        DO 35 IFISS=1,NFISS
          LSN(IFISS) = 0.D0
          LST(IFISS) = 0.D0
          DO 30 I=1,N
            LSN(IFISS)=LSN(IFISS)+ZR(JLSN-1+NFISS*(I-1)+IFISS)*FF(I)
            LST(IFISS)=LST(IFISS)+ZR(JLST-1+NFISS*(I-1)+IFISS)*FF(I)
 30       CONTINUE
 35     CONTINUE

      ENDIF

      CALL JEDEMA()

      END
