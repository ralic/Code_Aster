      SUBROUTINE  XPOLSN(ELREFP,INO,N,JLSN,JLST,IMA,IAD,IGEOM,NFISS,
     &                   NDIME,NDIM,JCONX1,JCONX2,CO,LSN,LST)
      IMPLICIT NONE

      INCLUDE 'jeveux.h'
      INTEGER     N,JLSN,JLST,NDIM,NDIME,INO,NFISS
      INTEGER     IAD,IGEOM,IMA,JCONX1,JCONX2
      CHARACTER*8 ELREFP
      REAL*8      CO(3),LSN(NFISS),LST(NFISS)

C            CONFIGURATION MANAGEMENT OF EDF VERSION
C MODIF PREPOST  DATE 19/11/2012   AUTEUR MARTIN A.MARTIN 
C TOLE CRS_1404
C ======================================================================
C COPYRIGHT (C) 1991 - 2012  EDF R&D                  WWW.CODE-ASTER.ORG
