      SUBROUTINE IRCAME ( FICH, NOCHMD,
     >                    NBCMP, NOMCMP,
     >                    NUMPT, INSTAN, UNIINS, NUMORD,
     >                    ADSK, ADSD, ADSC, ADSV, ADSL,
     >                    NBENEC, LIENEC,
     >                    SUPNOE, CODRET )
