C    IFM             IN    I     UNITE LOGIQUE D'IMPRESSION
C.========================= DEBUT DES DECLARATIONS ====================
C -----  ARGUMENTS
      CHARACTER*(*) NOMNOZ, NOMCMZ
      CHARACTER*120  FORM3, FORM4
      COMPLEX*16    VALE(*)
C -----  VARIABLES LOCALES
      CHARACTER*1   SLACH 
      CHARACTER*1   EXCLAM 
      CHARACTER*8   NOMNO1, NOMCM1
      CHARACTER*17  NOMNC1
C
C.========================= DEBUT DU CODE EXECUTABLE ==================
C
C --- INITIALISATIONS :
C     ---------------
      SLACH  = '/'
      EXCLAM = '!'
      NOMNO1 = NOMNOZ
      NOMCM1 = NOMCMZ
      NOMNC1 = NOMNO1(1:LXLGUT(NOMNO1))//SLACH//NOMCM1
C
C --- IMPRESSION DES VALEURS PAR LIGNE :
C     --------------------------------
      WRITE(IFM,FORM4) EXCLAM, NOMNC1, EXCLAM,
     +                        (DBLE(VALE(I)),DIMAG(VALE(I)),
     +                         EXCLAM,I=1,LONLIS)
      WRITE(IFM,FORM3) 
C
      END
