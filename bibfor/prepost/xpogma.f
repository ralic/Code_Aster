      IMPLICIT NONE

      INTEGER       NBGMA,NB,IMA,JLOGMA
      CHARACTER*24  LISTGR
C
C   AUGMENTATION DE LA TAILLE DES GROUP_MA A CREER
C
C   IN
C       NBGMA   : NOMBRE DE GROUP_MA EN TOUT
C       NB      : NOMBRE D'ELEMENTS A RAJOUTER
C       LISTGR  : LISTE DES GROUPES DE MAILLES
C       IMA     : NUMERO DE LA MAILLE

C   IN/OUT
C       JLOGMA  : ADRESSE DES VECTEUR DE TAILLE

      INTEGER       NGRM,IAGMA,I,IG
      CHARACTER*8   K8B

C -------------- DEBUT DECLARATIONS NORMALISEES  JEVEUX ----------------
      INTEGER ZI
      COMMON /IVARJE/ZI(1)
      REAL*8 ZR
      COMMON /RVARJE/ZR(1)
      COMPLEX*16 ZC
      COMMON /CVARJE/ZC(1)
      LOGICAL ZL
      COMMON /LVARJE/ZL(1)
      CHARACTER*8 ZK8
      CHARACTER*16 ZK16
      CHARACTER*24 ZK24
      CHARACTER*32 ZK32
      CHARACTER*80 ZK80
      COMMON /KVARJE/ZK8(1),ZK16(1),ZK24(1),ZK32(1),ZK80(1)
      CHARACTER*32 JEXNOM,JEXNUM,JEXATR
C---------------- FIN COMMUNS NORMALISES  JEVEUX  ----------------------

      CALL JEMARQ()

      IF (NBGMA.EQ.0) GOTO 999

      CALL JELIRA(JEXNUM(LISTGR,IMA),'LONMAX',NGRM,K8B)

C     NGRM = 0 : MAILLE N'APPARTENANT A AUCUN GROUPE, ON SORT
      IF (NGRM.EQ.0) GOTO 999

      CALL JEVEUO(JEXNUM(LISTGR,IMA),'L',IAGMA)

C     BOUCLE SUR LES GROUPES CONTENANT LA MAILLE IMA
      DO 10 I=1,NGRM
C       NUMERO DU GROUPE
        IG = ZI(IAGMA-1+I)
C       ON AUGMENTE DE NB
        ZI(JLOGMA-1+IG)=ZI(JLOGMA-1+IG)+NB
 10   CONTINUE

 999  CONTINUE
      CALL JEDEMA()
      END
